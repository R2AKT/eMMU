;;
; Смещение (Байт)  Размер   Имя поля            Описание и назначение для ядра POSIX
; -------------------------------------------------------------------------------------------------
; +0               1 B      O_PID               Идентификатор процесса (0 = Ядро, 1..254 = Задачи)
; +1               1 B      O_PPID              PID родителя (нужен для waitpid)
; +2               1 B      O_STATUS            Статус (FREE, CREATED, READY, RUNNING, WAITING, ZOMBIE)
; +3               2 B      O_SAVED_SP          Сохраненный указатель стека Ring 3 на момент прерывания
; +5               1 B      O_SYSCALL_RES       Код результата системного вызова / код возврата exit
; +6               1 B      O_UID               Идентификатор пользователя (User ID, 0 = ROOT)
; +7               1 B      O_GID               Идентификатор группы (Group ID)
; +8               4 B      O_PAGES_MAP         [ФИЗИЧЕСКИЕ] Номера физ. страниц ОЗУ для Сегментов 0–3
; +12              4 B      O_EXT_PAGES_MAP     [ВИРТУАЛЬНЫЕ] Страницы расширения (Сегменты 4–7 для оверлеев)
; +16              1 B      O_CF_SWAP_MASK      Битовая маска сегментов процесса в свопе на CF-карте
; +17              2 B      O_CWD_INODE         Номер сектора/инноды текущей директории процесса на CF
; +19              1 B      O_UMASK             Маска прав создания файлов (POSIX umask, дефолт 022)
; +20              8 B      O_FILE_TABLE        Таблица дескрипторов файлов (8 слотов: FD 0..7). 
                                              ; Значение - индекс в глобальной таблице файлов ядра (0xFF=свободен)

; ; === БЛОК ПЛАНИРОВАНИЯ И ПРИОРИТЕТОВ POSIX ===
; +28              1 B      O_PRIO_STATIC       Статический (базовый) приоритет процесса (0-255)
; +29              1 B      O_PRIO_NICE         Пользовательская nice-поправка (POSIX nice, 0-40, дефолт 20)
; +30              1 B      O_PRIO_DYNAMIC      Вычисляемый планировщиком динамический приоритет
; +31              1 B      O_CPU_TICKS         Штрафной счетчик времени, проведенного процессом на CPU

; ; === БЛОК АСИНХРОННЫХ СИГНАЛОВ И ТАЙМЕРОВ ===
; +32              2 B      O_SIG_PENDING       16-битная маска ожидающих доставку сигналов (POSIX)
; +34              2 B      O_SIG_BLOCKED       16-битная маска временно заблокированных сигналов
; +36              2 B      O_SIG_TABLE_PTR     Системный указатель на векторы обработчиков sigaction
; +38              2 B      O_ALARM_TICKS       Будильник POSIX таймера в 100-мс тиках (вызов alarm)
; +40              1 B      O_WAIT_CODE         wait-channel (причина блокировки процесса в ST_WAITING)
; +41 			   1 B      O_PAGE_DIRTY_MASK   Битовая маска (8 бит для 8 страниц процесса).
;												1 = страница изменена (грязная), 0 = страница чистая (совпадает с диском).
; +42              22 B     O_RESERVED_PAD      Чистый выравнивающий резерв ядра до границы ровно 64 байт
; -------------------------------------------------------------------------------------------------
; ИТОГО: 64 байта (2^6). За счет этого размер в коде переключения процессов вычисляется через 6 сдвигов (PID << 6).
;;
#target rom
#charset ascii
.asm8080

#code CODE, 0000h

; =============================================================================
; НАЧАЛО ТЕНЕВОГО ПЗУ (ХОЛОДНЫЙ СТАРТ С АДРЕСА 0x0000)
; =============================================================================
;    ORG 0000H
boot_start:
	DI                       ; Запрещаем прерывания на период холодной настройки БИС
	JMP real_init            ; Прыжок на процедуру первичной конфигурации

; =============================================================================
; ТАБЛИЦА ПРОГРАММНЫХ ВЕКТОРОВ (ЛОВУШЕК) PROCESSOR 8080 (0x0008 - 0x003F)
; =============================================================================
    ORG 0008H
	JMP rst_stub ; RST 1 — Системная заглушка программного сброса
    ORG 0010H
	JMP rst_stub ; RST 2
    ORG 0018H
	JMP rst_stub ; RST 3
    ORG 0020H
	JMP rst_stub ; RST 4
    ORG 0028H
	JMP rst_stub ; RST 5
    ORG 0030H
rst6_syscall:
    JMP sys_call_entry       ; RST 6 — МОНОПОЛЬНАЯ ТОЧКА ВХОДА SYSCALL ENTRY (Trap)
    ORG 0038H
	JMP rst_stub ; RST 7

; =============================================================================
; ТАБЛИЦА АППАРАТНЫХ ВЕКТОРОВ ПРЕРЫВАНИЙ ОТ КР580ВН59 (БАЗА 0x0040)
; =============================================================================
    ORG 0040H
	JMP hw_timer_entry ; ВН59 IR0 — Системный таймер ВИ53 №1 (20 мс)
    ORG 0048H
	JMP hw_uart_entry  ; ВН59 IR1 — Терминал ВВ51А (VT100 tty0)
    ORG 0050H
	JMP hw_dma_tc_entry; ВН59 IR2 - Контроллер ПДП (CF IDE16)
    ORG 0058H
	JMP hw_stub        ; ВН59 IR3 — Свободный аппаратный канал
    ORG 0060H
	JMP hw_stub        ; ВН59 IR4 — Свободный аппаратный канал
    ORG 0068H
	JMP hw_stub        ; ВН59 IR5 — Свободный аппаратный канал
    ORG 0070H
	JMP hw_stub        ; ВН59 IR6 — Свободный аппаратный канал
    ORG 0078H 
    JMP hw_privilege_violation_entry ; ВН59 IR7 — Исключение защиты eMMU

; =============================================================================
; НАЧАЛО ТЕНЕВОГО ПЗУ: БЛОК ХОЛОДНОЙ ИНИЦИАЛИЗАЦИИ И ПЕРИФЕРИИ (8 КБ)
; =============================================================================
real_init:
    ; Процессор только что сбросился. Мы находимся в Ring 0. 
    ; Триггер DD3 держит ~{ROM_EN}=0. Процессор читает код из ПЗУ (0x0000-0x1FFF).
    
    LXI  SP, KERNEL_STACK       ; Выставляем временный стек ядра в верхнем ОЗУ

    ; --- ЭТАП 1: Прямой перенос 8 КБ кода из нижнего ПЗУ в верхнее ОЗУ Ядра ---
    LXI  H, 00000H              ; HL = Начало Теневого ПЗУ (Источник)
    LXI  D, 08000H              ; DE = Временный буфер в верхнем ОЗУ (Назначение)
    LXI  B, 8192                ; Счетчик длины переноса: ровно 8 КБ (2000H)

copy_to_upper_ram_loop:
    MOV  A, M                   ; Считываем байт системного кода из ПЗУ
    STAX D                      ; Записываем напрямую в верхнее ОЗУ Ядра (адреса 0x8000...)
    INX  H                      ; Инкремент указателя источника ПЗУ
    INX  D                      ; Инкремент указателя назначения ОЗУ
    DCX  B                      ; Декремент 16-битного счетчика длины BC
    MOV  A, B
    ORA  C                      ; Проверяем, обнулился ли счетчик BC?
    JNZ  copy_to_upper_ram_loop ; Если нет — продолжаем качать ядро вверх

	; Копирование завершено. Переносим выполнение процессора в верхнее ОЗУ.
    ; ИСПРАВЛЕНО: Модификация адреса метки под верхнее окно ОЗУ (+0x8000)
    LXI  H, upper_ram_stage2;+08000H 
    PCHL                        ; Программный прыжок PC процессора в микросхемы верхнего ОЗУ!

; =============================================================================
; ЭТАП 2: ВЫПОЛНЕНИЕ ИЗ ВЕРХНЕГО ОЗУ ЯДРА И ОТКЛЮЧЕНИЕ ТЕНЕВОГО ПЗУ
; =============================================================================
upper_ram_stage2:
    ; Процессор аппаратно выполняет эту кодовую строчку уже из микросхем ОЗУ Ядра!
    
    ; АППАРАТНОЕ ОТКЛЮЧЕНИЕ ТЕНЕВОГО ПЗУ НА ПЛАТЕ eMMU
    ; Системный импульс OUT 0x75 сбрасывает внешний триггер DD3. 
    ; ПЗУ навсегда исчезает с шины. В нижнем окне 0x0000-0x1FFF открывается 
    ; физически изолированная «чистая» память нижнего ОЗУ Ядра (Ring 0).
    OUT  075H                   ; ТЕНЕВОЕ ПЗУ НАВСЕГДА ДЕАКТИВИРОВАНО

    ; --- ЭТАП 3: Зеркальный перенос всех 8 КБ кода из верхнего ОЗУ вниз ---
    ; Теперь, когда нижнее ОЗУ Ядра открыто, мы сажаем ядро на его каноническое место.
    LXI  H, 08000H              ; HL = Наш верхний временный буфер ОЗУ (Источник)
    LXI  D, 00000H              ; DE = Целевой физический адрес 0 ОЗУ Ядра (Назначение)
    LXI  B, 8192                ; Переносим 8 КБ системного кода вниз

copy_down_to_zero_loop:
    MOV  A, M                   ; Читаем байт ядра из верхней памяти ОЗУ
    STAX D                      ; Пишем в нижнее ОЗУ Ядра на канонический адрес 0
    INX  H                      ; Инкремент указателя источника
    INX  D                      ; Инкремент указателя назначения
    DCX  B                      ; Декремент 16-битного счетчика
    MOV  A, B
    ORA  C                      ; Проверяем окончание переноса
    ; ИСПРАВЛЕНО! Модифицируем адрес возврата петли цикла под верхнее ОЗУ (+0x8000).
    ; Теперь процессор будет крутиться строго внутри верхних физических адресов ОЗУ,
    ; пока полностью не восстановит нижнюю память ядра!
    JNZ  copy_down_to_zero_loop;+08000H 

    ; --- ЭТАП 4: Окончательный запуск операционной системы ---
    ; Передаем управление на канонический адрес старта ядра в нижнем ОЗУ
    LXI  H, kernel_clean_start  ; HL = Логический адрес старта в нижнем ОЗУ
    PCHL                        ; Процессор переходит на исполнение с адреса 0 ОЗУ Ядра!

; =============================================================================
; ЭТАП 5: ЧИСТЫЙ СТАРТ ИЗ НИЖНЕГО ОЗУ ЯДРА — НАСТРОЙКА БИС ПЕРИФЕРИИ
; =============================================================================
kernel_clean_start:
    ; --- ЭТАП 2: Инициализация БИС периферии системы ---
    ; Настройка Системного Таймера ВИ53 №1 (Квант 20 мс, 50 Гц)
    ; ; Делитель для получения прерываний каждые 20 мс (50 Гц) = 1 000 000 / 50 = 20 000 (4E20H)
    MVI  A, 00110110B        ; Канал 0, Чтение/Запись LSB/MSB, Режим 3 (Меандр)
    OUT  SYS_PIT_CMD
    MVI  A, 020H             ; Младший байт делителя (0x20)
    OUT  SYS_PIT_CH0
    MVI  A, 04EH             ; Старший байт делителя (0x4E)
    OUT  SYS_PIT_CH0

    ; Настройка Таймера Связи ВИ53 №2 (Раздельное тактирование Rx/Tx UART 9600 бод)
    MVI  A, 036H             ; CH0 (Тактирование ПРИЕМА RxC): Чтение/Запись LSB/MSB, Режим 3
    OUT  UART_PIT_CMD
    MVI  A, 00CH             ; Делитель приемника = 1 843 200 / (9600 * 16) = РОВНО 12
    OUT  UART_PIT_CH0
    MVI  A, 000H
	OUT UART_PIT_CH0
    
    MVI  A, 01110110B        ; CH1 (Тактирование ПЕРЕДАЧИ TxC): Чтение/Запись LSB/MSB, Режим 3
    OUT  UART_PIT_CMD
    MVI  A, 00CH             ; Делитель передатчика = РОВНО 12
    OUT  UART_PIT_CH1
    MVI  A, 000H
	OUT UART_PIT_CH1

    ; Инициализация USART КР580ВВ51А (8N1, фактор K=16)
    XRA  A
    OUT  UART_CMD
	OUT  UART_CMD
	OUT  UART_CMD ; Сброс автомата состояний БИС
    MVI  A, 040H
	OUT UART_CMD ; Внутренний программный сброс БИС
    MVI  A, 04EH
	OUT UART_CMD ; Режим: 1 стоп-бит, без четности, 8 бит данных, фактор K=16
    MVI  A, 037H
	OUT UART_CMD ; Команда: Включить TX/RX, сбросить ошибки, взвести RTS/DTR

    ; Инициализация Контроллера Прерываний КР580ВН59 (База векторов 0x0040)
    MVI  A, 013H
	OUT MASTER_8259_CMD  ; ICW1: По фронту прерываний, одиночный, нужен ICW4
    MVI  A, 040H
	OUT MASTER_8259_DATA ; ICW2: Базовое смещение аппаратной таблицы векторов = 0x0040
    MVI  A, 001H
	OUT MASTER_8259_DATA ; ICW4: Режим микропроцессора 8085/8086, обычный EOI
    MVI  A, 07CH
	OUT MASTER_8259_DATA ; OCW1 (Маска): Включаем IR0 (Таймер), IR1 (UART), IR7 (eMMU). Маска 0x7C 

    ; --- ЭТАП 3: Инициализация Системных Таблиц и Таблиц ОЗУ ---
    CALL init_pcb_table          ; Размечаем GLOBAL_RESOURCE_TABLE и слоты PCB

    ; --- ЭТАП 4: АППАРАТНЫЙ МОНТИНГ ФАЙЛОВОЙ СИСТЕМЫ (ВСТРАИВАНИЕ MBR) ---
    ; ВНИМАНИЕ! КРИТИЧЕСКАЯ ТОЧКА ВЫЗОВА:
    CALL vfs_init_mbr            ; Ядро считывает сектор 0 через ПДП и монтирует VFS на LBA 0x1000
    
    ; Проверяем результат монтирования. Если MBR поврежден (A != 0), уходим в панику ядра
    CPI  00H
    JNZ  kernel_panic_mbr_failed

    ; --- ЭТАП 5: Запуск многозадачной карусели POSIX ОС ---
    ; Здесь ядро создает первый пользовательский процесс PID=1 (наш разработанный Shell),
    ; прописывает его начальный контекст в PCB_TABLE и выставляет статус ST_READY.
    ; [Код спавна PID=1]

    EI                          ; ОФИЦИАЛЬНО РАЗРЕШАЕМ АППАРАТНЫЕ ПРЕРЫВАНИЯ!
    
idle_loop:
    HLT                         ; Спим в экономичном режиме PID=0 до первого тика таймера 20 мс
    JMP  idle_loop

kernel_panic_mbr_failed:
    ; Фатальная ошибка старта: MBR не валиден или раздел VFS 0x1000 не найден.
    ; Выключаем прерывания и намертво вешаем процессор для предотвращения порчи диска.
    DI
    HLT
    JMP  kernel_panic_mbr_failed

; =============================================================================
; АППАРАТНЫЙ ОБРАБОТЧИК IR0: КВАНТОВАНИЕ 20 МС И ВЫТЕСНЕНИЕ КОНТЕКСТА
; =============================================================================
hw_timer_entry:
    PUSH PSW					; Сохраняем регистры Ring 3 в стек текущей задачи
	PUSH B
	PUSH D
	PUSH H 
	
    LXI  H, 0
	DAD  SP
    LXI  SP, KERNEL_STACK   	; Жестко переключаемся на системный стек ядра ОС
    PUSH H                 		; Сохраняем пользовательский SP в стек ядра

    ; --- Вычисляем базовый адрес PCB текущей задачи ---
    LDA  CURRENT_PID
    MOV  L, A
    MVI  H, 0
    ; 6 сдвигов влево для быстрого умножения PID на 64 (PCB_SIZE)
    DAD  H						; HL = PID * 64
	DAD  H
	DAD  H
	DAD  H
	DAD  H
	DAD  H
    
    LXI  D, PCB_TABLE
    DAD  D                      ; HL = PCB_TABLE + (PID * 64) -> Базовый адрес PCB найден!

    ; --- Теперь прибавляем смещение O_SAVED_SP (+3) ---
    LXI  D, O_SAVED_SP          ; DE = смещение до поля (0003H)
    DAD  D                      ; HL = База_PCB + O_SAVED_SP (указывает точно на поле структуры)
    
    POP  D                      ; DE = ранее сохраненный пользовательский SP
    ; Записали SP в PCB текущего процесса
	MOV  M, E                   ; Запись младшего байта SP по адресу в HL
    INX  H
    MOV  M, D                   ; Запись старшего байта SP по адресу в HL + 1
	

    CALL get_current_pcb_address
    LXI  D, O_STATUS
	DAD D
    MOV  A, M
	CPI ST_RUNNING
	JNZ skip_timer_status_reset
    MVI  M, ST_READY        ; Текущий процесс вытеснен в очередь готовых задач
skip_timer_status_reset:

    ; --- СКАНИРОВАНИЕ И ДЕКРЕМЕНТ POSIX БУДИЛЬНИКОВ (ОТСЧЕТ КАЖДЫЕ 20 МС) ---
    LXI  H, PCB_TABLE
	MVI C, 0
alarm_scan_loop:
    PUSH H
    LXI  D, O_STATUS
	DAD D
	MOV A, M
	CPI ST_FREE
	JZ next_pid_alarm_tick
    POP  H
	PUSH H
    LXI  D, O_ALARM_TICKS
	DAD D
    MOV  A, M
	INX H
	ORA M
	JZ next_pid_alarm_tick ; Будильник равен 0000H?
    DCX  H
	MOV A, M
	SUI 1
	MOV M, A
	JNC check_alarm_zero
    INX  H
	MOV A, M
	SUI 1
	MOV M, A
	DCX  H
check_alarm_zero:
    MOV  A, M
	INX H
	ORA M
	JZ next_pid_alarm_tick
    POP  H
	PUSH H
    LXI  D, O_SIG_PENDING + 1
	DAD D
    MOV  A, M
	ORI SIGALRM_MASK_H
	MOV M, A       ; Доставили сигнал SIGALRM
next_pid_alarm_tick:
    POP  H
	LXI D, PCB_SIZE
	DAD D
    INR  C
	MOV A, C
	CPI MAX_PROCS + 1
	JNZ alarm_scan_loop

    CALL scheduler
	STA NEXT_PID                   ; Планировщик выбирает новую задачу

    ; --- ФИЗИЧЕСКАЯ ПЕРЕПРОШИВКА eMMU НА КАЖДОМ ТИКЕ (20 МС) ---
    LDA  NEXT_PID
	STA CURRENT_PID
    MOV  L, A
	MVI H, 0
	DAD H
	DAD H
	DAD H
	DAD H
	DAD H
	DAD H
    LXI  D, PCB_TABLE
	DAD D                       ; HL = базовый адрес PCB новой задачи
    PUSH H
	LXI D, O_STATUS
	DAD D
	MVI M, ST_RUNNING
	POP H

    PUSH H
    LXI  D, O_PAGES_MAP
	DAD D						; HL указывает на O_PAGES_MAP (+8)
    MOV  A, M
	OUT 070H					; Аппаратная прошивка Сегмента 0 (0x0000 - 0x3FFF)
    INX  H
	MOV A, M
	OUT 071H                    ; Аппаратная прошивка Сегмента 1 (0x4000 - 0x7FFF)
    INX  H
	MOV A, M
	OUT 072H                    ; Аппаратная прошивка Сегмента 2 (0x8000 - 0xBFFF)
    INX  H
	MOV A, M
	OUT 073H                    ; Аппаратная прошивка Сегмента 3 (0xC000 - 0xFFFF)
    POP  H

    PUSH H
    LXI  D, O_SIG_PENDING + 1
	DAD D
	MOV A, M
	ANI SIGKILL_MASK_H
	JZ restore_timer_context
    POP  H
	MVI A, 09H
	JMP sys_exit_fatal         ; Перехват и уничтожение задачи по SIGKILL

restore_timer_context:
    POP  H
	LXI D, O_SAVED_SP
	DAD D
    MOV  E, M
	INX H
	MOV D, M
	XCHG
	SPHL      ; Указатель SP перенесен на стек новой задачи
    MVI  A, 020H
	OUT MASTER_8259_CMD              ; Отправка EOI в контроллер ВН59
    POP  H
	POP  D
	POP  B
	POP  PSW             ; Восстановление регистров задачи из Ring 3
    RET                                             ; Прыжок в Ring 3 с аппаратным стробом ~{RET_STROBE}

; =============================================================================
; ПРОЦЕДУРА ИНИЦИАЛИЗАЦИИ ТАБЛИЦ ПРИ СТАРТЕ СИСТЕМЫ (РАЗМЕТКА PCB)
; =============================================================================
init_pcb_table:
    LXI  H, GLOBAL_RESOURCE_TABLE
    MVI  B, 0
init_res_loop:
    MVI  M, RES_FREE_MARKER     ; Записываем 0xFF во всю карту устройств (порты свободны)
    INX  H
	DCR B
	JNZ init_res_loop

    LXI  H, PCB_TABLE
	MVI C, 0 ; Инициализируем 256 слотов таблицы процессов
init_pcb_loop:
    PUSH H
    MOV  M, C                    ; Записываем O_PID
    INX  H
	MVI M, RES_FREE_MARKER ; Записываем O_PPID = 0xFF
    INX  H
	MVI M, ST_FREE      ; Записываем O_STATUS = ST_FREE (ЯВНО выставляем состояние)
    MVI  B, 61
clear_pcb_bytes:
    INX  H
	MVI M, 00H          ; Зануляем все маски сигналов, дескрипторы FD и приоритеты
    DCR  B
	JNZ clear_pcb_bytes
    POP  H
	LXI D, PCB_SIZE
	DAD D
    INR  C
	MOV A, C
	CPI 0
	JNZ init_pcb_loop

    ; --- РУЧНАЯ КОНФИГУРАЦИЯ КОНТЕКСТА ДЛЯ PID = 0 (ЯДРО ОС) ---
    LXI  H, PCB_TABLE
    PUSH H
	LXI D, O_STATUS
	DAD D
	MVI M, ST_RUNNING
	POP H ; Статус Ядра = ST_RUNNING (03H)
    PUSH H
	LXI D, O_UID
	DAD D
	MVI M, 00H
	INX H
	MVI M, 00H
	POP H ; UID/GID Ядра = 00H (ROOT)
    PUSH H
	LXI D, O_PAGES_MAP
	DAD D
    MVI  M, 00H
	INX H
	MVI M, 01H
	INX H
	MVI M, 02H
	INX H
	MVI M, 03H
	POP H ; Страницы Ядра 0,1,2,3
    PUSH H
	LXI D, O_PRIO_STATIC
	DAD D
    MVI  M, 00H
	INX H
	MVI M, 20
	INX H
	MVI M, 20
	POP H ; Абсолютный приоритет планировщика = 0
    RET

; =============================================================================
; ВЫТЕСНЯЮЩИЙ ПЛАНИРОВЩИК ДИНАМИЧЕСКИХ ПРИОРИТЕТОВ POSIX
; =============================================================================
scheduler:
    LDA  CURRENT_PID
	CPI 0
	JZ prep_scheduler_scan
    CALL get_current_pcb_address
	LXI D, O_CPU_TICKS
	DAD D
    MOV  A, M
	CPI 200
	JNC prep_scheduler_scan
    INR	A
	MOV M, A            ; Начисляем штрафной тик процессу за квант на CPU

prep_scheduler_scan:
    MVI  B, 0
	MVI  C, 0FFH
	MVI A, 00H
	STA NEXT_PID ; Дефолтный кандидат — Ядро PID=0
    LXI  H, PCB_TABLE
	MVI E, 0
scheduler_loop:
    PUSH H
	LXI D, O_STATUS
	DAD D
	MOV A, M
	CPI ST_READY
	JZ calc_priority
    JMP  next_scheduler_slot     ; Пропускаем задачи в статусе ST_CREATED, ST_WAITING или ST_ZOMBIE
calc_priority:
    POP  H
	PUSH H
	LXI D, O_PRIO_STATIC
	DAD D
    MOV  A, M
	INX H
	ADD M
	INX H
	INX H
	ADD M ; A = STATIC + NICE + CPU_TICKS
    DCX  H
	MOV M, A            ; Записали актуальный динамический приоритет в O_PRIO_DYNAMIC
    CMP  C
	JNC next_scheduler_slot
    MOV  C, A
	MOV A, E
	STA NEXT_PID ; Запоминаем PID лучшей (наименьшей по числу) задачи
next_scheduler_slot:
    POP  H
	LXI D, PCB_SIZE
	DAD D
    INR  E
	MOV A, E
	CPI MAX_PROCS + 1
	JNZ scheduler_loop

    LDA  NEXT_PID
	MOV L, A
	MVI H, 0
	DAD H
	DAD H
	DAD H
	DAD H
	DAD H
	DAD H
    LXI  D, PCB_TABLE
	DAD D
	LXI D, O_CPU_TICKS
	DAD D
	MVI M, 00H ; Сброс штрафа новой задаче
    LDA  NEXT_PID
	RET

; =============================================================================
; АППАРАТНЫЙ ОБРАБОТЧИК РЕАКТИВНОГО I/O FIREWALL (Адрес 0x0078, Вектор IR7 ВН59)
; =============================================================================
hw_privilege_violation_entry:
	; Прежде чем портить стек и регистры Ring 3, аппаратно проверяем, 
    ; является ли это прерывание истинным прерыванием от линии IR7.
    ; Посылаем в ВН59 команду OCW3 (00001011B = 0x0B) для чтения регистра ISR
    MVI  A, 00BH
    OUT  MASTER_8259_CMD
    IN   MASTER_8259_CMD        ; Считываем In-Service Register (ISR)
    ANI  080H                   ; Маскируем бит 7 (Текущая обработка IR7)
    JZ   hw_stub                ; ЕСЛИ БИТ 7 РАВЕН 0 — ЭТО ЛОЖНОЕ ПРЕРЫВАНИЕ!
                                ; Процессор мгновенно и безопасно уходит в hw_stub,
                                ; не затрагивая стек и не убивая текущий процесс!

    ; --- Сюда процессор попадает только при ИСТИННОМ Privilege Violation ---
    ; Теневое сохранение контекста нарушителя в стек Ring 3 задачи
    PUSH PSW
    PUSH B
    PUSH D
    PUSH H 
    
    ; Переключаемся на изолированный системный стек Ядра ОС
    LXI  H, 0
    DAD  SP
    LXI  SP, KERNEL_STACK
    PUSH H                      ; Сохранили пользовательский SP на стеке ядра
    
    ; Фиксируем ошибку EPERM в PCB нарушителя
    CALL get_current_pcb_address
    LXI  D, O_SYSCALL_RES
    DAD  D
    MVI  M, 0E4H                ; Запись кода 0xE4 (EPERM)
    
    ; Назначаем код фатального уничтожения задачи = 11 (Нарушение защиты)
    MVI  A, 0BH                  
    
    ; --- ИНСТРУКЦИЯ OUT 076H УДАЛЕНА: СБРОС ПРОИСХОДИТ АППАРАТНО ПО ~{INTA} ---
    
    ; Сброс вектора прерывания IR7 в КР580ВН59 (Неспецифический EOI)
    MVI  A, 020H
    OUT  MASTER_8259_CMD 
    
    ; Уходим в освобождение ОЗУ/портов и перезапуск планировщика
    JMP  sys_exit_fatal          

; =============================================================================
; ВСПОМОГАТЕЛЬНЫЕ СИСТЕМНЫЕ ЗАГЛУШКИ И СЛОТЫ ДАННЫХ
; =============================================================================
get_current_pcb_address:
    LDA CURRENT_PID
	MOV L, A
	MVI H, 0
    DAD H
	DAD H
	DAD H
	DAD H
	DAD H
	DAD H
	LXI D, PCB_TABLE
	DAD D
	RET

; =============================================================================
; СИСТЕМНЫЙ ПЕРЕХВАТЧИК СБОЕВ Ring 3: ЗАГЛУШКА НЕОБРАБОТАННЫХ ЛОВУШЕК (RST 1-5, 7)
; =============================================================================
rst_stub:
    ; Вход: Сюда процессор КР580ВМ80А попадает аппаратно при вызове нелегального RST.
    ;       В стеке задачи Ring 3 лежит адрес PC, где произошел сбой.
    ;       Внимание: Мы все еще находимся в пользовательском режиме Ring 3!
    
    ; Мы не можем напрямую вызвать системные таблицы ядра, так как eMMU еще 
    ; транслирует память пользователя. Поэтому мы принудительно вызываем 
    ; легитимный системный трап SYS_EXIT, чтобы ядро уничтожило эту задачу!
    
    DI                          ; Запрещаем прерывания таймера на время паники
    
    ; Загружаем в регистр C код легитимного системного вызова SYS_EXIT (1)
    MVI  C, 1                   ; SYS_EXIT EQU 1
    
    ; Загружаем в аккумулятор A код возврата фатального сбоя POSIX
    ; Стандартный exit-код при падении по ошибке сегментации/RST = 139 (08BH)
    MVI  A, 139                 ; 139 = 128 + SIGSEGV (Аварийное завершение)
    
    ; Нам нужно передать этот код возврата ядру через регистр C, но в C уже лежит 
    ; номер сисколла (1). Поэтому, согласно нашему соглашению о системных вызовах,
    ; пользовательский параметр exit-кода передается в регистре B (или аккумуляторе).
    MOV  B, A                   ; Переносим код паники 139 в регистр B для sys_exit
    
    ; Официально вызываем программную ловушку Ядра, которая переключит нас в Ring 0
    RST  6                      ; Прыгаем на sys_call_entry
    
    ; Сюда процессор физически никогда не вернется, так как ядро внутри sys_exit
    ; полностью освободит память текущей задачи и переключит eMMU на другой PID.

    ; Финальная аппаратная петля безопасности на случай тотального краха ОС
    HLT
    JMP  rst_stub

; =============================================================================
; УНИВЕРСАЛЬНАЯ АППАРАТНАЯ ЗАГЛУШКА ЯДРА (СЛОТЫ IR2 - IR6 И ЛОЖНЫЕ ТИКИ)
; =============================================================================
hw_stub:
    ; Вход: Сюда процессор прыгает при срабатывании пустых IRQ,
    ;       Либо при перенаправлении ложного тика IR7 из Firewall.
    ; Внимание: Если мы пришли сюда по ложному тику с адреса 0x007C (после JZ),
    ; регистры и стек Ring 3 находятся в идеальной чистоте, PUSH-данных нет!
    
    PUSH PSW                    ; Сохраняем аккумулятор на время отправки EOI
    
    ; Важнейшая физическая особенность КР580ВН59 (8259A):
    ; Ложные прерывания по умолчанию НЕ выставляют соответствующий бит в регистре ISR.
    ; Поэтому посылать команду EOI (End of Interrupt) для ложного тика НЕ ТРЕБУЕТСЯ,
    ; так как контроллер прерываний считает, что он никого не обслуживает.
    ; Однако, если мы пришли сюда от легитимных, но пустых аппаратных линий IR2-IR6,
    ; их биты в ISR взведены, и сброс EOI строго обязателен!
    
    ; Проверим, как именно мы сюда попали: если ISR пуст — это ложный тик
    MVI  A, 00BH
    OUT  MASTER_8259_CMD
    IN   MASTER_8259_CMD        ; Читаем актуальное состояние регистра ISR
    CPI  00H
    JZ   hw_stub_exit           ; Если ISR полностью пуст — это ложный тик, EOI не шлем

    ; Это легитимное пустое прерывание от линий IR2-IR6 — шлем неспецифический EOI
    MVI  A, 020H
    OUT  MASTER_8259_CMD

hw_stub_exit:
    POP  PSW                    ; Восстановили аккумулятор
    EI                          ; Снова разрешаем аппаратную карусель прерываний в Ring 3
    RET                         ; Возврат к прерванному кодовой строке пользователя

; =============================================================================
; ВНУТРЕННЯЯ ПРОЦЕДУРА ЯДРА: ПРИНУДИТЕЛЬНОЕ УНИЧТОЖЕНИЕ ПРОЦЕССА (АВАРИЙНЫЙ EXIT)
; =============================================================================
sys_exit_fatal:
    ; Вход: A = POSIX-код причины смерти процесса (09H - SIGKILL, 0BH - Исключение eMMU)
    MOV  C, A                   ; Сохраняем код завершения в регистр C

    ; --- ШАГ 1: Фиксация статуса ST_ZOMBIE и кода возврата ---
    CALL get_current_pcb_address ; HL = базовый адрес PCB текущей задачи
    
    PUSH H
    LXI  D, O_STATUS
	DAD D
    MVI  M, ST_ZOMBIE           ; Переводим процесс в состояние "Зомби" (05H)
    
    POP  H
	PUSH H
    LXI  D, O_SYSCALL_RES
	DAD D
    MOV  M, C                   ; Записываем код завершения (причину смерти)
    POP  H

    ; --- ШАГ 2: Освобождение захваченных процессом I/O портов ---
    LXI  H, GLOBAL_RESOURCE_TABLE
    MVI  B, 0                   ; Счетчик портов от 00H до 0FFH
    LDA  CURRENT_PID
	MOV C, A ; C = PID для сравнения
exit_fatal_ports_loop:
    MOV  A, M
	CMP C
    JNZ  exit_fatal_skip_port
    MVI  M, RES_FREE_MARKER     ; Освобождаем порт (0xFF)
exit_fatal_skip_port:
    INX  H
	INR B
	MOV A, B
	CPI 0
	JNZ exit_fatal_ports_loop

    ; --- ШАГ 3: Освобождение физических страниц ОЗУ процесса (Сегменты 0-3) ---
    CALL get_current_pcb_address
    LXI  D, O_PAGES_MAP
	DAD D ; HL указывает на начало карты физ. страниц
    MVI  B, 4                   ; У процесса ровно 4 базовых физических сегмента
exit_fatal_ram_loop:
    MOV  A, M
	CPI RES_FREE_MARKER
	JZ exit_fatal_skip_ram
    PUSH B
	PUSH H
    CALL free_physical_frame_page ; Освобождаем фрейм в аллокаторе ядра
    POP  H
	POP  B
    MVI  M, RES_FREE_MARKER     ; Затираем страницу в карте процесса
exit_fatal_skip_ram:
    INX  H
	DCR B
	JNZ exit_fatal_ram_loop

    ; --- ШАГ 4: Очистка системного стека и загрузка нового контекста ---
    LXI  SP, KERNEL_STACK       ; Полный сброс и очистка стека супервизора
    
    CALL scheduler              ; Запуск планировщика, он выберет лучший ST_READY PID в Аккумулятор
    STA  NEXT_PID
    
    CALL switch_mmu_context     ; Чистый символьный вызов функции перепрошивки eMMU!

    ; Восстанавливаем указатель стека и регистры выбранной планировщиком задачи
    CALL get_current_pcb_address
    LXI  D, O_SAVED_SP
	DAD D
    MOV  E, M
	INX H
	MOV D, M
	XCHG 
	SPHL ; Процессор переключен на стек Ring 3 новой задачи
    
    MVI  A, 020H
	OUT MASTER_8259_CMD ; Команда EOI в контроллер ВН59
    POP  H
	POP  D
	POP  B
	POP  PSW    ; Извлекаем регистры задачи из её личного стека
    RET                         ; Выход в Ring 3 через аппаратный RET-строб платы eMMU


; =============================================================================
; ВНУТРЕННЯЯ ФУНКЦИЯ ЯДРА: ФИЗИЧЕСКАЯ ПЕРЕПРОШИВКА КОНТЕКСТА eMMU
; =============================================================================
switch_mmu_context:
    ; Вход: NEXT_PID = PID процесса, на который переключаемся
    ; Выход: CURRENT_PID обновлен, порты 0x70-0x73 прошиты новой картой страниц
    
    LDA  NEXT_PID
    STA  CURRENT_PID            ; Новый процесс официально становится текущим
    
    ; Вычисляем адрес его PCB: PCB_TABLE + (PID * 64)
    MOV  L, A
    MVI  H, 0
    DAD  H
	DAD  H
	DAD  H
	DAD  H
	DAD  H
	DAD  H ; PID * 64
    LXI  D, PCB_TABLE
    DAD  D                      ; HL = базовый адрес PCB новой задачи

    ; Переводим статус новой задачи в состояние ST_RUNNING (Выполняется)
    PUSH H
    LXI  D, O_STATUS
    DAD  D
    MVI  M, ST_RUNNING
    POP  H

    ; Аппаратная перепрошивка 4 регистров К555ИР26 из массива O_PAGES_MAP (+8)
    LXI  D, O_PAGES_MAP
    DAD  D                      ; HL указывает на базовые 4 страницы памяти процесса
    
    MOV  A, M
    OUT  070H                   ; Прошивка Сегмента 0 (0x0000 - 0x3FFF) пользовательского ОЗУ
    
    INX  H
    MOV  A, M
    OUT  071H                   ; Прошивка Сегмента 1 (0x4000 - 0x7FFF) пользовательского ОЗУ
    
    INX  H
    MOV  A, M
    OUT  072H                   ; Прошивка Сегмента 2 (0x8000 - 0xBFFF) пользовательского ОЗУ
    
    INX  H
    MOV  A, M
    OUT  073H                   ; Прошивка Сегмента 3 (0xC000 - 0xFFFF) пользовательского стека
    
    RET

; =============================================================================
; ВОЗВРАТ С ОШИБКОЙ ПРИ ОТСУТСТВИИ СИСТЕМНОГО ВЫЗОВА
; =============================================================================

syscall_nosys:
    MVI  A, ENOSYS               ; Возвращает ошибку 0xE5 (Функция не реализована)
    RET

; =============================================================================
; ПРОГРАММНЫЙ ВЕКТОР RST 6 (Адрес 0x0030): ТОЧКА ВХОДА SYSCALL ENTRY
; =============================================================================
sys_call_entry:
    ; === ФАЗА 1: ТЕНЕВОЕ СОХРАНЕНИЕ ПОЛЬЗОВАТЕЛЬСКОГО КОНТЕКСТА ===
    ; В SP сейчас стек Ring 3 вызывающей задачи. eMMU уже забросил туда PC.
    PUSH PSW                    ; Сохраняем Аккумулятор и Флаги (A, F) в стек Ring 3
    PUSH B                      ; Сохраняем регистры B, C задачи в стек Ring 3
    PUSH D                      ; Сохраняем регистры D, E задачи в стек Ring 3
    PUSH H                      ; Сохраняем регистры H, L задачи в стек Ring 3

    ; Переключаемся на изолированный системный стек Ядра ОС
    LXI  H, 0
    DAD  SP                     ; HL = текущий упакованный SP процесса
    LXI  SP, KERNEL_STACK       ; Загружаем чистый изолированный стек ядра ОС
    PUSH H	                    ; Временно прячем пользовательский SP в стек ядра

    ; Вычисляем базовый адрес PCB текущей задачи: База = PCB_TABLE + (PID * 64)
    LDA  CURRENT_PID
    MOV  L, A
    MVI  H, 0
    ; 6 сдвигов влево для быстрого умножения PID на 64 (PCB_SIZE)
    DAD  H
	DAD  H
	DAD  H
	DAD  H
	DAD  H
	DAD  H  ; HL = PID * 64
    LXI  D, PCB_TABLE
    DAD  D                      ; HL = PCB_TABLE + (PID * 64) -> Базовый адрес PCB

    ; Прибавляем смещение O_SAVED_SP (+3) строго последовательно
    LXI  D, O_SAVED_SP          ; DE = смещение до поля (0003H)
    DAD  D                      ; HL = База_PCB + O_SAVED_SP
    
    POP  D                      ; DE = извлеченный из стека ядра пользовательский SP
    MOV  M, E                   ; Запись младшего байта SP
    INX  H
    MOV  M, D                   ; Запись старшего байта SP

    ; === ФАЗА 2: ДЕШИФРАЦИЯ И АВТОРИЗАЦИЯ СИС-ВЫЗОВА ===
    ; Номер системного вызова передан в регистре C
    MOV  A, C
    CPI  MAX_SYSCALL_NUM + 1    ; Проверяем границы таблицы системных вызовов
    JNC  syscall_bad_number     ; Если номер больше лимита — возвращаем ENOSYS

    ; Вычисляем адрес перехода по таблице указателей: Смещение = Номер * 2
    MOV  L, C
    MVI  H, 0
    DAD  H                      ; HL = Номер Syscall * 2
    LXI  D, SYSCALL_TABLE
    DAD  D                      ; HL указывает точно на ячейку DW в таблице
    MOV  E, M
    INX  H
    MOV  D, M                   ; DE = физический адрес функции syscall в ядре

    ; Передаем управление на функцию ядра. 
    ; Поскольку параметры от пользователя пришли в DE и HL задачи, а мы их 
    ; теневым образом сохранили в стек, при необходимости функция ядра может 
    ; извлечь их оттуда или прочитать напрямую, если они не затерлись при расчете.
    ; Имитируем вызов CALL через стек:
    PUSH D	                    ; Забрасываем адрес функции ядра в стек
    LXI  H, syscall_return      ; Регистрируем точку возврата после исполнения
    PUSH H
    PCHL                        ; Программный прыжок на исполнение системной функции

syscall_return:
    ; Системная функция выполнилась и вернула код результата в Аккумуляторе (A)
    MOV  C, A                   ; Временно спасем код результата в C
    
    ; Записываем результат выполнения в поле O_SYSCALL_RES (+5) текущего PCB
    CALL get_current_pcb_address ; HL = базовый адрес PCB текущей задачи
    LXI  D, O_SYSCALL_RES
    DAD  D                      ; HL указывает на O_SYSCALL_RES
    MOV  M, C                   ; Результат системного вызова жестко зафиксирован

syscall_exit:
    ; === ФАЗА 3: ВОССТАНОВЛЕНИЕ КОНТЕКСТА И ВЫХОД В USER SPACE ===
    CALL get_current_pcb_address
    LXI  D, O_SAVED_SP
    DAD  D                      ; HL указывает на сохраненный SP
    MOV  E, M
    INX  H
    MOV  D, M                   ; DE = восстановленный SP задачи
    XCHG                        ; HL = новый SP
    SPHL                        ; Процессор переключился обратно на стек Ring 3

    ; Извлекаем теневые регистры из стека Ring 3 пользовательского пространства
    POP  H                      ; Восстановили H, L задачи
    POP  D                      ; Восстановили D, E задачи
    POP  B                      ; Восстановили B, C задачи
    POP  PSW                    ; Восстановили Аккумулятор и Флаги (A, F) задачи
    
    ; На вершине стека Ring 3 остался лежать только PC
    RET                         ; Переход обратно в задачу. Аппаратный сигнал ~{RET_STROBE} 
                                ; на плате сбросит KERNEL_MODE обратно в Ring 3!

syscall_bad_number:
    MVI  A, 0E5H                ; Код ошибки: ENOSYS (Функция не реализована)
    JMP  syscall_return

; =============================================================================
; POSIX СИС-ВЫЗОВ №3: SYS_REQUEST_IO — МОНОПОЛЬНЫЙ ЗАХВАТ ПОРТА ВВОДА-ВЫВОДА
; =============================================================================
sys_request_io:
    ; Вход: C = запрашиваемый номер аппаратного порта/устройства (00H - 0FFH)
    ; Выход: A = 00H (УСПЕХ), A = 0E6H (КОД ОШИБКИ EBUSY — Ресурс занят)
    
    MOV  L, C
    MVI  H, 0
    LXI  D, GLOBAL_RESOURCE_TABLE
    DAD  D                      ; HL = точный адрес порта в таблице ядра
    
    MOV  A, M                   ; Считываем текущего владельца из таблицы
    CPI  RES_FREE_MARKER        ; Свободен ли порт (0xFF)?
    JNZ  io_port_busy           ; Если там чужой PID — порт занят, отлуп!
    
    ; Порт свободен, закрепляем его за текущим вызывающим процессом
    LDA  CURRENT_PID
    MOV  M, A                   ; Записываем CURRENT_PID в ячейку таблицы ресурсов
    MVI  A, 00H                 ; Код возврата: Успех
    RET

io_port_busy:
    MVI  A, 0E6H                ; Код ошибки: EBUSY (Ресурс занят другим процессом)
    RET

; =============================================================================
; POSIX СИС-ВЫЗОВ №4: SYS_RELEASE_IO — ОСВОБОЖДЕНИЕ ПОРТА ВВОДА-ВЫВОДА
; =============================================================================
sys_release_io:
    ; Вход: C = освобождаемый номер аппаратного порта (00H - 0FFH)
    ; Выход: A = 00H (УСПЕХ), A = 0E4H (КОД ОШИБКИ EPERM — Вы не владелец этого порта)
    
    MOV  L, C
    MVI  H, 0
    LXI  D, GLOBAL_RESOURCE_TABLE
    DAD  D                      ; HL = точный адрес порта в таблице ядра
    
    MOV  A, M                   ; Считываем из ОЗУ текущего владельца порта
    MOV  B, A                   ; Сохраняем владельца в B
    LDA  CURRENT_PID            ; Читаем PID вызывающей задачи
    CMP  B                      ; Вы являетесь легитимным владельцем этого устройства?
    JNZ  io_release_error       ; Если нет — чужой процесс не имеет права сбрасывать чужую периферию
    
    MVI  M, RES_FREE_MARKER     ; Записываем 0xFF — порт снова свободен для всей системы
    MVI  A, 00H                 ; Код возврата: Успех
    RET

io_release_error:
    MVI  A, 0E4H                ; Код ошибки: EPERM (Операция запрещена, недостаточно прав)
    RET

; =============================================================================
; POSIX СИС-ВЫЗОВ №0: SYS_FORK — ПОРОЖДЕНИЕ И ДУБЛИРОВАНИЕ ПРОЦЕССА С DIRTY BIT
; =============================================================================
sys_fork:
    ; Вход: Параметры отсутствуют. Контекст родителя сохранен в его PCB.
    ; Выход в родительский процесс: A = PID потомка (или код ошибки)
    ; Выход в процесс-потомок:      A = 00H (Абсолютный успех)

    ; --- ШАГ 1: ПОИСК СВОБОДНОГО СЛОТА В PCB_TABLE (Лимит 254) ---
    LXI  H, PCB_TABLE
    MVI  E, 1                   ; E = Тестируемый PID потомка (начиная с 1)
find_free_pid_loop:
    PUSH H                      ; Настоящий PUSH H (пары HL) для процессора 8080
    LXI  D, O_STATUS
    DAD  D                      ; HL указывает на поле O_STATUS тестируемого PCB
    MOV  A, M
    CPI  ST_FREE                ; Слот свободен (00H)?
    JZ   free_pid_found         ; Если да — захватываем этот PID!
    
    POP  H                      ; Настоящий POP H для процессора 8080
    LXI  D, PCB_SIZE            ; Шаг 64 байта
    DAD  D
    INR  E                      ; Инкремент тестируемого PID
    MOV  A, E
    CPI  MAX_PROCS + 1          ; Дошли до лимита 254? (255)
    JNZ  find_free_pid_loop
    
    ; Свободных слотов нет — возвращаем POSIX ошибку EAGAIN
    MVI  A, 0EAH                ; Код ошибки: EAGAIN (Превышен лимит процессов в системе)
    RET

free_pid_found:
    POP  H                      ; Восстанавливаем базовый адрес PCB найденного потомка
    MOV  A, E
    STA  FORK_CHILD_PID         ; Временно сохраняем выделенный PID потомка
    SHLD FORK_CHILD_PCB         ; Временно сохраняем адрес PCB потомка

    ; --- ШАГ 2: АЛЛОКАЦИЯ 4 ФИЗИЧЕСКИХ СТРАНИЦ ОЗУ ДЛЯ ПОТОМКА ---
    MVI  C, 0                   ; C = Индекс логического сегмента (0..3)
alloc_child_ram_loop:
    PUSH B
    CALL alloc_physical_frame_page ; Вызов аллокатора ядра. Возвращает фрейм в A
    POP  B
    CPI  0FFH                   ; Ошибка аллокатора ОЗУ (нет свободных страниц памяти)?
    JZ   fork_out_of_memory     ; Уходим на экстренный откат ресурсов
    
    ; Сохраняем выделенную страницу во временный массив ядра
    MOV  L, C
    MVI  H, 0
    LXI  D, FORK_TEMP_PAGES
    DAD  D
    MOV  M, A                   ; Номер физического фрейма сохранен
    
    INR  C
    MOV  A, C
    CPI  4                      ; Выделили все 4 базовые страницы памяти по 16 КБ?
    JNZ  alloc_child_ram_loop

    ; --- ШАГ 3: ЗЕРКАЛЬНОЕ ПОБАЙТОВОЕ КОПИРОВАНИЕ ПАМЯТИ (Сегменты 0-3) ---
    ; Активируем режим KERNEL_OVERRIDE для прямого доступа к физическим адресам ОЗУ
    OUT  076H                   ; Сигнал ~{KERNEL_OVERRIDE_EN} = 0 (Шина разблокирована)
    
    MVI  C, 0                   ; C = Текущий копируемый сегмент (0..3)
copy_ram_segment_loop:
    ; Вычисляем логический базовый адрес текущего сегмента (0x0000, 0x4000, 0x8000, 0xC000)
    MOV  A, C
    RRC
    RRC                         ; Сдвиг в старшие 2 бита аккумулятора (C * 64)
    ANI  0C0H                   ; Маска старшего байта адреса
    MOV  H, A
    MVI  L, 0                   ; HL = Начальный логический адрес сегмента (например, 0x8000)
    SHLD FORK_SRC_PTR           ; Указатель источника (Память Родителя)

    ; Перепрошиваем Сегмент 2 eMMU (порт 0x72), подставляя туда физическую страницу ПОТОМКА
    MOV  L, C
    MVI  H, 0
    LXI  D, FORK_TEMP_PAGES
    DAD  D
    MOV  A, M                   ; A = Физическая страница потомка для сегмента C
    OUT  072H                   ; Временно спроецировали ОЗУ потомка в окно 0x8000-0xBFFF
    
    LXI  D, 08000H              ; DE = Базовый адрес окна Сегмента 2 (Назначение)
    SHLD FORK_DST_PTR

    ; Цикл побайтового копирования 16 КБ фрейма памяти
    LXI  B, 16384               ; Счетчик на 16 384 байт
copy_bytes_16k_loop:
    ; Читаем байт из родного логического сегмента Родителя
    LHLD FORK_SRC_PTR
    MOV  A, M
    INX  H
    SHLD FORK_SRC_PTR
    
    ; Записываем байт в спроецированное окно Сегмента 2 Потомка
    XCHG
    MOV  M, A
    INX  H
    XCHG
    
    DCX  B
    MOV  A, B
    ORA  C
    JNZ  copy_bytes_16k_loop

    ; Возвращаем eMMU Сегмента 2 в исходное состояние (восстанавливаем страницу Родителя)
    CALL get_current_pcb_address
    LXI  D, O_PAGES_MAP + 2     ; Читаем, какая физ. страница была у Родителя в Сегменте 2
    DAD  D
    MOV  A, M
    OUT  072H                   ; Вернули память родителя на место
    
    INR  C
    MOV  A, C
    CPI  4
    JNZ  copy_ram_segment_loop
    
    OUT  076H                   ; Отключаем режим KERNEL_OVERRIDE, возвращаем Ring 0 трансляцию

    ; --- ШАГ 4: ДУБЛИРОВАНИЕ И МОДИФИКАЦИЯ СТРУКТУРЫ PCB ---
    CALL get_current_pcb_address ; HL = Адрес PCB Родителя (Источник)
    LHLD FORK_CHILD_PCB         ; DE = Адрес PCB Потомка (Назначение)
    XCHG                        ; Теперь HL = Потомок, DE = Родитель
    
    MVI  B, PCB_SIZE            ; Копируем ровно 64 байта структуры
copy_pcb_bytes_loop:
    LDAX D
    MOV  M, A
    INX  D
    INX  H
    DCR  B
    JNZ  copy_pcb_bytes_loop

    ; Накатываем новые индивидуальные параметры на PCB потомка
    LHLD FORK_CHILD_PCB
    LDA  FORK_CHILD_PID
    MOV  M, A                   ; Исправляем O_PID (+0) в структуре потомка
    
    INX  H
    LDA  CURRENT_PID
    MOV  M, A                   ; Записываем O_PPID (+1) = PID родителя
    
    INX  H
    MVI  M, ST_READY            ; Выставляем O_STATUS (+2) = Задача готова к исполнению!

    ; Прописываем потомку его новые физические страницы памяти в поле O_PAGES_MAP (+8)
    LHLD FORK_CHILD_PCB
    LXI  D, O_PAGES_MAP
    DAD  D                      ; HL указывает на карту страниц потомка
    LXI  D, FORK_TEMP_PAGES
    MVI  B, 4
write_child_maps_loop:
    LDAX D
    MOV  M, A
    INX  D
    INX  H
    DCR  B
    JNZ  write_child_maps_loop

    ; --- КРИТИЧЕСКИЙ УЗЕЛ ДЛЯ FORK: МАРКИРОВКА ВСЕХ СТРАНИЦ ПОТОМКА ГРЯЗНЫМИ ---
    LHLD FORK_CHILD_PCB
    LXI  D, O_PAGE_DIRTY_MASK
    DAD  D                      ; HL указывает на поле O_PAGE_DIRTY_MASK (+41) потомка
    MVI  M, 0FFH                ; Выставляем маску 0xFF (все страницы изменены в ОЗУ)

    ; --- ШАГ 5: РАЗДЕЛЕНИЕ ТОЧЕК ВОЗВРАТА СИС-ВЫЗОВА ---
    ; Для потомка: результат сисколла должен быть равен 00H
    LHLD FORK_CHILD_PCB
    LXI  D, O_SYSCALL_RES
    DAD  D
    MVI  M, 00H                 ; Записали 0 в результат вызова для потомка

    ; Увеличиваем глобальный счетчик активных задач в системе
    LDA  PROCS_COUNT
    INR  A
    STA  PROCS_COUNT

    ; Для родителя: результатом вызова является PID его нового потомка
    LDA  FORK_CHILD_PID         ; Возвращаем PID ребенка в Аккумуляторе родителю
    RET

; =============================================================================
; АППАРАТНЫЙ ОТКАТ ПРИ НЕХВАТКЕ ПАМЯТИ (FORK OUT OF MEMORY BACKEND)
; =============================================================================
fork_out_of_memory:
    ; Сюда система уходит, если память кончилась посреди аллокации (Откат назад)
    MVI  B, 4
    LXI  H, FORK_TEMP_PAGES
free_allocated_on_failure:
    MOV  A, M
    CPI  RES_FREE_MARKER        ; Была ли выделена эта страница?
    JZ   fork_failure_done      ; Если встретили 0xFF — этот и последующие фреймы не выделялись
    
    PUSH B
    PUSH H                      ; Легитимный PUSH H для КР580ВМ80А
    CALL free_physical_frame_page ; Возвращаем фрейм в пул свободных фреймов ядра
    POP  H                      ; Легитимный POP H для КР580ВМ80А
    POP  B
    
    MVI  M, RES_FREE_MARKER     ; Сбрасываем ячейку временного массива в исходные 0xFF
    INX  H
    DCR  B
    JNZ  free_allocated_on_failure

fork_failure_done:
    MVI  A, 0EBH                ; Код ошибки: ENOMEM (Недостаточно физической памяти для fork)
    RET

; =============================================================================
; POSIX СИС-ВЫЗОВ №8: SYS_WRITE — ВЫВОД ДАННЫХ В ДЕСКРИПТОР (ОБРАБОТКА tty0)
; =============================================================================
sys_write:
    ; Вход: D = Номер дескриптора файла (1 = stdout, 2 = stderr)
    ;       HL = Логический адрес буфера с данными в Ring 3 процесса
    ;       BC = Количество байт для записи (16-битный счетчик)
    ; Выход: A = Количество успешно записанных байт (или код ошибки)

    ; --- ШАГ 1: Валидация дескриптора файла ---
    MOV  A, D
    CPI  8                      ; Всего доступно 8 дескрипторов (0..7)
    JNC  write_bad_fd           ; Если FD >= 8 — возвращаем ошибку EBADF

    ; Проверяем, на какое устройство направлен дескриптор в O_FILE_TABLE (+20)
    MOV  B, A                   ; Сохраняем FD в регистр B
    CALL get_current_pcb_address
    LXI  D, O_FILE_TABLE
    DAD  D                      ; HL указывает на ячейку дескриптора
    MOV  A, M                   ; A = Системный индекс открытого файла ядра
    CPI  RES_FREE_MARKER        ; Дескриптор закрыт (0xFF)?
    JZ   write_bad_fd           ; Если закрыт — отлуп
    
    ; В нашей базовой конфигурации индекс 00H зарезервирован за консолью /dev/tty0
    CPI  00H
    JNZ  write_to_file_stub     ; Если индекс не 0 — уходим на заглушку файловой системы

    ; --- ШАГ 2: Подготовка конвейера записи в TTY ---
    ; Восстанавливаем параметры, переданные пользователем. 
    ; Поскольку мы внутри syscall_entry, оригинальные регистры HL и BC 
    ; были сохранены на пользовательском стеке, но их копии остались в ядре.
    ; На входе в функцию: BC = длина, HL = адрес пользовательского буфера.
    
    XRA  A
    STA  WRITE_BYTES_COUNT      ; Обнуляем счетчик успешно записанных байт

write_main_loop:
    ; Проверяем, не исчерпан ли счетчик байт пользователя (BC == 0?)
    MOV  A, B
    ORA  C
    JZ   write_success_exit     ; Если все байты перенесены — выходим с успехом

    ; Проверяем, есть ли место в системном кольцевом буфере передачи TTY_TX_BUF
    LDA  TTY_TX_HEAD
    MOV  E, A                   ; E = Текущая голова буфера передачи
    INR  A
    ANI  TTY_BUF_SIZE - 1       ; Ограничение по маске размера (63)
    MOV  D, A                   ; D = Предполагаемая новая голова
    
    LDA  TTY_TX_TAIL            ; Проверяем: Новая голова == Хвост?
    CMP  D
    JZ   write_buffer_full      ; Если буфер ядра полон — усыпляем процесс

    ; --- ШАГ 3: Чтение байта из Ring 3 через KERNEL_OVERRIDE ---
    PUSH B                      ; Спасаем счетчик длин
    PUSH D                      ; Спасаем указатели голов буфера передачи
    
    OUT  076H                   ; Включаем ~{KERNEL_OVERRIDE_EN} = 0 для чтения памяти задачи
    MOV  A, M                   ; Читаем символ напрямую из логического пространства Ring 3!
    OUT  076H                   ; Мгновенно отключаем оверрайд, возвращая безопасность Ring 0
    
    MOV  B, A                   ; Сохраняем извлеченный символ в регистр B
    INX  H                      ; Сдвигаем указатель пользовательского буфера вперед
    SHLD WRITE_TEMP_HL          ; Временно прячем пользовательский HL

    ; Записываем символ в системный буфер передачи ядра: TTY_TX_BUF + HEAD
    POP  D                      ; Восстанавливаем D = новая голова, E = текущая голова
    MOV  L, E
    MVI  H, 0
    PUSH D
    LXI  D, TTY_TX_BUF
    DAD  D                      ; HL = Точный физический адрес записи в ОЗУ ядра
    MOV  M, B                   ; Символ успешно скопирован в буфер ядра!
    POP  D
    
    MOV  A, D
    STA  TTY_TX_HEAD            ; Обновляем указатель TTY_TX_HEAD новой головой

    ; Увеличиваем счетчик записанных байт
    LDA  WRITE_BYTES_COUNT
    INR  A
    STA  WRITE_BYTES_COUNT

    LHLD WRITE_TEMP_HL          ; Восстанавливаем пользовательский HL для следующей итерации
    POP  B                      ; Восстанавливаем счетчик длины BC
    DCX  B                      ; Уменьшаем количество оставшихся байт
    JMP  write_main_loop        ; Переходим к следующему символу

write_buffer_full:
    ; Буфер передачи ядра забит. Чтобы не потерять данные, мы усыпляем процесс,
    ; пока асинхронный обработчик прерываний hw_uart_entry не освободит место.
    ; ПРИМЕЧАНИЕ: Перед засыпанием мы обязаны пнуть передатчик UART, если он спал!
    CALL trigger_uart_tx
    
    ; Переводим текущий процесс в состояние ST_WAITING
    CALL get_current_pcb_address
    PUSH H
    LXI  D, O_STATUS
    DAD  D
    MVI  M, ST_WAITING          ; Статус = Ожидание
    
    POP  H
    LXI  D, O_WAIT_CODE
    DAD  D
    MVI  M, 02H                 ; Код сна = W_TTY_OUT (Ожидание освобождения tty)
    
    ; Вызываем принудительное переключение контекста, так как текущая задача заблокирована
    LXI  SP, KERNEL_STACK       ; Очищаем стек ядра
    CALL scheduler
    STA  NEXT_PID
    CALL switch_mmu_context
    JMP  restore_timer_context  ; Уходим в контекст другой готовой задачи

write_success_exit:
    ; Перед выходом проверяем: если передатчик простаивал, запускаем конвейер печати
    CALL trigger_uart_tx
    LDA  WRITE_BYTES_COUNT      ; Возвращаем количество записанных байт в Аккумуляторе
    RET

write_bad_fd:
    MVI  A, 0EBH                ; Код ошибки: EBADF (Невалидный дескриптор файла)
    RET

write_to_file_stub:
    MVI  A, 0E5H                ; Код ошибки: ENOSYS (Запись в файлы пока не реализована)
    RET

; =============================================================================
; ВНУТРЕННЯЯ ФУНКЦИЯ: АКТИВАЦИЯ КОНВЕЙЕРА ПЕРЕДАЧИ USART
; =============================================================================
trigger_uart_tx:
    ; Проверяем, пуст ли буфер передачи (HEAD == TAIL)?
    LDA  TTY_TX_HEAD
    MOV  B, A
    LDA  TTY_TX_TAIL
    CMP  B
    RZ                          ; Если буфер пуст — активировать нечего, выходим
    
    ; Проверяем, готов ли сам чип КР580ВВ51А принять байт прямо сейчас?
    IN   UART_CMD
    ANI  01H                    ; Изолируем бит 0 (TxRDY БИС)
    RZ                          ; Если БИС занята (0) — она сама сгенерирует прерывание IR1, когда освободится
    
    ; БИС свободна, но прерывания не идут, так как конвейер простаивал. 
    ; Извлекаем первый байт из TTY_TX_BUF по указателю TAIL и проталкиваем его принудительно.
    LDA  TTY_TX_TAIL
    MOV  L, A
    MVI  H, 0
    LXI  D, TTY_TX_BUF
    DAD  D
    MOV  A, M
    OUT  UART_DATA              ; Первичный аппаратный толчок передан чипу последовательного порта!
    
    ; Сдвигаем хвост буфера вперед
    LDA  TTY_TX_TAIL
    INR  A
    ANI  TTY_BUF_SIZE - 1
    STA  TTY_TX_TAIL
    RET
	
; =============================================================================
; ПОЛНОСТЬЮ ОБНОВЛЕННЫЙ СИС-ВЫЗОВ №7: SYS_READ С ИНТЕГРАЦИЕЙ DIRTY BIT
; =============================================================================
sys_read:
    ; Вход: D = Номер дескриптора файла (0 = stdin)
    ;       HL = Логический адрес буфера назначения в Ring 3 процесса
    ;       BC = Максимальное количество байт для чтения
    ; Выход: A = Количество успешно прочитанных байт (или код ошибки)

    MOV  A, D
    CPI  8                      ; Проверка границ (0..7)
    JNC  read_bad_fd            

    MOV  B, A                   
    CALL get_current_pcb_address
    LXI  D, O_FILE_TABLE
    DAD  D                      
    MOV  A, M                   ; Читаем привязку дескриптора в PCB
    CPI  RES_FREE_MARKER        
    JZ   read_bad_fd            
    
    CPI  00H
    JNZ  read_from_file_stub    ; Если не консоль — уходим на заглушку ФС

    ; Фиксируем стартовые параметры пользователя в памяти ядра
    SHLD READ_TEMP_HL
    XRA  A
    STA  READ_BYTES_COUNT       

read_check_buffer_empty:
    LDA  TTY_RX_HEAD
    MOV  E, A                   
    LDA  TTY_RX_TAIL            
    CMP  E                      
    JNZ  read_buffer_has_data   ; Если в асинхронном TTY_RX_BUF есть данные — читаем

    ; Буфер пуст — проверяем, вычитали ли мы хоть что-то ранее?
    LDA  READ_BYTES_COUNT
    CPI  0
    JNZ  read_success_exit      ; Если байты уже есть — отдаем их без блокировки процесса

    ; Процесс ничего не прочитал — отправляем его в ST_WAITING
    CALL get_current_pcb_address
    PUSH H
    LXI  D, O_STATUS
    DAD  D
    MVI  M, ST_WAITING          
    
    POP  H
    LXI  D, O_WAIT_CODE
    DAD  D
    MVI  M, W_TTY_IN            ; Причина сна — ожидание ввода клавиатуры

    LXI  SP, KERNEL_STACK
    CALL scheduler
    STA  NEXT_PID
    CALL switch_mmu_context
    JMP  restore_timer_context  ; Уходим в контекст другой задачи

read_buffer_has_data:
    ; Проверяем, не исчерпан ли лимит длины, запрошенный пользователем (BC == 0?)
    MOV  A, B
    ORA  C
    JZ   read_success_exit      

    ; Извлекаем символ из кольцевого TTY_RX_BUF ядра по указателю TAIL
    LDA  TTY_RX_TAIL
    MOV  E, A
    MVI  H, 0
    LXI  D, TTY_RX_BUF
    DAD  D
    MOV  A, M                   ; Акум A = Считанный символ
    STA  READ_CHAR_CHAR         ; Спрятали символ во временную ячейку

    ; Обновляем хвост циклического буфера приема ядра
    LDA  TTY_RX_TAIL
    INR  A
    ANI  TTY_BUF_SIZE - 1
    STA  TTY_RX_TAIL

    ; Запись символа в Ring 3 пользователя через KERNEL_OVERRIDE
    PUSH B                      ; Сохраняем счетчик длины BC в стек ядра
    LHLD READ_TEMP_HL           ; HL = Актуальный адрес записи в памяти задачи
    
    LDA  READ_CHAR_CHAR         ; Восстанавливаем символ
    OUT  076H                   ; Включаем ~{KERNEL_OVERRIDE_EN} = 0
    MOV  M, A                   ; Записываем символ в буфер пользовательского процесса
    OUT  076H                   ; Отключаем оверрайд шины

    ; --- КРИТИЧЕСКИЙ УЗЕЛ: МАРКИРОВКА СТРАНИЦЫ КАК "ГРЯЗНОЙ" ---
    CALL mark_user_page_dirty_by_addr ; HL анализируется на номер сегмента, взводится Dirty Bit!

    INX  H                      ; Сдвигаем адрес буфера Ring 3 вперед
    SHLD READ_TEMP_HL           ; Сохраняем измененный адрес обратно в ядро
    POP  B                      ; Восстанавливаем счетчик BC

    ; Инкремент общего счетчика прочитанных ядром байт
    LDA  READ_BYTES_COUNT
    INR  A
    STA  READ_BYTES_COUNT

    DCX  B                      ; Декремент пользовательского счетчика длины
    JMP  read_check_buffer_empty

read_success_exit:
    LDA  READ_BYTES_COUNT       
    RET

read_bad_fd:
    MVI  A, 0EBH                
    RET
read_from_file_stub:
    MVI  A, 0E5H                
    RET

; =============================================================================
; POSIX СИС-ВЫЗОВ №1: SYS_EXIT — ШТАТНОЕ ЗАВЕРШЕНИЕ РАБОТЫ ПРОЦЕССА
; =============================================================================
sys_exit:
    ; Вход: C = Пользовательский код возврата процесса (exit status)
    ; Выход: Функция никогда не возвращает управление в вызывающий контекст.
    
    MOV  A, C
    STA  EXIT_TEMP_CODE         ; Сохраняем код возврата во временную ячейку ядра

    ; --- ШАГ 1: Изменение статуса на ST_ZOMBIE и запись кода возврата ---
    CALL get_current_pcb_address ; HL = Базовый адрес PCB текущего процесса
    
    PUSH H                      ; Сохраняем базу PCB в стек (8080)
    LXI  D, O_STATUS
    DAD  D                      ; HL указывает на поле O_STATUS (+2)
    MVI  M, ST_ZOMBIE           ; Процесс официально становится Зомби (05H)
    
    POP  H
    PUSH H
    LXI  D, O_SYSCALL_RES
    DAD  D                      ; HL указывает на поле O_SYSCALL_RES (+5)
    LDA  EXIT_TEMP_CODE
    MOV  M, A                   ; Зафиксировали код возврата в PCB для waitpid
    
    POP  H                      ; Стек ядра чист, HL = База PCB текущей задачи

    ; --- ШАГ 2: Освобождение захваченных портов в GLOBAL_RESOURCE_TABLE ---
    LXI  H, GLOBAL_RESOURCE_TABLE
    MVI  B, 0                   ; Счетчик портов от 00H до 0FFH (256 итераций)
    LDA  CURRENT_PID
    MOV  C, A                   ; C = PID завершающегося процесса для сравнения

exit_ports_cleanup_loop:
    MOV  A, M                   ; Читаем владельца текущего порта B из таблицы
    CMP  C                      ; Этот порт принадлежал нашей задаче?
    JNZ  exit_skip_port_release
    
    MVI  M, RES_FREE_MARKER     ; Принудительно освобождаем порт, записывая 0xFF
    
exit_skip_port_release:
    INX  H                      ; Сдвиг на следующий порт в таблице ядра
    INR  B                      ; Инкремент номера порта
    MOV  A, B
    CPI  0                      ; Проверили все 256 портов? (перенос в 0)
    JNZ  exit_ports_cleanup_loop

    ; --- ШАГ 3: Освобождение физических фреймов памяти ОЗУ (Сегменты 0-3) ---
    CALL get_current_pcb_address
    LXI  D, O_PAGES_MAP
    DAD  D                      ; HL указывает на начало карты физ. страниц (+8)
    
    MVI  B, 4                   ; У процесса ровно 4 базовых физических сегмента
exit_ram_cleanup_loop:
    MOV  A, M                   ; A = Номер физической страницы памяти
    CPI  RES_FREE_MARKER        ; Выделена ли была страница?
    JZ   exit_skip_ram_release
    
    PUSH B
    PUSH H                      ; Валидный PUSH H для процессора 8080
    CALL free_physical_frame_page ; Возвращаем страницу в битовую карту аллокатора ОЗУ
    POP  H                      ; Валидный POP H для процессора 8080
    POP  B
    
    MVI  M, RES_FREE_MARKER     ; Стираем фрейм из карты страниц процесса
    
exit_skip_ram_release:
    INX  H                      ; Сдвиг на следующий сегмент карты
    DCR  B
    JNZ  exit_ram_cleanup_loop

    ; --- ШАГ 4: Поиск и побудка родительского процесса (POSIX waitpid) ---
    CALL get_current_pcb_address
    LXI  D, O_PPID
    DAD  D                      ; HL указывает на PPID текущего процесса
    MOV  A, M
    STA  EXIT_PARENT_PID        ; Запоминаем PID родителя

    LXI  H, PCB_TABLE
    MVI  E, 0                   ; E = Тестируемый PID (0..254)
exit_parent_wakeup_loop:
    PUSH H
    LXI  D, O_STATUS
    DAD  D                      ; HL указывает на O_STATUS тестируемой задачи
    MOV  A, M
    CPI  ST_WAITING             ; Процесс заблокирован?
    JNZ  exit_parent_next_slot
    
    ; Проверяем, совпадает ли этот спящий процесс с нашим родителем
    LDA  EXIT_PARENT_PID
    CMP  E
    JNZ  exit_parent_next_slot  ; Если это не наш родитель — идем дальше
    
    ; Проверяем, спит ли родитель по причине ожидания потомков (wait-channel)
    POP  H
    PUSH H
    LXI  D, O_WAIT_CODE
    DAD  D                      ; HL указывает на поле O_WAIT_CODE (+40)
    MOV  A, M
    CPI  03H                    ; Код 03H = W_CHILD (Ожидание завершения дочернего процесса)
    JNZ  exit_parent_next_slot
    
    ; Родитель найден и он ждал нас! Переводим его статус в ST_READY
    POP  H
    PUSH H
    LXI  D, O_STATUS
    DAD  D
    MVI  M, ST_READY            ; Родитель успешно возвращен в очередь планировщика!

exit_parent_next_slot:
    POP  H
    LXI  D, PCB_SIZE
    DAD  D
    INR  E
    MOV  A, E
    CPI  MAX_PROCS + 1
    JNZ  exit_parent_next_slot

    ; Уменьшаем глобальный счетчик активных задач в системе
    LDA  PROCS_COUNT
    DCR  A
    STA  PROCS_COUNT

    ; --- ШАГ 5: Очистка стека супервизора и смена контекста ---
    LXI  SP, KERNEL_STACK       ; Сбрасываем и очищаем стек ядра
    
    CALL scheduler              ; Запуск планировщика, он выберет следующий лучший ST_READY PID
    STA  NEXT_PID
    
    CALL switch_mmu_context     ; Перепрошиваем микросхемы К555ИР26 новой картой страниц

    ; Восстанавливаем указатель стека и регистры выбранной планировщиком задачи
    CALL get_current_pcb_address
    LXI  D, O_SAVED_SP
    DAD  D                      ; HL указывает на поле O_SAVED_SP
    MOV  E, M
    INX  H
    MOV  D, M
    XCHG                        ; DE указывает на сохраненный SP, переносим в HL
    SPHL                        ; Переключили SP процессора на стек Ring 3 новой задачи
    
    MVI  A, 020H
    OUT  MASTER_8259_CMD        ; Отправка EOI в контроллер КР580ВН59
    POP  H
    POP  D
    POP  B
    POP  PSW                    ; Восстановили регистры новой задачи из её стека
    RET                         ; Выход в Ring 3 через аппаратный RET-строб

; =============================================================================
; POSIX СИС-ВЫЗОВ №2: SYS_WAITPID — СИНХРОНИЗАЦИЯ И СБОР СТАТУСОВ ПОТОМКОВ
; =============================================================================
sys_waitpid:
    ; Вход: BC = Искомый PID дочернего процесса (0FFFFH - любой потомок)
    ;       HL = Логический адрес в Ring 3 для записи кода возврата status
    ; Выход: A = PID успешно обработанного потомка (или код ошибки)

    SHLD WAIT_USER_STATUS_PTR   ; Сохраняем пользовательский адрес указателя
    MOV  A, B
    STA  WAIT_TARGET_PID_H      ; Сохраняем старший байт искомого PID
    MOV  A, C
    STA  WAIT_TARGET_PID_L      ; Сохраняем младший байт искомого PID

waitpid_scan_start:
    XRA  A
    STA  WAIT_HAS_CHILDREN      ; Флаг: найден ли хотя бы один живой потомок?

    LXI  H, PCB_TABLE
    MVI  E, 1                   ; E = Тестируемый PID (проверяем слоты 1..254)

waitpid_loop:
    PUSH H                      ; Сохраняем базу текущего тестируемого PCB (8080)
    
    ; Шаг 1: Проверяем, свободен ли слот?
    LXI  D, O_STATUS
    DAD  D
    MOV  A, M
    CPI  ST_FREE
    JZ   waitpid_next_slot      ; Если слот свободен — пропускаем его
    
    ; Шаг 2: Проверяем, является ли текущая задача нашим потомком?
    POP  H
    PUSH H
    LXI  D, O_PPID
    DAD  D
    MOV  A, M
    MOV  B, A                   ; B = PPID тестируемой задачи
    LDA  CURRENT_PID            ; A = PID родителя
    CMP  B
    JNZ  waitpid_next_slot      ; Если мы не родитель — пропускаем слот
    
    ; Мы нашли потомка текущего процесса! Выставляем флаг
    MVI  A, 1
    STA  WAIT_HAS_CHILDREN

    ; Шаг 3: Проверяем, совпадает ли PID этого потомка с запрашиваемым?
    LDA  WAIT_TARGET_PID_L
    CPI  0FFH                   ; Запрошен любой потомок (младший байт 0xFF)?
    JZ   waitpid_check_zombie   ; Если любой — сразу переходим к проверке статуса
    
    LDA  WAIT_TARGET_PID_L      ; Иначе проверяем точное совпадение PID
    CMP  E
    JNZ  waitpid_next_slot      ; Если PID не совпал — продолжаем поиск

waitpid_check_zombie:
    ; Шаг 4: Проверим, завершился ли потомок (Находится ли в ST_ZOMBIE)?
    POP  H
    PUSH H
    LXI  D, O_STATUS
    DAD  D
    MOV  A, M
    CPI  ST_ZOMBIE
    JNZ  waitpid_next_slot      ; Если ребенок еще живой — продолжаем сканирование

    ; --- ПОТОМОК-ЗОМБИ НАЙДЕН! ОКОНЧАТЕЛЬНАЯ УТИЛИЗАЦИЯ И СБОР СТАТУСА ---
    POP  H
    PUSH H
    LXI  D, O_SYSCALL_RES
    DAD  D
    MOV  A, M                   ; A = Извлеченный exit code умершего потомка
    STA  WAIT_TEMP_EXIT_CODE

    ; Записываем статус в память Ring 3 родителя через KERNEL_OVERRIDE
    LHLD WAIT_USER_STATUS_PTR   ; HL = Указатель в пользовательском пространстве
    MOV  A, H
    ORA  L                      ; Проверяем, не передал ли пользователь NULL (0000H)?
    JZ   waitpid_skip_status_write ; Если NULL — запись статуса пропускается
    
    LDA  WAIT_TEMP_EXIT_CODE    ; Восстанавливаем код возврата
    OUT  076H                   ; Включаем ~{KERNEL_OVERRIDE_EN} = 0 для доступа к Ring 3
    MOV  M, A                   ; Физически пишем код возврата в память родителя!
    OUT  076H                   ; Мгновенно отключаем оверрайд

waitpid_skip_status_write:
    ; Окончательно уничтожаем структуру PCB потомка, освобождая слот для fork
    POP  H
    PUSH H
    LXI  D, O_STATUS
    DAD  D
    MVI  M, ST_FREE             ; ИСПРАВЛЕНО! Теперь канонический MVI M для 8080!
    
    POP  H
    MOV  A, E                   ; Возвращаем родителю PID успешно утилизированного потомка
    RET

waitpid_next_slot:
    POP  H
    LXI  D, PCB_SIZE
    DAD  D                      ; HL переходит на следующий слот PCB
    INR  E
    MOV  A, E
    CPI  MAX_PROCS + 1          ; Проверили все 254 процесса?
    JNZ  waitpid_loop

    ; --- ШАГ 5: ОБРАБОТКА ОТСУТСТВИЯ ГОТОВЫХ ЗОМБИ ---
    LDA  WAIT_HAS_CHILDREN
    CPI  0
    JZ   waitpid_no_children    ; Если вообще нет ни одного потомка — возвращаем ECHILD

    ; Потомки есть, но они все еще выполняются. Переводим родителя в состояние ожидания (Сон)
    CALL get_current_pcb_address
    PUSH H
    LXI  D, O_STATUS
    DAD  D
    MVI  M, ST_WAITING          ; Статус Родителя = Ожидание
    
    POP  H
    LXI  D, O_WAIT_CODE
    DAD  D
    MVI  M, 03H                 ; Код сна = W_CHILD (Ожидание завершения дочернего процесса)

    ; Сбрасываем текущий стек и принудительно переключаем процессор на другую готовую задачу
    LXI  SP, KERNEL_STACK
    CALL scheduler
    STA  NEXT_PID
    CALL switch_mmu_context
    JMP  restore_timer_context  ; Уходим в контекст другой задачи

waitpid_no_children:
    MVI  A, 0E9H                ; Код ошибки: ECHILD (У процесса нет дочерних задач)
    RET

; =============================================================================
; POSIX СИС-ВЫЗОВ №12: SYS_EXECVE — ЗАМЕНА ОБРАЗА ПРОЦЕССА НОВЫМ БИНАРНИКОМ С ИНТЕГРАЦИЕЙ DIRTY BIT
; =============================================================================
sys_execve:
    ; Вход: DE = Логический адрес строки пути к файлу в Ring 3
    ;       HL = Логический адрес массива аргументов argv в Ring 3
    ; Выход: В случае успеха функция НЕ возвращает управление в старый код.
    ;        В случае ошибки возвращает код ошибки в Аккумуляторе A.

    SHLD EXEC_ARGV_PTR          ; Сохраняем указатель на аргументы пользователя
    
    ; --- ШАГ 1: Поиск файла на CompactFlash-карте ---
    ; В реальном ядре здесь вызывается драйвер VFS/IDE для чтения инноды по пути из DE.
    ; Для компилируемости имитируем успешный поиск файла и получение его первого сектора.
    MVI  A, 042H                ; Предположим, файл найден, его стартовый сектор 0x42
    STA  EXEC_START_SECTOR

    ; --- ШАГ 2: Полная очистка пользовательской памяти (Сегменты 0 и 1) ---
    ; Мы сохраняем физические фреймы ОЗУ текущего процесса, но полностью затираем
    ; их содержимое перед загрузкой нового бинарного кода Ring 3.
    OUT  076H                   ; Включаем ~{KERNEL_OVERRIDE_EN} = 0
    
    LXI  H, 00000H              ; Начинаем с самого начала логического адреса (Сегмент 0)
    LXI  B, 32768               ; Зануляем ровно 32 КБ памяти (Сегмент 0 + Сегмент 1)
exec_clear_ram_loop:
    MVI  M, 00H                 ; Очищаем ячейку памяти пользователя
    INX  H
    DCX  B
    MOV  A, B
    ORA  C
    JNZ  exec_clear_ram_loop
    
    OUT  076H                   ; Отключаем режим оверрайда шины

    ; --- ШАГ 3: Посекторная загрузка кода с CompactFlash в ОЗУ задачи ---
    LDA  EXEC_START_SECTOR
    MOV  C, A                   ; C = Стартовый номер сектора на CF
    LXI  H, 00000H              ; HL = Целевой логический адрес загрузки (Ring 3)
    MVI  B, 16                  ; Предположим, бинарник занимает 16 секторов по 512 байт (8 КБ)

exec_load_sectors_loop:
    PUSH B
    PUSH H                      ; Сохраняем текущий адрес назначения в ОЗУ
    
    ; Вызываем аппаратную подсистему чтения жесткого диска IDE / CF-карты
    ; Вход: C = Номер сектора на CF-карте. Выход: Системный буфер ядра забит данными.
    CALL ide_read_sector_to_buf
    
    ; Переносим 512 байт из буфера ядра в пользовательское ОЗУ через оверрайд
    POP  H                      ; HL = Текущий адрес назначения в памяти задачи
    LXI  D, IDE_SYS_BUFFER      ; DE = Системный буфер данных ядра
    LXI  B, 512                 ; Счетчик на один сектор
    
    OUT  076H                   ; Включаем KERNEL_OVERRIDE
exec_copy_sector_loop:
    LDAX D
    MOV  M, A                   ; Копируем байт бинарника в ОЗУ Ring 3
    INX  D
    INX  H
    DCX  B
    MOV  A, B
    ORA  C
    JNZ  exec_copy_sector_loop
    OUT  076H                   ; Выключаем KERNEL_OVERRIDE
    
    POP  B                      ; Восстанавливаем счетчики секторов
    INR  C                      ; Переходим к следующему сектору на CF
    DCR  B                      ; Уменьшаем количество оставшихся секторов
    JNZ  exec_load_sectors_loop

    ; --- ШАГ 4: Подготовка чистого POSIX-стека на дне Сегмента 3 ---
    ; Нам необходимо переинициализировать стек Ring 3 (окно 0xC000-0xFFFF).
    ; Очищаем верхнюю границу памяти стека под argc/argv.
    OUT  076H                   ; Включаем оверрайд для настройки стека задачи
    
    LXI  H, 0FFF0H              ; HL = Базовая вершина нового стека задачи
    MVI  M, 001H                ; Записываем argc = 1 (дефолтное количество аргументов)
    INX  H
    MVI  M, 00H
    
    INX  H                      ; Смещение под argv[0]
    MVI  M, 000H                ; Прописываем указатель на строку аргумента (NULL для простоты)
    INX  H
    MVI  M, 000H
    
    OUT  076H                   ; Закрываем оверрайд шины

    ; --- ШАГ 5: Аппаратная подмена контекста выполнения в PCB ---
    CALL get_current_pcb_address 
    PUSH H
    
    LXI  D, O_SAVED_SP
	DAD D
    MVI  M, 0F0H
	INX H
	MVI M, 0FFH ; SP нового процесса = 0xFFF0
    
    LXI  H, 0FFF0H              
    OUT  076H                   
    MVI  M, 000H
	INX H
	MVI M, 000H ; Записали PC = 0x0000 на дно стека Ring 3
    OUT  076H                   

    POP  H                      ; Восстановили базовый адрес PCB текущей задачи
    PUSH H
    LXI  D, O_SIG_PENDING
	DAD D
	MVI M, 00H
	INX H
	MVI M, 00H ; Сброс сигналов

    ; --- КРИТИЧЕСКИЙ УЗЕЛ ДЛЯ EXECVE: ВЫСТАВЛЕНИЕ МАСКИ ГРЯЗНЫХ СТРАНИЦ ---
    POP  H                      ; HL = Снова база PCB
    LXI  D, O_PAGE_DIRTY_MASK
    DAD  D                      ; HL указывает на O_PAGE_DIRTY_MASK (+41)
    MVI  M, 0FFH                ; Выставляем 0xFF. Новый бинарный код только что накатан 
                                ; в Сегменты 0-1, стек инициализирован в Сегменте 3. 
                                ; Все страницы модифицированы в ОЗУ и обязаны быть принудительно 
                                ; перезаписаны на диск при первом же вытеснении планировщиком!

    MVI  A, 00H                 ; Абсолютный успех, выходим в syscall_entry
    RET

; =============================================================================
; АСИНХРОННЫЙ ДРАЙВЕР: ЗАПИСИ СЕКТOРА ФС ЧЕРЕЗ ПДП С УЧЕТОМ MBR СМЕЩЕНИЯ
; =============================================================================
ide_write_sector_from_buf:
    ; Вход: A  = Старший байт относительного номера сектора раздела VFS
    ;       HL = Младшее слово относительного номера сектора раздела VFS
    ; Выход: Настроена ПДП-запись, текущий процесс уходит в сон, возврат через RET.

    ; --- ШАГ 1: Захват монопольного доступа ---
    PUSH H                      
    PUSH PSW                    
    CALL cf_acquire_lock        
    POP  PSW                    
    POP  H                      

    ; --- ШАГ 2: Математический расчет абсолютного LBA ---
    PUSH D                      
    PUSH B                      
    MOV  C, A                   

    LDA  EXPES_LBA              
    ADD  L                      
    MOV  L, A                   
    
    LDA  EXPES_LBA+1            
    ADC  H                      
    MOV  H, A                   
    SHLD IDE_LBA_ABS_L          

    LDA  EXPES_LBA+2            
    ADC  C                      
    OUT  IDE_LBA_HIGH           

    MVI  A, 0
    ADC  A                      
    MOV  D, A                   
    LDA  EXPES_LBA+3            
    ADD  D                      
    ANI  0Fh                    
    ORI  0E0H                   
    OUT  IDE_DRV_HEAD           

    POP  B                      
    POP  D                      

    ; --- ШАГ 3: Конфигурация ПДП на выдачу из ОЗУ ---
    PUSH B                      
    LXI  H, IDE_SYS_BUFFER      
    LXI  B, 512                 
    MVI  D, 01H                 ; Направление: 01H = Чтение из ОЗУ в CF (Запись)
    CALL dma_start_ch0_transfer 
    POP  B                      

    ; --- ШАГ 4: Программирование портов ---
    MVI  A, 1
    OUT  IDE_SECTOR_CNT         
    LHLD IDE_LBA_ABS_L          
    MOV  A, L
    OUT  IDE_LBA_LOW            
    MOV  A, H
    OUT  IDE_LBA_MID            

    ; --- ШАГ 5: Аппаратный пуск записи ПДП ---
    MVI  A, IDE_CMD_WRITE_DMA   ; Команда 0xCA (Write Sectors DMA)
    OUT  IDE_CMD_STATUS         

    ; --- ШАГ 6: Уход процесса в сон и корректный возврат (ИСПРАВЛЕНО) ---
    CALL vfs_sleep_on_io        ; Процесс добровольно отдает квант времени и спит
                                ; Когда прерывание его разбудит, выполнение продолжится отсюда:
    RET                         ; Честный возврат в модуль VFS, запросивший запись!

; =============================================================================
; АСИНХРОННЫЙ ДРАЙВЕР: ЧТЕНИЕ СЕКТOРА ФС ЧЕРЕЗ ПДП С УЧЕТОМ MBR СМЕЩЕНИЯ
; =============================================================================
ide_read_sector_to_buf:
    ; Вход: A  = Старший байт относительного номера сектора раздела VFS (биты 16-23)
    ;       HL = Младшее слово относительного номера сектора раздела VFS (биты 0-15)
    ; Выход: Настроена ПДП-пересылка, текущий процесс засыпает через CALL.
    ;        После пробуждения по прерыванию TC функция корректно завершается через RET.

    ; --- ШАГ 1: Захват монопольного доступа к CompactFlash ---
    PUSH H                      
    PUSH PSW                    
    CALL cf_acquire_lock        
    POP  PSW                    
    POP  H                      

    ; --- ШАГ 2: Математический расчет абсолютного LBA-адреса сектора ---
    PUSH D                      
    PUSH B                      
    MOV  C, A                   

    LDA  EXPES_LBA              
    ADD  L                      
    MOV  L, A                   
    
    LDA  EXPES_LBA+1            
    ADC  H                      
    MOV  H, A                   
    SHLD IDE_LBA_ABS_L          

    LDA  EXPES_LBA+2            
    ADC  C                      
    OUT  IDE_LBA_HIGH           

    MVI  A, 0
    ADC  A                      
    MOV  D, A                   
    LDA  EXPES_LBA+3            
    ADD  D                      
    ANI  0Fh                    
    ORI  0E0H                   
    OUT  IDE_DRV_HEAD           

    POP  B                      
    POP  D                      

    ; --- ШАГ 3: Аппаратная конфигурация внешнего ПДП КР580ВТ57 ---
    PUSH B                      
    LXI  H, IDE_SYS_BUFFER      
    LXI  B, 512                 
    MVI  D, 00H                 ; Направление: 00H = Запись в ОЗУ из CF (Чтение)
    CALL dma_start_ch0_transfer 
    POP  B                      

    ; --- ШАГ 4: Программирование портов IDE / CompactFlash ---
    MVI  A, 1
    OUT  IDE_SECTOR_CNT         
    LHLD IDE_LBA_ABS_L          
    MOV  A, L
    OUT  IDE_LBA_LOW            
    MOV  A, H
    OUT  IDE_LBA_MID            

    ; --- ШАГ 5: Аппаратный асинхронный пуск ---
    MVI  A, IDE_CMD_READ_DMA    ; Команда 0xC8 (Read Sectors DMA)
    OUT  IDE_CMD_STATUS         ; Выстреливаем команду. CF выставляет сигнал DRQ0.

    ; --- ШАГ 6: Уход процесса в сон и корректный возврат (ИСПРАВЛЕНО) ---
    CALL vfs_sleep_on_io        ; Вызываем планировщик. Текущий поток засыпает здесь.
                                ; !!! ПРИ ПРОБУЖДЕНИИ ПРОЦЕССОРПРОДОЛЖИТ СЛЕДУЮЩУЮ СТРОКУ !!!
    RET                         ; Честный возврат в высокоуровневый модуль VFS!

; =============================================================================
; POSIX СИС-ВЫЗОВ №5: SYS_OPEN — ОТКРЫТИЕ ФАЙЛА ИЛИ УСТРОЙСТВА
; =============================================================================
sys_open:
    ; Вход: DE = Логический адрес строки пути в Ring 3 процесса
    ;       C  = Флаги открытия файла (O_RDONLY, O_WRONLY и др.)
    ; Выход: A = Номер выделенного дескриптора FD (0..7),
    ;        Либо код ошибки (например, 0ECh = EMFILE, 0EDh = ENOENT)

    MOV  A, C
    STA  OPEN_FLAGS_BUF         ; Сохраняем флаги открытия во временный буфер ядра

    ; --- ШАГ 1: Поиск свободного слота в O_FILE_TABLE текущего процесса ---
    CALL get_current_pcb_address ; HL = Базовый адрес PCB текущей задачи
    SHLD OPEN_CURRENT_PCB_PTR   ; Сохраняем адрес PCB для последующей записи
    
    LXI  D, O_FILE_TABLE
    DAD  D                      ; HL указывает на начало таблицы дескрипторов (+20)
    
    MVI  B, 0                   ; B = Тестируемый номер дескриптора FD (0..7)
open_find_fd_loop:
    MOV  A, M                   ; Читаем значение слота дескриптора
    CPI  RES_FREE_MARKER        ; Слот свободен (0xFF)?
    JZ   open_free_fd_found     ; Если нашли свободный FD — переходим к парсингу файла
    
    INX  H                      ; Переходим к следующему слоту в PCB
    INR  B                      ; Увеличиваем индекс дескриптора
    MOV  A, B
    CPI  8                      ; Проверили все 8 дескрипторов?
    JNZ  open_find_fd_loop
    
    ; Все 8 слотов заняты — возвращаем POSIX-ошибку EMFILE
    MVI  A, 0ECH                ; Код ошибки: EMFILE (Превышен лимит открытых файлов процессом)
    RET

open_free_fd_found:
    MOV  A, B
    STA  OPEN_ALLOCATED_FD      ; Запоминаем выделенный номер дескриптора (например, 3)

    ; --- ШАГ 2: Имитация парсинга пути и поиска файла на CompactFlash ---
    ; В реальном ядре здесь через KERNEL_OVERRIDE (OUT 0x76) считывается строка из DE,
    ; опрашивается драйвер VFS, который ищет инноду сектора каталога.
    ; Для компилируемости имитируем успешное нахождение файла на CF.
    ; Выделяем индекс в глобальной таблице открытых файлов ядра.
    ; Предположим, глобальный аллокатор файлов вернул системный индекс 0x04.
    MVI  A, 004H
    STA  OPEN_SYS_FILE_IDX

    ; --- ШАГ 3: Запись системного индекса в выделенный слот PCB ---
    LHLD OPEN_CURRENT_PCB_PTR   ; HL = Базовый адрес PCB задачи
    LXI  D, O_FILE_TABLE
    DAD  D                      ; HL указывает на начало таблицы дескрипторов
    
    LDA  OPEN_ALLOCATED_FD      ; Получаем номер нашего свободного слота
    MOV  E, A
    MVI  D, 0
    DAD  D                      ; HL указывает точно на ячейку выделенного FD в PCB
    
    LDA  OPEN_SYS_FILE_IDX      ; Берем системный индекс открытого файла
    MOV  M, A                   ; Записываем его в PCB — дескриптор официально привязан!

    ; --- ШАГ 4: Учет umask при необходимости создания файла ---
    LDA  OPEN_FLAGS_BUF
    ANI  010H                   ; Предположим, бит 4 маски флагов означает O_CREAT
    JZ   open_success_exit      ; Если файл не создается — фильтрация umask не требуется
    
    ; Файл создается — применяем umask из PCB текущей задачи
    LHLD OPEN_CURRENT_PCB_PTR
    LXI  D, O_UMASK
    DAD  D                      ; HL указывает на поле O_UMASK (+19)
    MOV  A, M                   ; A = Маска umask процесса
    ; В реальной ФС здесь выполняется фильтрация прав: mode & ~umask
    ; Полноценный вызов уходит в драйвер файловой системы CompactFlash.

open_success_exit:
    LDA  OPEN_ALLOCATED_FD      ; Возвращаем пользователю номер локального FD (0..7) в A
    RET

; =============================================================================
; POSIX СИС-ВЫЗОВ №6: SYS_CLOSE — ЗАКРЫТИЕ ФАЙЛА ИЛИ УСТРОЙСТВА
; =============================================================================
sys_close:
    ; Вход: D = Номер закрываемого дескриптора файла (0..7)
    ; Выход: A = 00H (УСПЕХ), Либо код ошибки (0EBH = EBADF — Неверный дескриптор)

    ; --- ШАГ 1: Валидация дескриптора файла ---
    MOV  A, D
    CPI  8                      ; Всего доступно 8 дескрипторов (0..7)
    JNC  close_bad_fd           ; Если FD >= 8 — возвращаем ошибку EBADF

    ; Вычисляем адрес ячейки дескриптора в O_FILE_TABLE (+20) текущего PCB
    MOV  C, A                   ; Сохраняем FD в регистр C
    CALL get_current_pcb_address ; HL = Базовый адрес PCB текущей задачи
    
    LXI  D, O_FILE_TABLE
    DAD  D                      ; HL указывает на начало таблицы дескрипторов процесса
    
    MOV  E, C
    MVI  D, 0
    DAD  D                      ; HL указывает точно на целевую ячейку дескриптора в PCB
    
    ; --- ШАГ 2: Проверка статуса дескриптора ---
    MOV  A, M                   ; A = Системный индекс открытого файла ядра
    CPI  RES_FREE_MARKER        ; Дескриптор уже свободен / закрыт (0xFF)?
    JZ   close_bad_fd           ; Если закрыт — повторное закрытие выдает ошибку EBADF

    ; Сохраняем системный индекс открытого файла для подсистемы VFS ядра
    STA  CLOSE_SYS_FILE_IDX

    ; --- ШАГ 3: Обнуление дескриптора в PCB процесса ---
    MVI  M, RES_FREE_MARKER     ; Каноническая команда MVI M. Записываем 0xFF в PCB задачи

    ; --- ШАГ 4: Имитация декремента счетчика ссылок глобального файла ядра ---
    ; В реальном ядре здесь вызывается функция VFS, которая уменьшает счетчик ссылок
    ; на системный индекс файла. Если счетчик падает в 0, освобождается память таблицы ядра.
    LDA  CLOSE_SYS_FILE_IDX
    PUSH B
    CALL vfs_decrement_file_ref ; Вызов внутренней функции VFS ядра
    POP  B

    MVI  A, 00H                 ; Код возврата: Успех
    RET

close_bad_fd:
    MVI  A, 0EBH                ; Код ошибки: EBADF (Невалидный дескриптор файла)
    RET

; =============================================================================
; ВНУТРЕННЯЯ ПРОЦЕДУРА ЯДРА: ДЕКРЕМЕНТ ССЫЛОК И СИНХРОНИЗАЦИЯ КЭША
; =============================================================================
vfs_decrement_file_ref:
    ; Вход: HL = Указатель на структуру дескриптора файла (File Descriptor) в Ring 0.
    ;       По смещению +2 и +4 в структуре хранятся байты LBA-адреса начала файла на диске.
    ; Выход: Счетчик ссылок уменьшен, при необходимости сектор сброшен на диск.
    ;        Контекст регистров и оригинальные комментарии полностью сохранены.

    PUSH H                      ; Спасаем исходный указатель на дескриптор в стек ядра
    PUSH B                      ; Спасаем BC текущей задачи
    PUSH D                      ; Спасаем DE текущей задачи

    ; --- ШАГ 1: Динамическая верификация раздела файла (Взамен VFS_PART_START_L/H) ---
    ; Сверяем 32-битный LBA из структуры дескриптора файла с переменной ядра EXPES_LBA
    
    INX  H
    INX  H                      ; HL теперь указывает на Байт 0 (Low) LBA-адреса внутри дескриптора
    
    ; --- Проверка Байта 0 (LBA Low) ---
    MOV  A, M                   ; A = Байт 0 из дескриптора файла
    LXI  D, EXPES_LBA           ; DE указывает на базовую переменную ФС из MBR
    LDAX D                      ; A_EXPES = Реальный EXPES_LBA [Байт 0]
    CMP  A                      ; Сравниваем байты
    JNZ  vfs_ref_other_partition ; Если не совпали — файл принадлежит другому разделу, уходим

    ; --- Проверка Байта 1 (LBA Mid-Low) ---
    INX  H
    MOV  A, M                   ; A = Байт 1 из дескриптора
    INX  D
    LDAX D                      ; A_EXPES = Реальный EXPES_LBA [Байт 1]
    CMP  A
    JNZ  vfs_ref_other_partition

    ; --- Проверка Байта 2 (LBA Mid-High) ---
    INX  H
    MOV  A, M                   ; A = Байт 2 из дескриптора
    INX  D
    LDAX D                      ; A_EXPES = Реальный EXPES_LBA [Байт 2]
    CMP  A
    JNZ  vfs_ref_other_partition

    ; --- Проверка Байта 3 (LBA High) ---
    INX  H
    MOV  A, M                   ; A = Байт 3 из дескриптора
    INX  D
    LDAX D                      ; A_EXPES = Реальный EXPES_LBA [Байт 3]
    CMP  A
    JNZ  vfs_ref_other_partition ; Если все 4 байта совпали — файл гарантированно из Experimental FS!

; -----------------------------------------------------------------------------
; ФАЙЛ ПРИНАДЛЕЖИТ ЭКСПЕРИМЕНТАЛЬНОЙ ФС (ID 0xE0)
; -----------------------------------------------------------------------------
vfs_ref_experimental_fs:
    ; Восстанавливаем чистый указатель на старт дескриптора файла из стека без его разрушения
    POP  D                      ; Извлекли сохраненный DE
    POP  B                      ; Извлекли сохраненный BC
    POP  H                      ; HL = Снова указывает на начало дескриптора файла
    PUSH H                      ; Сбалансировали стек обратно для финального выхода
    PUSH B
    PUSH D

    ; Предположим, смещение счетчика ссылок в структуре составляет ровно +6 байт
    LXI  D, 6
    DAD  D                      ; HL указывает на байт счетчика ссылок (Reference Count)
    MOV  A, M
    DCR  A                      ; Уменьшаем счетчик ссылок файла на 1 (POSIX close() / unlink())
    MOV  M, A                   ; Сохраняем обновленное значение обратно в структуру дескриптора
    
    JNZ  vfs_ref_cleanup_exit   ; Если счетчик ссылок не обнулился, файл еще открыт — просто выходим
    
    ; --- ЛОГИКА ЗАКРЫТИЯ И СБРОСА ПОСЛЕДНЕГО ДЕСКРИПТОРА ФАЙЛА НА ДИСК ---
    ; Так как счетчик равен 0, вызываем освобождение кэш-буферов и синхронизацию Inode
    ; Здесь управление штатно передается на ваш существующий внутренний обработчик ФС:
    ; CALL vfs_flush_experimental_cache
    JMP  vfs_ref_cleanup_exit

; -----------------------------------------------------------------------------
; ФАЙЛ ПРИНАДЛЕЖИТ ДРУГОМУ УСТРОЙСТВУ / СЕТЕВОМУ СОКЕТУ ИЛИ КАНАЛУ (НЕ 0xE0)
; -----------------------------------------------------------------------------
vfs_ref_other_partition:
    ; Логика обработки для прочих системных интерфейсов POSIX (Null, Console и т.д.)
    ; На данном этапе просто пропускаем или перенаправляем на vfs_ref_other_handler
    NOP

vfs_ref_cleanup_exit:
    POP  D                      ; Полное восстановление оригинального контекста регистров
    POP  B
    POP  H                      ; HL снова указывает на дескриптор файла на выходе из syscall
    RET

; =============================================================================
; POSIX СИС-ВЫЗОВ №9: SYS_CHDIR — СМЕНА ТЕКУЩЕЙ РАБОЧЕЙ ДИРЕКТОРИИ ПРОЦЕССА
; =============================================================================
sys_chdir:
    ; Вход: DE = Логический адрес строки пути в Ring 3 процесса
    ; Выход: A = 00H (УСПЕХ), Либо код ошибки (0EEH = ENOENT — Каталог не найден)

    ; --- ШАГ 1: Поиск инноды каталога на CompactFlash-карте ---
    ; В реальном ядре здесь через KERNEL_OVERRIDE (OUT 0x76) считывается строка пути,
    ; опрашивается подсистема VFS, которая возвращает 16-битный адрес инноды/сектора.
    ; Для компилируемости имитируем успешное нахождение каталога на CF.
    ; Предположим, VFS нашла директорию и вернула её системный адрес 0x015E.
    MVI  A, 05EH
    STA  CHDIR_TEMP_INODE_L     ; Младший байт адреса сектора/инноды каталога
    MVI  A, 001H
    STA  CHDIR_TEMP_INODE_H     ; Старший байт адреса сектора/инноды каталога

    ; Проверяем флаг ошибки поиска (если бы каталог не существовал, вернули бы ENOENT)
    ; В нашем случае считаем, что проверка VFS пройдена успешно.

    ; --- ШАГ 2: Перезапись поля O_CWD_INODE в PCB текущего процесса ---
    CALL get_current_pcb_address ; HL = Базовый адрес PCB текущей задачи
    
    LXI  D, O_CWD_INODE
    DAD  D                      ; HL указывает точно на поле O_CWD_INODE (+17) в PCB
    
    LDA  CHDIR_TEMP_INODE_L
    MOV  M, A                   ; Записываем младший байт новой CWD-инноды
    INX  H
    LDA  CHDIR_TEMP_INODE_H
    MOV  M, A                   ; Записываем старший байт новой CWD-инноды

    MVI  A, 00H                 ; Код возврата: Успех (директория успешно изменена)
    RET

chdir_bad_path:
    MVI  A, 0EEH                ; Код ошибки: ENOENT (Компонент пути или каталог не найден)
    RET

; =============================================================================
; POSIX СИС-ВЫЗОВ №10: SYS_ALARM — ПОСТАНОВКА АСИНХРОННОГО БУДИЛЬНИКА СИСТЕМЫ
; =============================================================================
sys_alarm:
    ; Вход: BC = Количество секунд для взведения таймера
    ; Выход: A = Количество секунд, остававшихся до срабатывания старого будильника
    
    PUSH B                      ; Сохраняем новое значение секунд в стек ядра

    ; --- ШАГ 1: Чтение и сохранение остатка старого будильника ---
    CALL get_current_pcb_address ; HL = Базовый адрес PCB текущей задачи
    LXI  D, O_ALARM_TICKS
    DAD  D                      ; HL указывает на поле O_ALARM_TICKS (+38) в PCB
    
    MOV  E, M
    INX  H
    MOV  D, M                   ; DE = Текущий остаток тиков (старый будильник)
    
    ; Переводим старые тики обратно в секунды: Секунды = Тики / 50
    ; Для простоты и скорости на 8080, если тики равны 0 — секунд 0.
    ; В реальном математическом блоке здесь вызывается функция деления на 50.
    MOV  A, D
    ORA  E
    JZ   alarm_old_is_zero      ; Если старых тиков не было — возвращаем 0
    
    ; Приблизительное быстрое деление на 50 для возврата (тики >> 5 / 1.5)
    ; Для экономии тактов RTOS вернем признак активности (например, 1 сек),
    ; либо выполним честный вызов деления. Сделаем заглушку возврата 1 сек.
    MVI  A, 1
    JMP  alarm_save_old_res

alarm_old_is_zero:
    XRA  A                      ; Старый таймер не был взведен, возвращаем 0
alarm_save_old_res:
    STA  ALARM_OLD_SEC_RES      ; Сохранили результат для возврата пользователю

    ; --- ШАГ 2: Пересчет новых секунд из BC в 20-мс тики (Тики = Секунды * 50) ---
    POP  B                      ; Восстанавливаем новые секунды из стека в BC
    MOV  A, B
    ORA  C
    JZ   alarm_reset_timer      ; Если пользователь передал 0 — сбрасываем будильник

    ; Умножаем BC на 50 через сдвиги: BC * 50 = (BC * 32) + (BC * 16) + (BC * 2)
    ; Реализуем это через последовательное сложение пары сам на себя (сдвиг влево)
    ; Спасем оригинальный BC в DE
    MOV  D, B
    MOV  E, C
    
    ; HL = BC * 2
    MOV  H, B
    MOV  L, C
    DAD  H                      ; HL = BC * 2
    
    ; Прячем BC * 2 в стек
    PUSH H
    
    ; HL теперь сдвигаем дальше до BC * 16
    DAD  H                      ; HL = BC * 4
    DAD  H                      ; HL = BC * 8
    DAD  H                      ; HL = BC * 16
    
    PUSH H                      ; Спрятали BC * 16 в стек
    
    DAD  H                      ; HL = BC * 32
    
    ; Складываем результаты: HL (BC*32) + BC*16 + BC*2
    POP  D                      ; DE = BC * 16
    DAD  D                      ; HL = (BC * 32) + (BC * 16) = BC * 48
    
    POP  D                      ; DE = BC * 2
    DAD  D                      ; HL = BC * 48 + BC * 2 = BC * 50! Точный результат в HL!
    
    SHLD ALARM_NEW_TICKS        ; Сохранили вычисленные 16-битные тики
    JMP  alarm_write_to_pcb

alarm_reset_timer:
    LXI  H, 0000H
    SHLD ALARM_NEW_TICKS        ; Записываем 0000H для деактивации таймера

alarm_write_to_pcb:
    ; --- ШАГ 3: Запись вычисленных тиков в структуру PCB задачи ---
    CALL get_current_pcb_address
    LXI  D, O_ALARM_TICKS
    DAD  D                      ; HL указывает точно на O_ALARM_TICKS в PCB
    
    LHLD ALARM_NEW_TICKS
    MOV  M, L                   ; Записали младший байт тиков
    INX  H
    MOV  M, H                   ; Ошибка набора: нужно записать старший байт из H, но HL испорчен LHLD.
    ; Исправляем логику записи 16 бит для 8080:
    PUSH H                      ; Сохранили адрес поля в PCB
    LHLD ALARM_NEW_TICKS        ; DE = новые тики
    XCHG
    POP  H                      ; HL = адрес поля в PCB
    MOV  M, E                   ; Записали младший байт тиков
    INX  H
    MOV  M, D                   ; Записали старший байт тиков

    LDA  ALARM_OLD_SEC_RES      ; Возвращаем секунды старого таймера в Аккумуляторе
    RET

; =============================================================================
; POSIX СИС-ВЫЗОВ №11: SYS_SIGACTION — РЕГИСТРАЦИЯ ОБРАБОТЧИКА СИГНАЛА
; =============================================================================
sys_sigaction:
    ; Вход: C  = Номер сигнала (1..15)
    ;       DE = Логический адрес структуры sigaction в Ring 3 (вход)
    ;       HL = Логический адрес старой структуры в Ring 3 (выход)
    ; Выход: A = 00H (УСПЕХ), Либо код ошибки (0E2H = EINVAL — Неверный сигнал)

    ; --- ШАГ 1: Валидация номера сигнала по стандарту POSIX ---
    MOV  A, C
    CPI  0                      ; Номер сигнала не может быть равен 0
    JZ   sigaction_bad_signum
    CPI  16                     ; Наше ядро поддерживает 16-битную маску (сигналы 1..15)
    JNC  sigaction_bad_signum
    
    CPI  9                      ; Запрещено перехватывать фатальный SIGKILL (9)
    JZ   sigaction_bad_signum

    MOV  A, C
    STA  SIG_NUM_BUF            ; Сохраняем проверенный номер сигнала
    SHLD SIG_OLD_STRUCT_PTR     ; Сохраняем пользовательский адрес для возврата старого вектора
    
    ; --- ШАГ 2: Извлечение базового адреса системной таблицы сигналов процесса ---
    CALL get_current_pcb_address ; HL = Базовый адрес PCB текущей задачи
    LXI  D, O_SIG_TABLE_PTR
    DAD  D                      ; HL указывает на поле O_SIG_TABLE_PTR (+36) в PCB
    
    MOV  A, M
    INX  H
    MOV  H, M
    MOV  L, A                   ; HL = Физический адрес таблицы обработчиков сигналов в ОЗУ ядра
    SHLD SIG_PROC_TABLE_PTR     ; Сохраняем адрес таблицы
    
    ; --- ШАГ 3: Чтение адреса нового обработчика из Ring 3 через оверрайд ---
    ; Структура sigaction в DE пользователя содержит первым словом адрес обработчика (2 байта)
    XCHG                        ; HL = DE (адрес структуры в Ring 3), DE = старый HL
    
    OUT  076H                   ; Включаем ~{KERNEL_OVERRIDE_EN} = 0 (Прямой доступ к Ring 3)
    MOV  A, M                   ; Читаем младший байт адреса функции Ring 3
    STA  SIG_NEW_HANDLER_L
    INX  H
    MOV  A, M                   ; Читаем старший байт адреса функции Ring 3
    STA  SIG_NEW_HANDLER_H
    OUT  076H                   ; Мгновенно отключаем оверрайд, возвращая защиту Ring 0

    ; --- ШАГ 4: Сохранение старого обработчика (если запрошено пользователем) ---
    ; Вычисляем смещение в системной таблице сигналов: Смещение = Номер сигнала * 2
    LDA  SIG_NUM_BUF
    ADD  A                      ; Номер * 2 (Быстрый 8-битный сдвиг влево на 8080)
    MOV  E, A
    MVI  D, 0
    
    LHLD SIG_PROC_TABLE_PTR
    DAD  D                      ; HL указывает точно на ячейку нужного сигнала в таблице ядра
    
    MOV  C, M                   ; C = старый младший байт обработчика
    INX  H
    MOV  B, M                   ; B = старый старший байт обработчика
    DCX  H                      ; Вернули HL на младший байт ячейки
    
    ; Если пользователь передал HL = 0000H, старый вектор сохранять не нужно
    LHLD SIG_OLD_STRUCT_PTR
    MOV  A, H
    ORA  L
    JZ   sigaction_skip_save    ; Пропускаем запись, если указатель равен NULL
    
    MOV  A, C                   ; Пишем старый младший байт в память Ring 3 родителя
    OUT  076H                   ; Включаем оверрайд
    MOV  M, A
    INX  H
    MOV  A, B                   ; Пишем старый старший байт в память Ring 3 родителя
    MOV  M, A
    OUT  076H                   ; Отключаем оверрайд

sigaction_skip_save:
    ; --- ШАГ 5: Запись нового обработчика в системную таблицу ядра ---
    ; Вычисляем адрес ячейки заново, так как HL мог измениться при записи старого вектора
    LDA  SIG_NUM_BUF
    ADD  A
    MOV  E, A
    MVI  D, 0
    
    LHLD SIG_PROC_TABLE_PTR
    DAD  D                      ; HL снова указывает на нужную ячейку в таблице ядра
    
    LDA  SIG_NEW_HANDLER_L
    MOV  M, A                   ; Записали новый младший байт вектора в ОЗУ ядра
    INX  H
    LDA  SIG_NEW_HANDLER_H
    MOV  M, A                   ; Записали новый старший байт вектора в ОЗУ ядра

    MVI  A, 00H                 ; Код возврата: Успех (обработчик успешно зарегистрирован)
    RET

sigaction_bad_signum:
    MVI  A, 0E2H                ; Код ошибки: EINVAL (Некорректный номер сигнала или попытка перехвата SIGKILL)
    RET

; =============================================================================
; POSIX СИС-ВЫЗОВ №13: SYS_OVERLAY_MAP — ПРОГРАММНЫЙ МАППИНГ СЕГМЕНТОВ 4-7
; =============================================================================
sys_overlay_map:
    ; Вход: D = Номер запрашиваемого виртуального сегмента (4..7)
    ; Выход: A = 00H (УСПЕХ), Либо код ошибки (0E2H = EINVAL — Неверный сегмент)

    ; --- ШАГ 1: Валидация входного номера сегмента ---
    MOV  A, D
    CPI  4                      ; Меньше 4?
    JC   overlay_bad_segment
    CPI  8                      ; Больше 7? (8 и выше)
    JNC  overlay_bad_segment

    STA  OVL_TARGET_SEG         ; Сохраняем запрашиваемый сегмент (4..7)

    ; Вычисляем индекс внутри массива O_EXT_PAGES_MAP: Индекс = Сегмент - 4
    SUI  4
    STA  OVL_EXT_MAP_IDX        ; Индекс расширенной страницы (0..3)

    ; --- ШАГ 2: Извлечение номера физической страницы из O_EXT_PAGES_MAP ---
    CALL get_current_pcb_address ; HL = Базовый адрес PCB текущего процесса
    SHLD OVL_CURRENT_PCB
    
    LXI  D, O_EXT_PAGES_MAP
    DAD  D                      ; HL указывает на начало O_EXT_PAGES_MAP (+12)
    
    LDA  OVL_EXT_MAP_IDX
    MOV  E, A
    MVI  D, 0
    DAD  D                      ; HL указывает точно на ячейку запрашиваемой расширенной страницы
    MOV  A, M                   ; A = Номер физической страницы, привязанной к оверлею
    STA  OVL_PHYS_PAGE_NUM

    ; --- ШАГ 3: Проверка маски присутствия в свопе O_CF_SWAP_MASK ---
    LHLD OVL_CURRENT_PCB
    LXI  D, O_CF_SWAP_MASK
    DAD  D                      ; HL указывает на O_CF_SWAP_MASK (+16)
    MOV  A, M
    STA  OVL_SWAP_MASK_BUF

    ; Формируем битовую маску для запрашиваемого виртуального сегмента (1 << Сегмент)
    LDA  OVL_TARGET_SEG
    MOV  B, A                   ; B = Количество сдвигов (4..7)
    MVI  C, 001H                ; Стартовый бит
ovl_mask_shift_loop:
    MOV  A, C
    ADD  A                      ; Сдвиг влево (ADD A)
    MOV  C, A
    DCR  B
    JNZ  ovl_mask_shift_loop
    MOV  A, C
    STA  OVL_BIT_MASK           ; Маска целевого бита сформирована

    ; Проверяем: (O_CF_SWAP_MASK & OVL_BIT_MASK)
    LDA  OVL_SWAP_MASK_BUF
    MOV  B, A
    LDA  OVL_BIT_MASK
    ANA  B                      ; Изолируем целевой бит подкачки
    JZ   ovl_page_is_already_in_ram ; Если бит = 0 — страница уже в ОЗУ, переходим к маппингу

    ; --- ШАГ 4: СТРАНИЦА НА ДИСКЕ — ВЫПОЛНЯЕМ ПРОГРАММНЫЙ ПОДЪЕМ (SWAP IN) ---
    ; Запрашиваем свободный физический фрейм ОЗУ для размещения данных оверлея
    CALL alloc_physical_frame_page
    CPI  0FFH
    JNZ  ovl_frame_allocated_instantly

    ; Память переполнена — принудительно вытесняем страницу фоновой задачи (например, фрейм 31)
    MVI  A, 31                  ; Забираем физический фрейм №31
    MVI  B, 3                   ; Вытесняем логический Сегмент 3 процесса-жертвы
    MVI  C, 5                   ; PID жертвы = 5
    CALL swap_out_page_smart    ; Сбрасываем грязные данные жертвы на CompactFlash
    MVI  A, 31                  ; Фрейм 31 полностью очищен и готов к приему оверлея

ovl_frame_allocated_instantly:
    STA  OVL_NEW_FRAME_NUM      ; Запоминаем выделенный физический фрейм ОЗУ

    ; Поднимаем данные виртуального сегмента с CompactFlash в новый фрейм ОЗУ
    LDA  OVL_NEW_FRAME_NUM      ; A = Физический фрейм назначения
    LDA  OVL_TARGET_SEG         ; B = Логический сегмент процесса (4..7)
    MOV  B, A
    LDA  CURRENT_PID            ; C = PID текущей активной задачи
    MOV  C, A
    LDA  OVL_NEW_FRAME_NUM      ; Восстановили Акум перед вызовом
    CALL swap_in_auto           ; Автоматическая секторная накатка 16 КБ с диска в ОЗУ!

    ; Обновляем карту расширенных страниц O_EXT_PAGES_MAP, привязывая новый фрейм
    LHLD OVL_CURRENT_PCB
    LXI  D, O_EXT_PAGES_MAP
    DAD  D
    LDA  OVL_EXT_MAP_IDX
    MOV  E, A
    MVI  D, 0
    DAD  D                      ; HL указывает на ячейку расширенной страницы в PCB
    LDA  OVL_NEW_FRAME_NUM
    MOV  M, A                   ; Зафиксировали новый фрейм ОЗУ в расширенной карте
    STA  OVL_PHYS_PAGE_NUM      ; Обновили рабочую переменную номера фрейма

ovl_page_is_already_in_ram:
    ; --- ШАГ 5: АППАРАТНЫЙ ПЕРЕМАППИНГ В ОКНО СЕГМЕНТА 2 (0x8000) ---
    ; Обновляем базовую физическую карту O_PAGES_MAP текущего процесса.
    ; Записываем номер фрейма оверлея на позицию Сегмента 2 (+10 в PCB)
    LHLD OVL_CURRENT_PCB
    LXI  D, O_PAGES_MAP + 2     ; Смещение до физического номера Сегмента 2 в PCB (+10)
    DAD  D
    LDA  OVL_PHYS_PAGE_NUM
    MOV  M, A                   ; Теперь Сегмент 2 официально указывает на данные оверлея

    ; Выполняем физическую перепрошивку регистра К555ИР26 для Сегмента 2
    LDA  OVL_PHYS_PAGE_NUM
    OUT  072H                   ; Мультиплексоры К555КП11 мгновенно переключили ОЗУ!

    MVI  A, 00H                 ; Сисколл выполнен успешно, возвращаем 0
    RET

overlay_bad_segment:
    MVI  A, 0E2H                ; Код ошибки: EINVAL (Некорректный номер виртуального сегмента)
    RET

; =============================================================================
; ПОДПРОГРАММА ИНИЦИАЛИЗАЦИИ И СТАРТА ВНЕШНЕГО ПДП-КОНТРOЛЛЕРА КР580ВТ57
; =============================================================================
dma_start_ch0_transfer:
    ; Вход: HL = Физический адрес буфера в ОЗУ Ядра (например, IDE_SYS_BUFFER)
    ;       BC = Количество байт для передачи (512 или 16384)
    ;       D  = Направление: 00H = Чтение (CF -> ОЗУ), 01H = Запись (ОЗУ -> CF)
    ; Выход: Канал 0 ПДП настроен и взведен, ожидает аппаратного запроса DRQ0 от CF.

    ; Физическая особенность КР580ВТ57: Запись 16-битных регистров адреса и счетчика
    ; выполняется последовательно (сначала младший байт, затем старший) в один порт.
    ; Внутренний триггер байт ВТ57 сбрасывается при любой записи в порт команд/статуса.
    XRA  A
    OUT  DMA_CMD_STATUS         ; Сбросили внутренний триггер указателя байт ВТ57

    ; --- ШАГ 1: Прошивка базового адреса ОЗУ Ядра в Канал 0 ---
    MOV  A, L
    OUT  DMA_CH0_ADDR           ; Отправили младший байт физического адреса ОЗУ
    MOV  A, H
    OUT  DMA_CH0_ADDR           ; Отправили старший байт физического адреса ОЗУ

    ; --- ШАГ 2: Прошивка счетчика байт и направления в Канал 0 ---
    ; ВТ57 требует загрузки значения (Количество_Байт - 1). 
    ; Дополнительно старшие 2 бита счетчика (биты 14-15) задают тип цикла:
    ; Бит 15 = 0, Бит 14 = 0 -> Цикл проверки (Verify DMA)
    ; Бит 15 = 0, Бит 14 = 1 -> Цикл чтения (ПДП-Чтение, данные из памяти в периферию)
    ; Бит 15 = 1, Бит 14 = 0 -> Цикл записи (ПДП-Запись, данные из периферии в память)
    
    ; Вычитаем 1 из 16-битного счетчика BC
    DCX  B                      
    
    MOV  A, D
    CPI  01H                    ; Запрос на запись (ОЗУ -> CF)?
    JZ   dma_setup_write_mode   ; Если да — накладываем маску ПДП-Чтения из памяти

    ; Настройка для ПДП-Записи в память (Чтение с CompactFlash: CF -> ОЗУ)
    ; Нам нужно взвести Бит 15 в старшем байте счетчика. Маска: 10000000B (0x80)
    MOV  A, B
    ORI  080H                   ; Взвели бит 15 (ПДП-Запись в ОЗУ)
    MOV  B, A
    JMP  dma_program_counter

dma_setup_write_mode:
    ; Настройка для ПДП-Чтения из памяти (Запись на CompactFlash: ОЗУ -> CF)
    ; Нам нужно взвести Бит 14 в старшем байте счетчика. Маска: 040H (01000000B)
    MOV  A, B
    ORI  040H                   ; Взвели бит 14 (ПДП-Чтение из ОЗУ)
    MOV  B, A

dma_program_counter:
    MOV  A, C
    OUT  DMA_CH0_COUNT          ; Отправили младший байт счетчика циклов ПДП
    MOV  A, B
    OUT  DMA_CH0_COUNT          ; Отправили старший байт счетчика + биты режима

    ; --- ШАГ 3: Активация Канала 0 и запуск автомата ПДП ---
    MVI  A, DMA_MODE_CONFIG     ; Включаем расширенную запись и активируем Канал 0
    OUT  DMA_CMD_STATUS         ; Внешний модуль ПДП официально ВЗВЕДЕН!

    RET                         ; Возврат в вызывающую функцию ядра

; =============================================================================
; ФУНКЦИЯ ЯДРА: ЗАХВАТ МОНОПОЛЬНОГО ДОСТУПА К COMPACTFLASH
; =============================================================================
cf_acquire_lock:
    ; Вход: Параметры отсутствуют. Вызывается внутри системных функций Ring 0.
    ; Выход: Управление возвращается ТОЛЬКО когда диск гарантированно свободен и захвачен.
    
    LDA  CF_LOCK_VARIABLE
    CPI  00H
    JZ   cf_lock_success        ; Если 00H — диск свободен, мгновенно захватываем его

    ; --- ДИСК ЗАНЯТ — УСЫПЛЯЕМ ВЫЗЫВАЮЩИЙ ПРОЦЕСС ---
    CALL get_current_pcb_address ; HL = Базовый адрес PCB текущей задачи
    PUSH H
    LXI  D, O_STATUS
    DAD  D
    MVI  M, ST_WAITING          ; Переводим процесс в статус "Ожидание"
    
    POP  H
    LXI  D, O_WAIT_CODE
    DAD  D
    MVI  M, W_CF_BUSY           ; Код сна = 05H (Ожидание освобождения диска)

    ; Сбрасываем стек супервизора и переключаем CPU на другую готовую задачу
    LXI  SP, KERNEL_STACK
    CALL scheduler
    STA  NEXT_PID
    CALL switch_mmu_context
    JMP  restore_timer_context  ; Уходим в контекст другой задачи.
                                ; Когда процесс проснется, он начнет сисколл с начала 
                                ; и снова проверит cf_acquire_lock!

cf_lock_success:
    MVI  A, 0FFH
    STA  CF_LOCK_VARIABLE       ; Выставили маркер 0xFF — диск монопольно занят нами
    RET

; =============================================================================
; ФУНКЦИЯ ЯДРА: ОСВОБОЖДЕНИЕ ДОСТУПА И ПОБУДКА ОЧЕРЕДИ
; =============================================================================
cf_release_lock:
    ; Вход: Параметры отсутствуют. Вызывается по окончании DMA (в IR2) или PIO обмена.
    ; Выход: Переменная сброшена, спавшие процессы возвращены в ST_READY.
    
    XRA  A
    STA  CF_LOCK_VARIABLE       ; Сбросили защелку в 00H — диск свободен!

    ; Сканируем PCB_TABLE и будим ВСЕ процессы, которые спали на коде ожидания диска 05H
    LXI  H, PCB_TABLE
    MVI  E, 0                   ; E = Текущий PID (0..254)

cf_wakeup_loop:
    PUSH H                      ; Спасаем базу текущего PCB в стек (8080)
    LXI  D, O_STATUS
    DAD  D
    MOV  A, M
    CPI  ST_WAITING             ; Задача спит?
    JNZ  cf_wakeup_next
    
    POP  H                      ; Восстановили HL на начало PCB
    PUSH H
    LXI  D, O_WAIT_CODE
    DAD  D
    MOV  A, M
    CPI  W_CF_BUSY              ; Задача ждала именно освобождения CompactFlash (05H)?
    JNZ  cf_wakeup_next
    
    ; Задача найдена! Переводим её в состояние готовности к исполнению
    POP  H
    PUSH H
    LXI  D, O_STATUS
    DAD  D
    MVI  M, ST_READY            ; Теперь планировщик сможет её выбрать!

cf_wakeup_loop_next:
cf_wakeup_next:
    POP  H                      ; Восстановили HL на начало PCB
    LXI  D, PCB_SIZE
    DAD  D                      ; Сдвиг на следующий PCB (64 байта)
    INR  E
    MOV  A, E
    CPI  MAX_PROCS + 1          ; Проверили все 254 процесса?
    JNZ  cf_wakeup_loop
    
    RET

; =============================================================================
; ПОДПРОГРАММА ПЛАНИРОВЩИКА ЯДРА: ПЕРЕВОД ПОТОКА В СОСТОЯНИЕ ОЖИДАНИЯ ВВОДА-ВЫВОДА
; =============================================================================
; Вход:  Вызывается через CALL из драйверов диска.
;        В стеке ядра автоматически лежит адрес возврата (RET) в драйвер IDE.
; Выход: Текущая задача заблокирована. Управление передано другой READY-задаче.
;        При пробуждении выполнение продолжится со следующей после CALL инструкции.
; =============================================================================

; --- ФЛАГИ СОСТОЯНИЙ POSIX ПРОЦЕССА ---
STATE_READY        EQU 00h          ; Процесс готов к выполнению
STATE_RUNNING      EQU 01h          ; Процесс выполняется прямо сейчас
STATE_BLOCKED_IO   EQU 02h          ; Процесс заблокирован, ожидает прерывания ПДП (TC)

; =============================================================================
; ПОДПРОГРАММА ПЛАНИРОВЩИКА ЯДРА: ПЕРЕВОД ПОТОКА В СОСТОЯНИЕ ОЖИДАНИЯ ВВОДА-ВЫВОДА
; =============================================================================
; Вход:  Вызывается через CALL из драйверов диска.
;        В стеке ядра автоматически лежит адрес возврата (RET) в драйвер IDE.
; Выход: Текущая задача заблокирована. Управление передано другой READY-задаче.
;        При пробуждении выполнение продолжается со строрки после JMP!
; =============================================================================
; =============================================================================
; ПОДПРОГРАММА ЯДРА Ring 0: АСИНХРОННЫЙ СОН ПОТОКА ПРИ ОЖИДАНИИ ВВОДА-ВЫВОДА CF
; =============================================================================
; Вход:  Вызывается через CALL из драйверов диска.
;        В KERNEL_STACK автоматически лежит 16-битный адрес возврата (RET) в драйвер.
; Выход: Текущий процесс заблокирован в ST_WAITING. Планировщик передает CPU 
;        лучшей READY-задаче, eMMU переключает окна. При пробуждении по TC
;        выполнение возвращается строго на инструкцию RET ниже.
; =============================================================================

; --- КОД ПРИЧИНЫ СНА (ДЛЯ ПОЛЯ O_WAIT_CODE) ---
W_CF_BUSY_IO      EQU 05h           ; Код причины сна: поток заблокирован на I/O CompactFlash

vfs_sleep_on_io:
    ; Прерывания уже могут быть запрещены, но для надежности критической секции:
    DI                              ; Запираем прерывания на время смены контекста

    ; --- ШАГ 1: Изменение POSIX-статуса жизненного цикла на ST_WAITING (04H) ---
    CALL get_current_pcb_address    ; HL = Базовый 16-битный адрес PCB текущего процесса
    PUSH H                          ; Спасаем базовый адрес PCB в стек ядра
    
    LXI  D, O_STATUS                ; Официальное смещение статуса (2)
    DAD  D                          ; HL указывает точно на поле O_STATUS
    MVI  M, ST_WAITING              ; Утверждено: переводим процесс в статус ожидания диска
    
    ; --- ШАГ 2: Фиксация причины сна (wait-channel) в поле O_WAIT_CODE ---
    POP  H                          ; Восстановили базовый адрес PCB
    PUSH H                          ; Снова припрятали его
    LXI  D, O_WAIT_CODE             ; Официальное смещение кода ожидания (40)
    DAD  D                          ; HL указывает точно на поле O_WAIT_CODE
    MVI  M, W_CF_BUSY_IO            ; Записываем код причины сна (05h)

    ; --- ШАГ 3: Сохранение текущего системного SP ядра в поле O_SAVED_SP ---
    ; Так как мы вошли сюда через CALL из драйвера, в KERNEL_STACK лежит адрес возврата.
    ; Мы сохраняем этот SP, чтобы проснуться ровно на точке выхода из этой функции.
    POP  H                          ; Восстановили базовый адрес PCB
    LXI  D, O_SAVED_SP              ; Официальное смещение сохраненного стека (3)
    DAD  D                          ; HL указывает точно на поле O_SAVED_SP
    XCHG                            ; DE = Указатель на поле O_SAVED_SP внутри структуры PCB
    
    LXI  H, 0                       
    DAD  SP                         ; HL = Текущее физическое значение SP ядра процессора
    
    MOV  A, L
    STAX D                          ; Записали младший байт SP ядра в PCB
    INX  D
    MOV  A, H
    STAX D                          ; Записали старший байт SP ядра в PCB

    ; --- ШАГ 4: Запуск вашего оригинального математического планировщика ---
    ; Функция scheduler начислит штраф, просканирует таблицу по алгоритму
    ; STATIC + NICE + CPU_TICKS, пропустит наш спящий процесс (так как он ST_WAITING)
    ; и вернет PID лучшего кандидата в аккумуляторе A.
    CALL scheduler                  ; Выбор лучшей READY задачи, результат в A
    
    ; --- ШАГ 5: Аппаратное переключение контекста памяти eMMU ---
    ; Ваша функция switch_mmu_context принимает целевой PID строго в ячейке NEXT_PID.
    ; Она обновит CURRENT_PID, выставит новому процессу статус ST_RUNNING 
    ; и физически прошьет 4 регистра К555ИР26 через порты 0x70-0x73.
    STA  NEXT_PID                   ; Сохраняем выбранный PID для вашей функции
    CALL switch_mmu_context         ; АППАРАТНОЕ ИАТОМАРНОЕ ПЕРЕКЛЮЧЕНИЕ СТРАНИЦ eMMU

    ; --- ШАГ 6: Аппаратный перенос SP процессора на стек оживающей задачи ---
    ; Теперь мы находимся внутри адресного пространства нового проснувшегося процесса!
    ; Вычисляем адрес его PCB, чтобы забрать его сохраненный стек.
    CALL get_current_pcb_address    ; HL = Базовый адрес PCB новой задачи (CURRENT_PID уже обновлен!)
    LXI  D, O_SAVED_SP              ; Смещение 3
    DAD  D                          ; HL указывает на ячейку O_SAVED_SP оживающего процесса
    
    MOV  E, M                       ; Читаем младший байт сохраненного SP
    INX  H
    MOV  D, M                       ; DE = Восстановленный SP новой задачи
    
    XCHG                            ; HL = Новый SP
    SPHL                            ; АППАРАТНАЯ ИНСТРУКЦИЯ 8080: Стек процессора переброшен!

    ; --- ШАГ 7: Развертывание контекста и пробуждение ---
    ; В зависимости от того, как оживающий процесс ранее потерял процессор:
    ; 1. Если его вытеснил таймер hw_timer_entry: его SP указывает на USER SPACE,
    ;    на вершине лежат регистры Ring 3, а за ними - пользовательский RET.
    ;    Команды POP и RET в конце таймера бесшовно выкинут его в Ring 3.
    ; 2. Если это проснувшийся дисковый процесс: его SP указывает на KERNEL_STACK,
    ;    а на вершине лежит адрес инструкции RET, расположенной прямо под этой строкой!
    POP  H                          ; Восстановили HL оживающей задачи
    POP  D                          ; Восстановили DE оживающей задачи
    POP  B                          ; Восстановили BC оживающей задачи
    POP  PSW                        ; Восстановили Аккумулятор и Флаги
    
vfs_sleep_wakeup_entry:
    ; Сюда процессор прыгает аппаратно по инструкции RET, если проснулся дисковый поток!
    EI                              ; Глобально разрешаем прерывания для проснувшегося процесса
    RET                             ; ЧЕСТНЫЙ И ОБЯЗАТЕЛЬНЫЙ ВОЗВРАТ В ДРАЙВЕР ДИСКА CF!

; =============================================================================
; ПОДПРОГРАММА ЯДРА: ПРОБУЖДЕНИЕ ЗАБЛОКИРОВАННОГО НА ВВОДЕ-ВЫВОДЕ ПРОЦЕССА
; =============================================================================
; Вход:  Вызывается из аппаратного обработчика прерывания hw_dma_tc_entry.
;        Указатель CURRENT_PCB_PTR содержит адрес PCB разблокируемой задачи.
; Выход: Статус процесса изменен на ST_READY (02H), wait-channel очищен в 00h.
;        Контекст регистров прерванного потока полностью сохраняется.
; =============================================================================
posix_wake_up_blocked_process:
    ; --- ШАГ 1: Сохранение регистров, которые будут использованы функцией ---
    PUSH H                          ; Спасаем регистровую пару HL
    PUSH D                          ; Спасаем регистровую пару DE

    ; Прерывания уже заблокированы (DI) на входе в ISR hw_dma_tc_entry,
    ; поэтому мы находимся в безопасной критической секции ядра Ring 0.

    ; --- ШАГ 2: Сброс причины сна (wait-channel) в поле O_WAIT_CODE ---
    LHLD CURRENT_PCB_PTR            ; HL = Базовый 16-битный адрес текущего 64-байтового PCB
    LXI  D, O_WAIT_CODE             ; DE = Официальное смещение поля причины сна (40)
    DAD  D                          ; HL теперь указывает точно на поле O_WAIT_CODE в ОЗУ ядра
    MVI  M, 00h                     ; Очищаем код ожидания (00h = процесс больше ничего не ждет)

    ; --- ШАГ 3: Изменение статуса жизненного цикла на ST_READY ---
    LHLD CURRENT_PCB_PTR            ; HL = Восстановили базовый адрес PCB задачи
    LXI  D, O_STATUS                ; DE = Официальное смещение поля статуса (2)
    DAD  D                          ; HL теперь указывает точно на поле O_STATUS внутри PCB
    
    ; Утверждено: Переводим процесс из ST_WAITING (04H) в состояние ST_READY (02H)
    MVI  M, ST_READY                ; Записываем код готовности (02h) в дескриптор.
                                    ; Теперь при следующем проходе планировщик увидит эту 
                                    ; задачу и сможет вернуть ей управление.

    ; --- ШАГ 4: Восстановление контекста и выход ---
    POP  D                          ; Восстановили DE
    POP  H                          ; Восстановили HL текущего потока, который был прерван ПДП
    RET                             ; Возврат в тело аппаратного диспетчера прерываний

; =============================================================================
; ПОДПРОГРАММА ЯДРА Ring 0: АСИНХРОННАЯ ДОСТАВКА СИГНАЛА ТЕКУЩЕМУ ПРОЦЕССУ
; =============================================================================
; Вход:  A = Номер POSIX-сигнала (например, 0Bh = SIGBUS)
;        CURRENT_PCB_PTR хранит базовый адрес PCB целевой задачи.
; Выход: В поле O_SIG_PENDING дескриптора процесса взведен соответствующий бит.
;        Все регистры, кроме аккумулятора, строго сохраняются.
; =============================================================================
posix_signal_current_process:
    PUSH H                          ; Спасаем контекст регистров текущего потока
    PUSH B
    PUSH D

    MOV  C, A                       ; C = Номер сигнала (флаг сдвига, 1..16)
    
    ; --- ШАГ 1: Генерация 16-битной битовой маски из номера сигнала ---
    ; Сигнал 1 — это бит 0, Сигнал 2 — бит 1, ..., Сигнал 11 (SIGBUS) — бит 10.
    DCR  C                          ; Сдвигаем номер на -1, чтобы получить индекс бита (0..15)
    
    LXI  H, 0001h                   ; Стартовая маска: взведен самый младший 0-й бит
    MOV  A, C                       ; Проверяем, нужен ли сдвиг
    ORA  A
    JZ   sig_mask_ready             ; Если индекс бита = 0, маска 0001h готова

sig_shift_loop:
    DAD  H                          ; Сдвиг HL влево на 1 бит (умножение маски на 2)
    DCR  C
    JNZ  sig_shift_loop             ; Крутим цикл, пока не сформируем нужную маску в HL

sig_mask_ready:
    ; Теперь в паре HL лежит готовая 16-битная маска сигнала (для SIGBUS это 0400h)
    XCHG                            ; Переносим маску сигнала в пару DE (D=High, E=Low)

    ; --- ШАГ 2: Наложение маски на поле O_SIG_PENDING внутри PCB ---
    LHLD CURRENT_PCB_PTR            ; HL = Базовый 16-битный адрес 64-байтового PCB задачи
    PUSH H                          ; Сохраняем базу PCB в стек ядра
    
    LXI  B, O_SIG_PENDING           ; Смещение до маски ожидающих сигналов (32)
    DAD  B                          ; HL теперь указывает точно на младший байт поля O_SIG_PENDING
    
    ; Атомарно накладываем маску по ИЛИ (OR) на текущие ожидающие сигналы
    ; Младший байт:
    MOV  A, M                       ; Прочитали текущий младший байт сигналов из PCB
    ORA  E                          ; Наложили младший байт нашей маски
    MOV  M, A                       ; Сохранили обратно в PCB
    
    ; Старший байт:
    INX  H                          ; Переходим к старшему байту поля O_SIG_PENDING
    MOV  A, M                       ; Прочитали старший байт сигналов
    ORA  D                          ; Наложили старший байт нашей маски (для SIGBUS прилетит сюда)
    MOV  M, A                       ; Сохранили обратно в PCB

    ; --- ШАГ 3: Пробуждение процесса (ИЗМЕНЕНИЕ ДЛЯ АСИНХРОННОСТИ) ---
    ; Так как процесс мог спать на диске в состоянии ST_WAITING (04H), принудительный 
    ; прилет сигнала (особенно фатального SIGBUS) обязан перевести его в состояние готовности,
    ; чтобы процесс проснулся и обработал ошибку шины.
    POP  H                          ; Восстановили базовый адрес PCB в HL
    LXI  B, O_STATUS                ; Смещение до статуса жизненного цикла (2)
    DAD  B                          ; HL указывает на O_STATUS
    MOV  A, M
    CPI  ST_WAITING                 ; Процесс спал в ожидании I/O?
    JNZ  sig_exit                   ; Если не спал — просто выходим
    
    MVI  M, ST_READY                ; Насильно будим его, чтобы планировщик его вызвал!
    
    ; Очищаем код причины сна, так как мы прервали его сигналом ошибки
    LHLD CURRENT_PCB_PTR
    LXI  B, O_WAIT_CODE
    DAD  B
    MVI  M, 00h                     ; Очистили wait-channel

sig_exit:
    POP  D                          ; Полное восстановление контекста перед возвратом
    POP  B
    POP  H
    RET                             ; Возврат в вызвавший обработчик hw_dma_tc_entry


; =============================================================================
; АППАРАТНЫЙ ОБРАБОТЧИК IR2 ВН59 (АДРЕС 0x0050): ДЕМУЛЬТИПЛЕКСОР ПДП (TC РЕГИСТР)
; =============================================================================
hw_dma_tc_entry:
    PUSH PSW
    PUSH B
    PUSH D
    PUSH H                      ; Теневое сохранение контекста прерванной задачи

    ; --- ШАГ 1: Чтение и атомарный сброс Регистра Статуса КР580ВТ57 ---
    IN   DMA_CMD_STATUS         ; Вычитываем статус (одновременно сбрасываем флаги ВТ57)
    MOV  B, A                   ; Сохраняем считанный статус в регистр B для парсинга

    ; --- ШАГ 2: Тестирование Бита 0 (Флаг TC0 — Канал 0: CompactFlash IDE) ---
    MOV  A, B
    ANI  01H                    ; Изолируем Бит 0 (Маска 00000001B)
    JNZ  dma_handle_cf_ch0      ; Если Бит 0 = 1 — ПДП завершен для нашей CF-карты!

    ; --- ШАГ 3: Тестирование Бита 1 (Флаг TC1 — Канал 1: Устройство №2) ---
    MOV  A, B
    ANI  02H                    ; Изолируем Бит 1 (Маска 00000010B)
    JNZ  dma_handle_device_ch1  

    ; --- ШАГ 4: Тестирование Бита 2 (Флаг TC2 — Канал 2: Устройство №3) ---
    MOV  A, B
    ANI  04H                    ; Изолируем Бит 2 (Маска 00000100B)
    JNZ  dma_handle_device_ch2  

    ; --- ШАГ 5: Тестирование Бита 3 (Флаг TC3 — Канал 3: Устройство №4) ---
    MOV  A, B
    ANI  08H                    ; Изолируем Бит 3 (Маска 00001000B)
    JNZ  dma_handle_device_ch3  

    JMP  dma_tc_exit_eoi

; =============================================================================
; ПОДМОДУЛЬ ОБРАБОТКИ ПРЕРЫВАНИЯ КАНАЛА 0: COMPACTFLASH (IDE16 SWAP)
; =============================================================================
dma_handle_cf_ch0:
    IN   IDE_CMD_STATUS         ; Опрашиваем порт статуса диска (0x27)
    MOV  D, A                   ; Сохраняем статус в регистр D для анализа
    ANI  081h                   ; Маскируем биты: 7 (BSY — занят) и 0 (ERR — ошибка)
    CPI  000h                   ; Проверяем равенство нулю (BSY=0, ERR=0)
    JNZ  dma_cf_error_branch    ; Если диск выставил ошибку или завис — уходим на спец-ветку

    ; Диск чист, данные со 100% успехом перенесены в буфер
    CALL cf_release_lock        ; Атомарно освобождаем мьютекс диска для других задач ядра
    CALL posix_wake_up_blocked_process ; ИЗМЕНЕНО: Переводим поток из состояния WAIT_IO в READY

	JMP  dma_tc_exit_eoi

; -----------------------------------------------------------------------------
; ВЕТКА ОБРАБОТКИ АВАРИЙНЫХ СИТУАЦИЙ COMPACTFLASH
; -----------------------------------------------------------------------------
dma_cf_error_branch:
    MOV  A, D                   
    ANI  001h                   ; Проверяем конкретно бит ошибки ERR
    JZ   dma_cf_timeout         

    IN   021h                   ; Читаем регистр IDE_ERROR
    MOV  B, A                   
    MVI  A, 00Bh                ; Код POSIX-сигнала SIGBUS
    CALL posix_signal_current_process 
    
    CALL cf_release_lock        
    JMP  dma_tc_exit_eoi

dma_cf_timeout:
    MVI  A, 055h                ; Код-маркер "CF Deadlock"
    OUT  0FFh                   
    JMP  KERNEL_PANIC           

; =============================================================================
; СЕРВИСНЫЕ И АВАРИЙНЫЕ ПРОЦЕДУРЫ ЯДРА POSIX ОС (БЛОК ДЛЯ СБОРКИ В ZASM)
; =============================================================================
KERNEL_PANIC:
    ; --- ФАТАЛЬНЫЙ АППАРАТНЫЙ СБОЙ ЯДРА / ТАЙМАУТ ШИНЫ eMMU ---
    DI                              ; Намертво блокируем прерывания на CPU
    MVI  A, 0FFh                    ; Загружаем код критической системной паники
    OUT  0FFh                       ; Сбрасываем код в диагностический порт платы
    HLT                             ; Физический останов процессора КР580ВМ80А

; =============================================================================
; ДРАЙВЕРНЫЕ ЗАГЛУШКИ ДЛЯ ОСТАЛЬНЫХ ТРЕХ УСТРОЙСТВ НА КАНАЛАХ ПДП 1, 2, 3
; =============================================================================
dma_handle_device_ch1:
    ; [Здесь вызывается софтверный бэкэнд обслуживания Канала 1 ПДП]
    ; Например, инкремент счетчиков сетевых пакетов, побудка сетевых потоков.
    JMP  dma_tc_exit_eoi

dma_handle_device_ch2:
    ; [Бэкэнд обслуживания Канала 2 ПДП]
    JMP  dma_tc_exit_eoi

dma_handle_device_ch3:
    ; [Бэкэнд обслуживания Канала 3 ПДП]
    JMP  dma_tc_exit_eoi

; =============================================================================
; ЗАВЕРШЕНИЕ ПРЕРЫВАНИЯ И ВЫХОД В Ring 3
; =============================================================================
dma_tc_exit_eoi:
    MVI  A, 020H				; Команда неспецифического EOI (End of Interrupt)
    OUT  MASTER_8259_CMD        ; Посылаем команду неспецифического EOI в КР580ВН59 (линия IR2)

    POP  H
    POP  D
    POP  B
    POP  PSW                    ; Полное восстановление контекста Ring 3 задачи
    EI                          ; Разрешаем вложенные прерывания
    RET                         ; Выход из прерывания

; =============================================================================
; ФУНКЦИЯ ЯДРА: ВЫДЕЛЕНИЕ ОДНОЙ СВОБОДНОЙ ФИЗИЧЕСКОЙ СТРАНИЦЫ ОЗУ
; =============================================================================
alloc_physical_frame_page:
    ; Вход: Параметры отсутствуют
    ; Выход: A = Номер выделенной физ. страницы (0..31), 
    ;        Либо A = 0FFH (Код ошибки: нет свободной памяти)
    
    LXI  H, RAM_BITMAP          ; Начинаем сканирование с первого байта битовой карты
    MVI  B, 0                   ; B = Счетчик просмотренных страниц (0..31)

alloc_find_byte_loop:
    MOV  A, M                   ; Читаем текущий байт маски (8 страниц)
    CPI  0FFH                   ; Все 8 страниц в этом байте заняты?
    JZ   alloc_next_bitmap_byte ; Если да — быстро переходим к следующему байту

    ; В текущем байте есть свободный бит (равный 0). Ищем его позицию.
    MVI  C, 001H                ; C = Тестируемая битовая маска (00000001B)
alloc_bit_search_loop:
    MOV  A, M                   ; Снова берем байт маски
    ANA  C                      ; Изолируем тестируемый бит
    JZ   alloc_free_bit_found   ; Если результат 0 — мы нашли свободную страницу!
    
    ; Сдвигаем маску для проверки следующего бита в этом байте
    MOV  A, C
    ADD  A                      ; Быстрый сдвиг влево на 1 бит (8080)
    MOV  C, A
    
    INR  B                      ; Увеличиваем общий номер страницы
    MOV  A, B
    ANI  07H                    ; Проверяем, не вышли ли за границу байта (8 бит)?
    JNZ  alloc_bit_search_loop

alloc_next_bitmap_byte:
    ; Скорректируем счетчик B на начало следующего байта (выравнивание по 8 бит)
    MOV  A, B
    ANI  0F8H                   ; Сбрасываем младшие 3 бита
    ADI  8                      ; Прибавляем 8 страниц
    MOV  B, A
    
    INX  H                      ; Переходим к следующему байту RAM_BITMAP
    MOV  A, B
    CPI  TOTAL_RAM_PAGES        ; Проверили всю память (256 страниц)?
    JNZ  alloc_find_byte_loop
    
    ; Вся физическая память заполнена!
    MVI  A, 0FFH                ; Возвращаем маркер ошибки (нет свободных фреймов)
    RET

alloc_free_bit_found:
    ; Физическая страница найдена. Номер страницы находится в регистре B.
    ; Нам необходимо установить этот бит в 1, чтобы зафиксировать захват памяти.
    MOV  A, M
    ORA  C                      ; Выставляем бит занятости через логическое ИЛИ
    MOV  M, A                   ; Сохраняем обновленный байт маски обратно в RAM_BITMAP
    
    MOV  A, B                   ; Возвращаем номер выделенной страницы в Аккумуляторе
    RET

; =============================================================================
; ФУНКЦИЯ ЯДРА: ОСВОБОЖДЕНИЕ ФИЗИЧЕСКОЙ СТРАНИЦЫ ОЗУ
; =============================================================================
free_physical_frame_page:
    ; Вход: A = Номер освобождаемой физической страницы памяти (0..31)
    ; Выход: Параметры отсутствуют. Бит в RAM_BITMAP сбрасывается в 0.
    
    MOV  B, A                   ; Сохраняем номер страницы в B
    
    ; Вычисляем, в каком байте RAM_BITMAP лежит этот бит: Индекс = Номер / 8
    ANI  0F8H                   ; Изолируем старшие биты (выполняем деление на 8 со сдвигом)
    RRC
    RRC
    RRC                         ; Сдвиг на 3 бита вправо -> A = индекс байта (0..3)
    MOV  E, A
    MVI  D, 0
    LXI  H, RAM_BITMAP
    DAD  D                      ; HL указывает точно на нужный байт маски
    
    ; Вычисляем битовую маску сброса: Маска = 1 << (Номер % 8)
    MOV  A, B
    ANI  07H                    ; A = Номер бита внутри байта (0..7)
    MOV  B, A                   ; B = Счетчик сдвигов
    
    MVI  C, 001H                ; Стартовый бит
    MOV  A, B
    CPI  0
    JZ   free_mask_ready        ; Если номер бита = 0, маска 00000001B уже готова

free_mask_shift_loop:
    MOV  A, C
    ADD  A                      ; Сдвиг влево на 1 бит
    MOV  C, A
    DCR  B
    JNZ  free_mask_shift_loop

free_mask_ready:
    ; Инвертируем маску, чтобы получить 0 в целевом бите и 1 в остальных
    MOV  A, C
    CMA                         ; Инверсия аккумулятора (команда 8080)
    MOV  C, A                   ; Теперь в C маска вида 11110111B
    
    ; Сбрасываем бит в битовой карте памяти
    MOV  A, M
    ANA  C                      ; Сброс бита через логическое И
    MOV  M, A                   ; Сохраняем измененный байт маски назад в ОЗУ ядра
    
    RET

; =============================================================================
; 1. ПОДПРОГРАММА СВЕРХБЫСТРОГО РАСЧЕТА СЕКТОРОВ LBA НА COMPACTFLASH
; Формула: LBA = (PID * 8 + Page_Num) * 32
; =============================================================================
swap_calc_lba:
    ; Вход: SWAP_CALC_PID, SWAP_CALC_PAGE
    ; Выход: SWAP_LBA_RESULT (16-битное число в паре HL)
    LDA  SWAP_CALC_PID          ; A = PID процесса (1..254)
    MOV  B, A                   ; Сохраняем PID в B

    ; Вычисляем PID * 8 через 3 быстрых сдвига влево (ADD A)
    ADD  A                      ; PID * 2
    ADD  A                      ; PID * 4
    ADD  A                      ; PID * 8
    
    MOV  C, A                   ; C = PID * 8
    LDA  SWAP_CALC_PAGE         ; A = Page_Num (0..7)
    ADD  C                      ; A = PID * 8 + Page_Num
    
    ; Переносим результат в 16-битную пару HL для умножения на 32
    MOV  L, A
    MVI  H, 0                   ; HL = (PID * 8 + Page_Num)

    ; Выполняем 5 сдвигов влево 16-битной пары HL через DAD H (*32)
    DAD  H                      ; результат * 2
    DAD  H                      ; результат * 4
    DAD  H                      ; результат * 8
    DAD  H                      ; результат * 16
    DAD  H                      ; результат * 32! Стартовый LBA сектор найден!
    
    SHLD SWAP_LBA_RESULT        ; Сохраняем вычисленный сектор LBA в ОЗУ ядра
    RET

; =============================================================================
; ПОДПРОГРАММА ЯДРА: ВЫГРУЗКА СТРАНИЦЫ С ПРИМЕНЕНИЕМ ВНЕШНЕГО ПДП
; =============================================================================
swap_out_physical_page:
    ; Вход: A  = Номер физической страницы ОЗУ (0..31), которую нужно выгрузить
    ;       BC = Вычисленный 16-битный базовый номер LBA-сектора на CompactFlash
    ; Выход: Аппаратный фрейм сброшен на CF через ПДП
    
    STA  SWAP_PAGE_NUM_BUF      ; Спрятали номер фрейма ОЗУ
    MOV  A, B
    STA  SWAP_LBA_TARGET_H      ; Сохранили старший байт LBA
    MOV  A, C
    STA  SWAP_LBA_TARGET_L      ; Сохранили младший байт LBA

    ; --- ЭТАП 1: Программный перенос из ОЗУ Ring 3 в буфер ядра ---
    OUT  076H                   ; Включаем ~{KERNEL_OVERRIDE_EN} = 0
    LDA  SWAP_PAGE_NUM_BUF
    OUT  072H                   ; Проецируем вытесняемую страницу Ring 3 в Сегмент 2 (0x8000)

    LXI  H, 08000H              ; Источник: Окно Сегмента 2 пользовательской задачи
    LXI  D, SWAP_SYS_BUFFER     ; Назначение: Системный буфер ОЗУ Ядра (16 КБ)
    LXI  B, 4096                ; Счетчик на 4096 итераций (перенос по 4 байта)
swap_out_copy_loop:
    MOV  A, M
	STAX D
	INX H
	INX D
    MOV  A, M
	STAX D
	INX H
	INX D
    MOV  A, M 
	STAX D 
	INX H
	INX D
    MOV  A, M
	STAX D
	INX H
	INX D
    DCX  B
	MOV A, B
	ORA C
	JNZ swap_out_copy_loop

    ; Восстанавливаем оригинальную страницу Ядра в окне Сегмента 2 eMMU
    CALL get_current_pcb_address
    LXI  D, O_PAGES_MAP + 2
	DAD D
	MOV A, M
	OUT 072H 
    OUT  076H                   ; Выключили режим KERNEL_OVERRIDE

    ; --- ЭТАП 2: Аппаратный ПДП-вывод из буфера ядра на диск ---
    ; ПЕРВЫЙ КРИТИЧЕСКИЙ ВЫЗОВ: Взводим КР580ВТ57 на Канал 0!
    ; Параметры: HL = адрес буфера ядра, BC = 16384 байт, D = 01H (Режим Записи/Вывода из ОЗУ)
    LXI  H, SWAP_SYS_BUFFER     ; Источник для ПДП: буфер ядра
    LXI  B, 16384               ; Полный объем 16 КБ страницы
    MVI  D, 01H                 ; Направление: Запись (Данные из ОЗУ улетают в периферию)
    CALL dma_start_ch0_transfer ; ВЫЗОВ ФУНКЦИИ: Контроллер ПДП настроен и ждет DRQ0!

    ; Настраиваем контроллер диска на запись 32 секторов
    MVI  A, 0E0H
	OUT IDE_DRV_HEAD
    MVI  A, 32
	OUT IDE_SECTOR_CNT
    LDA  SWAP_LBA_TARGET_L
	OUT IDE_LBA_LOW
    LDA  SWAP_LBA_TARGET_H
	OUT IDE_LBA_MID
    XRA  A
	OUT IDE_LBA_HIGH

    ; Аппаратный старт: выдаем чипу команду записи
    MVI  A, IDE_CMD_WRITE
    OUT  IDE_CMD_STATUS         ; В этот микротакт CF выставит DRQ0, ВТ57 заберет шину!
                                ; CPU засыпает в HOLD на 26.2 мс, ПДП качает данные.
    RET

; =============================================================================
; 3. УМНАЯ ПОДПРОГРАММА МНОГОКРАТНОГО ПОВТОРНОГО ВЫТЕСНЕНИЯ СТРАНИЦЫ С DIRTY BIT
; =============================================================================
swap_out_page_smart:
    ; Вход: A = Номер физической страницы ОЗУ (0..31), которую ядро хочет забрать
    ;       B = Номер логической страницы процесса (0..7), которую вытесняем
    ;       C = PID процесса-жертвы
    ; Выход: Фрейм ОЗУ гарантированно синхронизирован с диском и освобожден.
    STA  SWAP_PAGE_NUM_BUF      ; Сохраняем физический номер фрейма ОЗУ
    MOV  A, B
    STA  SWAP_CALC_PAGE         ; Занесли логический номер страницы (0..7)
    MOV  A, C
    STA  SWAP_CALC_PID          ; Занесли PID жертвы

    ; Вычисляем адрес PCB жертвы: PCB_TABLE + (PID * 64)
    LDA  SWAP_CALC_PID
    MOV  L, A
    MVI  H, 0
    DAD  H
    DAD  H
    DAD  H
    DAD  H
    DAD  H
    DAD  H                      ; HL = PID * 64
    LXI  D, PCB_TABLE
    DAD  D                      ; HL = База PCB жертвы
    SHLD SWAP_VICTIM_PCB_PTR    ; Сохранили базовый адрес PCB жертвы

    LXI  D, O_PAGE_DIRTY_MASK
    DAD  D                      ; HL указывает на O_PAGE_DIRTY_MASK (+41)
    MOV  A, M
    STA  SWAP_DIRTY_MASK_BUF    ; Сохранили текущую маску загрязнения процесса

    ; Формируем битовую маску для нашей логической страницы: Маска = 1 << Page_Num
    LDA  SWAP_CALC_PAGE
    MOV  B, A                   ; B = Количество сдвигов (0..7)
    MVI  C, 001H                ; Стартовый тестирующий бит
    MOV  A, B
    CPI  0
    JZ   swap_smart_mask_ready
swap_smart_mask_shift:
    MOV  A, C
    ADD  A                      ; Сдвиг влево (ADD A)
    MOV  C, A
    DCR  B
    JNZ  swap_smart_mask_shift
swap_smart_mask_ready:
    MOV  A, C
    STA  SWAP_TARGET_BIT_MASK   ; Сохранили маску тестируемого бита

    ; Проверяем бит загрязнения: (O_PAGE_DIRTY_MASK & Target_Bit)
    LDA  SWAP_DIRTY_MASK_BUF
    MOV  B, A
    LDA  SWAP_TARGET_BIT_MASK
    ANA  B                      ; Изолируем целевой бит
    JZ   swap_out_page_is_clean ; Если бит = 0 — страница ЧИСТАЯ! Запись на CF не нужна!

    ; СТРАНИЦА ГРЯЗНАЯ — ВЫПОЛНЯЕМ ФИЗИЧЕСКУЮ ПЕРЕЗАПИСЬ НА ДИСК
    CALL swap_calc_lba          ; Расчет LBA сектора подкачки -> результат в HL
    MOV  B, H
    MOV  C, L                   ; BC = Стартовый LBA на CompactFlash
    
    LDA  SWAP_PAGE_NUM_BUF      ; Восстанавливаем номер физической страницы ОЗУ
    CALL swap_out_physical_page ; Физически пишем 16 КБ (32 сектора) на CF через порты IDE

swap_out_page_is_clean:
    ; Обновление системных масок в PCB процесса-жертвы
    LHLD SWAP_VICTIM_PCB_PTR
    LXI  D, O_CF_SWAP_MASK
    DAD  D                      ; HL указывает на O_CF_SWAP_MASK (+16)
    
    ; Выставляем флаг: страница теперь находится на диске
    MOV  A, M
    PUSH H
    LDA  SWAP_TARGET_BIT_MASK
    MOV  B, A
    POP  H
    MOV  A, M
    ORA  B                      ; Взвели бит присутствия в Swap
    MOV  M, A

    ; Сброс флага грязной страницы (так как ОЗУ и Диск теперь идентичны)
    LHLD SWAP_VICTIM_PCB_PTR
    LXI  D, O_PAGE_DIRTY_MASK
    DAD  D                      ; HL указывает на O_PAGE_DIRTY_MASK (+41)
    
    LDA  SWAP_TARGET_BIT_MASK
    CMA                         ; Инвертируем маску бита (например, 11111011B)
    MOV  B, A
    MOV  A, M
    ANA  B                      ; Сбросили бит Dirty в 0 — страница синхронизирована
    MOV  M, A

    RET

; =============================================================================
; ФУНКЦИЯ ЯДРА: ДВУХЭТАПНЫЙ ПОДЪЕМ СТРАНИЦЫ С УЧЕТОМ БЛОКИРОВОК (SWAP IN AUTO)
; =============================================================================
swap_in_auto:
    ; Вход: A = Номер свободной физической страницы ОЗУ Ring 3, куда принять данные
    ;       B = Номер логической страницы процесса (0..7), которую возвращаем
    ;       C = PID процесса-владельца
    ; Выход: Данные подняты через ПДП в буфер ядра, а затем программно скопированы 
    ;        в целевой фрейм Ring 3. Блокировки сняты, маски PCB обновлены.

    STA  SWAP_IN_FRAME_BUF      ; Сохраняем номер физического фрейма назначения
    MOV  A, B
    STA  SWAP_CALC_PAGE         ; Занесли логический номер страницы для расчета LBA
    MOV  A, C
    STA  SWAP_CALC_PID          ; Занесли PID владельца для расчета LBA

    ; --- ШАГ 1: Монопольный захват CompactFlash ---
    ; Если диск занят — cf_acquire_lock усыпит текущий процесс, сбросит стек 
    ; и планировщик переключит CPU. Безопасность портов 0x30-0x37 гарантирована.
    CALL cf_acquire_lock        

    ; Вычисляем базовый LBA-сектор на CompactFlash подкачки -> результат в HL
    CALL swap_calc_lba          

    ; --- ШАГ 2: ЭТАП 1 — АППАРАТНАЯ ПДП ПЕРЕКАЧКА (CF -> SWAP_SYS_BUFFER) ---
    ; Настраиваем внешний ПДП-контроллер КР580ВТ57 на Канал 0
    ; Передаем параметры: HL = адрес буфера ядра, BC = 16384 байт, D = 00H (Чтение)
    PUSH H                      ; Спасаем вычисленный LBA-адрес накопителя
    LXI  H, SWAP_SYS_BUFFER     ; Целевой физический буфер в ОЗУ Ядра
    LXI  B, 16384               ; Объем пересылки — полная страница 16 КБ
    MVI  D, 00H                 ; Направление: Чтение (CF -> ОЗУ)
    CALL dma_start_ch0_transfer ; Внешний модуль ПДП взведен и ждет DRQ0
    POP  H                      ; Восстановили LBA-адрес диска в HL

    ; Инициализируем контроллер CompactFlash на чтение 32 секторов подкачки
    MVI  A, 0E0H
    OUT  IDE_DRV_HEAD           ; Режим LBA (Master Drive)
    MVI  A, 32
    OUT  IDE_SECTOR_CNT         ; Настраиваем чтение 32 секторов (16 КБ за раз)
    
    MOV  A, L
    OUT  IDE_LBA_LOW            ; Прописали младший адрес LBA
    MOV  A, H
    OUT  IDE_LBA_MID            ; Прописали средний адрес LBA
    XRA  A
    OUT  IDE_LBA_HIGH           ; Старшие биты LBA для swap-зоны зануляем

    MVI  A, IDE_CMD_READ        ; Выстреливаем команду чтения секторов в чип диска
    OUT  IDE_CMD_STATUS

    ; !!! ВНИМАНИЕ: АППАРАТНЫЙ ПРОСТОЙ ПРОЦЕССОРА НА 26.2 МС !!!
    ; CompactFlash выставит сигнал готовности сектора, дернет DRQ0, и КР580ВТ57
    ; заблокирует процессор линией HOLD. Вся страница аппаратно перельется в SWAP_SYS_BUFFER.
    ; Как только ПДП закончит пересылку, снимется HOLD и сработает прерывание TC.
    ; Наша RTOS проснется в обработчике hw_dma_tc_entry, который вернет управление сюда.

    ; --- ШАГ 3: ЭТАП 2 — ПРОГРАММНЫЙ ПЕРЕНОС (Буфер Ядра -> ОЗУ Ring 3) ---
    ; Включаем режим программного оверрайд-окна пользовательской памяти
    OUT  076H                   ; Сигнал ~{KERNEL_OVERRIDE_EN} = 0
    
    ; Подключаем выделенный свободный фрейм Ring 3 в логическое окно Сегмента 2 (0x8000)
    LDA  SWAP_IN_FRAME_BUF
    OUT  072H                   ; Перепрошиваем К555ИР26 для Сегмента 2

    ; Переносим 16 КБ из буфера ядра в спроецированное окно ОЗУ Ring 3
    LXI  H, SWAP_SYS_BUFFER     ; HL = Наш буфер ядра (Источник данных)
    LXI  D, 08000H              ; DE = Адрес окна Сегмента 2 eMMU (Назначение)
    LXI  B, 4096                ; Переносим по 4 байта за шаг -> 4096 итераций

swap_in_copy_loop:
    ; СЛОВО 1 (2 байта)
    MOV  A, M
    STAX D
    INX  H
	INX  D
    MOV  A, M
    STAX D
    INX  H
	INX  D
    ; СЛОВО 2 (2 байта)
    MOV  A, M
    STAX D
    INX  H
	INX  D
    MOV  A, M
    STAX D
    INX  H
	INX  D
    
    DCX  B                      ; Декремент 16-битного счетчика итераций
    MOV  A, B
    ORA  C
    JNZ  swap_in_copy_loop      ; Цикл идет, пока все 16 КБ не сядут в ОЗУ Ring 3

    ; Восстанавливаем оригинальную страницу Ядра в окне Сегмента 2 eMMU
    CALL get_current_pcb_address
    LXI  D, O_PAGES_MAP + 2     
    DAD  D
    MOV  A, M
    OUT  072H                   ; Вернули системную страницу памяти на место в окно 0x8000
    
    ; Отключаем режим KERNEL_OVERRIDE, захлопывая окно доступа в Ring 3
    OUT  076H                   ; Сигнал ~{KERNEL_OVERRIDE_EN} = 1

    ; --- ШАГ 4: Сброс масок подкачки и загрязнения в PCB процесса ---
    ; Вычисляем маску бита логической страницы: Маска = 1 << Page_Num
    LDA  SWAP_CALC_PAGE
    MOV  B, A                   ; B = Количество сдвигов (0..7)
    MVI  C, 001H                ; Стартовый бит
    MOV  A, B
    CPI  0
    JZ   swap_in_mask_done
swap_in_mask_shift_loop:
    MOV  A, C
    ADD  A
    MOV  C, A
    DCR  B
    JNZ  swap_in_mask_shift_loop
swap_in_mask_done:
    MOV  A, C
    CMA                         ; Инвертируем маску (например, 11111011B)
    STA  SWAP_IN_MASK_BIT       ; Cохранили маску инверсии сброса бита

    ; Сбрасываем бит присутствия в Swap маске текущего PCB
    CALL get_current_pcb_address
    PUSH H                      ; Сохранили базовый адрес PCB
    LXI  D, O_CF_SWAP_MASK
    DAD  D                      ; HL указывает на O_CF_SWAP_MASK (+16)
    LDA  SWAP_IN_MASK_BIT
    MOV  B, A
    MOV  A, M
    ANA  B                      ; Сбросили бит присутствия в Swap в 0
    MOV  M, A

    ; Сбрасываем Dirty-бит этой страницы в 0 (так как ОЗУ и диск сейчас идентичны)
    POP  H                      ; Восстановили базовый адрес PCB
    LXI  D, O_PAGE_DIRTY_MASK
    DAD  D                      ; HL указывает на O_PAGE_DIRTY_MASK (+41)
    MOV  A, M
    ANA  B                      ; Сбросили бит грязной страницы в 0 (страница чистая)
    MOV  M, A

    ; --- ШАГ 5: Освобождение CompactFlash и побудка очереди ---
    CALL cf_release_lock        ; Диск свободен, будим все процессы из ожидания W_CF_BUSY

    RET

; =============================================================================
; ПОДПРОГРАММА ФИКСАЦИИ МОДИФИКАЦИИ ДАННЫХ (SWAP MARK DIRTY)
; =============================================================================
swap_mark_dirty:
    ; Вход: A = Номер измененной логической страницы процесса (0..7)
    ; Выход: Бит в O_PAGE_DIRTY_MASK взведен в 1.
    
    MOV  B, A                   ; Сохраняем номер логической страницы в B
    
    ; Формируем битовую маску: Маска = 1 << Номер страницы
    MVI  C, 001H                ; Стартовый бит
    MOV  A, B
    CPI  0
    JZ   swap_mark_mask_ready
swap_mark_mask_shift:
    MOV  A, C
    ADD  A                      ; Сдвиг влево (ADD A)
    MOV  C, A
    DCR  B
    JNZ  swap_mark_mask_shift
swap_mark_mask_ready:
    ; Маска сформирована в регистре C
    
    CALL get_current_pcb_address ; HL = Базовый адрес PCB текущей активной задачи
    LXI  D, O_PAGE_DIRTY_MASK
    DAD  D                      ; HL указывает на O_PAGE_DIRTY_MASK (+41)
    
    MOV  A, M
    ORA  C                      ; Взводим целевой бит Dirty-маски в 1
    MOV  M, A                   ; Сохраняем обновленный байт маски грязи в PCB
    
    RET

; =============================================================================
; ВСПРМОГАТЕЛЬНАЯ ФУНКЦИЯ ЯДРА: ОПРЕДЕЛЕНИЕ И МАРКИРОВКА ГРЯЗНОЙ СТРАНИЦЫ
; =============================================================================
mark_user_page_dirty_by_addr:
    ; Вход: HL = Логический адрес в пространстве Ring 3 (куда выполнялась запись)
    ; Выход: Вычисляется номер страницы (0..3) на основе старших бит адреса,
    ;        вызывается swap_mark_dirty. Регистры HL сохраняются.
    PUSH H
    PUSH B
    MOV  A, H                   ; Берём старший байт логического адреса (биты 15-8)
    RLC
    RLC                         ; Сдвигаем биты 15-14 в позиции 1-0 аккумулятора
    ANI  03H                    ; Изолируем номер логического сегмента (0..3)
    CALL swap_mark_dirty        ; Выставляем Dirty Bit для измененной страницы
    POP  B
    POP  H
    RET

; =============================================================================
; АППАРАТНЫЙ ОБРАБОТЧИК ПРЕРЫВАНИЙ USART КР580ВВ51А (Адрес 0x0048, Вектор IR1)
; =============================================================================
hw_uart_entry:
    PUSH PSW
	PUSH B
	PUSH D
	PUSH H ; Сохраняем регистры прерванного контекста
    
    ; Считываем регистр статуса ВВ51А для определения источника прерывания
    IN   UART_CMD               ; Аккумулятор A = статус UART
    MOV  B, A                   ; Сохраняем статус в регистре B

    ; --- ПРОВЕРКА И ПРИЕМ ДАННЫХ (Бит 1: RxRDY — Приемник готов) ---
    MOV  A, B
    ANI  02H                    ; Маска бита RxRDY (00000010B)
    JZ   uart_check_tx          ; Если бит равен 0 — символ не прилетал, проверяем передачу

    ; Проверяем аппаратные ошибки кадра (Бит 3: PE, Бит 4: OE, Бит 5: FE)
    MOV  A, B
    ANI  038H                   ; Маска ошибок 00111000B
    JZ   uart_read_valid_byte   ; Если ошибок нет — идем читать данные
    
    ; Ошибка обнаружена (оверран или фазирование) — сбрасываем флаги ошибок командным словом
    MVI  A, 037H                ; Команда: Сброс ошибок (ER), разрешить RX/TX
    OUT  UART_CMD
    IN   UART_DATA              ; Считываем "битый" байт в пустоту, чтобы очистить буфер БИС
    JMP  uart_check_tx

uart_read_valid_byte:
    IN   UART_DATA              ; Извлекаем легитимный символ из ВВ51А
    MOV  C, A                   ; Сохраняем символ в регистре C

    ; Помещаем символ C в системный кольцевой буфер TTY_RX_BUF
    LDA  TTY_RX_HEAD
    MOV  E, A                   ; E = текущая голова (Head)
    INR  A
    ANI  TTY_BUF_SIZE - 1       ; Циклический сдвиг по маске (63)
    MOV  D, A                   ; D = предполагаемая новая голова
    
    LDA  TTY_RX_TAIL            ; Проверяем, не переполнен ли буфер (Новая голова == Хвост?)
    CMP  D
    JZ   uart_rx_overflow       ; Если буфер переполнен — игнорируем входящий символ
    
    ; Записываем байт в массив: Адрес = TTY_RX_BUF + HEAD
    MOV  L, E
	MVI H, 0
    LXI  D, TTY_RX_BUF
	DAD D
    MOV  M, C                   ; Байт сохранен в буфере ядра
    
    MOV  A, D                   ; Обновляем указатель TTY_RX_HEAD новой головой
    STA  TTY_RX_HEAD

    ; --- МЕХАНИЗМ АКТИВАЦИИ POSIX-ПРОЦЕССОВ (Побудка по вводу) ---
    ; Сканируем таблицу процессов и будим ту задачу, которая спала на вводе tty0
    LXI  H, PCB_TABLE
	MVI E, 0
uart_wakeup_loop:
    PUSH H
    LXI  D, O_STATUS
	DAD D
	MOV A, M
	CPI ST_WAITING ; Процесс спит?
    JNZ  uart_wakeup_next
    
    POP  H
	PUSH H
    LXI  D, O_WAIT_CODE
	DAD D
	MOV A, M
	CPI W_TTY_IN ; Спит именно на вводе с клавиатуры?
    JNZ  uart_wakeup_next
    
    ; Задача найдена! Переводим её в статус ST_READY для планировщика
    POP  H
	PUSH H
    LXI  D, O_STATUS
	DAD D
	MVI M, ST_READY
    
uart_wakeup_next:
    POP  H
	LXI D, PCB_SIZE
	DAD D
    INR  E
	MOV A, E
	CPI MAX_PROCS + 1
	JNZ uart_wakeup_loop

uart_rx_overflow:
uart_check_tx:
    ; --- ПРОВЕРКА И ПЕРЕДАЧА ДАННЫХ (Бит 0: TxRDY — Передатчик готов) ---
    MOV  A, B
    ANI  01H                    ; Маска бита TxRDY (00000001B)
    JZ   uart_interrupt_done    ; Если передатчик занят — обработка завершена

    ; Проверяем, есть ли символы в выходном буфере ядра (HEAD == TAIL?)
    LDA  TTY_TX_HEAD
    MOV  E, A
    LDA  TTY_TX_TAIL
    CMP  E
    JZ   uart_tx_buffer_empty   ; Если буфер пуст — передавать нечего

    ; Извлекаем следующий байт из TTY_TX_BUF по указателю TAIL
    MOV  L, A
	MVI H, 0
    LXI  D, TTY_TX_BUF
	DAD D
    MOV  A, M                   ; Читаем символ из ОЗУ ядра
    OUT  UART_DATA              ; Отправляем аппаратно в КР580ВВ51А на линию связи!

    ; Инкремент циклического хвоста TTY_TX_TAIL
    LDA  TTY_TX_TAIL
    INR  A
    ANI  TTY_BUF_SIZE - 1       ; Ограничение по маске размера буфера
    STA  TTY_TX_TAIL
    JMP  uart_interrupt_done

uart_tx_buffer_empty:
    ; Буфер ядра опустел. Чтобы линия IR1 не залипала в 1 из-за постоянного флага TxRDY БИС,
    ; мы можем временно замаскировать или сбросить команду передачи, если это реализовано аппаратно.
    ; В нашей базовой конфигурации достаточно просто выйти, так как при записи нового символа 
    ; функция sys_write сама вытолкнет первый байт для старта конвейера прерываний.

uart_interrupt_done:
    MVI  A, 020H
    OUT  MASTER_8259_CMD        ; Посылаем команду неспецифического EOI в контроллер ВН59
    POP  H
	POP  D
	POP  B
	POP  PSW ; Восстанавливаем сохраненный контекст
    EI                          ; Разрешаем вложенные прерывания
    RET                         ; Аппаратный возврат

; =============================================================================
; ПОДПРОГРАММА ЯДРА: ПАРСИНГ MBR И ВЕРИФИКАЦИЯ РАЗДЕЛОВ ДИСКА ЧЕРЕЗ ПДП
; =============================================================================
; =============================================================================
; ПОДПРОГРАММА ЯДРА: ПАРСИНГ MBR И ВЕРИФИКАЦИЯ РАЗДЕЛОВ ДИСКА ЧЕРЕЗ ПДП
; =============================================================================
; Вход:  Сектор 0 (MBR) уже загружен контроллером/ПДП в буфер ядра IDE_SYS_BUFFER
; Выход: Системные переменные заполнены геометрией из таблицы разделов.
;        При отсутствии раздела подкачки или неверной структуре — KERNEL_PANIC.
; =============================================================================

; --- КОНСТАНТЫ АДРЕСАЦИИ ТАБЛИЦЫ MBR В СИСТЕМНОМ БУФЕРЕ ---
MBR_TABLE_OFFSET EQU 01BEh           ; Смещение таблицы разделов в секторе MBR
; Так как буфер расположен по фиксированному адресу IDE_SYS_BUFFER,
; адрес первой записи формируется как: IDE_SYS_BUFFER + MBR_TABLE_OFFSET

vfs_init_mbr:
    ; --- Шаг 3.1: Обнуление новых 32-битных системных переменных ядра ---
    LXI  H, SWAP_LBA
    MVI  B, 16                      ; 4 переменные (LBA/SIZE для двух ФС) * 4 байта = 16 байт
mbr_clear_loop:
    MVI  M, 0
    INX  H
    DCR  B
    JNZ  mbr_clear_loop

    ; --- Шаг 3.2: Инициализация цикла сканирования таблицы MBR ---
    LXI  H, IDE_SYS_BUFFER          ; Базовый адрес секторного буфера ОЗУ
    LXI  D, MBR_TABLE_OFFSET        ; Смещение до Partition 1
    DAD  D                          ; HL теперь указывает на старт Записи №1 (обычно 0x41BE)
    MVI  C, 4                       ; В классической структуре MBR ровно 4 первичные записи

mbr_scan_loop:
    PUSH H                          ; Спасаем в стек ядра указатель на начало текущей записи (16 байт)
    
    ; Смещение до байта File System ID (Partition Type) составляет ровно +4 байта от начала записи
    LXI  D, 4
    DAD  D
    MOV  A, M                       ; Считываем идентификатор файловой системы (ID) из таблицы

    CPI  SWAP_ID                    ; Проверяем совпадение с идентификатором "Memory Dump / SWAP" (0xDE)
    JZ   mbr_extract_swap

    CPI  EXPES_ID                   ; Проверяем совпадение с "Experimental FS" (0xE0)
    JZ   mbr_extract_expes

mbr_skip_partition:
    ; Если раздел не совпал с искомыми типами, переходим к следующему слоту таблицы
    POP  H                          ; Восстановили указатель на начало текущей записи из стека
    LXI  D, 16                      ; Размер одной записи таблицы MBR составляет строго 16 байт
    DAD  D                          ; HL указывает на следующую запись таблицы разделов
    DCR  C                          ; Уменьшаем счетчик оставшихся для анализа записей (4 -> 0)
    JNZ  mbr_scan_loop              ; Если проверили не все 4 записи — продолжаем цикл
    JMP  mbr_validate_results       ; Таблица полностью просканирована, переходим к валидации

; --- ПОДПРОГРАММЫ ПОБАЙТОВОГО ИЗВЛЕЧЕНИЯ 32-БИТНЫХ ПАРАМЕТРОВ ---

mbr_extract_swap:
    ; HL сейчас указывает на байт ID (+4 от старта записи). 
    ; Смещение до поля стартового LBA-адреса (+8) составляет еще +4 байта.
    LXI  D, 4
    DAD  D                          ; HL указывает на младший байт (Byte 0) поля 32-битного LBA
    LXI  D, SWAP_LBA                ; Целевой адрес назначения в защищенной памяти ядра
    CALL mbr_copy_32_bytes          ; Копируем 4 байта LBA-адреса. HL автоматически сдвинется на +4
    ; Теперь HL указывает точно на поле Partition Size (+12 от начала записи)
    LXI  D, SWAP_SIZE               ; Назначение: переменная размера раздела подкачки
    CALL mbr_copy_32_bytes
    JMP  mbr_skip_partition

mbr_extract_expes:
    LXI  D, 4
    DAD  D                          ; HL указывает на младший байт (Byte 0) 32-битного LBA Experimental FS
    LXI  D, EXPES_LBA               ; Целевой адрес переменной в ядре
    CALL mbr_copy_32_bytes          ; Извлекаем LBA-начало файловой системы
    LXI  D, EXPES_SIZE              ; Назначение: размер экспериментального раздела
    CALL mbr_copy_32_bytes          ; Извлекаем полный размер ФС в секторах
    JMP  mbr_skip_partition

; --- ВСПОМОГАТЕЛЬНАЯ ПРОЦЕДУРА ПОБАЙТОВОГО КОПИРОВАНИЯ (4 БАЙТА / 32 БИТА) ---
; Вход: HL - Источник (Буфер MBR), DE - Назначение (Переменная ОЗУ ядра)
; Выход: 4 байта скопированы, HL увеличился на 4, DE увеличился на 4.
mbr_copy_32_bytes:
    MVI  B, 4                       ; Счетчик циклов на 4 байта
mbr_copy_loop:
    MOV  A, M
    STAX D                          ; Сохраняем байт по адресу Little-Endian структуры
    INX  H
    INX  D
    DCR  B
    JNZ  mbr_copy_loop
    RET

; --- Шаг 3.3: Итоговая валидация структуры накопителя ---
mbr_validate_results:
    ; POSIX ОС eMMU требует обязательного наличия раздела подкачки для Ring 3.
    ; Проверяем, заполнился ли хоть один байт переменной SWAP_SIZE (не равен ли размер нулю).
    LDA  SWAP_SIZE
    MOV  B, A
    LDA  SWAP_SIZE+1
    ORA  B
    MOV  B, A
    LDA  SWAP_SIZE+2
    ORA  B
    MOV  B, A
    LDA  SWAP_SIZE+3
    ORA  B                          ; Сложили по ИЛИ все 4 байта размера своп-зоны
    RNZ                             ; Если результат не равен 0 — swap найден, успешный выход в ОС!

    ; Если раздел подкачки (ID 0xDE) отсутствует, запуск многозадачности невозможен.
    ; Уходим в аварийный останов ядра (KERNEL_PANIC).
    JMP  mbr_panic

mbr_panic:
    DI                              ; Аппаратно отключаем прерывания в Ring 0
    MVI  A, 0DEh                    ; Выставляем отладочный код ошибки SWAP_NOT_FOUND
    ;OUT  0FFh                       ; Сбрасываем код в диагностический порт отладки платы
    HLT                             ; Полный физический останов процессора КР580ВМ80А


