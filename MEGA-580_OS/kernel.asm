#target rom
#charset ascii
.asm8080

#include "./include/kernel.inc"

#code CODE, 0000h
; =============================================================================
; НАЧАЛО ТЕНЕВОГО ПЗУ (ХОЛОДНЫЙ СТАРТ С АДРЕСА 0x0000)
; =============================================================================
boot_start:
	DI                       ; Запрещаем прерывания на период холодной настройки БИС
	JMP cold_boot            ; Прыжок на процедуру первичной конфигурации

; =============================================================================
; ТАБЛИЦА ПРОГРАММНЫХ ВЕКТОРОВ (ЛОВУШЕК) PROCESSOR 8080 (0x0008 - 0x003F)
; =============================================================================
    ORG 0008H
	JMP rst_stub			; RST 1 — Системная заглушка программного сброса
    ORG 0010H
	JMP rst_stub			; RST 2 — Системная заглушка программного сброса
    ORG 0018H
	JMP rst_stub			; RST 3 — Системная заглушка программного сброса
    ORG 0020H
	JMP rst_stub 			; RST 4 — Системная заглушка программного сброса
    ORG 0028H
	JMP rst_stub 			; RST 5 — Системная заглушка программного сброса
    ORG 0030H
    JMP rst_stub;sys_call_entry		; RST 6 — МОНОПОЛЬНАЯ ТОЧКА ВХОДА SYSCALL ENTRY (Trap)
    ORG 0038H
	JMP rst_stub 			; RST 7  — Системная заглушка программного сброса

; =============================================================================
; ТАБЛИЦА АППАРАТНЫХ ВЕКТОРОВ ПРЕРЫВАНИЙ ОТ КР580ВН59 (БАЗА 0x0040)
; =============================================================================
    ORG 0040H
	JMP hw_timer_entry 					; ВН59 IR0 — Системный таймер ВИ53 №1 (20 мс)
    ORG 0048H
	JMP hw_stub;hw_uart_entry  					; ВН59 IR1 — Терминал ВВ51А (VT100 tty0)
    ORG 0050H
	JMP hw_stub;hw_dma_tc_entry					; ВН59 IR2 - Контроллер ПДП (CF IDE16)
    ORG 0058H
	JMP hw_stub;hw_net_isr     					; ВН59 IR3 — Контроллер сети (KSZ8851-16MML)
    ORG 0060H
	JMP hw_stub       					; ВН59 IR4 — Свободный аппаратный канал
    ORG 0068H
	JMP hw_stub        					; ВН59 IR5 — Свободный аппаратный канал
    ORG 0070H
	JMP hw_stub;hw_lpt_entry					; ВН59 IR6 — LPT (82C11)
    ORG 0078H 
    JMP hw_privilege_violation_entry	; ВН59 IR7 — Исключение защиты eMMU

; =============================================================================
; НАЧАЛО ТЕНЕВОГО ПЗУ: БЛОК ХОЛОДНОЙ ИНИЦИАЛИЗАЦИИ И ПЕРИФЕРИИ (8 КБ)
; =============================================================================
cold_boot:
    ; Процессор только что сбросился. Мы находимся в Ring 0. 
    ; Триггер DD3 держит ~{ROM_EN}=0. Процессор читает код из ПЗУ (0x0000-0x1FFF).
    
    LXI  SP, KERNEL_STACK_TOP       ; Выставляем временный стек ядра в верхнем ОЗУ

	; =============================================================================
    ; ЭТАП 1: Прямой перенос 8 КБ кода из нижнего ПЗУ в верхнее ОЗУ Ядра
	; =============================================================================
    LXI  H, 00000H              ; HL = Начало Теневого ПЗУ (Источник)
    LXI  D, 08000H              ; DE = Временный буфер в верхнем ОЗУ (Назначение)
    LXI  B, 8192                ; Счетчик длины переноса: ровно 8 КБ (2000H)

copy_to_upper_ram_loop:
    MOV  A, M                   ; Считываем байт системного кода из ПЗУ
    STAX D                      ; Записываем напрямую в верхнее ОЗУ Ядра (адреса 0x8000...)
    INX  H                      ; Инкремент указателя источника ПЗУ
    INX  D                      ; Инкремент указателя назначения ОЗУ
    DCX  B                      ; Декремент 16-битного счетчика длины BC
    MOV  A, B
    ORA  C                      ; Проверяем, обнулился ли счетчик BC?
    JNZ  copy_to_upper_ram_loop ; Если нет — продолжаем качать ядро вверх

	; Копирование завершено. Переносим выполнение процессора в верхнее ОЗУ.
    ; ИСПРАВЛЕНО: Модификация адреса метки под верхнее окно ОЗУ (+0x8000)
    LXI  H, upper_ram_stage2;+08000H 
    PCHL                        ; Программный прыжок PC процессора в микросхемы верхнего ОЗУ!

	; =============================================================================
	; ЭТАП 2: ВЫПОЛНЕНИЕ ИЗ ВЕРХНЕГО ОЗУ ЯДРА И ОТКЛЮЧЕНИЕ ТЕНЕВОГО ПЗУ
	; =============================================================================
upper_ram_stage2:
    ; Процессор аппаратно выполняет эту кодовую строчку уже из микросхем ОЗУ Ядра!
    
    ; АППАРАТНОЕ ОТКЛЮЧЕНИЕ ТЕНЕВОГО ПЗУ НА ПЛАТЕ eMMU
    ; Системный импульс OUT 0x75 сбрасывает внешний триггер DD3. 
    ; ПЗУ навсегда исчезает с шины. В нижнем окне 0x0000-0x1FFF открывается 
    ; физически изолированная «чистая» память нижнего ОЗУ Ядра (Ring 0).
    OUT  075H                   ; ТЕНЕВОЕ ПЗУ НАВСЕГДА ДЕАКТИВИРОВАНО

    ; =============================================================================
	; ЭТАП 3: Зеркальный перенос всех 8 КБ кода из верхнего ОЗУ вниз
	; =============================================================================
    ; Теперь, когда нижнее ОЗУ Ядра открыто, мы сажаем ядро на его каноническое место.
    LXI  H, 08000H              ; HL = Наш верхний временный буфер ОЗУ (Источник)
    LXI  D, 00000H              ; DE = Целевой физический адрес 0 ОЗУ Ядра (Назначение)
    LXI  B, 8192                ; Переносим 8 КБ системного кода вниз

copy_down_to_zero_loop:
    MOV  A, M                   ; Читаем байт ядра из верхней памяти ОЗУ
    STAX D                      ; Пишем в нижнее ОЗУ Ядра на канонический адрес 0
    INX  H                      ; Инкремент указателя источника
    INX  D                      ; Инкремент указателя назначения
    DCX  B                      ; Декремент 16-битного счетчика
    MOV  A, B
    ORA  C                      ; Проверяем окончание переноса
    ; Теперь процессор будет крутиться строго внутри верхних физических адресов ОЗУ,
    ; пока полностью не восстановит нижнюю память ядра!
    JNZ  copy_down_to_zero_loop

    ; =============================================================================
	; ЭТАП 4: Окончательный запуск операционной системы
	; =============================================================================
    ; Передаем управление на канонический адрес старта ядра в нижнем ОЗУ
    LXI  H, kernel_clean_start  ; HL = Логический адрес старта в нижнем ОЗУ
    PCHL                        ; Процессор переходит на исполнение с адреса 0 ОЗУ Ядра!

	; =============================================================================
	; ЭТАП 5: ЧИСТЫЙ СТАРТ ИЗ НИЖНЕГО ОЗУ ЯДРА — НАСТРОЙКА БИС ПЕРИФЕРИИ
	; =============================================================================
kernel_clean_start:
	; =============================================================================
    ; --- ЭТАП 5.1: Инициализация UART (ВВ51+ВИ53)---
	; =============================================================================
    CALL k_uart_hw_init
    ; =============================================================================
	; --- ЭТАП 5.2: Инициализация Контроллера Прерываний ВН59 (База векторов 0x0040)
	; =============================================================================
	CALL hw_int_init
	; =============================================================================
	; Инициализация сетевого контроллера KSZ8851-16MML
	; =============================================================================
	;CALL k_ksz8851_init             ; Перевод чипа 16MLL в 8-битный режим, пуск маски IRQ3
    ;CPI  0FFh                       ; Карта ответила?
    ;JZ   kernel_panic_no_net        ; Если сеть не обнаружена — критический останов (опционально)
	
	; =============================================================================
	; ЭТАП 6: Инициализация виртуальной VFS-таблицы монтирования
	; =============================================================================
    ; Вызываем последовательную цепочку внутренних подпрограмм ранней инициализации
    ;CALL sys_socketfs_init          ; Очистка NET_UNIX_HEADS и монтирование префикса "/dev/sock"
	
    ; =============================================================================
	; ЭТАП 7: Инициализация Системных Таблиц и Таблиц ОЗУ
	; =============================================================================
    ;CALL init_pcb_table          ; Размечаем GLOBAL_RESOURCE_TABLE и слоты PCB

    ; =============================================================================
	; ЭТАП 8: АППАРАТНЫЙ МОНТИНГ ФАЙЛОВОЙ СИСТЕМЫ (ВСТРАИВАНИЕ MBR)
	; =============================================================================
    ; ВНИМАНИЕ! КРИТИЧЕСКАЯ ТОЧКА ВЫЗОВА:
    ;CALL vfs_init_mbr            ; Ядро считывает сектор 0 через ПДП и монтирует VFS на LBA 0x1000
    
    ; Проверяем результат монтирования. Если MBR поврежден (A != 0), уходим в панику ядра
    CPI  00H
    JNZ  kernel_panic_mbr_failed

    ; =============================================================================
	; ЭТАП 9: Запуск многозадачной карусели POSIX ОС
	; =============================================================================
    ; Здесь ядро создает первый пользовательский процесс PID=1 (наш разработанный Shell),
    ; прописывает его начальный контекст в PCB_TABLE и выставляет статус ST_READY.
    ; [Код спавна PID=1]

    MVI  A, 020h                    ; Сброс маски прерываний в КР580ВН59 (EOI)
    OUT  MASTER_8259_CMD
	
    EI                          	; ОФИЦИАЛЬНО РАЗРЕШАЕМ АППАРАТНЫЕ ПРЕРЫВАНИЯ!
    
idle_loop:
    HLT                         	; Спим в экономичном режиме PID=0 до первого тика таймера 20 мс
    JMP  idle_loop

	; =============================================================================
	; --- КРИТИЧЕСКИЙ ОСТАНОВ ПРИ ЗАГРУЗКЕ ---
	; =============================================================================
kernel_panic_no_net:
	; Фатальная ошибка старта: Сетевая карта не найдена.
    ; Выключаем прерывания и намертво вешаем процессор.
    DI
    HLT
	JMP  kernel_panic_mbr_failed
	
kernel_panic_mbr_failed:
    ; Фатальная ошибка старта: MBR не валиден или раздел VFS 0x1000 не найден.
    ; Выключаем прерывания и намертво вешаем процессор для предотвращения порчи диска.
    DI
    HLT
    JMP  kernel_panic_mbr_failed
; =============================================================================
; АППАРАТНЫЙ ОБРАБОТЧИК IR0: КВАНТОВАНИЕ 20 МС И ВЫТЕСНЕНИЕ КОНТЕКСТА
; =============================================================================
hw_timer_entry:
    ; Вход: Аппаратный вектор прерывания 0x0040 (IRQ0).
    ;       SP указывает на стек Ring 0 пользовательской страницы.
    ;       Прерывания запрещены.

    PUSH PSW
    PUSH B
    PUSH D
    PUSH H
    
    LXI  H, 0
    DAD  SP
    LXI  SP, KERNEL_STACK_TOP
    PUSH H

    LHLD CURRENT_PCB_PTR
    LXI  D, O_SAVED_SP
    DAD  D
    POP  D
    MOV  M, E
    INX  H
    MOV  M, D

    LHLD CURRENT_PCB_PTR
    LXI  D, O_STATUS
    DAD  D
    MOV  A, M
    CPI  ST_RUNNING
    JNZ  skip_timer_status_reset
    MVI  M, ST_READY
skip_timer_status_reset:

    ; === ПРОВЕРКА SIGKILL ДЛЯ ТЕКУЩЕЙ ЗАДАЧИ (ДО ПЕРЕКЛЮЧЕНИЯ) ===
    LHLD CURRENT_PCB_PTR
    LXI  D, O_SIG_PENDING + 1
    DAD  D
    MOV  A, M
    ANI  SIGKILL_MASK_H
    JZ   _no_sigkill_current
    
    ; SIGKILL обнаружен — уничтожаем текущую задачу
    MVI  A, 09H
    JMP  sys_exit_fatal

_no_sigkill_current:
    ; === ВЫБОР И АКТИВАЦИЯ НОВОГО КОНТЕКСТА ===
    CALL scheduler
    CALL switch_mmu_context
    
    ; === КАНОНОВЫЙ ВЫХОД В USER SPACE ===
    MVI  A, 020H
    OUT  MASTER_8259_CMD
    EI
    RET

; =============================================================================
; ПОДПРОГРАММА ЯДРА (Ring 0): ДЕАЛЛОКАТОР ФИЗИЧЕСКИХ СТРАНИЦ ПАМЯТИ eMMU
; =============================================================================
; Вход:  A = Номер освобождаемой физической страницы ОЗУ eMMU (0..255).
; Выход: Соответствующий бит в системной RAM_BITMAP сброшен в 0.
;        Регистры BC, DE, HL полностью сохраняются, прерывания не трогаются.
; =============================================================================
; =============================================================================
; НАЗВАНИЕ: k_free_physical_page
; МОДУЛЬ:   Низкоуровневый постраничный деаллокатор ОЗУ eMMU РЕВ_2.0
; =============================================================================
k_free_physical_page:
    ; Вход:  A = Номер освобождаемой физической страницы ОЗУ eMMU (0..255).
    ; Выход: Соответствующий бит в системной RAM_BITMAP сброшен в 0.
    ;        Регистры BC, DE, HL полностью сохраняются.
    PUSH H
    PUSH D
    PUSH B

    MOV  C, A                       ; C = Исходный номер страницы (0..255)

    ; --- ШАГ 1: ТОЧНЫЙ ВЫЧИСЛИТЕЛЬ ИНДЕКСА БАЙТА И НОМЕРА БИТА ---
    ; Выделяем номер бита: Бит = Страница & 07h (Нижние 3 бита)
    ANI  07H
    MOV  B, A                       ; B = Номер бита (0..7) внутри целевого байта

    ; Выделяем индекс байта: Индекс = (Страница >> 3) & 01Fh
    MOV  A, C
    RRC
    RRC
    RRC                             ; Циклический сдвиг вправо на 3 бита
    ANI  01FH                       ; ЖЕСТКИЙ МАТЕМАТИЧЕСКИЙ ФИКС: Очищаем натекание 
                                    ; старших битов из-за циклического вращения RRC!
    MOV  E, A                       ; E = Индекс байта в RAM_BITMAP (0..31)
    MVI  D, 0                       ; DE = 16-битное смещение

    ; Вычисляем физический адрес целевого байта в битовой карте ядра
    LXI  H, RAM_BITMAP
    DAD  D                          ; HL указывает точно на нужный байт карты

    ; --- ШАГ 2: БЫСТРАЯ ГЕНЕРАЦИЯ МАСКИ СБРОСА БИТА ---
    MOV  A, B                       ; A = Номер бита (0..7)
    MOV  C, A                       ; C = Счетчик сдвигов для цикла
    MVI  A, 001H                    ; Стартовая базовая маска (взведен 0-й бит)

    MOV  B, A                       ; Временно прячем стартовую маску в B
    MOV  A, C
    ORA  A                          ; Проверяем счетчик сдвигов на равенство нулю
    MOV  A, B                       ; Восстановили базовую маску в аккумулятор
    JZ   _page_free_mask_ready      ; Если номер бита = 0, маска 01h уже готова

_page_free_shift_loop:
    ADD  A                          ; Эквивалент сдвига влево (A = A + A)
    DCR  C                          ; Уменьшаем счетчик сдвигов
    JNZ  _page_free_shift_loop      ; Цикл крутится ровно столько раз, каков номер бита

_page_free_mask_ready:
    ; Аккумулятор содержит прямую маску бита (например, 04h для 2-го бита).
    CMA                             ; Инвертируем маску (04h превращается в 0FBh)

    ; --- ШАГ 3: АТОМАРНЫЙ СБРОС БИТА В ПАМЯТИ ЯДРА ---
    ANA  M                          ; Сбрасываем целевой бит в 0, удерживая соседей
    MOV  M, A                       ; Записали обновленный байт обратно в RAM_BITMAP

    ; --- ШАГ 4: ВОССТАНОВЛЕНИЕ КОНТЕКСТА И ВЫХОД ---
    POP  B
    POP  D
    POP  H
    RET

; =============================================================================
; ПОДПРОГРАММА ЯДРА (Ring 0): ДИНАМИЧЕСКИЙ РЕГИСТРАТОР АППАРАТНЫХ ISR
; =============================================================================
; Вход:  A  = Номер аппаратной линии прерывания контроллера ВН59 (0..7).
;        HL = 16-битный физический адрес обработчика внутри загруженного модуля.
; Выход: Вектор прошит в релоцируемую ячейку SYS_ISR_TABLE.
; =============================================================================
k_register_isr:
    PUSH PSW
	PUSH B
	PUSH D
	PUSH H
    
    MOV  C, A                       ; C = Номер линии IRQ (0..7)
    ADD  A                          ; Умножаем на 2 (размер слова)
    MOV  E, A
	MVI D, 0            ; DE = Смещение в байтах
    
    LXI  H, SYS_ISR_TABLE           ; Плавающая метка из vfs.mem / инклюда ОЗУ
    DAD  D                          ; HL -> Указывает на целевую ячейку вектора
    
    MOV  M, E                       ; Записали младший байт адреса обработчика драйвера
    INX  H
    MOV  M, D                       ; Записали старший байт адреса

    POP  H
	POP  D
	POP  B
	POP  PSW
    RET

; =============================================================================
; СИСТЕМНЫЙ ПЕРЕХВАТЧИК СБОЕВ Ring 3: ЗАГЛУШКА НЕОБРАБОТАННЫХ ЛОВУШЕК (RST 1-7)
; =============================================================================
rst_stub:
    ; Вход: Сюда процессор КР580ВМ80А попадает аппаратно при вызове нелегального RST.
    ;       В стеке задачи Ring 3 лежит адрес PC, где произошел сбой.
    ;       Мы уже находимся в защищённом режиме Ring 0!
    
    ; Вызываем легитимный системный трап SYS_EXIT, чтобы ядро уничтожило эту задачу!
    
    DI                          	; Запрещаем прерывания таймера на время паники
    
    ; Загружаем в регистр C код легитимного системного вызова SYS_EXIT (1)
    MVI  C, SYS_EXIT               	; SYS_EXIT EQU 1
    
    ; Загружаем в аккумулятор A код возврата фатального сбоя POSIX
    ; Стандартный exit-код при падении по ошибке сегментации/RST = 139 (08BH)
    MVI  A, SIGSEGV+128            	; 139 = 128 + SIGSEGV (Аварийное завершение)
    
    ; Нам нужно передать этот код возврата ядру через регистр C, но в C уже лежит 
    ; номер сисколла (1). Поэтому, согласно нашему соглашению о системных вызовах,
    ; пользовательский параметр exit-кода передается в регистре B (или аккумуляторе).
    MOV  B, A                   	; Переносим код паники 139 в регистр B для sys_exit
    
    ; Официально вызываем программную ловушку Ядра, которая переключит нас в Ring 0
    RST  6                      	; Прыгаем на sys_call_entry
    
    ; Сюда процессор физически никогда не вернется, так как ядро внутри sys_exit
    ; полностью освободит память текущей задачи и переключит eMMU на другой PID.

    ; Финальная аппаратная петля безопасности на случай тотального краха ОС
    HLT
    JMP  rst_stub

; =============================================================================
; АППАРАТНЫЙ ОБРАБОТЧИК ПРЕРЫВАНИЯ: hw_privilege_violation_entry (IRQ7)
; =============================================================================
; Вход:  Сработал триггер нарушения привилегий eMMU. Прерывания заперты (DI).
;        Пользователь попытался сделать IN/OUT/EI/DI напрямую из Ring 3!
; Выход: Нарушитель мгновенно уничтожен, ресурсы освобождены, ЦП переключен.
; =============================================================================
hw_privilege_violation_entry:
    ; --- ШАГ 1: Консервация контекста прерванного потока ---
    PUSH PSW                        ; Сохраняем аккумулятор А и флаги PSW
	
    ; Проверим, как именно мы сюда попали: если ISR пуст — это ложный тик
    MVI  A, 00BH
    OUT  MASTER_8259_CMD
    IN   MASTER_8259_CMD        	; Читаем актуальное состояние регистра ISR
    CPI  00H
    JZ   hw_stub_exit           	; Если ISR полностью пуст — это ложный тик, EOI не шлем

    PUSH B                          ; Сохраняем регистровую пару BC
    PUSH D                          ; Сохраняем регистровую пару DE
    PUSH H                          ; Сохраняем регистровую пару HL

    ; --- ШАГ 2: Защита супервизора ядра от падения ---
    LDA  CURRENT_PID
    ORA  A                          ; Нарушение произошло внутри Ring 0 (Ядро)?
    JZ   privilege_panic_route      ; Если само ядро нарушило шину — Kernel Panic!

    ; --- ШАГ 3: ЖЕСТКОЕ ПЕРЕВЕДЕНИЕ НАРУШИТЕЛЯ В СТАТУС ZOMBIE ---
    LHLD CURRENT_PCB_PTR            ; HL = Адрес активного PCB из кэша ядра
    PUSH H                          ; Спасли базу PCB в стек супервизора
    
    LXI  D, O_STATUS                ; Смещение поля статуса (+2)
    DAD  D
    MVI  M, 005h                    ; Отрезали процесс от планировщика: статус = ST_ZOMBIE (05h)
    
    ; Записываем код завершения (SIGSEGV = 0x0B) в поле O_SYSCALL_RES (+5)
    POP  H
	PUSH H                 			; Восстановили базу PCB
    LXI  D, O_SYSCALL_RES           ; Смещение кода возврата (+5)
    DAD  D
    MVI  M, 00Bh                    ; Прописали флаг падения по аппаратному нарушению (0x0B)

    ; --- ШАГ 4: АТОМАРНОЕ ВЫСВОБОЖДЕНИЕ ФИЗИЧЕСКИХ СТРАНИЦ eMMU ---
    POP  H
	PUSH H                 			; HL = Базовый адрес PCB
    LXI  D, O_PAGES_MAP             ; Смещение карты физических фреймов (+8)
    DAD  D
    MVI  C, 4                       ; Нарушитель владел 4 сегментами памяти (0..3)

privilege_free_pages_loop:
    MOV  A, M                       ; A = Номер физической страницы в eMMU
    CPI  RES_FREE_MARKER            ; Страница уже свободна (0xFF)?
    JZ   privilege_page_next
    
    PUSH B
	PUSH H
    CALL k_free_physical_page       ; Возвращаем фрейм в глобальную карту RAM_BITMAP
    POP  H
	POP  B
    MVI  M, RES_FREE_MARKER         ; Обнулили маркер страницы в PCB (0xFF)

privilege_page_next:
    INX  H                          ; Переходим к следующему сегменту процесса
    DCR  C
    JNZ  privilege_free_pages_loop

    ; --- ШАГ 5: ПОЛНОЕ АВТОМАТИЧЕСКОЕ ЗАКРЫТИЕ ВСЕХ ФАЙЛОВ И СОКЕТОВ ---
    POP  H                          ; HL = Базовый адрес PCB нарушителя
    LXI  D, O_FILE_TABLE            ; Смещение локальной таблицы файлов (+20)
    DAD  D                          ; HL -> Начало O_FILE_TABLE процесса
    MVI  C, 8                       ; Итерируем по всем 8 слотам дескрипторов (FD 0..7)

privilege_close_fd_loop:
    MOV  A, M                       ; A = Глобальный системный индекс VFS (0..63)
    CPI  RES_FREE_MARKER            ; Дескриптор свободен (0xFF)?
    JZ   privilege_fd_next          ; Если пуст — шагаем мимо
    
    ; Слот занят — вызываем наш центральный VFS деаллокатор
    PUSH B
	PUSH H                 			; Сохранили итераторы и адрес таблицы файлов
;-!!!-
;    -!!!- CALL k_vfs_close_global_index   ; Освобождаем аппаратные сокеты чипа и пайпы ОЗУ!
;-!!!-
    POP  H
	POP  B                 			; Восстановили контекст
    
    MVI  M, RES_FREE_MARKER         ; Дескриптор в PCB официально очищен (0xFF)

privilege_fd_next:
    INX  H                          ; Сдвигаемся к следующему дескриптору
    DCR  C
    JNZ  privilege_close_fd_loop    ; Повторяем для всех 8 слотов задачи

    ; --- ШАГ 6: БЕСШОВНЫЙ СУПЕРВИЗОРНЫЙ ШЕДУЛЛИНГ ЖИВОЙ ЗАДАЧИ ---
    CALL scheduler                  ; Планировщик атомарно ищет живой READY поток в системе
    STA  NEXT_PID
    CALL switch_mmu_context         ; ЭВМ MEGA-580 переключает К555ИР26 на новые фреймы
    
    ; Передаем аппаратный EOI (End of Interrupt) в контроллер КР580ВН59
    MVI  A, 020H
    OUT  MASTER_8259_CMD            ; Сбросили триггер текущего уровня IRQ7 в ВН59

    ; Реставрируем контекст РОН (Уже из стека ядра совершенно нового, готового процесса!)
    POP  H
    POP  D
    POP  B
    POP  PSW                        
    
    EI                              ; АТОМАРНО СНИМАЕМ БЛОКИРОВКУ СИСТЕМНОЙ ШИНЫ ПРЕРЫВАНИЙ!
    RET                             ; Аппаратный прыжок в код живой задачи пользователя!

privilege_panic_route:
    ; --- КРИТИЧЕСКИЙ СБОЙ ЯДРА: НАРУШЕНИЕ НА ШИНЕ RING 0 ---
    DI                              ; Запереть прерывания намертво
    HLT                             ; Полный останов кристалла ЦП КР580ВМ80А

; =============================================================================
; УНИВЕРСАЛЬНАЯ АППАРАТНАЯ ЗАГЛУШКА ЯДРА (СЛОТЫ IRQ0 - IRQ7 И ЛОЖНЫЕ ТИКИ)
; =============================================================================
hw_stub:
    ; Вход: Сюда процессор прыгает при срабатывании пустых IRQ,
    ;       Либо при перенаправлении ложного (Spurious IRQ) тика IRQ7 из Violation.
    ; Внимание: Если мы пришли сюда по ложному тику с адреса 0x007C (после JZ),
    ; регистры и стек Ring 3 находятся в идеальной чистоте, PUSH-данных нет!
    
    PUSH PSW                    	; Сохраняем аккумулятор на время отправки EOI
    
    ; Важнейшая физическая особенность КР580ВН59 (8259A):
    ; Ложные прерывания по умолчанию НЕ выставляют соответствующий бит в регистре ISR.
    ; Поэтому посылать команду EOI (End of Interrupt) для ложного тика НЕ ТРЕБУЕТСЯ,
    ; так как контроллер прерываний считает, что он никого не обслуживает.
    ; Однако, если мы пришли сюда от легитимных, но пустых аппаратных линий IRQ0-IRQ7,
    ; их биты в ISR взведены, и сброс EOI строго обязателен!
    
    ; Проверим, как именно мы сюда попали: если ISR пуст — это ложный тик
    MVI  A, 00BH
    OUT  MASTER_8259_CMD
    IN   MASTER_8259_CMD        	; Читаем актуальное состояние регистра ISR
    CPI  00H
    JZ   hw_stub_exit           	; Если ISR полностью пуст — это ложный тик, EOI не шлем

    ; Это легитимное пустое прерывание от линий IRQ0-IRQ7 — шлем неспецифический EOI
    MVI  A, 020H
    OUT  MASTER_8259_CMD

hw_stub_exit:
    POP  PSW                    	; Восстановили аккумулятор
    EI                          	; Снова разрешаем аппаратную карусель прерываний в Ring 3
    RET                         	; Возврат к прерванному кодовой строке пользователя

