; =============================================================================
; ФУНКЦИЯ: strerror - Получение строки ошибки по коду
; =============================================================================
; Вход:  A = Код ошибки POSIX (0..107)
; Выход: HL = Указатель на ASCIIZ-строку ошибки
;        Если код не определён, возвращается указатель на "Unknown error"
; =============================================================================
strerror:
    PUSH B                          ; Сохраняем BC
    
    ; Проверка границ: код должен быть <= 107
    CPI  108                        ; A < 108?
    JNC  _strerror_unknown          ; Если A >= 108 — неизвестная ошибка
    
    ; Вычисляем адрес в таблице: HL = ERROR_STRINGS + (A * 2)
    MOV  L, A
    MVI  H, 0
    DAD  H                          ; HL = A * 2
    
    LXI  D, ERROR_STRINGS
    DAD  D                          ; HL = адрес ячейки в таблице
    
    ; Извлекаем указатель на строку
    MOV  E, M
    INX  H
    MOV  D, M                       ; DE = указатель на строку
    
    ; Проверяем, не нулевой ли указатель
    MOV  A, D
    ORA  E
    JZ   _strerror_unknown          ; Если 0000H — неизвестная ошибка
    
    MOV  H, D
    MOV  L, E                       ; HL = указатель на строку
    POP  B
    RET

_strerror_unknown:
    LXI  H, err_unknown             ; Возвращаем "Unknown error"
    POP  B
    RET
