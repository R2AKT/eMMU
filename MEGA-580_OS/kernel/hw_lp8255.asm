; =============================================================================
; НАЗВАНИЕ: hw_lp8255.asm (Конфигуратор и инициализатор БИС КР580ВВ55А)
; СРЕДА:    Ring 0 супервизора | Архитектура КР580ВМ80А | Драйверы Печати
; =============================================================================
; =============================================================================
; hw_i8255_lp_init (ИСПРАВЛЕННАЯ ВЕРСИЯ)
; =============================================================================
; =============================================================================
; ПРОЦЕДУРА: hw_i8255_lp_init
; Вход:  A  = Тестируемый 8-битный базовый порт данных (0x78 или 0x28).
;        DE = Физический адрес целевого свободного слота в LP_TABLE.
; Задача: Проверить физическое НАЛИЧИЕ чипа ВВ55А на шине методом записи/чтения
;         регистра данных, настроить Режим 1 Centronics, взвести прерывание
;         INTE A, выполнить тест принтера и заполнить структуру слота.
; Выход: CY = 0 — Успех. Чип ВВ55А верифицирован и готов к асинхронной печати.
;        CY = 1 — Ошибка: Чип отсутствует на шине или принтер не готов.
;        Все РОН полностью сохранены согласно контракту HAL Варианта B.
; =============================================================================
hw_i8255_lp_init:
    PUSH B
    PUSH D
    PUSH H                          ; Спасли рантайм-контекст супервизора Ring 0

    MOV  C, A                       ; C = Тестируемый BASE_PORT (LPT0/LPT1)

    ; --- ЭТАП 1: ДИНАМИЧЕСКИЙ SMC-РАСЧЕТ И ПАТЧИНГ ПОРТОВ ---
    ; DATA_PORT = BASE_PORT + 0
    STA  _lp_55_probe_out1 + 1
    STA  _lp_55_probe_in1 + 1
    STA  _lp_55_probe_out2 + 1
    STA  _lp_55_probe_in2 + 1
    
    ; STATUS_PORT = BASE_PORT + 1
    INR  A
    STA  _lp_55_init_status_in + 1
    
    ; РРУ (Регистр Управляющего Слова) = BASE_PORT + 3
    MOV  A, C
	ADI 3
    STA  _lp_55_probe_cmd_out + 1
    STA  _lp_55_init_cmd_out + 1
    STA  _lp_55_init_cmd_out2 + 1   ; Пропатчили все точки ввода-вывода

    ; --- ЭТАП 2: ЖЕСТКИЙ ТЕСТ ФИЗИЧЕСКОГO НАЛИЧИЯ ЧИПА (КРЕМНИЕВАЯ ПРОВЕРКА) ---
    ; Временно настраиваем ВВ55А в простой Режим 0: Порт А на вывод, Порты B/C на ввод.
    ; Управляющее слово для этого режима = 0x92
    MVI  A, 092H
_lp_55_probe_cmd_out:
    OUT  000H                       ; Сконфигурировали чип для теста. Патчится на BASE+3
    NOP
	NOP

    ; Тест 1: Запись и чтение шахматной маски 0x55
    MVI  A, 055H
_lp_55_probe_out1:
    OUT  000H                       ; Запись в Порт А. Патчится на BASE+0
    NOP
	NOP
_lp_55_probe_in1:
    IN   000H                       ; Чтение из Порта А. Патчится на BASE+0
    CPI  055H                       ; Проверяем фиксацию уровней защелки
    JNZ  _hw_lp_55_init_fail        ; Не совпало — кристалл отсутствует! [1.3]

    ; Тест 2: Запись и чтение инвертированной маски 0xAA
    MVI  A, 0AAH
_lp_55_probe_out2:
    OUT  000H                       ; Запись в Порт А. Патчится на BASE+0
    NOP
	NOP
_lp_55_probe_in2:
    IN   000H                       ; Чтение из Порта А. Патчится на BASE+0
    CPI  0AAH                       ; Проверяем инверсные уровни
    JNZ  _hw_lp_55_init_fail        ; Не совпало — кремний отсутствует! [1.3]

    ; --- ЭТАП 3: РАБОЧАЯ КОНФИГУРАЦИЯ И СБРОС КРИСТАЛЛА ---
    ; Чип живой. Переводим в боевой Режим 1 Handshake вывода. Упр.слово = 0xA8
    MVI  A, 0A8H
_lp_55_init_cmd_out:
    OUT  000H                       ; Конвейер прерываний активирован. Патчится на BASE+3

    ; Взвод триггера INTE A через РРУ по вашей проверенной маске BSR для бита PC3
    MVI  A, 007H                    ; Утвержденный фикс: PC3 Set = 0x07 [1.3]
_lp_55_init_cmd_out2:
    OUT  000H                       ; Линия прерываний INTE A открыта. Патчится на BASE+3
    NOP
	NOP

    ; --- ЭТАП 4: ТЕСТ ФУНКЦИОНИРОВАНИЯ ВНЕШНЕГO УСТРOЙСТВА (CENTRONICS) ---
_lp_55_init_status_in:
    IN   000H                       ; Считали статус Centronics. Патчится на BASE+1
    MOV  B, A                       ; B = Кэш статуса

    ; Валидация 1: Проверяем, выбран ли принтер (LP_SELECT / Бит 4 == 1)
    ANI  LP_SELECT                  ; Маска 0x10 из lp.inc
    JZ   _hw_lp_55_init_fail        ; Если 0 — принтер выключен или не подключен

    ; Валидация 2: Проверяем отсутствие конца бумаги (LP_PAPER_OUT / Бит 5 == 0)
    MOV  A, B
    ANI  LP_PAPER_OUT               ; Маска 0x20 из lp.inc
    JNZ  _hw_lp_55_init_fail        ; Если 1 — датчик бумаги активен, блокируем запуск

    ; --- ЭТАП 5: ПОСЛЕДОВАТЕЛЬНОЕ ЗАПОЛНЕНИЕ ДЕСКРИПТОРА СЛOТА ---
    MOV  H, D
    MOV  L, E                       ; HL = Истинный адрес слота LP_TABLE

    MVI  M, 00H                     ; O_LP_STATUS = 0
    
    INX  H                          ; HL -> O_LP_PORT_DATA (+1)
    MOV  M, C                       ; Физический порт DATA (BASE+0)
    
    MOV  A, C
	INR A
    INX  H                          ; HL -> O_LP_PORT_STATUS (+2)
    MOV  M, A                       ; Физический порт STATUS (BASE+1)
    
    INR  A
    INX  H                          ; HL -> O_LP_PORT_CTRL (+3)
    MOV  M, A                       ; Физический порт CTRL (BASE+2)
    
    INX  H                          ; HL -> O_LP_FLAGS (+4)
    MVI  M, LP_OPEN                 ; Выставили флаг "Порт успешно открыт" (0x01)
    
    ; Фиксация аппаратного типа чипа в дескрипторе
    MOV  H, D
    MOV  L, E                       ; HL = Восстановили чистую базу слота
    LXI  B, O_LP_HW_TYPE            ; Смещение +19 из lp.inc
    DAD  B                          ; HL -> O_LP_HW_TYPE
    MVI  M, LP_TYPE_8255            ; Зафиксировали тип кремния (01H) [1.3]

    POP  H
    POP  D
    POP  B                          ; Полное каноническое восстановление РОН по LIFO
    ANA  A                          ; СБРОС ФЛАГА ОШИБКИ: CY = 0 (Полный рантайм-успех!)
    RET

_hw_lp_55_init_fail:
    POP  H
    POP  D
    POP  B                          ; Восстановили оригинальный контекст супервизора
    STC                             ; ВЗВОД ФЛАГА ОШИБКИ: CY = 1 (Аппаратный отказ чипа / Centronics)
    RET

; =============================================================================
; АСИНХРОННЫЙ ОБРАБОТЧИК ПРЕРЫВАНИЙ ДЛЯ БИС КР580ВВ55А
; Вход:  Аппаратный CALL по вектору IRQ от КР580ВН59. Прерывания заперты (DI).
;        Состояние РОН и SP — случайное (из прерванного контекста Ring 3).
; Задача: Извлечь байт из TX-кольца ОЗУ, выдать в DATA_PORT, сформировать
;         импульс STROBE через BSR-команду РРУ, декрементировать BUFFER_COUNT.
; Выход: РОН полностью восстановлены.
; =============================================================================
; =============================================================================
; НАЗВАНИЕ: hw_lp8255.asm (Асинхронный монолитный ISR для КР580ВВ55А)
; СРЕДА:    Ring 0 супервизора | Архитектура КР580ВМ80А | Драйверы Печати
; =============================================================================
hw_lp8255_isr:
_smc_lp55_isr_base:
    ; === ЭТАП 0: АППАРАТНЫЙ ПОРТ ПРИВЯЗКИ БАЗЫ (SMC-ИНСТАНС) ===
    LXI  D, 0000H                   ; Сюда карусель lp_core.asm при старте
                                    ; пропишет истинный адрес структуры ОЗУ.

    PUSH B
    PUSH D
    PUSH H                          ; Спасли рантайм-контекст прерванного процесса

    ; === ЭТАП 1: ДИНАМИЧЕСКИЙ SMC-РАСЧЕТ ФИЗИЧЕСКИХ ПОРТОВ ВВОД-ВЫВОДА ===
    MOV  H, D
    MOV  L, E                       ; HL = Физическая база дескриптора принтера
    INX  H                          ; HL -> O_LP_PORT_DATA (+1)
    MOV  A, M                       ; A = Физический базовый порт DATA (0x78 или 0x28)
    MOV  C, A                       ; C = Кэш базового порта DATA
    
    ; Прошиваем операнд инструкции OUT для выдачи данных (BASE+0)
    STA  _lp_55_isr_data_out + 1
    
    ; === ЭТАП 1.1: ВЫЧИСЛЕНИЕ АДРЕСА РРУ (BASE+3) И ТОТАЛЬНЫЙ SMC ПАТЧ ===
    MOV  A, C
    ADI  3                          ; A = BASE_PORT + 3 (Регистр Управляющего Слова)
    STA  _lp_55_isr_bsr_out1 + 1    ; Прошили РРУ для BSR-сброса строба
    STA  _lp_55_isr_bsr_out2 + 1    ; Прошили РРУ для BSR-взвода строба
    STA  _lp_55_isr_empty_out + 1   ; Прошили РРУ для глушения INTE A прерываний [1.3]

    ; === ЭТАП 2: КОНТРОЛЬ НАЛИЧИЯ ДАННЫХ В TX КОЛЬЦЕ ОЗУ ===
    MOV  H, D
    MOV  L, E                       ; HL = База слота LPT
    LXI  B, O_LP_BUFFER_COUNT       ; Смещение +18 согласно lp.inc
    DAD  B                          ; HL -> O_LP_BUFFER_COUNT
    MOV  A, M
    ORA  A                          ; BUFFER_COUNT == 0 (Очередь отложенной записи пуста)?
    JZ   _lp_55_isr_empty_close     ; Да — данных для печати больше нет, глушим линию прерывания!

    ; === ЭТАП 3: ИЗВЛЕЧЕНИЕ БАЙТА ИЗ КОЛЬЦЕВОГО FIFO БУФЕРА ===
    DCR  M                          ; O_LP_BUFFER_COUNT--
    
    ; Извлекаем индекс чтения по указателю TAIL
    DCX  H                          ; HL -> O_LP_BUFFER_TAIL (Смещение +17)
    MOV  A, M                       ; A = TAIL_old
    MOV  B, A                       ; B = Кэш текущего индекса чтения
    
    ; Инкрементируем и зацикливаем TAIL в памяти ядра Ring 0
    INR  M                          ; TAIL++
    MOV  A, M
    CPI  48                         ; Буфер строго ограничивается 48 байтами [1.3]
    JC   _lp_55_isr_tail_no_wrap
    MVI  M, 00H                     ; Циклический заворот TAIL = 0

_lp_55_isr_tail_no_wrap:
    ; Двухступенчатый DAD-расчет физического адреса байта
    MOV  H, D
    MOV  L, E                       ; HL = Восстановили чистую базу слота LPT
    PUSH D                          ; Спрятали базу слота
    
    LXI  D, 21                      ; Смещение начала сектора данных буфера FIFO
    DAD  D                          ; HL = База + Начало буфера вывода
    
    MOV  E, B
    MVI  D, 00H                     ; DE = 0000H + TAIL_old
    DAD  D                          ; HL = Точный физический адрес байта в ОЗУ ядра Ring 0!
    MOV  A, M                       ; А = ASCII-символ, готовый к выгрузке в принтер
    MOV  B, A                       ; B = Спасли символ от разрушения при POP
    POP  D                          ; DE = Восстановили базу слота LPT

    ; === ЭТАП 3.1: ПОЛНЫЙ 32-БИТНЫЙ ИНКРЕМЕНТ СЧЕТЧИКА O_LP_BYTES_WRITTEN ===
    MOV  H, D
    MOV  L, E
    LXI  D, O_LP_BYTES_WRITTEN      ; Смещение +8 из lp.inc
    DAD  D                          ; HL -> Байт 0 (младший) счётчика
    
    INR  M                          ; Байт 0++
    JNZ  _lp_bytes_inc_done         ; Если не было переполнения — выходим из каскада
    
    INX  H                          ; Переход к Байту 1
    INR  M
    JNZ  _lp_bytes_inc_done
    
    INX  H                          ; Переход к Байту 2
    INR  M
    JNZ  _lp_bytes_inc_done
    
    INX  H                          ; Переход к Байту 3 (старший)
    INR  M

_lp_bytes_inc_done:
    ; === ЭТАП 4: ФИЗИЧЕСКИЙ ВЫСТРЕЛ В КРЕМНИЙ И BSR-СТРОБИРОВАНИЕ CENTRONICS ===
    MOV  A, B                       ; А = Извлеченный ASCII-символ
_lp_55_isr_data_out:
    OUT  000H                       ; Выставили символ на шину данных LPT. Патчится на BASE+0

    ; Формируем импульс фиксации STROBE для Centronics через BSR-команды в РРУ.
    ; BSR-команда: 0000 000 0 = 0x00 (Reset PC0 -> STROBE уходит в низкий активный уровень)
    MVI  A, 000H                    
_lp_55_isr_bsr_out1:
    OUT  000H                       ; STROBE = 0 (Начало фиксации данных принтером). Патчится на BASE+3 [1.3]
    
    ; Микровыдержка импульса согласно временным диаграммам Centronics
    NOP
	NOP
	NOP
	NOP

    ; Завершаем импульс, возвращая STROBE в единицу через BSR.
    ; BSR-команда: 0000 000 1 = 0x01 (Set PC0 -> STROBE возвращается в высокий пассивный уровень)
    MVI  A, 001H
_lp_55_isr_bsr_out2:
    OUT  000H                       ; STROBE = 1 (Данные запечатаны принтером). Патчится на BASE+3 [1.3]
    JMP  _lp_55_isr_close_8259      ; Символ улетел, шагаем на закрытие прерывания ВН59

_lp_55_isr_empty_close:
    ; Очередь ОЗУ пуста — принудительно гасим асинхронный триггер прерываний чипа ВВ55А.
    ; Сброс бита PC3 (INTE A) в РРУ ВВ55А (Команда BSR: Бит=3, Уровень=0) = 0x06
    MVI  A, 006H
_lp_55_isr_empty_out:
    OUT  000H                       ; Атомарно заперли INTE A прерываний ВВ55А! Патчится на BASE+3 [1.3]

    ; Переводим логический статус порта в состояние покоя (O_LP_STATUS = 0)
    MOV  H, D
    MOV  L, E
    MVI  M, 00H                     

_lp_55_isr_close_8259:
    ; === ЭТАП 5: СБРОС ТРИГГЕРА КОНТРОЛЛЕРА ПРЕРЫВАНИЙ КР580ВН59 ===
    MVI  A, 020H
    OUT  MASTER_8259_CMD            ; Неспецифический EOI в суверенный ВН59 (Порты 0xA0/0xA1) [1.3]
    
    POP  H
    POP  D
    POP  B                          ; Полное каноническое восстановление контекста прерванной задачи
    EI                              ; Разрешили асинхронный конвейер шины eMMU
    RET

