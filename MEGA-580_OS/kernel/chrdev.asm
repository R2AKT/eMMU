; =============================================================================
; НАЗВАНИЕ: chrdev.asm (Системный Plug-and-Play регистратор драйверов)
; СРЕДА:    Ring 0 супервизора | Архитектура КР580ВМ80А | Модуль VFS
; =============================================================================
; =============================================================================
; ПРОЦЕДУРА: k_register_chrdev
; Вход:  A  = Целевой MAJOR номер устройства (0..31).
;        HL = Физический адрес функции write драйвера (HAL-мост).
;        DE = Физический адрес функции read драйвера (HAL-мост).
; Задача: Проверить Major на выход за границы таблицы. Рассчитать смещение 
;         Major * 4 и атомарно прошить адреса методов в SYS_CHRDEV_SWITCH.
; Выход: CY = 0 — Драйвер успешно встроен в коммутатор VFS.
;        CY = 1 — Провал: Передан недопустимый Major >= 32 (Защита памяти ядра).
;        Все РОН полностью сохранены согласно контракту HAL Варианта B.
; =============================================================================
k_register_chrdev:
    ; --- ЭТАП 1: РАНТАЙМ-КОНТРОЛЬ СИСТЕМНЫХ ГРАНИЦ MAJOR ---
    CPI  VFS_MAX_TYPES              ; Сравниваем переданный Major с лимитом 32 [1.3]
    JNC  _k_reg_chrdev_fault        ; Если Major >= 32 — прерываем операцию (защита памяти) [1.3]

    PUSH B
    PUSH D
    PUSH H                          ; Спасли рантайм-контекст супервизора Ring 0
    PUSH PSW                        ; Спасли валидированный MAJOR номер на стек

    ; --- ЭТАП 2: ВЫЧИСЛЕНИЕ СМЕЩЕНИЯ (MAJOR * 4) ЧЕРЕЗ СДВИГИ i8080 ---
    ADD  A                          ; A = MAJOR * 2
    ADD  A                          ; A = MAJOR * 4
    MOV  C, A
    MVI  B, 00H                     ; BC = Точное байт-смещение слота в таблице ОЗУ

    ; --- ЭТАП 3: РАСЧЕТ И ПРИВЯЗКА К РЕЛОЦИРУЕМОЙ ТАБЛИЦЕ ОЗУ ---
    LXI  H, SYS_CHRDEV_SWITCH       ; Символический релоцируемый адрес таблицы из BSS [1.3]
    DAD  B                          ; HL = SYS_CHRDEV_SWITCH + (MAJOR * 4)

    ; Извлекаем оригинальный адрес метода write (HL_old) со стека без нарушения LIFO
    ; На вершине стека сейчас лежит сохраненный PSW, а под ним (SP+2) — оригинальный HL_old.
    ; Чтобы достать его атомарно, временно снимем PSW, сделаем обмен, и вернем PSW на место.
    POP  PSW                        ; Извлекли MAJOR в аккумулятор А
    XTHL                            ; ТРЮК i8080: HL = оригинальный HL_old (метод write),
                                    ; а на стек (SP+0) лег наш вычисленный адрес таблицы ОЗУ!
    MOV  C, L
    MOV  B, H                       ; BC = Истинный физический адрес функции write драйвера
    XTHL                            ; HL = Восстановили адрес таблицы ОЗУ, BC = метод write
    PUSH PSW                        ; Вернули MAJOR на вершину стека для баланса LIFO

    ; --- ЭТАП 4: АТОМАРНАЯ ФИКСАЦИЯ МЕТОДОВ ДРАЙВЕРА В ТАБЛИЦЕ ---
    MOV  M, C                       ; Прошили младший байт адреса функции write
    INX  H
    MOV  M, B                       ; Прошили старший байт адреса функции write
    INX  H                          ; HL перешагнул на поле O_CHR_READ (+2)

    ; Прошиваем метод READ (Адрес передан и сохранен в регистровой паре DE)
    MOV  M, E                       ; Прошили младший байт адреса функции read
    INX  H
    MOV  M, D                       ; Прошили старший байт адреса функции read

    POP  PSW                        ; Вытолкнули MAJOR со стека
    POP  H
    POP  D
    POP  B                          ; Полное каноническое восстановление РОН супервизора
    ANA  A                          ; СБРОС ФЛАГА ОШИБКИ: CY = 0 (Успех)
    RET

_k_reg_chrdev_fault:
    STC                             ; ВЗВОД ФЛАГА ОШИБКИ: CY = 1 (Ложный Major-номер)
    RET

; =============================================================================
; НАЗВАНИЕ: chrdev.asm (Табличный сквозной коммутатор записи VFS)
; СРЕДА:    Ring 0 супервизора | Архитектура КР580ВМ80А | Модуль VFS
; =============================================================================
; =============================================================================
; ПРОЦЕДУРА: vfs_chrdev_write
; Вход:  А  = Выводимый ASCII-символ.
;        C  = Major-номер символьного устройства, извлеченный из Inode (0..31).
;        B  = Minor-номер конкретного порта / устройства из Inode (0..3).
; Задача: Валидировать Major. Рассчитать адрес слота в SYS_CHRDEV_SWITCH,
;         извлечь 16-битный указатель метода write драйвера и выполнить 
;         косвенный переход по PCHL с сохранением РОН.
; Выход: Все РОН полностью сохранены согласно контракту HAL Варианта B.
; =============================================================================
vfs_chrdev_write:
    ; --- ЭТАП 1: ПРЕДВАРИТЕЛЬНЫЙ КОНТРОЛЬ СИСТЕМНЫХ ГРАНИЦ MAJOR ---
    MOV  A, C                       ; А = MAJOR номер устройства из Inode
    CPI  VFS_MAX_TYPES              ; Запрос выходит за рамки таблицы (>=32)?
    JNC  _chr_table_wr_fault        ; Да — прерываем операцию (защита от краха ядра) [1.3]

    PUSH B
    PUSH D
    PUSH H                          ; Спасли рантайм-контекст супервизора Ring 0

    MOV  E, A                       ; ЭВАКУАЦИЯ: спрятали выводимый символ в регистр E [1.3]

    ; --- ЭТАП 2: ТАБЛИЧНЫЙ РАСЧЕТ СМЕЩЕНИЯ (MAJOR * 4) ---
    MOV  A, C                       ; А = Валидированный MAJOR номер
    ADD  A                          ; A = MAJOR * 2
    ADD  A                          ; A = MAJOR * 4
    MOV  L, A
    MVI  H, 00H                     ; HL = Смещение слота драйвера

    ; --- ЭТАП 3: ИЗВЛЕЧЕНИЕ АДРЕСА HAL-ШЛЮЗА ИЗ ОЗУ ТАБЛИЦЫ ---
    LXI  D, SYS_CHRDEV_SWITCH       ; Релоцируемый базовый адрес из BFS-кучи [1.3]
    DAD  D                          ; HL -> Указывает точно на поле O_CHR_WRITE слота MAJOR
    
    MOV  A, M
    INX  H
    MOV  H, M
    MOV  L, A                       ; HL = Исполняемый физический адрес HAL-моста (напр. tty_write_bridge)
    
    ; Проверка на пустой указатель (если для этого MAJOR драйвер еще не зарегистрирован)
    MOV  A, H
    ORA  L
    JZ   _chr_table_wr_empty        ; Указатель равен 0000H — пассивный сброс байта

    ; --- ЭТАП 4: ВОССТАНОВЛЕНИЕ КОНТРАКТА И КОСВЕННЫЙ ПРЫЖОК ПО PCHL ---
    MOV  A, E                       ; ВОССТАНОВЛЕНИЕ: вернули выводимый ASCII-символ в аккумулятор А [1.3]
    ; Регистр B по-прежнему содержит Minor-номер для вызываемого моста драйвера

    LXI  D, _chr_table_wr_ret       ; Вручную положили адрес возврата на стек Ring 0
    PUSH D
    PCHL                             ; Прыжок на исполнение в зарегистрированный мост драйвера!

_chr_table_wr_empty:
_chr_table_wr_ret:
    POP  H
    POP  D
    POP  B                          ; Идеальное каноническое восстановление РОН супервизора по LIFO
_chr_table_wr_fault:
    RET

; =============================================================================
; НАЗВАНИЕ: chrdev.asm (Табличный сквозной коммутатор чтения VFS)
; СРЕДА:    Ring 0 супервизора | Архитектура КР580ВМ80А | Модуль VFS
; =============================================================================
; =============================================================================
; ПРОЦЕДУРА: vfs_chrdev_read
; Вход:  C  = Major-номер символьного устройства, извлеченный из Inode (0..31).
;        B  = Minor-номер конкретного порта / устройства из Inode (0..3).
; Задача: Валидировать Major. Рассчитать адрес слота в SYS_CHRDEV_SWITCH,
;         извлечь 16-битный указатель метода read драйвера и выполнить
;         косвенный переход по PCHL. Защитить принятый символ от затирания POP.
; Выход: А  = Принятый из драйвера ASCII-символ (00H — если буфер пуст / пустой слот).
;        Все остальные РОН (BC, DE, HL) полностью сохранены по контракту.
; =============================================================================
vfs_chrdev_read:
    ; --- ЭТАП 1: ПРЕДВАРИТЕЛЬНЫЙ КОНТРОЛЬ СИСТЕМНЫХ ГРАНИЦ MAJOR ---
    MOV  A, C                       ; А = MAJOR номер устройства из Inode
    CPI  VFS_MAX_TYPES              ; Запрос выходит за рамки таблицы (>=32)?
    JC   _chr_table_rd_valid        ; В границах — шагаем на обработку
    
    XRA  A                          ; Вне границ — принудительно возвращаем 00H
    RET

_chr_table_rd_valid:
    PUSH B
    PUSH D
    PUSH H                          ; Спасли рантайм-контекст супервизора Ring 0

    ; --- ЭТАП 2: ТАБЛИЧНЫЙ РАСЧЕТ СМЕЩЕНИЯ К ПОЛЮ O_CHR_READ (MAJOR * 4 + 2) ---
    MOV  A, C                       ; А = Валидированный MAJOR номер
    ADD  A                          ; A = MAJOR * 2
    ADD  A                          ; A = MAJOR * 4
    MOV  L, A
    MVI  H, 00H                     ; HL = Смещение начала слота Major
    
    LXI  D, SYS_CHRDEV_SWITCH       ; Релоцируемый базовый адрес таблицы из BSS [1.3]
    DAD  D                          ; HL -> Указывает на поле O_CHR_WRITE слота MAJOR
    INX  H
    INX  H                          ; HL -> Указывает точно на поле O_CHR_READ (+2) слота MAJOR

    ; --- ЭТАП 3: ИЗВЛЕЧЕНИЕ АДРЕСА HAL-ШЛЮЗА ИЗ ОЗУ ТАБЛИЦЫ ---
    MOV  A, M
    INX  H
    MOV  H, M
    MOV  L, A                       ; HL = Исполняемый физический адрес HAL-моста (напр. tty_read_bridge)
    
    ; Проверка на пустой указатель
    MOV  A, H
    ORA  L
    JZ   _chr_table_rd_empty        ; Указатель равен 0000H — возвращаем дефолтный нуль 00H

    ; --- ЭТАП 4: КОСВЕННЫЙ ПРЫЖОК ПО PCHL И ЭВАКУАЦИЯ РЕЗУЛЬТАТА ---
    ; Регистр B по-прежнему содержит Minor-номер для вызываемого моста драйвера
    LXI  D, _chr_table_rd_ret       ; Вручную положили адрес возврата на стек Ring 0
    PUSH D
    PCHL                             ; Прыжок на исполнение в зарегистрированный мост драйвера!

_chr_table_rd_empty:
    MVI  E, 00H                     ; Если слот пуст — фиксируем дефолтный нуль в регистре E
    JMP  _chr_table_rd_done

_chr_table_rd_ret:
    ; Мы вернулись из физического моста (например, tty_read_bridge). 
    ; Символ лежит в аккумуляторе А. Все остальные РОН мостом сохранены.
    MOV  E, A                       ; ИЗОЛЯЦИЯ: немедленно спрятали принятый символ в регистр E [1.3]
                                    ; Теперь А можно затирать командами восстановления контекста!

_chr_table_rd_done:
    POP  H
    POP  D
    POP  B                          ; Идеальное каноническое восстановление РОН супервизора по LIFO
    MOV  A, E                       ; ВЫДАЧА РЕЗУЛЬТАТА: перенесли защищенный символ из E в аккумулятор А [1.3]
    RET
