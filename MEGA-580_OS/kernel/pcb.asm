; =============================================================================
; /kernel/pcb.asm - ДИНАМИЧЕСКИЙ ПРИОРИТЕТНЫЙ ПЛАНИРОВЩИК И КОНТЕКСТНЫЙ ДИСПЕТЧЕР
; =============================================================================
; =============================================================================
; k_pcb_subsys_init - ИНИЦИАЛИЗАЦИЯ ПОДСИСТЕМЫ ПЛАНИРОВАНИЯ И ТАБЛИЦЫ PCB
; =============================================================================
; Вход:  Cold Boot, Ring 0, прерывания запрещены.
; Задача: Полностью подготовить PCB_TABLE (32 слота × 128 байта = 4 КБ)
;         и управляющие ячейки планировщика к работе.
; Выход: CURRENT_PCB_PTR указывает на PCB PID 0 (idle-задача ядра).
; =============================================================================
k_pcb_subsys_init:
    ; === ШАГ 1: Сброс управляющих ячеек планировщика ===
    XRA  A
    STA  CURRENT_PID
    STA  NEXT_PID
    STA  SCHED_BEST_PRIO
    STA  SCHED_BEST_PID
    STA  CPU_USAGE

    ; === ШАГ 2: Разметка таблицы PCB (32 слота по 128 байт) ===
    LXI  H, PCB_TABLE
    MVI  E, 0                ; E = текущий PID (0..31), освобождаем BC

_init_pcb_loop:
    ; Очистка 128 байт слота
    PUSH H
    LXI  B, PCB_SIZE         ; BC используется как чистый счетчик
    XRA  A
_clear_pcb_slot:
    MOV  M, A
    INX  H
    DCX  B
    MOV  A, B
    ORA  C
    JNZ  _clear_pcb_slot
    POP  H
    
    ; Установка стартовых маркеров задачи
    MOV  M, E                ; O_PID (+0) = значение из E
    INX  H
    MVI  M, ST_FREE          ; O_STATUS (+1) = 00H (Слот свободен для sys_fork)
    DCX  H                   ; HL возвращен на базу слота
    
    PUSH H
    LXI  D, O_PPID
    DAD  D
    MVI  M, RES_FREE_MARKER  ; PPID = 0xFF
    POP  H
    
    PUSH H
    LXI  D, O_TTY_INDEX
    DAD  D
    MVI  M, RES_FREE_MARKER  ; TTY = 0xFF
    POP  H
    
    PUSH H
    LXI  D, O_FILE_TABLE
    DAD  D
    MVI  B, 32               ; 32 дескриптора POSIX VFS
_init_file_table_loop:
    MVI  M, RES_FREE_MARKER  ; FD = 0xFF
    INX  H
    DCR  B
    JNZ  _init_file_table_loop
    POP  H
    
    ; Переход к следующем 128-байтному слоту
    LXI  D, PCB_SIZE
    DAD  D                   ; HL = База следующей PCB
    
    INR  E                   ; PID++
    MOV  A, E
    CPI  MAX_PROCS           ; Проверка достижения 32
    JNZ  _init_pcb_loop

    ; === ШАГ 3: Конфигурация PID 0 (Idle-задача ядра) ===
    LXI  H, PCB_TABLE
    
    INX  H
    MVI  M, ST_RUNNING       ; PID 0 получает статус ST_RUNNING (03H)
    DCX  H
    
    ; Заполнение блока безопасности POSIX (UID, GID, EUID, EGID)
    PUSH H
    LXI  D, O_UID
    DAD  D
    MVI  M, 00H              ; UID = 0 (Root)
    INX  H
    MVI  M, 00H              ; GID = 0
    INX  H
    MVI  M, 00H              ; EUID = 0
    INX  H
    MVI  M, 00H              ; EGID = 0 (Каноничный POSIX)
    POP  H
    
    ; Прописка flat-карты памяти К555ИР26
    PUSH H
    LXI  D, O_PAGES_MAP
    DAD  D
    MVI  M, 00H              ; Окно 0 = физ. страница 0
    INX  H
    MVI  M, 01H              ; Окно 1 = физ. страница 1
    INX  H
    MVI  M, 02H              ; Окно 2 = физ. страница 2
    INX  H
    MVI  M, 03H              ; Окно 3 = физ. страница 3
    POP  H
    
    ; Настройка статических и динамических приоритетов
    PUSH H
    LXI  D, O_PRIO_STATIC
    DAD  D
    MVI  M, 00H              ; STATIC = 0 (Высший приоритет ядра)
    INX  H
    MVI  M, 20               ; NICE = 20
    INX  H
    MVI  M, 20               ; DYNAMIC = 20
    POP  H

    ; === ШАГ 4: Фиксация кэш-указателя ядра ===
    LXI  H, PCB_TABLE
    SHLD CURRENT_PCB_PTR     ; Указатель CURRENT_PCB_PTR намертво фиксирует PID 0

    ; === ШАГ 5: Инвариантный аппаратный возврат в RING0 ===
    IRET                     ; Извлекает адрес возврата, возвращая в kernel_clean_start

; -----------------------------------------------------------------------------
; ЦЕНТРАЛЬНАЯ ПОДПРОГРАММА: СКАНИРОВАНИЕ И НАЧИСЛЕНИЕ ДИНАМИЧЕСКИХ ПРИОРИТЕТОВ
; -----------------------------------------------------------------------------
; Вызывается из прерываний Ring 0 при DI.
; Выход: А = PID процесса, назначенного на исполнение (NEXT_PID).
; =============================================================================
; Вызывается из прерываний Ring 0 при DI через CALL.
; Выход: А = PID процесса, назначенного на исполнение (NEXT_PID).
; Уничтожает: Только флаги. ВСЕ РОН восстанавливаются из KERNEL_STACK.
; =============================================================================
scheduler:
    PUSH B
    PUSH D
    PUSH H                          ; Сохранили РОН супервизора в KERNEL_STACK

    ; =============================================================================
    ; --- ЭТАП 1: ШТРАФOВАНИЕ ВЫПОЛНЯВШЕГОСЯ ПРОЦЕССА ---
    ; =============================================================================
    LHLD CURRENT_PCB_PTR            ; HL = Адрес active PCB из кэша ядра
    MOV  A, H
    ORA  L
    JZ   sched_find_candidate       ; Если холодный старт (указатель NULL) — к поиску

    ; Проверяем статус задачи
    PUSH H                          ; Сохранили базу текущего PCB на стеке
    LXI  D, O_STATUS                ; DE = +0001H
    DAD  D                          ; HL -> O_STATUS
    MOV  A, M
    CPI  ST_RUNNING
    JNZ  sched_skip_penalty_stack   ; Если процесс сам уснул (ST_WAITING...) — не штрафуем
    
    MVI  M, ST_READY                ; Вытесняем процесс по таймеру обратно в READY
    POP  H                          ; Сняли базу текущего PCB в HL
    
    ; Увеличиваем штрафной счетчик времени (кванты CPU) задачи
    PUSH H                          ; Удержали базу на стеке
    LXI  D, O_CPU_TICKS             ; DE = +0007H
    DAD  D                          ; HL -> O_CPU_TICKS
    MOV  A, M
    CPI  0FFH                       ; Защита от переполнения байта
    JZ   skip_tick_inc              
    INR  M                          ; O_CPU_TICKS++
skip_tick_inc:

    ; === БЕЗОПАСНЫЙ ПЕРЕСЧЕТ ДИНАМИЧЕСКОГО ПРИOРИТЕТА С НАСЫЩЕНИЕМ ===
    POP  H                          ; HL = Чистая база текущего PCB
    PUSH H                          ; Вернули на стек для баланса
    LXI  D, O_PRIO_STATIC           ; DE = +0004H
    DAD  D                          ; HL -> O_PRIO_STATIC
    MOV  A, M                       ; A = STATIC
    
    INX  H                          ; HL -> O_PRIO_NICE (+0005H)
    ADD  M                          ; A = STATIC + NICE
    JC   _sched_saturation          ; Переполнение — на жесткое насыщение
    
    INX  H                          ; HL -> O_PRIO_DYNAMIC (+0006H)
    INX  H                          ; HL -> O_CPU_TICKS (+0007H)
    ADD  M                          ; A = (STATIC + NICE) + CPU_TICKS
    JC   _sched_saturation          ; Переполнение — на жесткое насыщение

_sched_save_calc:
    DCX  H                          ; Фикс указателя назад: HL -> O_PRIO_DYNAMIC (+0006H)
    MOV  M, A                       ; Зафиксировали честно вычисленный приоритет
    POP  H                          ; Сбалансировали стек
    JMP  sched_skip_penalty

_sched_saturation:
    POP  H                          ; Восстановили базу текущего PCB
    PUSH H                          ; Вернули для баланса ветки
    LXI  D, O_PRIO_DYNAMIC          ; DE = +0006H
    DAD  D                          ; HL -> O_PRIO_DYNAMIC
    MVI  M, 0FFH                    ; Выставили потолок 255 (Наихудший приоритет)
    POP  H                          ; Сбалансировали стек
    JMP  sched_skip_penalty

sched_skip_penalty_stack:
    POP  H                          ; Очистили локальный снапшот адреса базы PCB
sched_skip_penalty:

    ; =============================================================================
    ; --- ЭТАП 2: ПОИСК READY-ЗАДАЧИ С НАИЛУЧШИМ ПРИOРИТЕТОМ (128 БАЙТ) ---
    ; =============================================================================
sched_find_candidate:
    MVI  A, 0FFH
    STA  SCHED_BEST_PRIO            ; Инициализируем минимум наихудшим весом (255)
    
    XRA  A                          ; A = 00H
    STA  SCHED_BEST_PID             ; Фоллбэк кандидат = PID 0 (Idle task ядра)

    LXI  H, PCB_TABLE               ; HL -> Стартовый адрес таблицы процессов
    MVI  B, MAX_PROCS               ; Счетчик цикла = 32 слота

sched_scan_loop:
    PUSH H                          ; [СТЭК +2 БАЙТА]: Сохранили физический адрес начала PCB
    
    ; Проверяем статус слота
    LXI  D, O_STATUS                ; DE = +0001H
    DAD  D                          ; HL -> O_STATUS
    MOV  A, M
    CPI  ST_FREE                    
    JZ   sched_pop_next_slot        ; Слот пуст — снимаем стек и на шаг вперед
    
    CPI  ST_READY                   ; Процесс готов к выполнению (02H)?
    JNZ  sched_check_zombie         ; Не READY — уходим на проверку зомби и старения

    ; Процесс готов! Извлекаем его динамический приоритет O_PRIO_DYNAMIC (+0006H)
    POP  H                          ; HL = База текущего PCB
    PUSH H                          ; Вернули для баланса конца цикла
    PUSH H                          ; Локальный дубликат для смещения
    LXI  D, O_PRIO_DYNAMIC          ; DE = +0006H
    DAD  D                          ; HL -> O_PRIO_DYNAMIC
    MOV  A, M                       ; A = Вычисленный динамический вес задачи
    POP  H                          ; HL = База текущего PCB
    
    PUSH H                          ; Временная эвакуация HL
    LXI  H, SCHED_BEST_PRIO
    CMP  M                          ; Сравниваем текущий вес с рекордом (A - M)
    POP  H                          ; Вернули HL = База текущего PCB
    
    ; Если вес больше или равен рекорду (приоритет хуже) — шагаем на Aging
    JNC  sched_aging_execute        

    ; Нашли процесс с лучшим приоритетом!
    STA  SCHED_BEST_PRIO            ; Обновили рекорд минимума
    MOV  A, M                       ; База указывает строго на O_PID (+0000H)
    STA  SCHED_BEST_PID             ; Зафиксировали PID лучшего кандидата
    JMP  sched_pop_next_slot        ; Прямой уход на следующую итерацию

sched_check_zombie:
    ; Регистр А все еще удерживает байт O_STATUS
    CPI  ST_ZOMBIE                  
    JZ   sched_pop_next_slot        ; Если Zombie — полностью изолируем от Aging, сдвига нет

sched_aging_process:
sched_aging_execute:
    ; Вход на Aging: Стек сбалансирован, на вершине лежит ровно 1 копия адреса базы PCB!
    POP  H                          ; HL = Единственная чистая база PCB
    PUSH H                          ; Вернули её обратно для финального sched_pop_next_slot
    LXI  D, O_CPU_TICKS             ; DE = +0007H
    DAD  D                          ; HL -> O_CPU_TICKS
    MOV  A, M
    ORA  A                          ; Проверка на 0
    JZ   sched_pop_next_slot        ; Штраф равен 0 — уменьшать некуда
    DCR  M                          ; O_CPU_TICKS-- (Aging-эффект: приоритет растет)

sched_pop_next_slot:
    POP  H                          ; СТРОГО ОДИНАКОВЫЙ ФИНАЛ: HL = База текущей PCB

sched_next_slot:
    LXI  D, PCB_SIZE                ; Длина шага строго 128 байт!
    DAD  D                          ; HL переместился на следующий PCB
    DCR  B                          ; Модифицируем счетчик слотов (32..0)
    JNZ  sched_scan_loop            ; Повторяем для всей таблицы

    ; =============================================================================
    ; --- ЭТАП 3: ФИКСАЦИЯ РЕЗУЛЬТАТА И ОЧИСТКА ТИКОВ ИЗБРАННИКА ---
    ; =============================================================================
    LDA  SCHED_BEST_PID
    STA  NEXT_PID                   ; Назначили PID следующей исполняемой задачи

    ; Высокоскоростной и безопасный расчет 16-битного смещения (NEXT_PID * 128)
    LDA  SCHED_BEST_PID
    MOV  L, A
    MVI  H, 0                       ; HL = NEXT_PID (0..31)
    
    ; Выполняем 7 аппаратных сдвигов пары HL влево для безопасного умножения на 128
    DAD  H                          ; *2
    DAD  H                          ; *4
    DAD  H                          ; *8
    DAD  H                          ; *16
    DAD  H                          ; *32
    DAD  H                          ; *64
    DAD  H                          ; *128! HL = Чистое смещение слота (до 3968 байт)
    
    LXI  D, PCB_TABLE
    DAD  D                          ; HL = Настоящий физический адрес выбранной PCB
    
    ; Обнуляем счётчик тиков O_CPU_TICKS избранной задачи
    PUSH H                          ; Спрятали адрес избранной PCB
    LXI  D, O_CPU_TICKS             ; DE = +0007H
    DAD  D                          ; HL -> O_CPU_TICKS
    MVI  M, 00H                     ; Сброс штрафных тиков запускаемого процесса
    POP  H                          ; HL = Чистая база выбранной PCB

    POP  H
    POP  D
    POP  B                          ; Восстановили РОН супервизора из KERNEL_STACK
    LDA  NEXT_PID                   ; Возврат результирующего PID в аккумуляторе А
    
    ; === ИНВАРИАНТНЫЙ ВЫХОД ИЗ ПОДПРОГРАММЫ ЯДРА ===
    IRET                             ; Возврат без смены кольца, остаемся в RING0 (0xD9)

; -----------------------------------------------------------------------------
; АППАРАТНЫЙ ДИСПЕТЧЕР КОНТЕКСТОВ: switch_mmu_context
; -----------------------------------------------------------------------------
; Атомарно прошивает flat-карту памяти Ring 3 и обновляет кэш CURRENT_PCB_PTR.
; Вызывается строго внутри критических секций ядра при заблокированных прерываниях (DI).
; -----------------------------------------------------------------------------
; Функция: switch_mmu_context
; Описание: Атомарная коммутация flat-карты eMMU через регистры 070H-073H,
;           фиксация CURRENT_PID/CURRENT_PCB_PTR, переключение SP на задачу,
;           сброс оверрайда ядра и санкционированный выход в RING3.
; Вход: Переменная NEXT_PID содержит индекс целевого процесса (0..31).
; Выход: Процессор выполняет код задачи в RING3. В ядро возврата нет.
; Уничтожает: Полностью перехватывает контекст процессора.
; -----------------------------------------------------------------------------
; =============================================================================
; MEGA-580 OS v1.5.177 — АППАРАТНЫЙ ДИСПЕТЧЕР КОНТЕКСТОВ (ОПТИМИЗИРОВАННЫЙ)
; =============================================================================
; Вход:  Переменная NEXT_PID содержит индекс запускаемого процесса (0..31).
; Выход: Процессор выполняет код НОВОЙ задачи в RING3.
; =============================================================================

; =============================================================================
; MEGA-580 OS v1.5.177 — АППАРАТНЫЙ ДИСПЕТЧЕР КОНТЕКСТОВ (ПОЛНЫЙ МОНОЛИТ ЯДРА)
; =============================================================================
; Вход:  Переменная NEXT_PID содержит индекс запускаемого процесса (0..31).
; Выход: Процессор выполняет код НОВОЙ задачи в RING3.
; =============================================================================

switch_mmu_context:
    DI                          ; КРИТИЧЕСКАЯ СЕКЦИЯ: Блокировка eMMU

    ; --- Шаг 5.1: Вычисление 16-битного адреса новой PCB (NEXT_PID * 128) ---
    LDA  NEXT_PID
    MOV  L, A
    MVI  H, 0                   ; HL = NEXT_PID (0..31)

    ; 7 аппаратных сдвигов пары HL влево под 128-байтный шаг таблицы
    DAD  H \ DAD  H \ DAD  H \ DAD  H \ DAD  H \ DAD  H \ DAD  H
    
    LXI  D, PCB_TABLE
    DAD  D                      ; HL = Абсолютный адрес НОВОЙ PCB в ОЗУ ядра

    ; --- Шаг 5.2: Обновление внутренних кэш-указателей супервизора ---
    SHLD CURRENT_PCB_PTR        ; Кэш намертво указывает на НОВУЮ активную PCB
    
    LDA  NEXT_PID
    STA  CURRENT_PID            ; CURRENT_PID = NEXT_PID

    ; --- Шаг 5.3: Атомарная eMMU-коммутация flat-карты К555ИР26 (Секция Г) ---
    PUSH H                      ; Спрятали базу новой PCB на стеке ядра
    LXI  D, O_PAGES_MAP         ; DE = +001CH
    DAD  D                      ; HL -> O_PAGES_MAP новой задачи
    
    ; Выводим 4 байта новой flat-карты строго в утвержденные регистры окон eMMU
    MOV  A, M
    OUT  EMMU_PAGE_REG0         ; Скоммутировали Сегмент 0 (OUT 070H)
    INX  H
    MOV  A, M
    OUT  EMMU_PAGE_REG1         ; Скоммутировали Сегмент 1 (OUT 071H)
    INX  H
    MOV  A, M
    OUT  EMMU_PAGE_REG2         ; Скоммутировали Сегмент 2 (OUT 072H)
    INX  H
    MOV  A, M
    OUT  EMMU_PAGE_REG3         ; Скоммутировали Сегмент 3 (OUT 073H)
    
    POP  H                      ; HL = Восстановили чистую базу новой PCB

    ; --- Шаг 5.4: Актуализация статуса нового процесса (Секция А) ---
    INX  H                      ; HL -> O_STATUS (+0001H)
    MVI  M, ST_RUNNING          ; Новый процесс переведен в состояние RUNNING (03H)
    DCX  H                      ; HL -> Чистая база новой PCB

    ; --- Шаг 5.5: Извлечение сохраненного SP ядра новой задачи ---
    PUSH H                      ; Спрятали базу новой PCB
    LXI  D, O_SAVED_SP
    DAD  D                      ; HL -> O_SAVED_SP новой задачи (+0008H)
    
    MOV  E, M                   ; E = Младший байт сохраненного SP ядра
    INX  H
    MOV  D, M                   ; D = Старший байт сохраненного SP ядра
    
    XCHG                        ; DE = База PCB, HL = Новый SP ядра
    SPHL                        ; Процессор переключился на стек ядра НОВОЙ задачи!

    ; --- Шаг 5.6: Извлечение регистрового кадра новой задачи со стека ядра ---
    POP  PSW                    ; Восстановили PSW (A, F) нового процесса
    POP  B                      ; Восстановили BC нового процесса
    POP  D                      ; Восстановили DE нового процесса
    POP  H                      ; HL = Извлеченный оригинальный SP_user нового процесса

    ; --- Шаг 5.7: Физический переход на стек Ring 3 новой задачи ---
    MVI  A, KERNEL_OVERRIDE_OFF
    OUT  EMMU_OVERRIDE          ; Закрываем оверрайд (триггер DD19, порт 0x76).
    
    SPHL                        ; Аппаратный сдвиг стека: процессор встал на родной стек задачи Ring 3!
    POP  H                      ; Сняли со стека Ring 3 оригинальный HL_user новой задачи.

    ; --- Шаг 5.8: Санкционированный аппаратный вылет в Ring 3 ---
    EI                          ; Программное разрешение прерываний (задержка в 1 такт)
    RET                         ; Извлекает PC со стека Ring 3 и летит в RING3!

; =============================================================================
; MEGA-580 OS v1.5.177 — ПОДПРОГРАММА РАСЧЕТА НАГРУЗКИ ЦП (КАНОН 128 БАЙТ)
; =============================================================================
k_calc_cpu_usage:
    ; Вход: Вызывается планировщиком или системным таймером через CALL.
    ; Задача: Подсчитать количество пользовательских задач в состоянии ST_READY
    ;         и спроецировать результат в метрику CPU_USAGE (0..4).
    ; Выход: Возврат в вызывающий контекст Ring 0 по IRET.
    ; =============================================================================
    PUSH B
    PUSH D
    PUSH H                          ; Сохраняем контекст супервизора

    XRA  A                          
    MOV  C, A                       ; C = 0 (Счетчик процессов в состоянии ST_READY)
    
    LXI  H, PCB_TABLE               ; HL -> Начало таблицы процессов (PID 0)
    MVI  B, MAX_PROCS               ; Счетчик цикла = 32 слота

_cpu_scan_loop:
    PUSH H                          ; Сохраняем базовый адрес текущей PCB
    
    ; Проверяем O_PID (+0000H). Слот IDLE-процесса (PID == 0) исключаем из нагрузки.
    MOV  A, M
    ORA  A                          ; Проверка на PID == 0
    JZ   _cpu_skip_slot             ; Пропускаем системный swapper
    
    ; Переходим к проверке статуса процесса O_STATUS (+0001H)
    INX  H                          ; HL -> O_STATUS
    MOV  A, M
    CPI  ST_READY                   ; Задача ожидает выполнения (02H)?
    JNZ  _cpu_skip_slot             ; Если свободна, спит или зомби — игнорируем
    
    INR  C                          ; Нашли READY-задачу: C++

_cpu_skip_slot:
    POP  H                          ; Восстанавливаем чистую базу текущей PCB
    LXI  D, PCB_SIZE                ; КАНОН: Шаг строго 128 байт!
    DAD  D                          ; Сдвиг HL на следующую структуру PCB
    
    DCR  B                          ; Уменьшаем счётчик слотов (32..0)
    JNZ  _cpu_scan_loop             ; Повторяем для всех 32 процессов

    ; --- ЭТАП 2: ТАБЛИЧНОЕ МАСШТАБИРОВАНИЕ В ДИАПАЗОН 0..4 ---
    MOV  A, C                       ; A = Общее число найденных READY процессов
    
    CPI  1
    JC   _load_0                    ; Если 0 процессов -> Загрузка 0%
    
    CPI  2
    JC   _load_25                   ; Если 1 процесс   -> Загрузка 25%
    
    CPI  3
    JC   _load_50                   ; Если 2 процесса  -> Загрузка 50%
    
    CPI  4
    JC   _load_75                   ; Если 3 процесса  -> Загрузка 75%
    
    ; Если процессов >= 4, то наступает 100% насыщение очереди задач
    MVI  A, CPU_LOAD_100
    JMP  _write_metric

_load_0:
    MVI  A, CPU_LOAD_0
    JMP  _write_metric

_load_25:
    MVI  A, CPU_LOAD_25
    JMP  _write_metric

_load_50:
    MVI  A, CPU_LOAD_50
    JMP  _write_metric

_load_75:
    MVI  A, CPU_LOAD_75

_write_metric:
    STA  CPU_USAGE                  ; Записываем результат (0..4) в память ядра

    POP  H
    POP  D
    POP  B                          ; Восстанавливаем контекст супервизора
    IRET                             ; Инвариантный возврат Ring0 (макрос 0xD9)
