; =============================================================================
; НАЗВАНИЕ: hw_lp82c11.asm (Инициализатор БИС Intel 82C11 с аппаратным детектом)
; СРЕДА:    Ring 0 супервизора | Архитектура КР580ВМ80А | Драйверы Печати
; =============================================================================
; =============================================================================
; ПРОЦЕДУРА: hw_82c11_lp_init
; Вход:  A  = Тестируемый 8-битный базовый порт данных (0x78 или 0x28).
;        DE = Физический адрес целевого свободного слота в LP_TABLE.
; Задача: Проверить физическое НАЛИЧИЕ чипа 82C11 на шине ввода-вывода. В случае
;         успеха взвести маску IRQ, выполнить тест принтера и заполнить слот.
; Выход: CY = 0 — Успех. Чип 82C11 найден, исправен и готов к асинхронной печати.
;        CY = 1 — Ошибка: Чип отсутствует на шине или принтер не готов.
;        Все РОН полностью сохранены согласно контракту HAL Варианта B.
; =============================================================================
hw_82c11_lp_init:
    PUSH B
    PUSH D
    PUSH H                          ; Спасли рантайм-контекст супервизора Ring 0

    MOV  C, A                       ; C = Тестируемый BASE_PORT (LPT0/LPT1)

    ; --- ЭТАП 1: ДИНАМИЧЕСКИЙ SMC-РАСЧЕТ И ПАТЧИНГ ПОРТОВ ---
    INR  A
    STA  _lp_c11_probe_out + 1
    STA  _lp_c11_probe_in + 1
    STA  _lp_c11_init_cmd_out + 1
    STA  _lp_c11_init_status_in + 1 ; Пропатчили все точки ввода-вывода на (BASE + 1)

    ; --- ЭТАП 2: ЖЕСТКИЙ ТЕСТ ФИЗИЧЕСКОГO НАЛИЧИЯ ЧИПА (КРЕМНИЕВАЯ ПРОВЕРКА) ---
    ; Формируем тестовую маску: совмещаем IRQ_EN и BIDIR фланги (0x01 | 0x02 = 0x03)
    MVI  A, C11_CTRL_IRQ_EN | C11_CTRL_BIDIR
_lp_c11_probe_out:
    OUT  000H                       ; Стреляем маску в регистр управления. Патчится на BASE+1
    
    NOP
	NOP                       ; Микропауза стабилизации шины КР580ВМ80А
    
_lp_c11_probe_in:
    IN   000H                       ; Читаем обратно этот же регистр. Патчится на BASE+1
    
    ; Выделяем проверяемые R/W биты управления
    ANI  C11_CTRL_IRQ_EN | C11_CTRL_BIDIR
    CPI  C11_CTRL_IRQ_EN | C11_CTRL_BIDIR ; Чип вернул записанные биты?
    JNZ  _hw_lp_c11_init_fail       ; Нет — кристалл физически отсутствует на шине! [1.3]

    ; --- ЭТАП 3: РАБОЧАЯ КОНФИГУРАЦИЯ И СБРОС КРИСТАЛЛА ---
    ; Активируем чистый вывод данных с разрешением асинхронных прерываний от ACK
    MVI  A, C11_CTRL_IRQ_EN                    
_lp_c11_init_cmd_out:
    OUT  000H                       ; Рабочая прошивка БИС 82C11. Патчится на BASE+1
    NOP
	NOP                       ; Стабилизация внутренних триггеров чипа

    ; --- ЭТАП 4: ТЕСТ ФУНКЦИОНИРОВАНИЯ ВНЕШНЕГO УСТРOЙСТВА (CENTRONICS) ---
_lp_c11_init_status_in:
    IN   000H                       ; Считали внешние флаги статуса. Патчится на BASE+1
    MOV  B, A                       ; B = Кэш регистра статуса

    ; Валидация 1: Проверяем сигнал выбора принтера (LP_SELECT / Бит 4 == 1)
    ANI  LP_SELECT                  ; Маска 0x10 из lp.inc
    JZ   _hw_lp_c11_init_fail       ; Если 0 — принтер выключен или не подключен

    ; Валидация 2: Проверяем датчик окончания бумаги (LP_PAPER_OUT / Бит 5 == 0)
    MOV  A, B
    ANI  LP_PAPER_OUT               ; Маска 0x20 из lp.inc
    JNZ  _hw_lp_c11_init_fail       ; Если 1 — лоток пуст, блокируем запуск

    ; --- ЭТАП 5: ПОСЛЕДОВАТЕЛЬНОЕ ЗАПОЛНЕНИЕ ДЕСКРИПТОРА СЛOТА ---
    MOV  H, D
    MOV  L, E                       ; HL = Истинный адрес слота LP_TABLE

    MVI  M, 00H                     ; O_LP_STATUS = 0
    
    INX  H                          ; HL -> O_LP_PORT_DATA (+1)
    MOV  M, C                       ; Физический порт DATA (BASE+0)
    
    MOV  A, C
	INR A
    INX  H                          ; HL -> O_LP_PORT_STATUS (+2)
    MOV  M, A                       ; Физический порт STATUS (BASE+1)
    
    INX  H                          ; HL -> O_LP_PORT_CTRL (+3)
    MOV  M, A                       ; Физический порт CTRL (Для 82C11 совмещен с STATUS!)
    
    INX  H                          ; HL -> O_LP_FLAGS (+4)
    MVI  M, LP_OPEN                 ; Выставили флаг "Порт успешно открыт" (0x01)
    
    ; Фиксация типа чипа в дескрипторе
    MOV  H, D
    MOV  L, E                       ; HL = Восстановили чистую базу слота
    LXI  B, O_LP_HW_TYPE            ; Смещение +19 из lp.inc
    DAD  B                          ; HL -> O_LP_HW_TYPE
    MVI  M, LP_TYPE_82C11           ; Зафиксировали тип кремния (02H) [1.3]

    POP  H
    POP  D
    POP  B                          ; Полное каноническое восстановление РОН по LIFO
    ANA  A                          ; СБРОС ФЛАГА ОШИБКИ: CY = 0 (Полный успех!)
    RET

_hw_lp_c11_init_fail:
    POP  H
    POP  D
    POP  B                          ; Восстановили оригинальный контекст супервизора
    STC                             ; ВЗВОД ФЛАГА ОШИБКИ: CY = 1 (Аппаратный отказ чипа / Centronics)
    RET

; =============================================================================
; НАЗВАНИЕ: hw_lp82c11.asm (Асинхронный монолитный ISR для Intel 82C11)
; СРЕДА:    Ring 0 супервизора | Архитектура КР580ВМ80А | Драйверы Печати
; =============================================================================
; =============================================================================
; АСИНХРОННЫЙ ОБРАБОТЧИК ПРЕРЫВАНИЙ ДЛЯ БИС Intel 82C11
; Вход:  Аппаратный CALL по вектору IRQ от КР580ВН59. Прерывания заперты (DI).
;        Состояние РОН и SP — случайное (из прерванного контекста Ring 3).
; =============================================================================
hw_lp82c11_isr:
_smc_c11_isr_base:
    ; === ЭТАП 0: АППАРАТНЫЙ ПОРТ ПРИВЯЗКИ БАЗЫ (SMC-ИНСТАНС) ===
    LXI  D, 0000H                   ; Сюда карусель lp_core.asm при старте
                                    ; пропишет истинный адрес ОЗУ-структуры слота.

    PUSH B
    PUSH D
    PUSH H                          ; Спасли рантайм-контекст прерванного процесса

    ; === ЭТАП 1: ДИНАМИЧЕСКИЙ SMC-РАСЧЕТ АППАРАТНЫХ ПОРТОВ ---
    MOV  H, D
    MOV  L, E                       ; HL = Физическая база дескриптора принтера
    INX  H                          ; HL -> O_LP_PORT_DATA (+1)
    MOV  A, M                       ; A = Физический базовый порт DATA (0x78 или 0x28)
    MOV  C, A                       ; C = Кэш базового порта DATA
    
    ; Прошиваем операнд инструкции OUT для выдачи данных (BASE+0)
    STA  _lp_c11_isr_data_out + 1
    
    ; Вычисляем и прошиваем управляющий порт (BASE+1)
    MOV  A, C \ INR  A
    STA  _lp_c11_isr_ctrl_out + 1   ; SMC-патч операндов ввода-вывода завершен!

    ; === ЭТАП 2: КОНТРОЛЬ НАЛИЧИЯ ДАННЫХ В TX КОЛЬЦЕ ОЗУ ===
    MOV  H, D
    MOV  L, E                       ; HL = База слота LPT
    LXI  B, O_LP_BUFFER_COUNT       ; Смещение +18 из lp.inc
    DAD  B                          ; HL -> O_LP_BUFFER_COUNT
    MOV  A, M
    ORA  A                          ; BUFFER_COUNT == 0 (Очередь вывода пуста)?
    JZ   _lp_c11_isr_empty_close    ; Да — уходим на глушение линии прерываний чипа

    ; === ЭТАП 3: ИЗВЛЕЧЕНИЕ БАЙТА ИЗ КОЛЬЦЕВОГО FIFO БУФЕРА ===
    DCR  M                          ; O_LP_BUFFER_COUNT-- (Фиксация в памяти ядра)
    
    ; Извлекаем индекс чтения по указателю TAIL
    DCX  H                          ; HL -> O_LP_BUFFER_TAIL (Смещение +17)
    MOV  A, M                       ; A = TAIL_old
    MOV  B, A                       ; B = Кэш текущего индекса чтения
    
    ; Инкрементируем и зацикливаем TAIL в памяти ядра Ring 0
    INR  M                          ; TAIL++
    MOV  A, M
    CPI  48                         ; Граница буфера строго 48 байт (21+48=69 < 80) [1.3]
    JC   _lp_c11_isr_tail_no_wrap
    MVI  M, 00H                     ; Циклический заворот TAIL = 0

_lp_c11_isr_tail_no_wrap:
    ; Двухступенчатый DAD-расчет физического адреса байта в ОЗУ
    MOV  H, D
    MOV  L, E                       ; HL = Восстановили чистую базу слота LPT
    PUSH D                          ; Спрятали базу слота
    
    LXI  D, 21                      ; Смещение начала сектора данных буфера FIFO
    DAD  D                          ; HL = База + Начало буфера вывода
    
    MOV  E, B
    MVI  D, 00H                     ; DE = 0000H + TAIL_old
    DAD  D                          ; HL = Точный физический адрес байта в ОЗУ ядра Ring 0!
    MOV  A, M                       ; А = ASCII-символ, готовый к печати
    MOV  B, A                       ; B = Спасли символ от затирания при POP
    POP  D                          ; DE = Восстановили базу слота LPT

    ; === ЭТАП 3.1: ПОЛНЫЙ 32-БИТНЫЙ ИНКРЕМЕНТ СЧЕТЧИКА O_LP_BYTES_WRITTEN ===
    ; Наш утвержденный каноничный каскад обработки переносов Little-Endian
    MOV  H, D
    MOV  L, E
    LXI  D, O_LP_BYTES_WRITTEN      ; Смещение +8 из lp.inc
    DAD  D                          ; HL -> Байт 0 (младший) счётчика
    
    INR  M                          ; Байт 0++
    JNZ  _lp_c11_bytes_inc_done     ; Если не было переполнения — выходим из каскада
    
    INX  H                          ; Переход к Байту 1
    INR  M
    JNZ  _lp_c11_bytes_inc_done
    
    INX  H                          ; Переход к Байту 2
    INR  M
    JNZ  _lp_c11_bytes_inc_done
    
    INX  H                          ; Переход к Байту 3 (старший)
    INR  M

_lp_c11_bytes_inc_done:
    ; === ЭТАП 4: ФИЗИЧЕСКИЙ ВЫСТРЕЛ В КРЕМНИЙ Intel 82C11 ===
    MOV  A, B                       ; А = Извлеченный ASCII-символ
_lp_c11_isr_data_out:
    OUT  000H                       ; Выставили символ в регистр данных. Патчится на BASE+0
                                    ; ВНИМАНИЕ: Чип 82C11 сам аппаратно сформирует строб! [1.3]
    JMP  _lp_c11_isr_close_8259

_lp_c11_isr_empty_close:
    ; Очередь ОЗУ пуста — принудительно гасим асинхронные прерывания чипа 82C11.
    MVI  A, C11_CTRL_RESET
_lp_c11_isr_ctrl_out:
    OUT  000H                       ; Прерывания чипа 82C11 заблокированы. Патчится на BASE+1 [1.3]

    ; Переводим логический статус порта в состояние покоя (O_LP_STATUS = 0)
    MOV  H, D
    MOV  L, E
    MVI  M, 00H                     

_lp_c11_isr_close_8259:
    ; === ЭТАП 5: СБРОС ТРИГГЕРА КОНТРОЛЛЕРА ПРЕРЫВАНИЙ КР580ВН59 ===
    ; ИСПРАВЛЕНО: отправляем EOI в реальный порт ВН59 согласно вашему стандарту
    MVI  A, 020H
    OUT  MASTER_8259_CMD            ; Неспецифический EOI в главный контроллер прерываний ВН59
    
    POP  H
    POP  D
    POP  B                          ; Полное каноническое восстановление контекста прерванной задачи
    EI                              ; Разрешили асинхронный конвейер шины eMMU
    RET

