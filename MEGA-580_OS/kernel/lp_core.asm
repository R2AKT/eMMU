;*
;[User Space (Ring 3)]
;└── write(fd, buf, len) / read(fd, buf, len)
;     │
;     ▼ (Аппаратное прерывание INT / RST 6)
;[Шлюз Системных Вызовов (Ring 0)]
;└── sys_call_entry (main.asm)
;     ├── sys_write (sys_io.asm) ──► vfs_chrdev_write (chrdev.asm) ──┐
;     └── sys_read  (sys_io.asm) ──► vfs_chrdev_read  (chrdev.asm) ──┤
;                                                                    │
;    ┌───────────────────────────────────────────────────────────────┘
;    ▼ (Динамическая индексация по таблице SYS_CHRDEV_SWITCH [MAJOR_LP = 7])
;[Коммутационный слой VFS -> HAL Мосты]
;     ├── lp_write_bridge (lp_core.asm) [inline расчет: Minor * 80]
;     │    ├── Атомарная укладка байта (под DI/EI) в FIFO-кольцо ОЗУ Ring 0
;     │    │    └── LP_TABLE[Minor].O_LP_BUFFER_RESERVED
;     │    └── Аппаратный старт заснувшего конвейера (Если BUFFER_COUNT == 0)
;     │         ├── OUT РРУ (BSR Взвод INTE A) ──► [ Кремний КР580ВВ55А ]
;     │         └── OUT Регистр управления ──► [ Кристалл Intel 82C11 ]
;     │
;     └── lp_read_bridge (lp_core.asm) [Валидация границ Minor]
;          └── Мгновенный POSIX-возврат константы 00H (Символ EOF — Конец файла)
;*
; =============================================================================
; НАЗВАНИЕ: lp_core.asm (Центральный высокоуровневый диспетчер подсистемы /dev/lp*)
; СРЕДА:    Ring 0 супервизора | Архитектура КР580ВМ80А | Модуль VFS / ЯДРО
; =============================================================================
; =============================================================================
; НАЗВАНИЕ: lp_core.asm (Логический конфигуратор ОЗУ подсистемы печати)
; СРЕДА:    Ring 0 супервизора | Архитектура КР580ВМ80А | Модуль ПЕЧАТИ
; =============================================================================
; =============================================================================
; ПРОЦЕДУРА: k_lp_subsys_init
; Вход:  Cold Boot, Ring 0, прерывания гарантированно заперты (DI).
; Задача: Обнулить все 160 байт глобального массива LP_TABLE, ликвидируя
;         мусор рантайма после подачи питания на ОЗУ eMMU РЕВ_2.0.
; Выход: Все РОН (BC, DE, HL) полностью сохранены согласно контракту HAL.
; =============================================================================
k_lp_subsys_init:
    PUSH B
    PUSH D
    PUSH H                          ; Спасли рантайм-контекст супервизора Ring 0

    ; --- ЭТАП 1: ПОДГОТОВКА УКАЗАТЕЛЕЙ ЦИКЛА ОЧИСТКИ ---
    LXI  H, LP_TABLE                ; HL = Релоцируемый адрес начала таблицы из BSS [1.3]
    
    ; Вычисляем полный объем очистки: 2 слота * 80 байт = 160 байт (0x00A0)
    MVI  B, LP_MAX_DEVICES * LP_STRUCT_SIZE ; B = Счетчик итераций (160 циклов)
    XRA  A                          ; A = 00H (Замыкающий маркер очистки)

    ; --- ЭТАП 2: ВЫСОКОСКОРОСТНОЙ ПОБАЙТОВЫЙ СКАН-СБРОС В ОЗУ ---
_lp_subsys_clear_loop:
    MOV  M, A                       ; Записали чистый нуль в текущую ячейку ОЗУ [1.3]
    INX  H                          ; Сдвинули указатель на следующий байт структуры
    DCR  B                          ; Уменьшаем счетчик оставшихся проходов
    JNZ  _lp_subsys_clear_loop     ; Цикл крутится, пока регистр B не сойдется в ноль

    ; --- ЭТАП 3: КАНОНИЧЕСКОЕ ВОССТАНОВЛЕНИЕ КОНТЕКСТА ---
    POP  H
    POP  D
    POP  B                          ; Полное восстановление РОН супервизора по LIFO
    RET

; =============================================================================
; НАЗВАНИЕ: lp_core.asm (Канонический высокоуровневый HAL-мост записи)
; СРЕДА:    Ring 0 супервизора | Архитектура КР580ВМ80А | Модуль ПЕЧАТИ
; =============================================================================
; =============================================================================
; ПРОЦЕДУРА: lp_write_bridge
; Вход:  А  = Выводимый ASCII-символ.
;        B  = Номер Minor (0..1), полученный от коммутатора символьных устройств.
; Задача: POSIX-валидация границ и флагов. Неблокирующее ожидание места в FIFO.
;         Атомарная запись байта в ОЗУ-кольцо и асинхронный разгон прерываний чипа.
; Выход: Все РОН полностью восстановлены по контракту HAL Варианта B.
; =============================================================================
lp_write_bridge:
    PUSH B
    PUSH D
    PUSH H                          ; Спасли рантайм-контекст супервизора Ring 0

    MOV  C, A                       ; C = ЭВАКУАЦИЯ: сохранили выводимый символ

    ; === ЭТАП 1: ВАЛИДАЦИЯ ГРАНИЦ MINOR-НОМЕРА ===
    MOV  A, B                       ; A = Minor (0..1)
    CPI  LP_MAX_DEVICES             ; Сравнение с лимитом (2) из lp.inc
    JNC  _lp_wr_err_inval           ; Если B >= 2 — ошибка EINVAL (Неверный аргумент)

    ; === ЭТАП 2: МГНОВЕННЫЙ РАСЧЁТ АДРЕСА СТРУКТУРЫ LP_SLOT (Minor * 80) ===
    MOV  L, A
    MVI  H, 00H                     ; HL = Minor (0000H .. 0001H)

    ; Быстрое умножение Minor на 80 через сдвиги: (Minor * 64) + (Minor * 16)
    DAD  H
	DAD  H
	DAD  H
	DAD  H  ; HL = Minor * 16
    XCHG                             ; DE = Minor * 16
    DAD  H
	DAD  H                  ; HL = (Minor * 16) * 4 = Minor * 64
    DAD  D                           ; HL = (Minor * 64) + (Minor * 16) = Minor * 80
    
    LXI  D, LP_TABLE                ; Релоцируемый базовый адрес таблицы из BSS
    DAD  D                           ; HL = Физический адрес структуры активного LP-слота!
    XCHG                             ; DE = Зафиксировали базу LP-структуры в паре DE

    ; === ЭТАП 3: ПРОВЕРКА ФЛАГА LP_OPEN (ПОРТ ДОЛЖЕН БЫТЬ ОТКРЫТ) ===
    MOV  H, D
    MOV  L, E                       ; HL = База LP-слота
    LXI  B, O_LP_FLAGS              ; Смещение +4 из lp.inc
    DAD  B                           ; HL -> O_LP_FLAGS
    MOV  A, M
    ANI  LP_OPEN                     ; Проверка бита 0 (0x01)
    JZ   _lp_wr_err_ebadf            ; Если 0 — порт не открыт (EBADF)

    ; === ЭТАП 4: ПРОВЕРКА ФЛАГА LP_ERROR_STATE (АППАРАТНАЯ ОШИБКА) ===
    MOV  A, M                       ; Восстановили флаги из кэша
    ANI  LP_ERROR_STATE              ; Проверка бита 2 (0x04)
    JNZ  _lp_wr_err_io               ; Если 1 — порт в состоянии фатальной ошибки (EIO)

    ; === ЭТАП 5: ЦИКЛ ОЖИДАНИЯ ОСВОБОЖДЕНИЯ МЕСТА В FIFO (С ТАЙМАУТОМ) ===
    MVI  H, 0FFH                     ; H = 255 (Таймаут ожидания)

_lp_wr_br_wait_fifo:
    PUSH D                          ; СТЭК-ЗАЩИТА: спрятали чистую базу слота в стек Ring 0!
    
    MOV  H, D
    MOV  L, E                       ; HL = Истинный адрес слота LPT
    LXI  B, O_LP_BUFFER_COUNT       ; Смещение +18 из lp.inc
    DAD  B                           ; HL -> O_LP_BUFFER_COUNT
    MOV  A, M                       ; A = Текущее количество байт в FIFO-очереди
    CPI  48                         ; Буфер полон (Достиг лимита 48 байт)?
    
    POP  D                          ; СТЭК-ВОССТАНОВЛЕНИЕ: вернули чистую базу слота в DE!
    JC   _lp_wr_br_space_ok         ; Место есть (COUNT < 48) — шагаем на запись

    ; Буфер полон — кратковременно открываем прерывания для выгрузки байт асинхронным ISR
    EI
	NOP
	NOP
	NOP
	NOP
	NOP
	DI

    ; Проверка и декремент 8-битного таймаута в регистре H
    MOV  A, H
    DCR  A                          ; H--
    JZ   _lp_wr_err_eagain           ; Таймаут исчерпан — уходим на отбой с EAGAIN
    MOV  H, A                       ; Сохранили обновленный таймаут в H
    JMP  _lp_wr_br_wait_fifo

_lp_wr_br_space_ok:
    ; === ЭТАП 6: АТОМАРНАЯ КРИТИЧЕСКАЯ СЕКЦИЯ ЗАПИСИ В FIFO ОЗУ ===
    DI                              ; НАМЕРТВО ЗАПЕРЛИ ПРЕРЫВАНИЯ! Защита от коллизии с ISR

    ; Вычисляем адрес записи по указателю HEAD
    MOV  H, D
    MOV  L, E                       ; ИСПРАВЛЕНО: Старший в старший (H=D), младший в младший (L=E)!
    LXI  B, O_LP_BUFFER_HEAD        ; Смещение +16 из lp.inc
    DAD  B                           ; HL -> O_LP_BUFFER_HEAD
    MOV  A, M                       ; А = HEAD_old
    STA  _lp_wr_br_tmp_head         ; ЭВАКУАЦИЯ: сбросили индекс в локальную переменную ОЗУ ядра

    ; Инкрементируем и зацикливаем HEAD на границе 48 байт
    INR  M                           ; O_LP_BUFFER_HEAD++ (Фиксация в памяти дескриптора)
    MOV  A, M
    CPI  48
    JC   _lp_wr_br_head_no_wrap
    MVI  M, 00H                      ; Циклический заворот HEAD = 0

_lp_wr_br_head_no_wrap:
    ; Двухступенчатый расчет физического адреса ячейки: База + 21 + HEAD_old
    MOV  H, D
    MOV  L, E                       ; HL = Восстановили чистую базу LP-слота
    PUSH D                           ; Спрятали базу слота на стек Ring 0

    LXI  D, 21                       ; Смещение начала сектора данных буфера FIFO
    DAD  D                           ; HL = База + 21
    
    LDA  _lp_wr_br_tmp_head          ; ИСПРАВЛЕНО: Загрузили истинный HEAD_old из переменной ОЗУ
    MOV  E, A
    MVI  D, 00H                      ; DE = 0000H + HEAD_old
    DAD  D                           ; HL = Точный физический адрес фиксации байта!
    MOV  M, C                        ; АТОМАРНАЯ ФИКСАЦИЯ: выводимый символ лег в ОЗУ Ring 0!
    POP  D                           ; DE = Восстановили базу LP-слота

    ; Инкрементируем общий счётчик байт в очереди вывода
    MOV  H, D
    MOV  L, E                       ; ИСПРАВЛЕНО: Восстановили HL параллельно структуре пары DE!
    LXI  B, O_LP_BUFFER_COUNT       ; Смещение +18 из lp.inc
    DAD  B                           ; HL -> O_LP_BUFFER_COUNT
    MOV  A, M                        ; A = BUFFER_COUNT_old
    INR  M                           ; O_LP_BUFFER_COUNT++ (Зафиксировали в памяти ядра)

    ; === ЭТАП 7: АППАРАТНЫЙ СТАРТ КОНВЕЙЕРА (ПИНАЕМ КРЕМНИЙ) ===
    ORA  A                           ; Проверяем BUFFER_COUNT_old
    JNZ  _lp_wr_br_exit_clear        ; Если буфер не был пуст — ISR уже крутится на шине, уходим!

    ; Извлекаем физический порт управления (O_LP_PORT_CTRL) для прямого пинка
    MOV  H, D
    MOV  L, E                       ; ИСПРАВЛЕНО: Восстановили HL параллельно DE!
    LXI  B, O_LP_PORT_CTRL          ; Смещение +3 из lp.inc
    DAD  B                           ; HL -> O_LP_PORT_CTRL
    MOV  C, M                        ; C = Физический порт управления (BASE+2 или BASE+1)

    ; Считываем тип активного чипа для корректной Handshake-сигнализации
    MOV  H, D
    MOV  L, E                       ; ИСПРАВЛЕНО: Восстановили HL параллельно DE!
    LXI  B, O_LP_HW_TYPE            ; Смещение +19 из lp.inc
    DAD  B                           ; HL -> O_LP_HW_TYPE
    MOV  A, M                        ; A = LP_TYPE_8255 / 82C11

    CPI  LP_TYPE_82C11
    JZ   _lp_wr_br_kick_c11          ; Прыгаем на разгон Intel 82C11

    ; --- Активация прерываний для КР580ВВ55А ---
    MOV  A, C
    INR  A                           ; A = (BASE+2) + 1 = BASE+3 (РРУ чипа ВВ55А)
    MOV  C, A
    
    MVI  A, 007H                     ; Наш проверенный BSR-фикс взвода триггера INTE A (PC3 Set)
    STA  _lp_wr_br_kick_out1 + 1     ; SMC патч порта вывода
_lp_wr_br_kick_out1:
    OUT  000H                       ; Прерывания ВВ55А взведены! Патчится на BASE+3
    JMP  _lp_wr_br_exit_clear

_lp_wr_br_kick_c11:
    ; --- Активация прерываний для Intel 82C11 ---
    MOV  A, C                       ; А = Порт управления чипа 82C11 (BASE+1)
    STA  _lp_wr_br_kick_out2 + 1
    MVI  A, 001H                    ; Константа C11_CTRL_IRQ_EN
_lp_wr_br_kick_out2:
    OUT  000H                       ; Прерывания 82C11 взведены! Патчится на BASE+1

_lp_wr_br_exit_clear:
    EI                               ; ВЫШЛИ ИЗ КРИТИЧЕСКОЙ СЕКЦИИ, ОТКРЫЛИ ПРЕРЫВАНИЯ!
    POP  H
    POP  D
    POP  B                           ; Идеальное каноническое восстановление РОН по LIFO
    ANA  A                           ; СБРОС ФЛАГА ОШИБКИ: CY = 0 (Успех!)
    RET

; =============================================================================
; КАНАЛЫ АВАРИЙНЫХ ВОЗВРАТОВ С POSIX-КОДАМИ ОШИБОК
; =============================================================================
_lp_wr_err_inval:
    POP  H
    POP  D
    POP  B                           ; Восстановили РОН
    MVI  A, EINVAL                   ; A = 22 (Invalid argument — Minor вне диапазона)
    STC                              ; CY = 1 (Провал)
    RET

_lp_wr_err_ebadf:
    POP  H
    POP  D
    POP  B                           ; Восстановили РОН
    MVI  A, EBADF                    ; A = 9 (Bad file number — порт не открыт)
    STC                              ; CY = 1 (Провал)
    RET

_lp_wr_err_io:
    POP  H
    POP  D
    POP  B                           ; Восстановили РОН
    MVI  A, EIO                      ; A = 5 (I/O error — аппаратная ошибка принтера)
    STC                              ; CY = 1 (Провал)
    RET

_lp_wr_err_eagain:
    POP  H
    POP  D
    POP  B                           ; Восстановили РОН
    MVI  A, EAGAIN                   ; A = 11 (Try again — таймаут ожидания FIFO)
    STC                              ; CY = 1 (Провал)
    RET

; =============================================================================
; НАЗВАНИЕ: lp_core.asm (POSIX-совместимый HAL-мост чтения LP_READ_BRIDGE) 
; СРЕДА:    Ring 0 супервизора | Архитектура КР580ВМ80А | Модуль ПЕЧАТИ
; =============================================================================
; =============================================================================
; ПРОЦЕДУРА: lp_read_bridge
; Вход:  B  = Номер Minor (0..1), полученный от коммутатора символьных устройств.
; Задача: Имитировать каноничное поведение POSIX LPT-устройства на чтение.
;         Выполнить валидацию Minor и мгновенно вернуть признак EOF (00H).
; Выход: А  = 00H (Символ конца файла EOF).
;        CY = 0 — Системная операция завершена успешно.
;        Регистры BC, DE, HL полностью сохранены согласно контракту HAL.
; =============================================================================
lp_read_bridge:
    PUSH B
    PUSH D
    PUSH H                          ; Спасли рантайм-контекст супервизора Ring 0

    ; === ЭТАП 1: РАНТАЙМ-ВАЛИДАЦИЯ ГРАНИЦ MINOR ===
    MOV  A, B                       ; A = Minor (0..1)
    CPI  LP_MAX_DEVICES             ; Сравниваем с лимитом (2) из lp.inc
    JNC  _lp_rd_br_err_inval        ; Если Minor >= 2 — уходим на аварийный выход

    ; === ЭТАП 2: ФОРМИРОВАНИЕ ПОТОКА КОНЦА ФАЙЛА (EOF) ===
    ; Согласно UNIX-канону, чтение из write-only устройства Centronics 
    ; не вешает систему, а выдает 0 прочитанных байт (атомарный EOF = 00H).
    MVI  A, 00H                     ; Аккумулятор А = 00H (EOF)

    POP  H
    POP  D
    POP  B                          ; Идеальное каноническое восстановление РОН по LIFO
    ANA  A                          ; СБРОС ФЛАГА ОШИБКИ: CY = 0 (Полный рантайм-успех)
    RET

; =============================================================================
; КАНАЛ АВАРИЙНОГО ВОЗВРАТА ОШИБКИ
; =============================================================================
_lp_rd_br_err_inval:
    POP  H
    POP  D
    POP  B                          ; Изолированно разгрузили рантайм-контекст по LIFO
    MVI  A, EINVAL                  ; А = Константа 22 (0x16) из lp.inc / vfs.inc
    STC                             ; ВЗВОД ФЛАГА ОШИБКИ: CY = 1 (Провал операции)
    RET

; =============================================================================
; ПРОЦЕДУРА: k_lp_carousel_auto_build
; Вход:  Cold Boot, Ring 0, прерывания гарантированно заперты (DI).
; Задача: Последовательно опросить физические адреса LPT0 (0x78) и LPT1 (0x28).
;         Аллоцировать свободные 80-байтовые слоты в LP_TABLE, назначить IRQ
;         и привязать специализированные ISR к вектору прерываний ВН59.
; Выход: Все РОН полностью сохранены. Прерывания заперты.
; =============================================================================
k_lp_carousel_auto_build:
    PUSH B
    PUSH D
    PUSH H                          ; Спасли рантайм-контекст супервизора

    ; Инициализируем динамический указатель целевого логического LP-слота ОЗУ
    LXI  D, LP_TABLE                ; DE = База первого слота (обслуживает /dev/lp0)

    ; --- ТЕСТИРОВАНИЕ ФИЗИЧЕСКОГО АДРЕСА LPT0 ---
    MVI  A, LPT0_BASE_PORT          ; A = 0x78 (из lp.inc)
    MVI  B, 5                       ; B = Аппаратная линия IRQ5 для первого порта
    CALL _lp_probe_hardware_gate    ; Запуск шлюза автодетекта
    
    ; --- ТЕСТИРОВАНИЕ ФИЗИЧЕСКОГО АДРЕСА LPT1 ---
    MVI  A, LPT1_BASE_PORT          ; A = 0x28 (из lp.inc)
    MVI  B, 6                       ; B = Аппаратная линия IRQ6 для второго порта
    CALL _lp_probe_hardware_gate    ; Запуск шлюза автодетекта

    POP  H
    POP  D
    POP  B                          ; Полное каноническое восстановление РОН по LIFO
    RET

; =============================================================================
; ВНУТРЕННИЙ ШЛЮЗ АВТОДЕТЕКТА: _lp_probe_hardware_gate (ИСПРАВЛЕННАЯ ВЕРСИЯ)
; =============================================================================
_lp_probe_hardware_gate:
    PUSH B                          ; Спрятали пару BC (B=IRQ, C=мусор)
    PUSH PSW                        ; Спрятали тестируемый порт на стек

    ; --- ПРИОРИТЕТНЫЙ ТЕСТ №1: Проверка наличия специализированной БИС 82C11 ---
    CALL hw_82C11_lp_init           ; Функция в файле hw_lp82c11.asm [1.3]
    JNC  _lp_gate_device_found      ; CY=0 -> Чип 82C11 успешно детектирован!

    ; --- РЕЗЕРВНЫЙ ТЕСТ №2: Проверка наличия универсального адаптера КР580ВВ55А ---
    POP  PSW
	PUSH PSW             ; Восстановили тестируемый порт в аккумулятор A
    CALL hw_i8255_lp_init           ; Функция в файле hw_lp8255.asm [1.3]
    JNC  _lp_gate_device_found      ; CY=0 -> Нашли КР580ВВ55А на этом адресе!

    ; --- АППАРАТНАЯ ПУСТОТА: Ни одного чипа на данном адресе шины нет ---
    POP  PSW                        ; Очистили стек от сохраненного порта
    POP  B                          ; Восстановили назначенное IRQ
    RET                             ; Слот в DE остается свободным

_lp_gate_device_found:
    ; === КРЕМНИЙ НАЙДЕН И ИНИЦИАЛИЗИРОВАН! ===
    POP  PSW                        ; Выбросили порт из стека (не нужен)
    POP  B                          ; Восстановили IRQ (B = 5 или 6)
    
    ; Записываем вычисленную линию IRQ в дескриптор текущего слота
    MOV  H, D
    MOV  L, E                       ; HL = Чистая база текущего слота ОЗУ
    PUSH H                          ; Запомнили базу слота на стеке
    
    ; ИСПРАВЛЕНО: Удалена мёртвая инструкция LXI D, O_LP_HW_LINE
    LXI  D, O_LP_HW_IRQ_LINE        ; Смещение +20 согласно lp.inc
    DAD  D                          ; HL -> Поле O_LP_HW_IRQ_LINE
    MOV  M, B                       ; Прошили номер IRQ в структуру дескриптора
    
    POP  H                          ; HL = Восстановили чистую базу слота
    
    ; Считываем тип чипа для выбора правильного ISR
    PUSH B                          ; Спасли IRQ
    LXI  B, O_LP_HW_TYPE            ; Смещение +19
    DAD  B                          ; HL -> Поле типа кремния
    MOV  A, M                       ; A = Считанный тип (01H или 02H)
    POP  B                          ; Восстановили IRQ (B = 5 или 6)
    
    CPI  LP_TYPE_82C11              ; На этом адресе ожил Intel 82C11?
    JZ   _lp_gate_register_c11      ; Да — прыгаем на регистрацию вектора 82C11

    ; --- Регистрация обработчика прерываний для КР580ВВ55А ---
    MOV  A, B                       ; А = Номер линии IRQ (5 или 6)
    LXI  H, hw_lp8255_isr           ; HL = Адрес ISR для ВВ55А
    CALL k_register_isr             ; Встроили вектор в таблицу КР580ВН59!
    JMP  _lp_gate_slot_allocated

_lp_gate_register_c11:
    ; --- Регистрация обработчика прерываний для Intel 82C11 ---
    MOV  A, B                       ; А = Номер линии IRQ (5 или 6)
    LXI  H, hw_lp82c11_isr          ; HL = Адрес ISR для 82C11
    CALL k_register_isr             ; Встроили вектор в таблицу КР580ВН59!

_lp_gate_slot_allocated:
    ; === СДВИГ УКАЗАТЕЛЯ: ПОРТ УСПЕШНО ЗАНЯЛ СЛОТ, ШАГАЕМ ВПЕРЕД ===
    MOV  H, D
    MOV  L, E                       ; HL = Текущая база слота
    LXI  D, LP_STRUCT_SIZE          ; DE = 80 байт — шаг структуры
    DAD  D                          ; HL = HL + 80
    XCHG                            ; DE = Указатель на следующий СВОБОДНЫЙ слот!
    RET

; =============================================================================
; НАЗВАНИЕ: lp_core.asm (Авторегистратор LPT-драйвера в коммутаторе VFS)
; СРЕДА:    Ring 0 супервизора | Архитектура КР580ВМ80А | Модуль ПЕЧАТИ
; =============================================================================
; =============================================================================
; ПРОЦЕДУРА: k_lp_register_vfs
; Вход:  Cold Boot, Ring 0, прерывания заперты (DI).
; Задача: Зарегистрировать HAL-мосты чтения/записи подсистемы параллельной печати
;         в глобальной таблице SYS_CHRDEV_SWITCH под MAJOR-номером 7 (MAJOR_LP).
; Выход: CY = 0 — Успех. Драйвер LPT успешно интегрирован в коммутатор VFS.
;        CY = 1 — Критическая ошибка: Неверный Major-номер (Защита границ таблицы).
;        Все РОН (BC, DE, HL) полностью сохранены по контракту LIFO.
; =============================================================================
k_lp_register_vfs:
    PUSH B
    PUSH D
    PUSH H                          ; Спасли рантайм-контекст супервизора Ring 0

    ; --- ЭТАП 1: ПОДГОТОВКА ПАРАМЕТРОВ ДЛЯ СИСТЕМНОГО РЕГИСТРАТОРА VFS ---
    MVI  A, MAJOR_LP                ; A = MAJOR_LP (Константа 7 из lp.inc) [1.3]
    LXI  H, lp_write_bridge         ; HL = Физический адрес HAL-моста записи LPT [1.3]
    LXI  D, lp_read_bridge          ; DE = Физический адрес HAL-моста чтения LPT [1.3]

    ; --- ЭТАП 2: АТОМАРНАЯ СВЯЗКА В ОЗУ ТАБЛИЦЕ ДРАЙВЕРОВ ЯДРА ---
    CALL k_register_chrdev          ; Вызов Plug-and-Play регистратора VFS [1.3]
                                    ; (При успехе возвращает CY=0, при ошибке границ — CY=1)

    ; Перехват ошибки: если Major >= 32 — аварийный выход
    JC   _k_lp_reg_vfs_fault

    ; --- ЭТАП 3: КАНОНИЧЕСКОЕ ВОССТАНОВЛЕНИЕ КОНТЕКСТА (УСПЕХ) ---
    POP  H
    POP  D
    POP  B                          ; Полное каноническое восстановление РОН по LIFO
    ANA  A                          ; СБРОС ФЛАГА ОШИБКИ: CY = 0 (Полный успех операции)
    RET

; =============================================================================
; КАНАЛ АВАРИЙНОГО ВОЗВРАТА ОШИБКИ
; =============================================================================
_k_lp_reg_vfs_fault:
    ; ИСПРАВЛЕНО: Удалена дублирующая метка _k_lp_register_vfs_fault
    POP  H
    POP  D
    POP  B                          ; Зеркально разгрузили оригинальный РОН-кадр со стека
    STC                             ; ВЗВОД ФЛАГА ОШИБКИ: CY = 1 (Провал коммутации)
    RET

; =============================================================================
; ПРОЦЕДУРА: vfs_mount_lp_nodes
; Вход:  DE = Физический адрес первого СВОБОДНОГО слота в LP_TABLE после карусели.
; Задача: По разности адресов (16-битная DAD-арифметика с дополнительным кодом)
;         вычислить число реально найденных LPT-контроллеров, последовательно
;         зарегистрировать для них файлы /dev/lp0 и /dev/lp1 в VFS.
; =============================================================================
vfs_mount_lp_nodes:
    PUSH B
    PUSH D
    PUSH H                          ; Спасли контекст супервизора Ring 0

    ; --- ВЫЧИСЛЕНИЕ СУММАРНОЙ ДЕЛЬТЫ АДРЕСOВ ОЗУ ТАБЛИЦЫ ---
    LXI  H, LP_TABLE
    MOV  A, E
    SUB  L
    MOV  E, A                       ; E = Младший байт 16-битной дельты
    MOV  A, D
    SBB  H
    MOV  D, A                       ; DE = Итоговая 16-битная дельта адресов (DE - LP_TABLE)

    ; --- ДЕЛЕНИЕ НА РАЗМЕР СТРУКТУРЫ (80 БАЙТ) ЧЕРЕЗ ДОПОЛНИТЕЛЬНЫЙ КОД ---
    MVI  B, 0                       ; B = Накопительный счетчик найденных LPT (0..2)

_vfs_lp_calc_active:
    MOV  A, E
    ORA  D
    JZ   _vfs_lp_mount_start        ; Если дельта сошлась в ноль — расчет окончен, на монтаж
    
    LXI  H, -80                     ; HL = 0xFFB0 (Дополнительный код числа 80)
    DAD  D                          ; HL = DE - 80
    JNC  _vfs_lp_mount_start        ; CY=0 -> Остаток дельты меньше 80 (Завершение расчета)
    
    MOV  D, H
    MOV  E, L                       ; DE = Декрементированная дельта (DE - 80)
    INR  B                          ; Счетчик активных Minor-номеров принтеров++
    JMP  _vfs_lp_calc_active

_vfs_lp_mount_start:
    MOV  A, B                       ; А = Общее число успешно оживленных LPT-портов
    ORA  A
    JZ   _vfs_lp_no_hardware        ; Если 0 — принтеров на шине нет, тихо выходим без создания файлов

    MOV  C, A                       ; C = Счетчик циклов монтирования файлов в директорию /dev
    MVI  B, 0                       ; B = Начальный Minor-номер устройства (0..1)

_vfs_lp_mount_loop:
    PUSH B                          ; Спасли пару BC (B = Minor, C = Ограничитель итераций)

    MOV  A, B                       ; A = Текущий Minor (0..1)
    CALL _vfs_lp_get_string_ptr     ; Выход: DE = Адрес изолированной ASCIIZ-строки пути
    
    MVI  A, I_TYPE_CHRDEV           ; A = 02H (Тип файла: Специальное символьное устройство) [1.3]
    MVI  C, MAJOR_LP                ; C = MAJOR_LP (7) из за заголовочного lp.inc
    ; Регистр B из сохраненной пары по-прежнему содержит текущий Minor
    
    CALL vfs_mknod                  ; Физическая интеграция Inode в RAM-диск VFS! [1.3]

    POP  B                          ; Восстановили пару BC со стека ядра
    INR  B                          ; Переходим к следующему Minor-номеру (Minor++)
    DCR  C                          ; Уменьшаем счетчик оставшихся проходов
    JNZ  _vfs_lp_mount_loop

_vfs_lp_no_hardware:
    POP  H
    POP  D
    POP  B                          ; Полное каноническое восстановление РОН по LIFO
    RET

; =============================================================================
; ВНУТРЕННЯЯ ПОДПРОГРАММА: _vfs_lp_get_string_ptr (С ВАЛИДАЦИЕЙ ВЕРХНЕЙ ГРАНИЦЫ)
; =============================================================================
_vfs_lp_get_string_ptr:
    CPI  0
	JZ  _vfs_lp_ret_dev0
    CPI  1
	JZ  _vfs_lp_ret_dev1
    
    ; Индексный оверран: Minor > 1 — повреждение памяти Ring 0, вешаем систему
    LXI  H, _vfs_lp_panic_msg
    JMP  kernel_panic_lp_indx_err

_vfs_lp_ret_dev0:
    LXI  D, _vfs_lp_str_dev0
	RET
_vfs_lp_ret_dev1:
    LXI  D, _vfs_lp_str_dev1
	RET

kernel_panic_lp_indx_err:
	; Фатальная ошибка старта: Нарушение структуры LP.
    ; Выключаем прерывания и намертво вешаем процессор.
    DI
    HLT
	JMP  kernel_panic_lp_indx_err

; --- Изолированные системные ASCIIZ-строки и ловушки ошибок (Секция ROM) ---
_vfs_lp_str_dev0:    DB  "/dev/lp0", 0
_vfs_lp_str_dev1:    DB  "/dev/lp1", 0
_vfs_lp_panic_msg:   DB  "VFS FATAL: INVALID LPT MINOR NUMBER EXTRACTED DURING COLD BOOT!", 0
