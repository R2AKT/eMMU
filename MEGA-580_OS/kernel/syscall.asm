; =============================================================================
; ТОЧКА ВХОДА SYSCALL ENTRY
; =============================================================================
sys_call_entry:
	DI							; Отключаем прерывания

    ; === ФАЗА 1: ТЕНЕВОЕ СОХРАНЕНИЕ ПОЛЬЗОВАТЕЛЬСКОГО КОНТЕКСТА ===
    ; В SP сейчас стек Ring 3 вызывающей задачи. eMMU уже забросил туда PC.
    PUSH PSW                    ; Сохраняем Аккумулятор и Флаги (A, F) в стек Ring 0
    PUSH B                      ; Сохраняем регистры B, C задачи в стек Ring 0
    PUSH D                      ; Сохраняем регистры D, E задачи в стек Ring 0
    PUSH H                      ; Сохраняем регистры H, L задачи в стек Ring 0

    ; Переключаемся на изолированный системный стек Ядра ОС
    LXI  H, 0
    DAD  SP                     ; HL = текущий упакованный SP процесса
    LXI  SP, KERNEL_STACK_TOP   ; Загружаем чистый изолированный стек ядра ОС
    PUSH H	                    ; Временно прячем пользовательский SP в стек ядра

    LHLD CURRENT_PCB_PTR        ; HL = Адрес активного PCB из кэша ядра

    ; Прибавляем смещение O_SAVED_SP (+3) строго последовательно
    LXI  D, O_SAVED_SP          ; DE = смещение до поля (0003H)
    DAD  D                      ; HL = База_PCB + O_SAVED_SP
    
    POP  D                      ; DE = извлеченный из стека ядра пользовательский SP
    MOV  M, E                   ; Запись младшего байта SP
    INX  H
    MOV  M, D                   ; Запись старшего байта SP

    ; === ФАЗА 2: ДЕШИФРАЦИЯ И АВТОРИЗАЦИЯ СИС-ВЫЗОВА ===
    ; Номер системного вызова передан в регистре C
    MOV  A, C
    CPI  DEF_SYS_MAX_SYSCALLS + 1    ; Проверяем границы таблицы системных вызовов
    JNC  syscall_bad_number     ; Если номер больше лимита — возвращаем ENOSYS

    ; Вычисляем адрес перехода по таблице указателей: Смещение = Номер * 2
    MOV  L, C
    MVI  H, 0
    DAD  H                      ; HL = Номер Syscall * 2
    LXI  D, SYSCALL_TABLE
    DAD  D                      ; HL указывает точно на ячейку DW в таблице
    MOV  E, M
    INX  H
    MOV  D, M                   ; DE = физический адрес функции syscall в ядре

    ; Передаем управление на функцию ядра. 
    ; Поскольку параметры от пользователя пришли в DE и HL задачи, а мы их 
    ; теневым образом сохранили в стек, при необходимости функция ядра может 
    ; извлечь их оттуда или прочитать напрямую, если они не затерлись при расчете.
    ; Имитируем вызов CALL через стек:
    PUSH D	                    ; Забрасываем адрес функции ядра в стек
    LXI  H, syscall_return      ; Регистрируем точку возврата после исполнения
    PUSH H
    PCHL                        ; Программный прыжок на исполнение системной функции

syscall_return:
    ; Системная функция выполнилась и вернула код результата в Аккумуляторе (A)
    MOV  C, A                   ; Временно спасем код результата в C
    
    ; Записываем результат выполнения в поле O_SYSCALL_RES (+5) текущего PCB
    LHLD CURRENT_PCB_PTR        ; HL = базовый адрес PCB текущей задачи
    LXI  D, O_SYSCALL_RES
    DAD  D                      ; HL указывает на O_SYSCALL_RES
    MOV  M, C                   ; Результат системного вызова жестко зафиксирован

syscall_exit:
    ; === ФАЗА 3: ВОССТАНОВЛЕНИЕ КОНТЕКСТА И ВЫХОД В USER SPACE ===
	LHLD CURRENT_PCB_PTR        ; HL = базовый адрес PCB текущей задачи
    LXI  D, O_SAVED_SP
    DAD  D                      ; HL указывает на сохраненный SP
    MOV  E, M
    INX  H
    MOV  D, M                   ; DE = восстановленный SP задачи
    XCHG                        ; HL = новый SP
    SPHL                        ; Процессор переключился обратно на стек Ring 3

	; Запускаем проверку дефолтных политик сигналов (включая SIGBUS)
    ;CALL k_signal_default_policy_check
	
    ; Извлекаем теневые регистры из стека Ring 3 пользовательского пространства
    POP  H                      ; Восстановили H, L задачи
    POP  D                      ; Восстановили D, E задачи
    POP  B                      ; Восстановили B, C задачи
    POP  PSW                    ; Восстановили Аккумулятор и Флаги (A, F) задачи

	EI                          ; Снова разрешаем аппаратную карусель прерываний в Ring 3

    ; На вершине стека Ring 3 остался лежать только PC
    RET                         ; Переход обратно в задачу. Аппаратный сигнал ~{RET_STROBE} 
                                ; на плате сбросит KERNEL_MODE обратно в Ring 3!

syscall_bad_number:
    MVI  A, ENOSYS              ; Код ошибки: ENOSYS (Функция не реализована)
    JMP  syscall_return

; =============================================================================
; ЦЕНТРАЛЬНЫЙ СИНХРОННЫЙ ШЛЮЗ ВЫХОДА СИСКОЛЛОВ
; =============================================================================
syscall_exit_route:
    DI                              ; Критическая секция ядра

    ; СТРАХОВОЧНЫЙ ИНВАРИАНТ: Принудительно взводим Firewall через константу
    MVI  A, KERNEL_OVERRIDE_OFF
    OUT  EMMU_OVERRIDE             ; Аппаратно закрываем оверран памяти [1.3]

    ; Восстанавливаем регистровый контекст Ring 3 задачи из KERNEL_STACK
    POP  H
    POP  D
    POP  B
    POP  PSW                        
    
    EI                              ; Разрешили аппаратные прерывания ВН59
    RET                             ; Аппаратный прыжок обратно в Ring 3

; =============================================================================
; ВОЗВРАТ С ОШИБКОЙ ПРИ ОТСУТСТВИИ СИСТЕМНОГО ВЫЗОВА
; =============================================================================
syscall_nosys:
    MVI  A, ENOSYS               ; Возвращает ошибку 0xE5 (Функция не реализована)
    RET

; =============================================================================
; НАЗВАНИЕ: pcb.asm (Аварийное завершение и уничтожение процесса)
; ТОКЕН:    TOKEN::MEGA-580_OS_v1.5.9_SYS_EXIT_FATAL_STABLE_BUILD_20260705
; =============================================================================
sys_exit_fatal:
    MOV  C, A

    ; --- ШАГ 1: ФИКСАЦИЯ СТАТУСА ST_ZOMBIE И КОДА ВОЗВРАТА ---
    LHLD CURRENT_PCB_PTR
    PUSH H
    
    LXI  D, O_STATUS
    DAD  D
    MVI  M, ST_ZOMBIE
    
    POP  H
    PUSH H
    LXI  D, O_SYSCALL_RES
    DAD  D
    MOV  M, C
    POP  H

    ; --- ШАГ 2: ОСВОБОЖДЕНИЕ I/O ПОРТОВ ---
    LXI  H, GLOBAL_RESOURCE_TABLE
    MVI  B, 0
    LDA  CURRENT_PID
    MOV  C, A

_exit_fatal_ports_loop:
    MOV  A, M
    CMP  C
    JNZ  _exit_fatal_skip_port
    MVI  M, RES_FREE_MARKER

_exit_fatal_skip_port:
    INX  H
    INR  B
    MOV  A, B
    CPI  0
    JNZ  _exit_fatal_ports_loop

    ; --- ШАГ 3: ОСВОБОЖДЕНИЕ ФИЗИЧЕСКИХ СТРАНИЦ ---
    LHLD CURRENT_PCB_PTR
    LXI  D, O_PAGES_MAP
    DAD  D
    MVI  B, 4

_exit_fatal_ram_loop:
    MOV  A, M
    CPI  RES_FREE_MARKER
    JZ   _exit_fatal_skip_ram
    
    PUSH B
    PUSH H
    CALL k_free_physical_page       ; ← ИСПРАВЛЕНО: правильное имя функции
    
    POP  H
    POP  B
    MVI  M, RES_FREE_MARKER

_exit_fatal_skip_ram:
    INX  H
    DCR  B
    JNZ  _exit_fatal_ram_loop

    ; --- ШАГ 4: СБРОС СТЭКА И ВЫХОД ---
    LXI  SP, KERNEL_STACK_TOP
    
    CALL scheduler
    STA  NEXT_PID
    
    MVI  A, 020H
    OUT  MASTER_8259_CMD            
    
    EI                              ; ← КРИТИЧЕСКИ ВАЖНО: Разрешаем прерывания!
    
    JMP  switch_mmu_context

