; =============================================================================
; ТОЧКА ВХОДА SYSCALL ENTRY
; =============================================================================
; =============================================================================
; MEGA-580 OS v1.5.177 — ЕДИНАЯ ТОЧКА ВХОДА В СИСТЕМНЫЕ ВЫЗОВЫ (ИТОГОВЫЙ МОНОЛИТ)
; =============================================================================
; Вход:  C  = Номер системного вызова (0 .. DEF_SYS_MAX_SYSCALLS-1)
;        DE, HL = Пользовательские аргументы функции из Ring 3
; Выход: А  = Код результата выполнения сисколла (дублируется в O_SYSCALL_RES)
; =============================================================================
sys_call_entry:
    DI                          ; Блокируем аппаратную карусель прерываний (ВИ53/RTC)

    ; =============================================================================
    ; === ФАЗА 1: ПЕРЕКЛЮЧЕНИЕ СТЕКОВ И ЭВАКУАЦИЯ ПОЛЬЗОВАТЕЛЬСКОГО КОНТЕКСТА   ===
    ; =============================================================================
    ; eMMU РЕВИЗИИ 2.0: Оверрайд НЕ включается. Ядро находится в инвариантном 
    ; Сегменте 0 (0000H-3FFFH) и имеет прямой доступ к структурам и TPA.

    ; --- Шаг 1.1: Страховочная эвакуация HL задачи на ее родной стек ---
    PUSH H                      ; [Стек пользователя Ring 3]: HL_user изолирован

    ; --- Шаг 1.2: Захват текущего указателя стека задачи ---
    LXI  H, 0
    DAD  SP                     ; HL = Чистый SP_user (указывает на HL_user)
    
    ; Физически переводим процессор на изолированный стек Супервизора
    LXI  SP, KERNEL_STACK_TOP   ; Теперь все PUSH/POP изолированы внутри Ring 0

    ; --- Шаг 1.3: Формирование кадра контекста на стеке ядра ---
    PUSH H                      ; [Ядро+6]: SP_user
    PUSH D                      ; [Ядро+4]: DE_user
    PUSH B                      ; [Ядро+2]: BC_user (C = Номер syscall)
    PUSH PSW                    ; [Ядро+0]: PSW_user

    ; === Топология стека ядра на данный момент (от вершины SP): ===
    ; SP+0: PSW_user
    ; SP+2: BC_user  (C = номер syscall)
    ; SP+4: DE_user
    ; SP+6: SP_user  (Указывает на HL_user на стеке пользователя)

    ; --- Шаг 1.4: Запись SP_user в O_USER_SP (+000AH) структуры PCB ---
    LHLD CURRENT_PCB_PTR        ; HL = Абсолютный базовый адрес PCB текущей задачи
    PUSH H                      ; [Ядро-2]: Временно сохранили базу PCB на стеке ядра
    
    LXI  D, 0
    DAD  SP                     ; HL = Текущая вершина SP ядра (+0, указывает на базу PCB)
    LXI  D, 8                   ; Шаг до кадра SP_user (+2 байта PUSH H, +6 байт кадра)
    DAD  D                      ; HL -> Точка хранения SP_user на стеке ядра
    
    MOV  E, M
    INX  H
    MOV  D, M                   ; DE = Восстановленный SP_user задач Ring 3
    
    POP  H                      ; HL = Извлекли обратно чистую базу PCB со стека ядра
    PUSH H                      ; Вернули базу на стек для следующего шага
    
    LXI  B, O_USER_SP
    DAD  B                      ; HL -> O_USER_SP (+000AH)
    MOV  M, E                   ; Запись младшего байта SP_user
    INX  H
    MOV  M, D                   ; Запись старшего байта SP_user

    ; --- Шаг 1.5: Запись текущего SP ядра в O_SAVED_SP (+0008H) ---
    ; Фиксируем SP ядра, который указывает на PSW_user (это SP+2 от текущей вершины)
    LXI  H, 2
    DAD  SP                     ; HL = Физический адрес кадра PSW_user на стеке ядра
    MOV  D, H
    MOV  E, L                   ; DE = SP ядра (Кадр каноничного контекста супервизора)
    
    POP  H                      ; HL = Извлекли финальную чистую базу PCB. Стек ядра чист!
    
    LXI  B, O_SAVED_SP
    DAD  B                      ; HL -> O_SAVED_SP (+0008H)
    MOV  M, E                   ; Запись младшего байта SP ядра
    INX  H
    MOV  M, D                   ; Запись старшего байта SP ядра


    ; =============================================================================
    ; === ФАЗА 2: ДЕШИФРАЦИЯ, ТРАНСПОРТ АРГУМЕНТОВ И ИСПОЛНЕНИЕ СИСКОЛЛА        ===
    ; =============================================================================

_sys_call_authorization:
    ; --- Шаг 2.1: Безопасное извлечение номера сисколла из кадра ---
    LXI  H, 2
    DAD  SP                     ; HL -> BC_user (+2 от текущей вершины)
    MOV  A, M                   ; А = C_user (младший байт пары BC)
    MOV  C, A                   ; C = Номер syscall для валидации

    ; --- Шаг 2.2: Валидация строгих границ таблицы POSIX ABI ---
    CPI  DEF_SYS_MAX_SYSCALLS
    JNC  syscall_bad_number     ; Если номер не существует (A >= лимита) — на ошибку ENOSYS

    ; --- Шаг 2.3: Вычисление абсолютного адреса обработчика ---
    MOV  L, C
    MVI  H, 0
    DAD  H                      ; HL = Номер Syscall * 2 (Шаг адреса таблицы DW)
    LXI  D, SYSCALL_TABLE
    DAD  D                      ; HL -> Слот в таблице переходов системных вызовов
    MOV  E, M
    INX  H
    MOV  D, M                   ; DE = Физический адрес целевой функции ядра

    ; --- Шаг 2.4: Восстановление аргументов пользователя (Транспорт параметров) ---
    PUSH D                      ; [Стек ядра]: Адрес функции супервизора (-2 байта от SP)
    
    ; Топология относительно нового SP:
    ; SP+0: Адрес функции ядра, SP+2: PSW_user, SP+4: BC_user, SP+6: DE_user, SP+8: SP_user

    ; Восстанавливаем DE = DE_user
    LXI  H, 6
    DAD  SP                     ; HL -> DE_user на стеке ядра (+6)
    MOV  E, M
    INX  H
    MOV  D, M                   ; DE = Истинный оригинальный DE_user задачи

    ; Извлекаем SP_user, чтобы добраться до HL_user
    PUSH D                      ; Временно спрятали восстановленный DE_user
    LXI  H, 10                  ; Сдвиг увеличился: +8 исходный + 2 байта PUSH D
    DAD  SP                     ; HL -> SP_user на стеке ядра
    MOV  E, M
    INX  H
    MOV  D, M                   ; DE = SP_user (Прямой указатель на стек Ring 3)
    
    ; Без оверрайда eMMU читаем HL_user с реального стека пользовательской задачи
    XCHG                        ; HL = SP_user, DE = сохраненный DE_user
    MOV  A, M                   ; А = Младший байт HL_user (L)
    INX  H                      ; Продвигаем SP_user на старший байт
    MOV  H, M                   ; H = Старший байт HL_user (H)
    MOV  L, A                   ; HL = Полностью восстановленный HL_user задачи!
    
    POP  D                      ; Восстановили пару DE = DE_user обратно

    ; --- Шаг 2.5: Шлюзование и прыжок на функцию ядра ---
    XCHG                        ; HL = Адрес функции, DE = HL_user
    POP  B                      ; Извлекли адрес функции ядра в BC, стек поднялся
    
    PUSH D                      ; [Стек ядра]: Сохранили HL_user (Для функции ядра)
    LXI  D, syscall_return
    PUSH D                      ; [Стек ядра]: Зарегистрировали адрес возврата
    
    PUSH B                      ; [Стек ядра]: Положили адрес функции на вершину
    XCHG                        ; HL = Возвращенный HL_user, DE = Мусор
    RET                         ; Физический вызов функции Ring 0 по адресу из стека

syscall_return:
    ; Системная функция выполнилась и вернула код результата в Аккумуляторе A
    MOV  B, A                   ; Спасаем результат в регистр B

    ; --- Шаг 2.6: Фиксация кода возврата в Секции А структуры PCB ---
    LHLD CURRENT_PCB_PTR        ; HL = Абсолютный базовый адрес PCB
    LXI  D, O_SYSCALL_RES       ; DE = +0003H (Строгий POSIX-канон)
    DAD  D                      ; HL -> O_SYSCALL_RES
    MOV  M, B                   ; Зафиксировали результат сисколла в памяти ядра

    JMP  syscall_exit_route     ; Уходим на центральный шлюз выхода


    ; =============================================================================
    ; === ФАЗА 3: ВОССТАНОВЛЕНИЕ КОНТЕКСТА И САНКЦИОНИРОВАННЫЙ ВЫХОД В RING 3   ===
    ; =============================================================================

syscall_bad_number:
    MVI  A, ENOSYS              ; Ошибка POSIX: Функция не реализована
    MOV  B, A
    LHLD CURRENT_PCB_PTR        ; Абсолютный базовый адрес PCB
    LXI  D, O_SYSCALL_RES       ; DE = +0003H
    DAD  D                      ; HL -> O_SYSCALL_RES
    MOV  M, B                   ; Зафиксировали ошибку в структуре процесса

syscall_exit:
syscall_exit_route:
    DI                          ; КРИТИЧЕСКАЯ СЕКЦИЯ: Изоляция восстановления стеков

    ; --- ШАГ 3.1: ЗАГРУЗКА СОХРАНЕННОГО SP ЯДРА ---
    LHLD CURRENT_PCB_PTR        ; HL = База текущей PCB
    LXI  D, O_SAVED_SP          ; DE = +0008H
    DAD  D                      ; HL -> O_SAVED_SP
    MOV  E, M
    INX  H
    MOV  D, M                   ; DE = Адрес вершины кадра регистров ядра
    XCHG                        
    SPHL                        ; SP процессора = Восстановленный системный стек ядра

    ; --- ШАГ 3.2: ИЗВЛЕЧЕНИЕ КОНТЕКСТА РЕГИСТРОВ ЗАДАЧИ ---
    POP  PSW                    ; Восстановили оригинальный PSW_user (A, F)
    POP  B                      ; Восстановили оригинальный BC_user
    POP  D                      ; Восстановили оригинальный DE_user
    POP  H                      ; HL = Извлеченный SP_user (Указывает на HL_user)

    ; --- ШАГ 3.3: СТРАХОВОЧНОЕ ОТКЛЮЧЕНИЕ OVERRIDE ЯДРА ---
    MVI  A, KERNEL_OVERRIDE_OFF
    OUT  EMMU_OVERRIDE          ; Страховочный инвариант файрвола перед вылетом

    ; --- ШАГ 3.4: ПЕРЕКЛЮЧЕНИЕ НА СТЭК ПОЛЬЗОВАТЕЛЯ И СЪЕМ HL ---
    SPHL                        ; SP процессора = Чистый SP_user в пространстве Ring 3
    POP  H                      ; Извлекли HL_user с вершины пользовательского стека!
                                ; Теперь ВСЕ РОН (A, B, C, D, E, H, L) полностью восстановлены.

    ; --- ШАГ 3.5: САНКЦИОНИРОВАННЫЙ ВЫХОД В Ring 3 С РАЗРЕШЕНИЕМ ПРЕРЫВАНИЙ ---
    EI                          ; Программное разрешение прерываний (срабатывает через 1 такт)
    RET                         ; Аппаратный вылет в Ring 3! Снимает PC со стека задачи,
                                ; возвращая управление приложению с открытыми прерываниями.

; =============================================================================
; ВОЗВРАТ С ОШИБКОЙ ПРИ ОТСУТСТВИИ СИСТЕМНОГО ВЫЗОВА / ПУСТОМ ВЫЗОВЕ
; =============================================================================
syscall_nosys:
    MVI  A, ENOSYS              ; Возвращаем пользователю ошибку POSIX ENOSYS
    JMP  syscall_return         ; Уходим в Фазу 2/3: прописка ошибки в PCB

; =============================================================================
; MEGA-580 OS v1.5.177 — СИСКОЛЛ ПРИНУДИТЕЛЬНОГО ЗАВЕРШЕНИЯ ПРОЦЕССА (МОНОЛИТ)
; =============================================================================
; Вход:  А = Выходной код завершения процесса (Exit Code)
; Выход: В текущую задачу возврата нет. Происходит вытеснение и запуск READY.
; -----------------------------------------------------------------------------
; КРИТИЧЕСКИЙ КОНТЕКСТ ВЫЗОВА:
; Может вызываться как штатно (sys_exit), так и асинхронно из обработчика IRQ7 
; (Вектор 0x0038), отвечающего за аппаратный TRAP процессора при нарушении 
; процессом Ring 3 границ eMMU, изоляции памяти или лимитов супервизора.
; =============================================================================
sys_exit_fatal:
    DI                              ; Монолитный запрет прерываний на время уничтожения

    MOV  C, A                       ; С = Спасаем Exit Code

    ; =============================================================================
    ; --- ШАГ 1: ФИКСАЦИЯ СТАТУСА ST_ZOMBIE И КОДА ВОЗВРАТА (128 БАЙТ)          ---
    ; =============================================================================
    LHLD CURRENT_PCB_PTR            ; HL = Базовый адрес PCB текущей задачи
    
    ; Переводим процесс в состояние ST_ZOMBIE для последующей уборки через waitpid()
    INX  H                          ; HL -> O_STATUS (+0001H)
    MVI  M, ST_ZOMBIE               ; O_STATUS = 05H
    DCX  H                          ; HL -> Чистая база PCB
    
    ; Записываем финальный результат сисколла (Exit Code) в O_SYSCALL_RES (+0003H)
    PUSH H                          ; Сохранили базу на стеке ядра
    LXI  D, O_SYSCALL_RES           ; DE = +0003H
    DAD  D                          ; HL -> O_SYSCALL_RES
    MOV  M, C                       ; Зафиксировали код смерти процесса
    POP  H                          ; HL = Восстановили чистую базу PCB

    ; =============================================================================
    ; --- ШАГ 2: БЕЗОПАСНОЕ ОСВОБОЖДЕНИЕ ВСЕХ 256 АППАРАТНЫХ I/O ПОРТОВ        ---
    ; =============================================================================
    LDA  CURRENT_PID                ; А = Идентификатор умирающего процесса
    MOV  C, A                       ; С = Искомый PID для очистки портов
    
    LXI  H, GLOBAL_RESOURCE_TABLE   ; HL -> Системная таблица владения портами (256 байт)
    MVI  B, 0                       ; Инвариантный счетчик на 256 итераций

_exit_fatal_ports_loop:
    MOV  A, M
    CMP  C                          ; Данный порт принадлежит нашему процессу?
    JNZ  _exit_fatal_skip_port      ; Если нет — игнорируем
    MVI  M, RES_FREE_MARKER         ; Свободно! Сброс в 0xFF

_exit_fatal_skip_port:
    INX  H                          ; Переходим к следующему порту в таблице
    DCR  B                          ; Декремент (0 -> 255 -> ... -> 0)
    MOV  A, B
    ORA  A                          ; Проверяем, совершил ли счетчик полный круг до 0
    JNZ  _exit_fatal_ports_loop     ; Ровно 256 проходов без риска зацикливания

    ; =============================================================================
    ; --- ШАГ 3: ОСВОБОЖДЕНИЕ ФИЗИЧЕСКИХ СТРАНИЦ ПАМЯТИ (СЕКЦИЯ Г)               ---
    ; =============================================================================
    LHLD CURRENT_PCB_PTR            ; HL = Снова база PCB
    LXI  D, O_PAGES_MAP             ; DE = +001CH (Flat-карта К555ИР26 окон 0..3)
    DAD  D                          ; HL -> O_PAGES_MAP
    MVI  B, 4                       ; Очищаем 4 системных окна Ring 3

_exit_fatal_ram_loop:
    MVI  A, RES_FREE_MARKER         ; А = Инициализируем маркер свободы (0xFF)
    CMP  M                          ; Прямое сравнение 0xFF с физической картой в M (Оптимизировано)
    JZ   _exit_fatal_skip_ram       ; Если уже свободна — пропускаем
    
    MOV  A, M                       ; Читаем реальный номер физической страницы для k_free_physical_page
    PUSH B
    PUSH H                          ; Эвакуируем указатели flat-карты на стек ядра
    
    CALL k_free_physical_page       ; Возврат страницы в глобальный пул RAM-модели
    
    POP  H
    POP  B                          ; Восстановили контекст итерации
    MVI  M, RES_FREE_MARKER         ; Маркируем окно в PCB как свободное (0xFF)

_exit_fatal_skip_ram:
    INX  H                          ; Шагаем по flat-карте к следующему окну
    DCR  B
    JNZ  _exit_fatal_ram_loop       ; Повторяем для всех 4 окон eMMU

    ; =============================================================================
    ; --- ШАГ 4: СБРОС ТЕКУЩЕГО СТЭКА, ВЫЗОВ ПЛАНИРОВЩИКА И ДИНАМИЧЕСКИЙ EOI   ---
    ; =============================================================================
    LXI  SP, KERNEL_STACK_TOP       ; Принудительно очищаем и сбрасываем стек ядра Ring 0

    ; Вызываем планировщик для поиска READY-задачи взамен уничтоженного процесса
    CALL scheduler                  ; Функция отработает и сама вернет NEXT_PID в А
    STA  NEXT_PID

    ; --- ЗАЩИТНЫЙ ДИНАМИЧЕСКИЙ EOI (КОРРЕКТНЫЙ КАНОН ПО ПОРТАМ A0=0) ---
    MVI  A, 00BH                    ; Команда OCW3: Чтение регистра ISR (Требует A0=0) [INDEX]
    OUT  MASTER_8259_CMD            ; Отправляем команду строго в порт 0x20 [INDEX]
    
    IN   MASTER_8259_CMD            ; Читаем значение ISR из порта 0x20 (A0=0) [INDEX]
    ANI  080H                       ; Проверяем старший бит 7 (Линия IRQ7 / Аппаратный Trap)
    JZ   _skip_eoi                  ; Если бит равен 0 (вызов из сисколла) -> пропускаем EOI

    MVI  A, 020H                    ; Non-specific EOI (Команда OCW2, требует A0=0) [INDEX]
    OUT  MASTER_8259_CMD            ; Отправляем строго в порт 0x20 для сброса бита IRQ7 [INDEX]

_skip_eoi:
    ; Мы уходим в диспетчер контекстов строго при DI, гарантируя атомарность eMMU.
    JMP  switch_mmu_context         ; Безопасный прыжок. Переключаем ИР26 и вылетаем в Ring 3!


#include "sys_write.asm"
