;        │     │     └── hw_ns16550_get_char (hw_ns16550.asm) ──────── [SMC-ввод из FIFO RBR 16550]
;        │     └── [Абстрактная ОЗУ-логика: запись в O_TTY_RX_BUFFER (+3), инкремент RX_COUNT]
;        │
;        ├── [Ветка Б: Если взведен TTY_HAL_TXRDY (Передатчик свободен)]
;        │     ├── [Проверка O_TTY_TX_COUNT (+69) в ОЗУ Ring 0]
;        │     ├── НЕ ПУСТ ──► [Извлечение символа из O_TTY_TX_BUFFER (+70)]
;        │     │                 └── O_TTY_HW_PUT_CHAR (+149) ───────── [Косвенный вызов через PCHL]
;        │     │                       ├── hw_i8251_put_char ───────── [SMC-вывод в DATA порт ВВ51]
;        │     │                       └── hw_ns16550_put_char ─────── [SMC-вывод в FIFO THR 16550]
;        │     │
;        │     └── ПУСТ  ──► [Очередь ОЗУ исчерпана: глушение линии прерываний]
;        │                       ├── hw_i8251_tx_ctrl (hw_i8251.asm) ── [Для ВВ51: сброс бита TxEN (0x26)]
;        │                       └── hw_ns16550_tx_ctrl ─────────────── [Для 16550: сброс IER_ENABLE_TX]
;        │
;        └── [Выдача EOI в ВН59 (0x20) ──► POP РОН ──► EI ──► RET] ──── [Выход из прерывания]
;*
;[User Space (Ring 3): write(fd, buf, len)]
;  └── RST 6 (Вектор 0x0030)
;        └── sys_call_entry (pcb.asm) ──────────────────────────────── [DI, перенос SP, эвакуация USER_PC через Сегмент 2]
;              └── SYSCALL_TABLE [Номер sys_write] ─────────────────── [Косвенный переход через PCHL на стек ядра]
;                    └── sys_write (vfs_core.asm / tty_core.asm)
;                          ├── K_VFS_DISPATCH_REG_SAFE (порт 0x76) ─── [Взвод триггера DD19: открыли оверран Сегмента 2]
;                          ├── K_VFS_RESOLVE_DEV_MONOLITH ──────────── [Определили по fd, что таргет = TTY-слот]
;                          │     └── [Цикл побайтового вычитывания буфера Ring 3 через Сегмент 2]
;                          │           └── k_tty_tx_char (tty_core.asm)
;                          │                 ├── [Если O_TTY_TX_COUNT == 0 (Кольцо ОЗУ пусто)]
;                          │                 │     ├── O_TTY_HW_POLL_STAT (+153) ──► hw_*_poll_status
;                          │                 │     └── СВОБОДЕН ──► O_TTY_HW_PUT_CHAR (+149) ──► hw_*_put_char [Прямой выстрел]
;                          │                 │
;                          │                 └── [Если Занято или O_TTY_TX_COUNT > 0]
;                          │                       ├── [Укладка символа в кольцо O_TTY_TX_BUFFER (+70)]
;                          │                       └── [Включение прерываний для разгрузки кольца]
;                          │                             ├── hw_i8251_tx_ctrl (A=01H) ──► [Взвод бита TxEN (0x27)]
;                          │                             └── hw_ns16550_tx_ctrl (A=01H) ──► [Взвод IER_ENABLE_TX]
;                          │
;                          ├── K_VFS_DISPATCH_REG_SAFE (KERNEL_OVERRIDE_OFF) ── [Закрыли оверран Сегмента 2, триггер DD19 опущен]
;                          └── syscall_return (pcb.asm) ─────────────── [Фиксация результата в TCB, SPHL, POP Ring 3, EI, RET]
;*
;[User Space (Ring 3): read(fd, buf, len)]
;  └── RST 6 (Вектор 0x0030)
;        └── sys_call_entry (pcb.asm)
;              └── SYSCALL_TABLE [Номер sys_read]
;                    └── sys_read (vfs_core.asm / tty_core.asm)
;                          ├── K_VFS_DISPATCH_REG_SAFE (порт 0x76) ─── [Открыли оверран Сегмента 2 для записи в юзер-буфер]
;                          ├── K_VFS_RESOLVE_DEV_MONOLITH
;                          │     └── [Цикл чтения байт до достижения len]
;                          │           └── k_tty_rx_char (tty_core.asm) ── [АППАРАТНО-НЕЗАВИСИМАЯ ФУНКЦИЯ]
;                          │                 ├── [Проверка O_TTY_RX_COUNT (+2) в ОЗУ Ring 0]
;                          │                 ├── ПУСТ  ──► [Выход с CY = 1 (Возврат ошибки EAGAIN / Сон задачи)]
;                          │                 └── ЕСТЬ  ──► [Извлечение символа из O_TTY_RX_BUFFER (+3), декремент COUNT]
;                          │
;                          │     └── [Запись извлеченного символа в буфер Ring 3 через Сегмент 2]
;                          ├── K_VFS_DISPATCH_REG_SAFE (KERNEL_OVERRIDE_OFF) ── [Закрыли оверран Сегмента 2]
;                          └── syscall_return (pcb.asm) ─────────────── [Возврат len или ошибки в А, RET в Ring 3]
;
