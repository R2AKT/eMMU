; =============================================================================
; НАЗВАНИЕ: tty_core.asm (Интеграция TTY драйвера с коммутатором VFS)
; ТОКЕН:    TOKEN::MEGA-580_OS_v1.5.44_TTY_VFS_STUBS_VALIDATED_BUILD_20260705
; =============================================================================
k_tty_register_vfs:
    ; Вход:  Вызывается из основного стаба kernel.asm после k_tty_subsys_init.
    ; Задача: Зарегистрировать структуру 5 векторов функций TTY в глобальном коммутаторе VFS
    LXI  H, tty_vfs_methods     ; HL = Физический 16-битный адрес структуры векторов
    MVI  A, VFS_TYPE_TTY        ; A  = Канонический Major-номер TTY драйвера (3) из vfs.inc
    JMP  k_vfs_register_driver  ; Безусловный переход (Хвостовая оптимизация вместо CALL + RET)

; --- СТРУКТУРА 5 МЕТОДОВ ОПЕРАЦИЙ ДРАЙВЕРА TTY (10 БАЙТ) ---
tty_vfs_methods:
    DW   k_tty_open
    DW   k_tty_close
    DW   k_tty_read
    DW   k_tty_write
    DW   k_tty_ioctl

; --- СИСТЕМНЫЕ ЗАГЛУШКИ ФУНКЦИЙ ДРАЙВЕРА (МЕТОДЫ VFS) ---
k_tty_open:
    XRA  A                      ; Возвращаем 00h (Успешное открытие канала, CY = 0)
    RET

k_tty_close:
    XRA  A                      ; Возвращаем 00h (Успешное закрытие канала, CY = 0)
    RET

k_tty_read:
    MVI  A, ENOSYS              ; A = Канонический код ошибки 38 из vfs.inc
    STC                         ; АППАРАТНЫЙ ФИКС: Взводим флаг ошибки CY = 1 для ABI ядра
    RET

k_tty_write:
    MVI  A, ENOSYS              ; A = Канонический код ошибки 38 из vfs.inc
    STC                         ; АППАРАТНЫЙ ФИКС: Взводим флаг ошибки CY = 1 для ABI ядра
    RET

k_tty_ioctl:
    MVI  A, ENOSYS              ; A = Канонический код ошибки 38 из vfs.inc
    STC                         ; АППАРАТНЫЙ ФИКС: Взводим флаг ошибки CY = 1 для ABI ядра
    RET

; =============================================================================
; НАЗВАНИЕ: tty_core.asm (Расчёт физического адреса слота терминала)
; =============================================================================
k_tty_get_struct_address:
    ; Вход:  A  = Номер запрашиваемой TTY-консоли (0..3)
    ;        Прерывания заперты (DI), стек в Ring 0.
    ; Задача: Проверить аппаратные границы номера (0..MAX_TTY_DEVS-1).
    ;         Вычислить физический адрес слота по формуле: TTY_TABLE + (A * 160).
    ; Выход: В случае успеха:
    ;           Флаг переноса CY = 0 (Успех)
    ;           Пара HL = Точный 16-битный физический адрес слота в TTY_TABLE.
    ;        В случае ошибки (Нелегальный минор / номер консоли):
    ;           Флаг переноса CY = 1 (Провал)
    ;           Пара HL = 0000H
    ; ВНИМАНИЕ: Все РОН супервизора (кроме HL) полностью сохраняются!
    PUSH B
    PUSH D                          ; Сохранили контекст планировщика

    ; === ШАГ 1: ВАЛИДАЦИЯ ГРАНИЦ НOМЕРА УСТРOЙСТВА ===
    CPI  MAX_TTY_DEVS               ; Сравниваем с лимитом (A - 4)
    JNC  _tty_get_addr_err          ; Если A >= 4 — нелегальный минор, уходим на ошибку

    ; === ШАГ 2: БЫСТРOЕ СДВИГOВOЕ УМНOЖЕНИЕ НА 160 (128 + 32) ===
    MOV  L, A
    MVI  H, 0                       ; HL = Номер консоли (0..3)

    ; Сдвигаем HL влево на 5 бит (получаем умножение на 32)
    DAD  H                          ; *2
    DAD  H                          ; *4
    DAD  H                          ; *8
    DAD  H                          ; *16
    DAD  H                          ; HL = Номер * 32
    
    PUSH H                          ; Спрятали промежуточное значение (A * 32) на стек ядра
    
    ; Продолжаем сдвиг ещё на 2 бита, чтобы дойти до умножения на 128 (5 + 2 = 7 сдвигов)
    DAD  H                          ; *64
    DAD  H                          ; HL = Номер * 128
    
    POP  D                          ; DE = Номер * 32
    DAD  D                          ; HL = (Номер * 128) + (Номер * 32) = Номер * 160!

    ; === ШАГ 3: ПРИВЯЗКА К БАЗЕ МАССИВА ТЕРМИНАЛOВ ===
    LXI  D, TTY_TABLE
    DAD  D                          ; HL = Физический адрес целевого слота терминала
    
    ANA  A                          ; АППАРАТНЫЙ СБРОС: Сбрасываем флаг ошибки CY = 0 (УСПЕХ)
    JMP  _tty_get_addr_exit         ; На выход

_tty_get_addr_err:
    LXI  H, 0000H                   ; HL = 0000H (Null pointer)
    STC                             ; АППАРАТНЫЙ ВЗВОД: Взводим флаг ошибки CY = 1 (POSIX ENODEV)

_tty_get_addr_exit:
    POP  D
    POP  B                          ; Восстановили РОН супервизора в исходное состояние
    RET
