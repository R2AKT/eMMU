; =============================================================================
; НАЗВАНИЕ: tty_core.asm (Интеграция TTY драйвера с коммутатором VFS)
; ТОКЕН:    TOKEN::MEGA-580_OS_v1.5.44_TTY_VFS_STUBS_VALIDATED_BUILD_20260705
; =============================================================================
k_tty_register_vfs:
    ; Вход:  Вызывается из основного стаба kernel.asm после k_tty_subsys_init.
    ; Задача: Зарегистрировать структуру 5 векторов функций TTY в глобальном коммутаторе VFS
    LXI  H, tty_vfs_methods     ; HL = Физический 16-битный адрес структуры векторов
    MVI  A, VFS_TYPE_TTY        ; A  = Канонический Major-номер TTY драйвера (3) из vfs.inc
    JMP  k_vfs_register_driver  ; Безусловный переход (Хвостовая оптимизация вместо CALL + RET)

; --- СТРУКТУРА 5 МЕТОДОВ ОПЕРАЦИЙ ДРАЙВЕРА TTY (10 БАЙТ) ---
tty_vfs_methods:
    DW   k_tty_open
    DW   k_tty_close
    DW   k_tty_read
    DW   k_tty_write
    DW   k_tty_ioctl

; --- СИСТЕМНЫЕ ЗАГЛУШКИ ФУНКЦИЙ ДРАЙВЕРА (МЕТОДЫ VFS) ---
k_tty_open:
    XRA  A                      ; Возвращаем 00h (Успешное открытие канала, CY = 0)
    RET

k_tty_close:
    XRA  A                      ; Возвращаем 00h (Успешное закрытие канала, CY = 0)
    RET

k_tty_read:
    MVI  A, ENOSYS              ; A = Канонический код ошибки 38 из vfs.inc
    STC                         ; АППАРАТНЫЙ ФИКС: Взводим флаг ошибки CY = 1 для ABI ядра
    RET

k_tty_write:
    MVI  A, ENOSYS              ; A = Канонический код ошибки 38 из vfs.inc
    STC                         ; АППАРАТНЫЙ ФИКС: Взводим флаг ошибки CY = 1 для ABI ядра
    RET

k_tty_ioctl:
    MVI  A, ENOSYS              ; A = Канонический код ошибки 38 из vfs.inc
    STC                         ; АППАРАТНЫЙ ФИКС: Взводим флаг ошибки CY = 1 для ABI ядра
    RET

; =============================================================================
; НАЗВАНИЕ: tty_core.asm (Расчёт физического адреса слота терминала)
; =============================================================================
k_tty_get_struct_address:
    ; Вход:  A  = Номер запрашиваемой TTY-консоли (0..3)
    ;        Прерывания заперты (DI), стек в Ring 0.
    ; Задача: Проверить аппаратные границы номера (0..MAX_TTY_DEVS-1).
    ;         Вычислить физический адрес слота по формуле: TTY_TABLE + (A * 160).
    ; Выход: В случае успеха:
    ;           Флаг переноса CY = 0 (Успех)
    ;           Пара HL = Точный 16-битный физический адрес слота в TTY_TABLE.
    ;        В случае ошибки (Нелегальный минор / номер консоли):
    ;           Флаг переноса CY = 1 (Провал)
    ;           Пара HL = 0000H
    ; ВНИМАНИЕ: Все РОН супервизора (кроме HL) полностью сохраняются!
    PUSH B
    PUSH D                          ; Сохранили контекст планировщика

    ; === ШАГ 1: ВАЛИДАЦИЯ ГРАНИЦ НOМЕРА УСТРOЙСТВА ===
    CPI  MAX_TTY_DEVS               ; Сравниваем с лимитом (A - 4)
    JNC  _tty_get_addr_err          ; Если A >= 4 — нелегальный минор, уходим на ошибку

    ; === ШАГ 2: БЫСТРOЕ СДВИГOВOЕ УМНOЖЕНИЕ НА 160 (128 + 32) ===
    MOV  L, A
    MVI  H, 0                       ; HL = Номер консоли (0..3)

    ; Сдвигаем HL влево на 5 бит (получаем умножение на 32)
    DAD  H                          ; *2
    DAD  H                          ; *4
    DAD  H                          ; *8
    DAD  H                          ; *16
    DAD  H                          ; HL = Номер * 32
    
    PUSH H                          ; Спрятали промежуточное значение (A * 32) на стек ядра
    
    ; Продолжаем сдвиг ещё на 2 бита, чтобы дойти до умножения на 128 (5 + 2 = 7 сдвигов)
    DAD  H                          ; *64
    DAD  H                          ; HL = Номер * 128
    
    POP  D                          ; DE = Номер * 32
    DAD  D                          ; HL = (Номер * 128) + (Номер * 32) = Номер * 160!

    ; === ШАГ 3: ПРИВЯЗКА К БАЗЕ МАССИВА ТЕРМИНАЛOВ ===
    LXI  D, TTY_TABLE
    DAD  D                          ; HL = Физический адрес целевого слота терминала
    
    ANA  A                          ; АППАРАТНЫЙ СБРОС: Сбрасываем флаг ошибки CY = 0 (УСПЕХ)
    JMP  _tty_get_addr_exit         ; На выход

_tty_get_addr_err:
    LXI  H, 0000H                   ; HL = 0000H (Null pointer)
    STC                             ; АППАРАТНЫЙ ВЗВОД: Взводим флаг ошибки CY = 1 (POSIX ENODEV)

_tty_get_addr_exit:
    POP  D
    POP  B                          ; Восстановили РОН супервизора в исходное состояние
    RET

; =============================================================================
; НАЗВАНИЕ: tty_core.asm (Низкоуровневый посимвольный вывод в TTY)
; =============================================================================
k_tty_tx_char:
    ; Вход:  A  = ASCII-символ для вывода
    ;        HL = Адрес 160-байтовой структуры TTY-слота
    ;        Прерывания заперты (DI), стек в Ring 0.
    ; Выход: CY = 0 (Успех), РОН супервизора полностью сохранены.
    PUSH B
    PUSH D
    PUSH H                          ; Сохранили контекст супервизора

    MOV  C, A                       ; C = выводимый символ
    
    ; === ШАГ 1: ПРОВЕРКА ПУСТОТЫ TX-БУФЕРА И ГОТОВНОСТИ UART ===
    PUSH H
    LXI  D, O_TTY_TX_COUNT
    DAD  D                          ; HL -> O_TTY_TX_COUNT (+69)
    MOV  A, M
    ORA  A                          ; TX_COUNT == 0?
    POP  H
    JNZ  _tty_tx_push_to_ring       ; Буфер не пуст — строго в очередь

    ; Буфер пуст — проверяем аппаратный статус TXRDY контроллера КР580ВВ51А
    IN   UART_CMD
    ANI  UART_STAT_TXRDY
    JZ   _tty_tx_push_to_ring       ; UART аппаратно занят — уходим в кольцо
    
    ; Передатчик полностью свободен и кольцо пусто — прямой выстрел в UART!
    MOV  A, C
    OUT  UART_DATA
    JMP  _tty_tx_char_success

_tty_tx_push_to_ring:
    ; === ШАГ 2: ПРОВЕРКА ПЕРЕПОЛНЕНИЯ TX КОЛЬЦА ===
    PUSH H
    LXI  D, O_TTY_TX_COUNT
    DAD  D
    MOV  A, M
    CPI  TTY_BUF_SIZE               ; Сверяем с лимитом 64 байта
    POP  H
    JZ   _tty_tx_char_success       ; Буфер полон — пассивный drop

    ; === ШАГ 3: ЧТЕНИЕ ТЕКУЩЕГО УКАЗАТЕЛЯ HEAD ===
    PUSH H
    LXI  D, O_TTY_TX_HEAD
    DAD  D                          ; HL -> O_TTY_TX_HEAD (+67)
    MOV  E, M                       ; E = текущий срез HEAD (0..63)
    
    ; === ШАГ 4: ИНКРЕМЕНТ HEAD С АППАРАТНЫМ ЗАЦИКЛИВАНИЕМ ===
    INR  M                          ; HEAD++
    MOV  A, M
    CPI  TTY_BUF_SIZE
    JC   _tty_tx_no_wrap_head
    MVI  M, 00H                     ; HEAD = 0 (зацикливание кольца вывода)
    
_tty_tx_no_wrap_head:
    ; === ШАГ 5: ЗАПИСЬ СИМВОЛА В БУФЕР ОЗУ ЯДРА ===
    POP  H                          ; HL = Базовый адрес TTY-слота
    PUSH H
    LXI  D, O_TTY_TX_BUFFER
    DAD  D                          ; HL -> TTY_TX_BUFFER (+70)
    MVI  D, 00H                     ; ИСПРАВЛЕНО: Каноничная команда зануления 8080!
    DAD  D                          ; HL = TTY_TX_BUFFER + HEAD_old
    MOV  M, C                       ; АТОМАРНАЯ ЗАПИСЬ: Символ зафиксирован в ОЗУ Ring 0

    ; === ШАГ 6: ИНКРЕМЕНТ СЧЕТЧИКА TX_COUNT ===
    POP  H
    PUSH H
    LXI  D, O_TTY_TX_COUNT
    DAD  D
    INR  M                          ; TX_COUNT++ 
    POP  H                          ; Сбалансировали стек локального снапшота базы

_tty_tx_char_success:
    ANA  A                          ; Сброс флага ошибки: CY = 0 (УСПЕХ)
    POP  H
    POP  D
    POP  B                          ; Восстановили РОН супервизора
    RET

; =============================================================================
; НАЗВАНИЕ: tty_core.asm (Аппаратный ISR-обработчик передачи данных UART)
; =============================================================================
k_tty_tx_isr:
    ; Вход:  HL = Физический 16-битный адрес 160-байтовой структуры TTY-слота,
    ;             прерывание которой обрабатывается системой.
    ;        Прерывания гарантированно заперты контроллером (DI), стек в Ring 0.
    ; Задача: Проверить счетчик TTY_TX_COUNT. Если буфер пуст — пассивно выйти.
    ;         Если в буфере есть данные — извлечь байт по TTY_TX_TAIL, выдать его
    ;         в UART_DATA_PORT, декрементировать счетчик и зациклить TAIL.
    ; Выход: РОН супервизора полностью сохранены.
    PUSH B
    PUSH D
    PUSH H                          ; Сохранили контекст планировщика

    ; === ШАГ 1: ПРОВЕРКА НАЛИЧИЯ ДАННЫХ В КОЛЬЦЕ ВЫВОДА ===
    PUSH H
    LXI  D, O_TTY_TX_COUNT
    DAD  D                          ; HL -> O_TTY_TX_COUNT (+69)
    MOV  A, M
    ORA  A                          ; Счетчик равен 0?
    POP  H
    JZ   _tty_tx_isr_done           ; Если пусто — выходим

    ; === ШАГ 2: ИЗВЛЕЧЕНИЕ ТЕКУЩЕГО УКАЗАТЕЛЯ TAIL ===
    PUSH H                          
    LXI  D, O_TTY_TX_TAIL
    DAD  D                          ; HL -> O_TTY_TX_TAIL (+68)
    MOV  E, M                       ; E = Текущий индекс TAIL (0..63)
    
    INR  M                          ; TAIL++
    MOV  A, M
    CPI  TTY_BUF_SIZE
    JC   _tty_tx_isr_no_wrap_tail
    MVI  M, 00H                     ; TAIL = 0

_tty_tx_isr_no_wrap_tail:
    ; === ШАГ 3: ЧТЕНИЕ СИМВОЛА ИЗ ОЗУ ЯДРА И ОТПРАВКА В ПОРТ ===
    POP  H
    PUSH H
    LXI  D, O_TTY_TX_BUFFER
    DAD  D                          ; HL -> TTY_TX_BUFFER (+70)
    MVI  D, 00H                     ; Зануляем D
    DAD  D                          ; HL = TTY_TX_BUFFER + TAIL_old
    MOV  A, M                       ; A = Целевой ASCII-символ
    
    ; === ИСПРАВЛЕНО: ОДНА ОТПРАВКА ВМЕСТО ДВУХ ===
    OUT  UART_DATA		            ; Байт вытолкнут на линию связи!

    ; === ШАГ 4: АТОМАРНЫЙ ДЕКРЕМЕНТ СЧЕТЧИКА БАЙТ ===
    POP  H
    LXI  D, O_TTY_TX_COUNT
    DAD  D                          ; HL -> O_TTY_TX_COUNT (+69)
    DCR  M                          ; TTY_TX_COUNT--

_tty_tx_isr_done:
    POP  H
    POP  D
    POP  B                          ; Полное восстановление оригинального контекста регистров
    RET                             ; Сквозной возврат в общий прерываний диспетчер ядра

; =============================================================================
; НАЗВАНИЕ: tty_core.asm (Аппаратный ISR-обработчик приёма данных UART)
; =============================================================================
k_tty_rx_isr:
    ; Вход:  HL = Физический адрес 160-байтовой структуры TTY-слота.
    ;        Прерывания заперты (DI), стек в Ring 0.
    ; Задача: Проверить аппаратный статус UART. Если байт готов, считать его.
    ;         Если в кольце приема есть место — записать по HEAD, инкрементировать HEAD и COUNT.
    ;         Если буфер полон — пассивный drop (игнорирование символа).
    ; Выход: РОН полностью сохранены.
    PUSH B
    PUSH D
    PUSH H                          ; Сохранили контекст планировщика

    ; === ШАГ 1: АППАРАТНОЕ ЧТЕНИЕ СИМВОЛА ===
    IN   UART_CMD                   ; Читаем статус КР580ВВ51А
    ANI  UART_STAT_RXRDY            ; Проверяем флаг готовности данных
    JZ   _tty_rx_isr_empty_exit     ; Если ложное прерывание — уходим

    IN   UART_DATA                  ; Байт считан из контроллера в аккумулятор!
    MOV  C, A                       ; C = принятый ASCII-символ

    ; === ШАГ 2: ПРОВЕРКА НА ПЕРЕПОЛНЕНИЕ КОЛЬЦА ПРИЕМА ===
    PUSH H                          ; Сохраняем базу HL в локальном стеке
    LXI  D, O_TTY_RX_COUNT
    DAD  D                          ; HL -> O_TTY_RX_COUNT
    MOV  A, M
    CPI  TTY_BUF_SIZE               ; Буфер заполнен (64 байта)?
    POP  H                          ; Восстановили базу HL
    JZ   _tty_rx_isr_done           ; Полно — сброс символа (защита памяти ядра)

    ; === ШАГ 3: ЧТЕНИЕ HEAD И ЗАПИСЬ В БУФЕР ===
    PUSH H                          ; Сохраняем базу HL
    LXI  D, O_TTY_RX_HEAD
    DAD  D                          ; HL -> O_TTY_RX_HEAD
    MOV  E, M                       ; E = текущий индекс HEAD (0..63)
    
    ; Инкремент и цикление HEAD
    INR  M                          ; HEAD++
    MOV  A, M
    CPI  TTY_BUF_SIZE
    JC   _tty_rx_isr_no_wrap_head
    MVI  M, 00H                     ; HEAD = 0

_tty_rx_isr_no_wrap_head:
    POP  H                          ; HL = Восстановленная база TTY-слота
    PUSH H                          ; Снова зафиксировали базу на стеке
    LXI  D, O_TTY_RX_BUFFER
    DAD  D                          ; HL -> O_TTY_RX_BUFFER
    MVI  D, 00H                     ; Идеальное зануление старшего регистра для i8080
    DAD  D                          ; HL = O_TTY_RX_BUFFER + HEAD_old
    MOV  M, C                       ; Символ атомарно сохранен в ОЗУ Ring 0

    ; === ШАГ 4: ИНКРЕМЕНТ СЧЕТЧИКА СИМВОЛОВ RX_COUNT ===
    POP  H                          ; HL = База
    LXI  D, O_TTY_RX_COUNT
    DAD  D                          ; HL -> O_TTY_RX_COUNT
    INR  M                          ; RX_COUNT++

_tty_rx_isr_done:
    ; Сюда сходимся как при успешной записи, так и при drop-переполнении
    POP  H
    POP  D
    POP  B                          ; Идеальный баланс РОН
    RET

_tty_rx_isr_empty_exit:
    POP  H
    POP  D
    POP  B
    RET

; =============================================================================
; НАЗВАНИЕ: tty_core.asm (Низкоуровневый посимвольный ввод из TTY)
; =============================================================================
k_tty_rx_char:
    ; Вход:  HL = Адрес 160-байтовой структуры TTY-слота
    ;        Прерывания гарантированно заперты вызывающим кодом (DI), стек в Ring 0.
    ; Выход: CY = 0 — Успех, регистр A = ASCII-символ из буфера.
    ;        CY = 1 — Буфер пуст, данных для чтения нет (EAGAIN / EWOULDBLOCK).
    ;        РОН супервизора (BC, DE, HL) полностью сохранены.
    PUSH B
    PUSH D
    PUSH H                          ; Сохранили контекст вызова

    ; === ШАГ 1: ПРОВЕРКА НАЛИЧИЯ ДАННЫХ ===
    PUSH H
    LXI  D, O_TTY_RX_COUNT
    DAD  D                          ; HL -> O_TTY_RX_COUNT
    MOV  A, M
    ORA  A                          ; RX_COUNT == 0?
    POP  H
    JZ   _tty_rx_char_empty         ; Буфер пуст — выходим с флагом переноса

    ; === ШАГ 2: ИЗВЛЕЧЕНИЕ СИМВОЛА ПО УКАЗАТЕЛЮ TAIL ===
    PUSH H                          ; Сохранили базу HL
    LXI  D, O_TTY_RX_TAIL
    DAD  D                          ; HL -> O_TTY_RX_TAIL
    MOV  E, M                       ; E = Текущий индекс TAIL (0..63)
    
    ; Инкремент и цикление TAIL
    INR  M                          ; TAIL++
    MOV  A, M
    CPI  TTY_BUF_SIZE
    JC   _tty_rx_char_no_wrap_tail
    MVI  M, 00H                     ; TAIL = 0

_tty_rx_char_no_wrap_tail:
    POP  H                          ; HL = База
    PUSH H                          ; Сохранили базу
    LXI  D, O_TTY_RX_BUFFER
    DAD  D                          ; HL -> O_TTY_RX_BUFFER
    MVI  D, 00H                     ; Зануление D
    DAD  D                          ; HL = O_TTY_RX_BUFFER + TAIL_old
    MOV  C, M                       ; C = Прочитанный из кольца символ

    ; === ШАГ 3: ДЕКРЕМЕНТ СЧЕТЧИКА RX_COUNT ===
    POP  H                          ; HL = База
    PUSH H                          ; Сохранили базу
    LXI  D, O_TTY_RX_COUNT
    DAD  D                          ; HL -> O_TTY_RX_COUNT
    DCR  M                          ; RX_COUNT--

    ; === ШАГ 4: ФОРМИРОВАНИЕ УСПЕШНОГО ВЫХОДА ===
    POP  H                          ; Полностью очистили локальный стек до оригинального кадра
    POP  H                          ; Достали сохраненный на входе HL супервизора
    POP  D                          ; Достали оригинальный DE
    POP  B                          ; Достали оригинальный BC
    
    MOV  A, C                       ; Результат помещен в регистр A
    ANA  A                          ; Сброс флага переноса: CY = 0 (УСПЕХ)
    RET

_tty_rx_char_empty:
    POP  H
    POP  D
    POP  B                          ; Восстановили исходный контекст супервизора
    STC                             ; Установка флага переноса: CY = 1 (НЕТ ДАННЫХ)
    RET
