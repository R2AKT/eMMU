; /kernel/tty_core.asm - ИСПОЛНЯЕМЫЙ КОД ПОДСИС ТЕМЫ ТЕРМИНАЛЬНОГО ДВИЖКА ЯДРА
; =============================================================================
; =============================================================================
; НАЗВАНИЕ: tty_core.asm (Инициализация структур ОЗУ массива TTY)
; =============================================================================
k_tty_subsys_init:
    ; Вход:  Cold Boot, Ring 0, прерывания гарантированно заперты (DI).
    ; Задача: Полностью очистить и инициализировать все 4 независимых TTY-слота
    ;         внутри TTY_TABLE каноническими стартовыми значениями.
    
    LXI  H, TTY_TABLE               ; HL = Базовый физический адрес старта массива TTY
    MVI  B, MAX_TTY_DEVS            ; B = Счетчик внешнего цикла (4 консоли)

_tty_quad_alignment_loop:
    PUSH H                          ; Сохранили базовый адрес начала текущего 160-байтового слота

    ; --- ЭТАП 1: Полное предварительное зануление всего 160-байтового слота TTY ---
    MVI  C, TTY_STRUCT_SIZE         ; C = 160 байт
_tty_clear_row_loop:
    MVI  M, 00H
    INX  H
    DCR  C
    JNZ  _tty_clear_row_loop

    ; --- ЭТАП 2: Точечная инициализация POSIX атрибутов слота ---
    POP  H                          ; HL = Восстановили базовый адрес текущего TTY слота
    PUSH H                          ; Отложили на стек для перехода к следующей строке

    ; Прописываем O_TTY_C_IFLAG (+134) из дефолтных констант tty.inc
    LXI  D, O_TTY_C_IFLAG
    DAD  D                          ; HL -> Поле O_TTY_C_IFLAG (+134)
    MVI  M, DEF_C_IFLAG
    
    ; Прописываем O_TTY_C_OFLAG (+135) из дефолтных констант tty.inc
    INX  H                          ; HL -> Поле O_TTY_C_OFLAG (+135)
    MVI  M, DEF_C_OFLAG
    
    ; Прописываем O_TTY_C_LFLAG (+136) из дефолтных констант tty.inc
    INX  H                          ; HL -> Поле O_TTY_C_LFLAG (+136)
    MVI  M, DEF_C_LFLAG

    ; Инициализируем 8 байт массива управляющих символов O_TTY_C_CC (+137..+144)
    INX  H                          ; HL -> O_TTY_C_CC[VEOF] (+137)
    MVI  M, DEF_VEOF                ; Ctrl+D
    INX  H
    MVI  M, DEF_VEOL                ; \n
    INX  H
    MVI  M, DEF_VERASE              ; Backspace
    INX  H
    MVI  M, DEF_VKILL               ; Ctrl+U
    INX  H
    MVI  M, DEF_VINTR               ; Ctrl+C
    INX  H
    MVI  M, DEF_VQUIT               ; Ctrl+\
    INX  H
    MVI  M, DEF_VSTART              ; Ctrl+Q
    INX  H
    MVI  M, DEF_VSTOP               ; Ctrl+S

    ; Извлекаем чистую базу слота, чтобы исключить накопление ошибок INX H
    POP  H
    PUSH H
    LXI  D, O_TTY_WAIT_PID
    DAD  D                          ; HL -> Указывает точно на поле O_TTY_WAIT_PID (+145)
    MVI  M, UNBOUND_WAIT_PID        ; Зафиксировали 0xFF
    
    ; Явная инициализация группы переднего плана по строгому смещению структуры
    POP  H
    PUSH H
    LXI  D, O_TTY_PGRP
    DAD  D                          ; HL -> Указывает точно на поле O_TTY_PGRP (+147)
    MVI  M, UNBOUND_WAIT_PID        ; Зафиксировали 0xFF (Группа переднего плана отвязана)

    ; --- ЭТАП 3: Шаг к следующему TTY слоту таблицы ---
    POP  H                          ; HL = Базовый адрес текущего TTY слота
    LXI  D, TTY_STRUCT_SIZE         ; Сдвиг равен ровно 160 байтам (00A0h)
    DAD  D                          ; HL указывает строго на старт следующей TTY структуры
    
    DCR  B                          ; Уменьшаем счётчик сконфигурированных консолей
    JNZ  _tty_quad_alignment_loop   ; Повторяем цикл, пока не настроим все 4 терминала
    
    RET                             ; Возврат в k_power_on_init

; -----------------------------------------------------------------------------
; Инициализация USART КР580ВВ51А (9600,8N1, фактор K=16)
; -----------------------------------------------------------------------------
k_uart_hw_init:
    ; Вход:  Cold Boot, Ring 0, прерывания заперты (DI).
    ; Задача: Настроить Таймер Связи КР580ВИ53 №2 (Раздельное тактирование Rx/Tx UART 9600 бод)
    ;         и инициализировать USART КР580ВВ51А в режим 8N1 с фактором K=16.
    
    ; 1. Настройка канала 0 ВИ53 (Тактирование ПРИЕМА RxC): Чтение/Запись LSB/MSB, Режим 3
    MVI  A, 036H             
    OUT  UART_PIT_CMD
    MVI  A, 00CH             ; Делитель приемника = 1 843 200 / (9600 * 16) = РОВНО 12
    OUT  UART_PIT_CH0
    MVI  A, 000H
    OUT  UART_PIT_CH0
    
    ; 2. Настройка канала 1 ВИ53 (Тактирование ПЕРЕДАЧИ TxC): Чтение/Запись LSB/MSB, Режим 3
    MVI  A, 076H             ; Исправлен двоичный литерал: 01110110B = 076h
    OUT  UART_PIT_CMD
    MVI  A, 00CH             ; Делитель приемника = 1 843 200 / (9600 * 16) = РОВНО 12
    OUT  UART_PIT_CH1
    MVI  A, 000H
    OUT  UART_PIT_CH1

    ; 3. Инициализация БИС USART КР580ВВ51А
    XRA  A
    OUT  UART_CMD
    OUT  UART_CMD
    OUT  UART_CMD      ; Тройной сброс автомата БИС
    
    MVI  A, 040H
    OUT  UART_CMD      ; Внутренний программный сброс (Internal Reset)
    NOP
    
    ; === МАТЕМАТИЧЕСКИЙ КАНOНИЧНЫЙ ИНВАРИАНТ: 8N1 ×16 ===
    MVI  A, 04EH             ; 01001110b: 1 стоп-бит, без чётности, 8 бит, фактор ×16
    OUT  UART_CMD
    NOP
    
    ; 4. Сброс ошибок + активация линий
    MVI  A, 037H             ; Error Reset + TX/RX Enable + RTS/DTR
    OUT  UART_CMD
    NOP
    
    ; 5. Рабочая команда (без удержания импульсного Error Reset)
    MVI  A, 027H             ; TX/RX разрешены, RTS/DTR активны (00100111b)
    OUT  UART_CMD
    RET

