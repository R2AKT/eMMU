;  └── hw_uart_entry (tty_isr.asm) ─────────────────────────────────── [Top Half: Сохранение контекста Ring 0]
;        ├── O_TTY_HW_POLL_STAT (+153) ──────────────────────────────── [Косвенный вызов через PCHL]
