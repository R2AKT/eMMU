; НАЗВАНИЕ: tty.asm (Диспетчер последовательной сборки активных TTY-слотов)
; =============================================================================
; =============================================================================
; НАЗВАНИЕ: tty.asm (Инстанцирующий диспетчер последовательной сборки карусели)
; ТОКЕН:    TOKEN::MEGA-580_OS_v1.5.81_K_TTY_CAROUSEL_INSTANCED_BUILD_20260709
; =============================================================================

; =============================================================================
; ПРОЦЕДУРА: k_tty_carousel_auto_build
; Вход:  Cold Boot, Ring 0, прерывания гарантированно заперты (DI).
; Задача: Перебрать все 4 физических порта, протестировать наличие железа
;         с приоритетом КР580ВВ51А, и связать их с ОЗУ-структурами ПОСЛЕДОВАТЕЛЬНО
;         методом SMC-инстанцирования базы в тела обработчиков прерываний.
; Выход: Все РОН полностью сохранены. Прерывания заперты.
; =============================================================================
k_tty_carousel_auto_build:
    PUSH B
    PUSH D
    PUSH H                          ; Спасли контекст супервизора Ring 0

    ; Инициализируем динамический указатель целевого логического TTY-слота
    LXI  D, TTY_TABLE               ; DE = Текущий свободный слот ОЗУ (на старте TTY0)

    ; --- ТЕСТИРОВАНИЕ АДРЕСА ПОРТА 0xA0 ---
    MVI  A, 0A0H
    CALL _tty_probe_hardware_gate
    
    ; --- ТЕСТИРОВАНИЕ АДРЕСА ПОРТА 0xB0 ---
    MVI  A, 0B0H
    CALL _tty_probe_hardware_gate
    
    ; --- ТЕСТИРОВАНИЕ АДРЕСА ПОРТА 0xC0 ---
    MVI  A, 0C0H
    CALL _tty_probe_hardware_gate
    
    ; --- ТЕСТИРОВАНИЕ АДРЕСА ПОРТА 0xD0 ---
    MVI  A, 0D0H
    CALL _tty_probe_hardware_gate

    POP  H
    POP  D
    POP  B                          ; Полное восстановление РОН супервизора
    RET

; =============================================================================
; ПОДПРОГРАММА: _tty_probe_hardware_gate
; Вход:  DE = Физический адрес ТЕКУЩЕГО СВОБОДНОГО TTY-слота в ОЗУ ядра
;        A  = Проверяемый физический базовый порт (0xA0, 0xB0, 0xC0, 0xD0)
; =============================================================================
_tty_probe_hardware_gate:
    PUSH B
    PUSH H                          ; Сохранили контекст текущего шага сканирования
    PUSH PSW                        ; Спасли проверяемый порт на стек

    ; 1. Временно прописываем порт и дефолтные делители в текущий свободный слот DE
    MOV  H, D
    MOV  L, E                       ; HL = Текущий свободный слот ОЗУ
    PUSH H                          ; Запомнили базу структуры
    LXI  B, O_TTY_HW_BASE_PORT
    DAD  B                          ; HL -> O_TTY_HW_BASE_PORT (+155)
    MOV  M, A                       ; Записали тестируемый порт
    
    INX  H                          ; HL -> O_TTY_HW_DIV_LOW (+156)
    MVI  M, 00CH                    ; Дефолт speed = 9600 бод (делитель 12)
    INX  H                          ; HL -> O_TTY_HW_DIV_HIGH (+157)
    MVI  M, 00H
    POP  H                          ; HL = Восстановили чистую базу слота

    ; 2. ПРИОРИТЕТНЫЙ ТЕСТ №1: Двухчиповый автоопрос КР580ВВ51А + КР580ВИ53
    CALL hw_i8251_init              ; Запуск физического инициализатора. Сохраняет DE!
    JNC  _gate_i8251_found          ; CY=0 -> Нашли стабильную связку ВВ51!

    ; 3. РЕЗЕРВНЫЙ ТЕСТ №2: Автоопрос чипа NS16550 (Если ВВ51 ответил ошибкой)
    CALL hw_ns16550_init            ; Запуск инициализатора 16550. Сохраняет DE!
    JNC  _gate_ns16550_found        ; CY=0 -> Нашли альтернативный 16550!

    ; 4. АППАРАТНАЯ ПУСТОТА: Ни одного чипа на этом адресе нет
    POP  PSW                        ; Очистили стек от сохраненного порта
    POP  H
    POP  B                          ; Восстановили РОН. DE ОСТАЛСЯ НА МЕСТЕ!
    RET                             ; Слот DE будет перезаписан следующим физическим портом

_gate_i8251_found:
    ; === СВЯЗКА КР580ВВ51А + КР580ВИ53 НАЙДЕНА! ВНЕДРЯЕМ SMC-ИНСТАНС БАЗЫ ===
    ; Нам нужно прошить адрес слота (из пары DE) в операнд инструкции LXI D внутри ISR.
    ; Адрес команды: _smc_isr_51_base. Операнд младшего байта: +1, старшего: +2.
    MOV  A, E                       ; А = Младший байт физического адреса ОЗУ-слота
    STA  _smc_isr_51_base + 1       ; Атомарно прошили младший байт операнда LXI D! [1.3]
    MOV  A, D                       ; А = Старший байт физического адреса ОЗУ-слота
    STA  _smc_isr_51_base + 2       ; Атомарно прошили старший байт операнда LXI D! [1.3]

    ; Физический адрес базы ОЗУ-слота жестко вплавлен в код обработчика. Регистрируем вектор:
    POP  PSW                        ; Восстановили базовый порт в аккумулятор А
    CALL _gate_calculate_irq        ; Вычислили номер IRQ-линии ВН59 (А = 1..4)
    
    LXI  H, hw_i8251_isr            ; HL = Адрес индивидуального быстрого ISR драйвера ВВ51
    CALL k_register_isr             ; Пропатчили CALL-вектор ВН59 и открыли линию! [1.3]
    JMP  _gate_slot_allocated       ; Слот успешно занят, уходим на сдвиг указателя ОЗУ

_gate_ns16550_found:
    ; === МИКРОСХЕМА NS16550 НАЙДЕНА! ВНЕДРЯЕМ SMC-ИНСТАНС БАЗЫ ===
    ; Прошиваем адрес слота (из пары DE) в операнд LXI D обработчика hw_ns16550_isr.
    ; Адрес команды: _smc_isr_ns_base. Операнд младшего байта: +1, старшего: +2.
    MOV  A, E                       ; А = Младший байт физического адреса ОЗУ-слота
    STA  _smc_isr_ns_base + 1       ; Атомарно прошили младший байт операнда LXI D! [1.3]
    MOV  A, D                       ; А = Старший байт физического адреса ОЗУ-слота
    STA  _smc_isr_ns_base + 2       ; Атомарно прошили старший байт операнда LXI D! [1.3]

    ; Физический адрес базы ОЗУ-слота жестко вплавлен в код обработчика. Регистрируем вектор:
    POP  PSW                        ; Восстановили базовый порт в аккумулятор А
    CALL _gate_calculate_irq        ; Вычислили номер IRQ-линии ВН59 (А = 1..4)
    
    LXI  H, hw_ns16550_isr          ; HL = Адрес индивидуального быстрого ISR драйвера 16550
    CALL k_register_isr             ; Пропатчили CALL-вектор ВН59 и открыли линию! [1.3]

_gate_slot_allocated:
    ; === СДВИГ УКАЗАТЕЛЯ: ЛОГИЧЕСКИЙ СЛOТ УСПЕШНО ЗАНЯТ, ШАГАЕМ ВПЕРЕД ===
    MOV  H, D
    MOV  L, E                       ; HL = Текущий занятый слот
    LXI  D, TTY_STRUCT_SIZE         ; DE = 160 (00A0H) — шаг структуры слота [1.3]
    DAD  D                          ; HL = HL + 160 (Адрес следующего чистого ОЗУ-слота таблицы)
    
    XCHG                            ; DE = Указатель на следующий СВОБОДНЫЙ логический слот ОЗУ!
    
    POP  H
    POP  B                          ; Полностью восстановили контекст текущей итерации
    RET

; =============================================================================
; ПОДПРОГРАММА: _gate_calculate_irq
; Вход:  A = Базовый физический порт данных консоли (0xA0, 0xB0, 0xC0, 0xD0)
; Выход: A = Номер соответствующей аппаратной линии прерывания IRQ (1..4)
; =============================================================================
_gate_calculate_irq:
    CPI  0A0H
	JNZ _g_irq_b0
	MVI A, 1
	RET
_g_irq_b0:
    CPI  0B0H
	JNZ _g_irq_c0
	MVI A, 2
	RET
_g_irq_c0:
    CPI  0C0H
	JNZ _g_irq_d0
	MVI A, 3
	RET
_g_irq_d0:
    MVI  A, 4
	RET

; =============================================================================
; НАЗВАНИЕ: tty.asm (Авторегистратор TTY-драйвера в коммутаторе VFS)
; СРЕДА:    Ring 0 супервизора | Архитектура КР580ВМ80А | Модуль ТЕРМИНАЛОВ
; =============================================================================
; =============================================================================
; ПРОЦЕДУРА: k_tty_register_vfs
; Вход:  Cold Boot, Ring 0, прерывания заперты (DI).
; Задача: Зарегистрировать HAL-мосты чтения/записи подсистемы терминалов
;         в глобальной таблице SYS_CHRDEV_SWITCH под MAJOR-номером 4 (MAJOR_TTY).
; Выход: CY = 0 — Успех. Класс терминалов интегрирован в коммутатор VFS.
;        CY = 1 — Критическая ошибка: Неверный Major-номер (Защита границ таблицы).
;        Все РОН (BC, DE, HL) полностью сохранены по контракту LIFO.
; =============================================================================
k_tty_register_vfs:
    PUSH B
    PUSH D
    PUSH H                          ; Спасли рантайм-контекст супервизора Ring 0

    ; --- ЭТАП 1: ПОДГOТOВКА ПАРАМЕТРOВ ДЛЯ СИСТЕМНОГO РЕГИСТРАТOРА VFS ---
    MVI  A, MAJOR_TTY               ; А  = MAJOR_TTY (Константа 4 из tty.inc) [1.3]
    LXI  H, tty_write_bridge         ; HL = Физический адрес HAL-моста записи TTY [1.3]
    LXI  D, tty_read_bridge          ; DE = Физический адрес HAL-моста чтения TTY [1.3]

    ; --- ЭТАП 2: АТОМАРНАЯ СВЯЗКА В ОЗУ ТАБЛИЦЕ ДРАЙВЕРOВ ЯДРА ---
    CALL k_register_chrdev          ; Вызов Plug-and-Play регистратора VFS [1.3]
                                    ; (При успехе возвращает CY=0, при ошибке границ — CY=1)
    
    ; Перехват ошибки границ таблицы Major-номеров (<32)
    JC   _k_tty_reg_vfs_fault

    ; --- ЭТАП 3: КАНOНИЧЕСКOЕ ВОССТАНОВЛЕНИЕ КОНТЕКСТА (УСПЕХ) ---
    POP  H
    POP  D
    POP  B                          ; Полное каноническое восстановление РОН по LIFO
    ANA  A                          ; СБРОС ФЛАГА ОШИБКИ: CY = 0 (Полный успех операции)
    RET

; =============================================================================
; КАНАЛ АВАРИЙНОГО ВОЗВРАТА ОШИБКИ
; =============================================================================
_k_tty_reg_vfs_fault:
    POP  H
    POP  D
    POP  B                          ; Зеркально разгрузили оригинальный РОН-кадр со стека
    STC                             ; ВЗВОД ФЛАГА ОШИБКИ: CY = 1 (Провал коммутации)
    RET

; =============================================================================
; НАЗВАНИЕ: tty.asm (Канонический HAL-мост отправки TTY_WRITE_BRIDGE)
; СРЕДА:    Ring 0 супервизора | Архитектура КР580ВМ80А | Модуль ТЕРМИНАЛОВ
; =============================================================================
; =============================================================================
; ПРОЦЕДУРА: tty_write_bridge
; Вход:  А  = Выводимый ASCII-символ.
;        B  = Номер Minor (0..3), полученный от коммутатора символьных устройств.
; Задача: По Minor-номеру мгновенно вычислить адрес ОЗУ-структуры TTY слота,
;         извлечь из неё адрес физического метода put_char конкретного чипа 
;         и выполнить косвенный переход по трюку PCHL.
; Выход: Все РОН (BC, DE, HL) полностью сохранены согласно контракту Варианта B.
; =============================================================================
tty_write_bridge:
    PUSH B
    PUSH D
    PUSH H                          ; Спасли рантайм-контекст супервизора Ring 0

    MOV  C, A                       ; C = ЭВАКУАЦИЯ: сохранили выводимый символ от разрушения

    ; === ЭТАП 1: МГНОВЕННЫЙ 16-БИТНЫЙ РАСЧЕТ АДРЕСА TTY-СТРУКТУРЫ ===
    MOV  A, B                       ; А = Minor (0..3)
    MOV  L, A
    MVI  H, 00H                     ; HL = Minor (0000H .. 0003H)
    
    ; Быстрое умножение Minor на 160 через сдвиги i8080: (Minor * 128) + (Minor * 32)
    DAD  H
	DAD  H
	DAD  H
	DAD  H
	DAD  H ; HL = Minor * 32
    XCHG                             ; DE = Minor * 32
    DAD  H
	DAD  H                  ; HL = (Minor * 32) * 4 = Minor * 128
    DAD  D                           ; HL = (Minor * 128) + (Minor * 32) = Minor * 160
    
    LXI  D, TTY_TABLE               ; Символический релоцируемый адрес начала таблицы
    DAD  D                           ; HL = Физический адрес структуры активного TTY-слота!
    XCHG                             ; DE = Зафиксировали базу TTY-структуры в паре DE [1.3]

    ; === ЭТАП 2: ИЗВЛЕЧЕНИЕ ФИЗИЧЕСКОГО ВЕКТОРА ДРАЙВЕРА ИЗ ХВОСТА СЛOТА ===
    MOV  H, D
    MOV  L, E                       ; HL = Перенесли базу слота в HL для DAD-индексации
    LXI  B, O_TTY_HW_PUT_CHAR       ; Смещение к указателю метода отправки (+158)
    DAD  B                           ; HL -> Точный адрес ячейки памяти метода драйвера
    
    MOV  A, M
    INX  H
    MOV  H, M
    MOV  L, A                       ; HL = Истинный физический адрес hw_i8251_put_char / 16550

    ; === ЭТАП 3: ВОССТАНОВЛЕНИЕ КОНТРАКТА И КОСВЕННЫЙ ВЫСТРЕЛ ЧЕРЕЗ PCHL ===
    MOV  A, C                       ; А = Восстановили выводимый ASCII-символ
    ; Регистровая пара DE содержит истинный базовый адрес TTY-структуры для драйвера
    
    LXI  B, _tty_wr_bridge_ret      ; Вручную затолкнули адрес возврата в стек супервизора
    PUSH B
    PCHL                             ; Прыжок на исполнение в тело физического put_char чипа!

_tty_wr_bridge_ret:
    POP  H
    POP  D
    POP  B                          ; Идеальное каноническое восстановление РОН по LIFO
    RET

; =============================================================================
; НАЗВАНИЕ: tty.asm (Канонический HAL-мост приема TTY_READ_BRIDGE)
; СРЕДА:    Ring 0 супервизора | Архитектура КР580ВМ80А | Модуль ТЕРМИНАЛОВ
; =============================================================================
; =============================================================================
; ПРОЦЕДУРА: tty_read_bridge
; Вход:  B  = Номер Minor (0..3), полученный от коммутатора символьных устройств.
; Задача: Локализовать ОЗУ-структуру TTY слота, атомарно (под DI/EI) проверить
;         наличие данных в O_TTY_RX_COUNT. Если есть — извлечь байт по RX_TAIL,
;         инкрементировать индекс с зацикливанием и обновить счетчик.
; Выход: А  = Принятый ASCII-символ (Если буфер пуст — возвращает 00H).
;        Все остальные РОН (BC, DE, HL) полностью сохранены по контракту.
; =============================================================================
tty_read_bridge:
    PUSH B
    PUSH D
    PUSH H                          ; Спасли рантайм-контекст супервизора Ring 0

    ; === ЭТАП 1: МГНОВЕННЫЙ 16-БИТНЫЙ РАСЧЕТ АДРЕСА TTY-СТРУКТУРЫ ===
    MOV  A, B                       ; А = Minor (0..3)
    MOV  L, A
    MVI  H, 00H                     ; HL = Minor (0000H .. 0003H)
    
    ; Быстрое умножение Minor на 160: (Minor * 128) + (Minor * 32)
    DAD  H
	DAD  H
	DAD  H
	DAD  H
	DAD  H ; HL = Minor * 32
    XCHG                             ; DE = Minor * 32
    DAD  H
	DAD  H                  ; HL = (Minor * 32) * 4 = Minor * 128
    DAD  D                           ; HL = (Minor * 128) + (Minor * 32) = Minor * 160
    
    LXI  D, TTY_TABLE               ; Релоцируемый адрес начала таблицы слотов
    DAD  D                           ; HL = Физический адрес структуры активного TTY-слота!
    XCHG                             ; DE = Зафиксировали базу TTY-структуры в паре DE

    ; === ЭТАП 2: КОНТРОЛЬ НАЛИЧИЯ ДАННЫХ ПОД ЗАЩИТОЙ КРИТИЧЕСКОЙ СЕКЦИИ ===
    DI                              ; Заперли прерывания! Защита от коллизии с ISR [1.3]
    
    MOV  H, D
    MOV  L, E                       ; HL = База TTY-слота
    LXI  B, O_TTY_RX_COUNT
    DAD  B                          ; HL -> O_TTY_RX_COUNT
    MOV  A, M
    ORA  A                          ; RX_COUNT == 0 (Очередь приема ОЗУ пуста)?
    JZ   _tty_rd_buffer_empty       ; Да — уходим на безопасный неблокирующий выход

    ; === ЭТАП 3: ВЫБОРКА СИМВОЛА ИЗ КОЛЬЦЕВОГО БУФЕРА ===
    DCX  H                          ; HL -> O_TTY_RX_TAIL (Смещение +1)
    MOV  C, M                       ; C = RX_TAIL_old (Текущий индекс чтения)
    
    ; Инкремент и циклический заворот указателя чтения в памяти ядра
    INR  M                          ; RX_TAIL++
    MOV  A, M
    CPI  TTY_BUF_SIZE               ; Достигли физического края кольца (64 байта)?
    JC   _tty_rd_no_wrap
    MVI  M, 00H                     ; Циклический сброс указателя: TAIL = 0

_tty_rd_no_wrap:
    ; Двухступенчатый расчет физического адреса байта: База + O_TTY_RX_BUFFER + TAIL_old
    MOV  H, D
    MOV  L, E                       ; HL = Восстановили чистую базу TTY-слота
    PUSH D                          ; Спрятали базу слота на стек Ring 0
    LXI  D, O_TTY_RX_BUFFER
    DAD  D                          ; HL = База + O_TTY_RX_BUFFER
    
    MOV  E, C
    MVI  D, 00H                     ; DE = 0000H + RX_TAIL_old
    DAD  D                          ; HL = (База + O_TTY_RX_BUFFER) + RX_TAIL_old! Указатель точен!
    MOV  A, M                       ; А = Истинный ASCII-символ из ОЗУ ядра Ring 0!
    MOV  C, A                       ; C = Временно спрятали символ в регистр C
    POP  D                          ; DE = Восстановили базу TTY-слота

    ; Декремент счетчика непрочитанных символов в структуре слота
    MOV  H, D
    MOV  L, E
    LXI  B, O_TTY_RX_COUNT
    DAD  B                          ; HL -> O_TTY_RX_COUNT
    DCR  M                          ; O_TTY_RX_COUNT--

    EI                              ; Открыли прерывания! Вышли из критической секции [1.3]
    MOV  A, C                       ; А = Символ готов к передаче в коммутатор
    JMP  _tty_rd_exit

_tty_rd_buffer_empty:
    EI                              ; Открыли прерывания перед выходом! [1.3]
    XRA  A                          ; А = 00H (Признак отсутствия символа в неблокирующем режиме)

_tty_rd_exit:
    POP  H
    POP  D
    POP  B                          ; Полное восстановление РОН супервизора по LIFO
    RET

; =============================================================================
; ПРОЦЕДУРА: vfs_mount_tty_nodes
; Вход:  DE = Физический адрес первого СВОБОДНОГО слота TTY в памяти ядра, 
;             вернувшийся после выполнения k_tty_carousel_auto_build.
; Задача: По разнице адресов определить число реально найденных чипов UART,
;         последовательно зарегистрировать для них файлы устройств /dev/ttyX
;         с MAJOR=4 и MINOR=0..3 в RAM-диске VFS.
; Выход: Все РОН полностью сохранены согласно контракту HAL Варианта B.
; =============================================================================
vfs_mount_tty_nodes:
    PUSH B
    PUSH D
    PUSH H                          ; Сохранили контекст супервизора Ring 0

    ; === ЭТАП 1: ИСПРАВЛЕННОЕ 16-БИТНОЕ ВЫЧИСЛЕНИЕ ДЕЛЬТЫ АДРЕСОВ ===
    LXI  H, TTY_TABLE
    MOV  A, E
    SUB  L
    MOV  E, A                       ; E = младший байт дельты
    MOV  A, D
    SBB  H                          ; A = старший байт дельты с переносом
    MOV  D, A                       ; DE = 16-битная дельта

    ; === ЭТАП 2: ДЕЛЕНИЕ НА 160 С КАНOНИЧЕСКИМ КОНТРОЛЕМ ПЕРЕНОСА ===
    MVI  B, 0                       ; B = Итоговый счетчик активных Minor (0..4)

_vfs_tty_calc_active:
    MOV  A, E
    ORA  D
    JZ   _vfs_tty_mount_start       ; Если DE = 0, чистый расчет окончен, уходим на монтаж
    
    LXI  H, -160                    ; HL = 0xFF60 (Дополнительный код числа 160)
    DAD  D                          ; HL = DE - 160
    
    ; ИСПРАВЛЕНО: Если CY=0, значит DE было меньше 160 (заем бита). 
    ; Это признак завершения деления либо остаточная ошибка выравнивания ОЗУ!
    JNC  _vfs_tty_mount_start       
    
    MOV  D, H
    MOV  E, L                       ; DE = Успешно декрементированная дельта (DE - 160)
    INR  B                          ; Увеличиваем счетчик найденных терминалов
    JMP  _vfs_tty_calc_active

_vfs_tty_mount_start:
    MOV  A, B                       ; A = Общее количество живых терминалов
    ORA  A
    JZ   _vfs_tty_no_uart_panic     ; Если 0 — ни одного UART не найдено (Kernel Panic!)
    MOV  C, A                       ; C = Лимит цикла монтирования файлов устройств

    MVI  B, 0                       ; B = Текущий монтируемый Minor-номер (0..3)

_vfs_tty_mount_loop:
    PUSH B                          ; ← ИСПРАВЛЕНО: Сохраняем ВСЮ пару BC на стек!
                                    ; [Стек: B = Minor, C = Оставшийся лимит цикла]

    ; Подготавливаем параметры для системного вызова k_vfs_mknod()
    MOV  A, B                       ; A = Текущий Minor (0..3)
    CALL _vfs_tty_get_string_ptr    ; Выход: DE = Адрес ASCIIZ-строки пути
    
    MVI  C, 4                       ; MAJOR_TTY = 4 
    ; Регистр B по-прежнему содержит текущий Minor-номер
    
    CALL k_vfs_mknod                  ; Создаем специальный символьный Inode в VFS [1.3]

    POP  B                          ; ← ИСПРАВЛЕНО: Извлекаем ВСЮ пару BC со стека!
                                    ; Теперь B = наш Minor, C = наш лимит цикла
    INR  B                          ; Переходим к следующему Minor (Minor++)
    DCR  C                          ; Уменьшаем счетчик оставшихся проходов (Лимит--)
    JNZ  _vfs_tty_mount_loop        ; Цикл продолжается, пока C != 0
    
    POP  H
    POP  D
    POP  B                          ; Полное восстановление оригинального РОН-контекста
    RET

_vfs_tty_no_uart_panic:
    LXI  H, _vfs_tty_panic_msg_no_uart
    JMP  kernel_panic_no_tty           ; Намертво вешаем систему — нет консоли вывода

kernel_panic_no_tty:
	; Фатальная ошибка старта: TTY0 не найден.
    ; Выключаем прерывания и намертво вешаем процессор.
    DI
    HLT
	JMP  kernel_panic_no_tty

; =============================================================================
; ПОДПРОГРАММА: _vfs_tty_get_string_ptr (ИЗОЛИРОВАННАЯ С ПРОВЕРКОЙ ГРАНИЦ)
; Вход:  A = Номер Minor (0..3)
; Выход: DE = Физический адрес ASCIIZ-строки пути устройства
; =============================================================================
_vfs_tty_get_string_ptr:
    CPI  0
	JZ  _vfs_tty_ret_dev0
    CPI  1
	JZ  _vfs_tty_ret_dev1
    CPI  2
	JZ  _vfs_tty_ret_dev2
    CPI  3
	JZ  _vfs_tty_ret_dev3
    
    ; Перехват критической коллизии: A > 3 — индекс разрушен!
    LXI  H, _vfs_tty_panic_msg_inv_minor
    JMP  kernel_panic_tty_indx_err

_vfs_tty_ret_dev0:
    LXI  D, _vfs_tty_str_dev0
	RET
_vfs_tty_ret_dev1:
    LXI  D, _vfs_tty_str_dev1
	RET
_vfs_tty_ret_dev2:
    LXI  D, _vfs_tty_str_dev2
	RET
_vfs_tty_ret_dev3:
    LXI  D, _vfs_tty_str_dev3
	RET

kernel_panic_tty_indx_err:
	; Фатальная ошибка старта: Нарушение структуры TTY.
    ; Выключаем прерывания и намертво вешаем процессор.
    DI
    HLT
	JMP  kernel_panic_tty_indx_err

; --- Изолированные статические системные ASCIIZ-строки (Секция ROM VFS) ---
_vfs_tty_str_dev0:              DB  "/dev/tty0", 0
_vfs_tty_str_dev1:              DB  "/dev/tty1", 0
_vfs_tty_str_dev2:              DB  "/dev/tty2", 0
_vfs_tty_str_dev3:              DB  "/dev/tty3", 0
_vfs_tty_panic_msg_no_uart:     DB  "VFS CRITICAL: NO ACTIVE UART DEVICES FOUND!", 0
_vfs_tty_panic_msg_inv_minor:   DB  "VFS CRITICAL: INVALID TTY MINOR NUMBER!", 0


