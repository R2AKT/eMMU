; =============================================================================
; НАЗВАНИЕ: tty_core.asm (Инициализация структур ОЗУ массива TTY)
; =============================================================================
k_tty_subsys_init:
    ; Вход:  Cold Boot, Ring 0, прерывания гарантированно заперты (DI).
    ; Задача: Полностью очистить и инициализировать все 4 независимых TTY-слота
    ;         внутри TTY_TABLE каноническими стартовыми значениями.
    
    LXI  H, TTY_TABLE               ; HL = Базовый физический адрес старта массива TTY
    MVI  B, MAX_TTY_DEVS            ; B = Счетчик внешнего цикла (4 консоли)

_tty_quad_alignment_loop:
    PUSH H                          ; Сохранили базовый адрес начала текущего 160-байтового слота

    ; --- ЭТАП 1: Полное предварительное зануление всего 160-байтового слота TTY ---
    MVI  C, TTY_STRUCT_SIZE         ; C = 160 байт
_tty_clear_row_loop:
    MVI  M, 00H
    INX  H
    DCR  C
    JNZ  _tty_clear_row_loop

    ; --- ЭТАП 2: Точечная инициализация POSIX атрибутов слота ---
    POP  H                          ; HL = Восстановили базовый адрес текущего TTY слота
    PUSH H                          ; Отложили на стек для перехода к следующей строке

    ; Прописываем O_TTY_C_IFLAG (+134) из дефолтных констант tty.inc
    LXI  D, O_TTY_C_IFLAG
    DAD  D                          ; HL -> Поле O_TTY_C_IFLAG (+134)
    MVI  M, DEF_C_IFLAG
    
    ; Прописываем O_TTY_C_OFLAG (+135) из дефолтных констант tty.inc
    INX  H                          ; HL -> Поле O_TTY_C_OFLAG (+135)
    MVI  M, DEF_C_OFLAG
    
    ; Прописываем O_TTY_C_LFLAG (+136) из дефолтных констант tty.inc
    INX  H                          ; HL -> Поле O_TTY_C_LFLAG (+136)
    MVI  M, DEF_C_LFLAG

    ; Инициализируем 8 байт массива управляющих символов O_TTY_C_CC (+137..+144)
    INX  H                          ; HL -> O_TTY_C_CC[VEOF] (+137)
    MVI  M, DEF_VEOF                ; Ctrl+D
    INX  H
    MVI  M, DEF_VEOL                ; \n
    INX  H
    MVI  M, DEF_VERASE              ; Backspace
    INX  H
    MVI  M, DEF_VKILL               ; Ctrl+U
    INX  H
    MVI  M, DEF_VINTR               ; Ctrl+C
    INX  H
    MVI  M, DEF_VQUIT               ; Ctrl+\
    INX  H
    MVI  M, DEF_VSTART              ; Ctrl+Q
    INX  H
    MVI  M, DEF_VSTOP               ; Ctrl+S

    ; Извлекаем чистую базу слота, чтобы исключить накопление ошибок INX H
    POP  H
    PUSH H
    LXI  D, O_TTY_WAIT_PID
    DAD  D                          ; HL -> Указывает точно на поле O_TTY_WAIT_PID (+145)
    MVI  M, UNBOUND_WAIT_PID        ; Зафиксировали 0xFF
    
    ; Явная инициализация группы переднего плана по строгому смещению структуры
    POP  H
    PUSH H
    LXI  D, O_TTY_PGRP
    DAD  D                          ; HL -> Указывает точно на поле O_TTY_PGRP (+147)
    MVI  M, UNBOUND_WAIT_PID        ; Зафиксировали 0xFF (Группа переднего плана отвязана)

    ; --- ЭТАП 3: Шаг к следующему TTY слоту таблицы ---
    POP  H                          ; HL = Базовый адрес текущего TTY слота
    LXI  D, TTY_STRUCT_SIZE         ; Сдвиг равен ровно 160 байтам (00A0h)
    DAD  D                          ; HL указывает строго на старт следующей TTY структуры
    
    DCR  B                          ; Уменьшаем счётчик сконфигурированных консолей
    JNZ  _tty_quad_alignment_loop   ; Повторяем цикл, пока не настроим все 4 терминала
    
    RET                             ; Возврат в k_power_on_init

; =============================================================================
; НАЗВАНИЕ: tty.asm (Диспетчер автоматической сборки гетерогенной карусели UART)
; =============================================================================
; =============================================================================
; ПРОЦЕДУРА: k_tty_carousel_auto_build
; Вход:  Cold Boot, Ring 0, прерывания гарантированно заперты (DI).
; Задача: Пошагово инициализировать слоты TTY0..TTY3, провести автодетект чипов
;         с приоритетом КР580ВВ51А, и зарегистрировать ISR через k_register_isr.
; Выход: Все РОН полностью сохранены. Прерывания заперты.
; =============================================================================
; =============================================================================
; НАЗВАНИЕ: tty.asm (Диспетчер последовательной сборки активных TTY-слотов)
; =============================================================================
; =============================================================================
; ПРОЦЕДУРА: k_tty_carousel_auto_build
; Вход:  Cold Boot, Ring 0, прерывания гарантированно заперты (DI).
;        Таблица TTY_TABLE предварительно занулена логическим k_tty_subsys_init.
; Задача: Перебрать все 4 физических порта, протестировать наличие железа
;         (ВВ51 приоритет, затем 16550) и заполнить TTY-слоты СТРОГО ПОСЛЕДОВАТЕЛЬНО.
; Выход: Все РОН полностью сохранены. Прерывания заперты.
; =============================================================================
k_tty_carousel_auto_build:
    PUSH B
    PUSH D
    PUSH H                          ; Спасли контекст супервизора Ring 0

    ; Инициализируем динамический указатель целевого логического TTY-слота
    LXI  D, TTY_TABLE               ; DE = Текущий заполняемый слот (на старте TTY0)

    ; --- ТЕСТИРОВАНИЕ АДРЕСА ПОРТА 0xA0 ---
    MVI  A, 0A0H
    CALL _probe_hardware_gate
    
    ; --- ТЕСТИРОВАНИЕ АДРЕСА ПОРТА 0xB0 ---
    MVI  A, 0B0H
    CALL _probe_hardware_gate
    
    ; --- ТЕСТИРОВАНИЕ АДРЕСА ПОРТА 0xC0 ---
    MVI  A, 0C0H
    CALL _probe_hardware_gate
    
    ; --- ТЕСТИРОВАНИЕ АДРЕСА ПОРТА 0xD0 ---
    MVI  A, 0D0H
    CALL _probe_hardware_gate

    POP  H
    POP  D
    POP  B                          ; Полное восстановление РОН
    RET

; =============================================================================
; ПОДПРОГРАММА: _probe_hardware_gate
; Вход:  DE = Физический адрес ТЕКУЩЕГО СВОБОДНОГО TTY-слота в ОЗУ ядра
;        A  = Проверяемый физический базовый порт (0xA0, 0xB0, 0xC0, 0xD0)
; Задача: Провести двухчиповый тест. Если железо найдено — заполнить слот DE,
;         зарегистрировать ISR и ДВИГАТЬ указатель DE на 160 байт вперед.
;         Если на порту пусто — оставить DE неизменным для следующего шага.
; Выход: DE = Обновленный (или сохраненный) указатель свободного ОЗУ-слота.
; =============================================================================
_probe_hardware_gate:
    PUSH B
    PUSH H                          ; Сохранили контекст
    PUSH PSW                        ; Спасли проверяемый порт на стек

    ; 1. Временно прописываем порт и дефолтные делители в текущий слот DE
    MOV  H, D
    MOV  L, E                       ; HL = Текущий свободный слот ОЗУ
    PUSH H                          ; Запомнили базу слота
    LXI  B, O_TTY_HW_BASE_PORT
    DAD  B                          ; HL -> O_TTY_HW_BASE_PORT (+155)
    MOV  M, A                       ; Записали тестируемый порт
    
    INX  H                          ; HL -> O_TTY_HW_DIV_LOW (+156)
    MVI  M, 00CH                    ; Дефолт speed = 9600 бод (делитель 12)
    INX  H                          ; HL -> O_TTY_HW_DIV_HIGH (+157)
    MVI  M, 00H
    POP  H                          ; HL = Восстановили базу слота

    ; 2. ПРИOРИТЕТНЫЙ ТЕСТ №1: Двухчиповый автоопрос КР580ВВ51А + КР580ВИ53
    ; Согласно контракту Варианта B, функция сохраняет пару DE неизменной!
    CALL hw_i8251_init              ; Вызов физического инициализатора с тестом ВИ53
    JNC  _gate_i8251_found          ; CY=0 -> Нашли стабильную связку ВВ51!

    ; 3. РЕЗЕРВНЫЙ ТЕСТ №2: Автоопрос чипа NS16550 (Если ВВ51 ответил ошибкой)
    CALL hw_ns16550_init            ; Вызов физического инициализатора 16550
    JNC  _gate_ns16550_found        ; CY=0 -> Нашли альтернативный 16550!

    ; 4. АППАРАТНАЯ ПУСТОТА: Ни одного чипа на этом адресе нет
    POP  PSW                        ; Очистили стек от сохраненного порта
    POP  H
    POP  B                          ; Восстановили РОН. DE ОСТАЛСЯ НА МЕСТЕ!
    RET                             ; Уходим к следующему порту, слот DE будет перезаписан

_gate_i8251_found:
    ; Связка КР580ВВ51А + КР580ВИ53 успешно подтверждена и заполнена в слоте DE!
    POP  PSW                        ; Восстановили базовый порт в аккумулятор А
    CALL _gate_calculate_irq        ; Вычислили номер IRQ-линии (А = 1..4)
    
    LXI  H, hw_i8251_isr            ; HL = Адрес индивидуального быстрого ISR драйвера
    CALL k_register_isr             ; Пропатчили CALL-вектор ВН59 и открыли линию! [1.3]
    JMP  _gate_slot_allocated       ; Слот успешно занят, уходим на сдвиг указателя

_gate_ns16550_found:
    ; Чип NS16550 успешно подтвержден и заполнен в текущем слоте DE!
    POP  PSW                        ; Восстановили базовый порт в аккумулятор А
    CALL _gate_calculate_irq        ; Вычислили номер IRQ-линии (А = 1..4)
    
    LXI  H, hw_ns16550_isr          ; HL = Адрес индивидуального быстрого ISR драйвера 16550
    CALL k_register_isr             ; Пропатчили CALL-вектор ВН59 и открыли линию! [1.3]

_gate_slot_allocated:
    ; === СДВИГ УКАЗАТЕЛЯ: ТЕКУЩИЙ СЛOТ ЗАНЯТ, ПЕРЕХOДИМ К СЛЕДУЮЩЕМУ ===
    ; Нам нужно прибавить к паре DE каноничный размер структуры 160 байт
    MOV  H, D
    MOV  L, E                       ; HL = Текущий занятый слот
    LXI  D, TTY_STRUCT_SIZE         ; DE = 160 (00A0H)
    DAD  D                          ; HL = HL + 160 (Адрес следующего чистого ОЗУ-слота)
    
    XCHG                            ; DE = Указатель на следующий СВОБОДНЫЙ слот ОЗУ!
    
    POP  H
    POP  B                          ; Восстановили контекст
    RET

; =============================================================================
; ПОДПРОГРАММА: _gate_calculate_irq
; Вход:  A = Базовый физический порт данных консоли (0xA0, 0xB0, 0xC0, 0xD0)
; Выход: A = Номер соответствующей аппаратной линии прерывания IRQ (1..4)
; =============================================================================
_gate_calculate_irq:
    CPI  0A0H
	JNZ _g_irq_b0
	MVI A, 1
	RET
_g_irq_b0:
    CPI  0B0H
	JNZ _g_irq_c0
	MVI A, 2
	RET
_g_irq_c0:
    CPI  0C0H
	JNZ _g_irq_d0
	MVI A, 3
	RET
_g_irq_d0:
    MVI  A, 4
	RET

; =============================================================================
; НАЗВАНИЕ: tty_core.asm (Интеграция TTY драйвера с коммутатором VFS)
; ТОКЕН:    TOKEN::MEGA-580_OS_v1.5.44_TTY_VFS_STUBS_VALIDATED_BUILD_20260705
; =============================================================================
k_tty_register_vfs:
    ; Вход:  Вызывается из основного стаба kernel.asm после k_tty_subsys_init.
    ; Задача: Зарегистрировать структуру 5 векторов функций TTY в глобальном коммутаторе VFS
    LXI  H, tty_vfs_methods     ; HL = Физический 16-битный адрес структуры векторов
    MVI  A, VFS_TYPE_TTY        ; A  = Канонический Major-номер TTY драйвера (3) из vfs.inc
    JMP  k_vfs_register_driver  ; Безусловный переход (Хвостовая оптимизация вместо CALL + RET)

; --- СТРУКТУРА 5 МЕТОДОВ ОПЕРАЦИЙ ДРАЙВЕРА TTY (10 БАЙТ) ---
tty_vfs_methods:
    DW   k_tty_open
    DW   k_tty_close
    DW   k_tty_read
    DW   k_tty_write
    DW   k_tty_ioctl

; --- СИСТЕМНЫЕ ЗАГЛУШКИ ФУНКЦИЙ ДРАЙВЕРА (МЕТОДЫ VFS) ---
k_tty_open:
    XRA  A                      ; Возвращаем 00h (Успешное открытие канала, CY = 0)
    RET

k_tty_close:
    XRA  A                      ; Возвращаем 00h (Успешное закрытие канала, CY = 0)
    RET

k_tty_read:
    MVI  A, ENOSYS              ; A = Канонический код ошибки 38 из vfs.inc
    STC                         ; АППАРАТНЫЙ ФИКС: Взводим флаг ошибки CY = 1 для ABI ядра
    RET

k_tty_write:
    MVI  A, ENOSYS              ; A = Канонический код ошибки 38 из vfs.inc
    STC                         ; АППАРАТНЫЙ ФИКС: Взводим флаг ошибки CY = 1 для ABI ядра
    RET

k_tty_ioctl:
    MVI  A, ENOSYS              ; A = Канонический код ошибки 38 из vfs.inc
    STC                         ; АППАРАТНЫЙ ФИКС: Взводим флаг ошибки CY = 1 для ABI ядра
    RET

; =============================================================================
; НАЗВАНИЕ: tty_core.asm (Расчёт физического адреса слота терминала)
; =============================================================================
k_tty_get_struct_address:
    ; Вход:  A  = Номер запрашиваемой TTY-консоли (0..3)
    ;        Прерывания заперты (DI), стек в Ring 0.
    ; Задача: Проверить аппаратные границы номера (0..MAX_TTY_DEVS-1).
    ;         Вычислить физический адрес слота по формуле: TTY_TABLE + (A * 160).
    ; Выход: В случае успеха:
    ;           Флаг переноса CY = 0 (Успех)
    ;           Пара HL = Точный 16-битный физический адрес слота в TTY_TABLE.
    ;        В случае ошибки (Нелегальный минор / номер консоли):
    ;           Флаг переноса CY = 1 (Провал)
    ;           Пара HL = 0000H
    ; ВНИМАНИЕ: Все РОН супервизора (кроме HL) полностью сохраняются!
    PUSH B
    PUSH D                          ; Сохранили контекст планировщика

    ; === ШАГ 1: ВАЛИДАЦИЯ ГРАНИЦ НOМЕРА УСТРOЙСТВА ===
    CPI  MAX_TTY_DEVS               ; Сравниваем с лимитом (A - 4)
    JNC  _tty_get_addr_err          ; Если A >= 4 — нелегальный минор, уходим на ошибку

    ; === ШАГ 2: БЫСТРOЕ СДВИГOВOЕ УМНOЖЕНИЕ НА 160 (128 + 32) ===
    MOV  L, A
    MVI  H, 0                       ; HL = Номер консоли (0..3)

    ; Сдвигаем HL влево на 5 бит (получаем умножение на 32)
    DAD  H                          ; *2
    DAD  H                          ; *4
    DAD  H                          ; *8
    DAD  H                          ; *16
    DAD  H                          ; HL = Номер * 32
    
    PUSH H                          ; Спрятали промежуточное значение (A * 32) на стек ядра
    
    ; Продолжаем сдвиг ещё на 2 бита, чтобы дойти до умножения на 128 (5 + 2 = 7 сдвигов)
    DAD  H                          ; *64
    DAD  H                          ; HL = Номер * 128
    
    POP  D                          ; DE = Номер * 32
    DAD  D                          ; HL = (Номер * 128) + (Номер * 32) = Номер * 160!

    ; === ШАГ 3: ПРИВЯЗКА К БАЗЕ МАССИВА ТЕРМИНАЛOВ ===
    LXI  D, TTY_TABLE
    DAD  D                          ; HL = Физический адрес целевого слота терминала
    
    ANA  A                          ; АППАРАТНЫЙ СБРОС: Сбрасываем флаг ошибки CY = 0 (УСПЕХ)
    JMP  _tty_get_addr_exit         ; На выход

_tty_get_addr_err:
    LXI  H, 0000H                   ; HL = 0000H (Null pointer)
    STC                             ; АППАРАТНЫЙ ВЗВОД: Взводим флаг ошибки CY = 1 (POSIX ENODEV)

_tty_get_addr_exit:
    POP  D
    POP  B                          ; Восстановили РОН супервизора в исходное состояние
    RET

; =============================================================================
; НАЗВАНИЕ: tty_core.asm (Низкоуровневый посимвольный вывод в TTY с HAL абстракцией)
; =============================================================================
k_tty_tx_char:
    ; Вход:  A  = ASCII-символ для вывода
    ;        HL = Адрес 160-байтовой структуры TTY-слота
    ;        Прерывания заперты (DI), стек в Ring 0.
    ; Выход: CY = 0 (Успех), РОН супервизора полностью сохранены.
    PUSH B
    PUSH D
    PUSH H                          ; Сохранили контекст супервизора

    MOV  C, A                       ; C = выводимый символ
    
    ; === ШАГ 1: ПРОВЕРКА ПУСТОТЫ TX-БУФЕРА И ГОТОВНОСТИ UART ===
    PUSH H
    LXI  D, O_TTY_TX_COUNT
    DAD  D                          ; HL -> O_TTY_TX_COUNT (+69)
    MOV  A, M
    ORA  A                          ; TX_COUNT == 0?
    POP  H
    JNZ  _tty_tx_push_to_ring       ; Буфер не пуст — строго в очередь

    ; --- ИНКРЕМЕНТ HAL: КОСВЕННЫЙ ОПРОС АППАРАТНОГО СТАТУСА ЧИПА ---
    PUSH H                          ; Сохранили базу TTY-слота
    LXI  D, O_TTY_HW_POLL_STAT
    DAD  D                          ; HL указывает строго на вектор функции (+153)
    MOV  E, M
    INX  H
    MOV  D, M                       ; DE = Физический адрес hw_poll_status драйвера
    POP  H                          ; HL = Восстановили базу TTY-слота
    
    ; Имитируем косвенный CALL на аппаратную процедуру драйвера
    PUSH H                          ; Сохраняем базу на время исполнения драйвера
    LXI  H, _tty_hal_tx_stat_ret    ; Записали адрес возврата на стек
    PUSH H
    XCHG                            ; HL = Адрес функции драйвера, DE = База слота
    PCHL                            ; Прыжок в драйвер! (Вход: DE = База TTY)

_tty_hal_tx_stat_ret:
    POP  H                          ; HL = Восстановили базу TTY-слота после вызова
    ANI  TTY_HAL_TXRDY              ; Драйвер обязан вернуть статус HAL в регистре А
    JZ   _tty_tx_push_to_ring       ; Передатчик чипа занят — уходим в кольцо ОЗУ
    
    ; --- ИНКРЕМЕНТ HAL: КОСВЕННЫЙ ВЫСТРЕЛ СИМВОЛА В ПОРТ ---
    PUSH H                          ; Сохранили базу TTY-слота
    LXI  D, O_TTY_HW_PUT_CHAR
    DAD  D                          ; HL указывает строго на вектор функции (+149)
    MOV  E, M
    INX  H
    MOV  D, M                       ; DE = Физический адрес hw_put_char драйвера
    POP  H                          ; HL = Восстановили базу TTY-слота
    
    ; Подготовка параметров для вызова hw_put_char
    MOV  A, C                       ; Регистр А = выводимый ASCII-символ
    PUSH H                          ; Сохраняем базу на время исполнения драйвера
    LXI  H, _tty_hal_tx_put_ret     ; Записали адрес возврата на стек
    PUSH H
    XCHG                            ; HL = Адрес функции драйвера, DE = База слота
    PCHL                            ; Прыжок в драйвер! (Вход: А = Символ, DE = База TTY)

_tty_hal_tx_put_ret:
    POP  H                          ; HL = Восстановили базу TTY-слота
    JMP  _tty_tx_char_success       ; Символ успешно вытолкнут в порт, выходим!

_tty_tx_push_to_ring:
    ; === ШАГ 2: ПРОВЕРКА ПЕРЕПОЛНЕНИЯ TX КОЛЬЦА ===
    PUSH H
    LXI  D, O_TTY_TX_COUNT
    DAD  D
    MOV  A, M
    CPI  TTY_BUF_SIZE               ; Сверяем с лимитом 64 байта
    POP  H
    JZ   _tty_tx_char_success       ; Буфер полон — пассивный drop

    ; === ШАГ 3: ЧТЕНИЕ ТЕКУЩЕГО УКАЗАТЕЛЯ HEAD ===
    PUSH H
    LXI  D, O_TTY_TX_HEAD
    DAD  D                          ; HL -> O_TTY_TX_HEAD (+67)
    MOV  E, M                       ; E = текущий срез HEAD (0..63)
    
    ; === ШАГ 4: ИНКРЕМЕНТ HEAD С АППАРАТНЫМ ЗАЦИКЛИВАНИЕМ ===
    INR  M                          ; HEAD++
    MOV  A, M
    CPI  TTY_BUF_SIZE
    JC   _tty_tx_no_wrap_head
    MVI  M, 00H                     ; HEAD = 0 (зацикливание кольца вывода)
    
_tty_tx_no_wrap_head:
    ; === ШАГ 5: ЗАПИСЬ СИМВОЛА В БУФЕР ОЗУ ЯДРА ===
    POP  H                          ; HL = Базовый адрес TTY-слота
    PUSH H
    LXI  D, O_TTY_TX_BUFFER
    DAD  D                          ; HL -> TTY_TX_BUFFER (+70)
    MVI  D, 00H                     ; Каноничная команда зануления старшей пары для 8080!
    DAD  D                          ; HL = TTY_TX_BUFFER + HEAD_old
    MOV  M, C                       ; АТОМАРНАЯ ЗАПИСЬ: Символ зафиксирован в ОЗУ Ring 0

    ; === ШАГ 6: ИНКРЕМЕНТ СЧЕТЧИКА TX_COUNT ===
    POP  H
    PUSH H
    LXI  D, O_TTY_TX_COUNT
    DAD  D
    INR  M                          ; TX_COUNT++ 
    POP  H                          ; Сбалансировали стек локального снапшота базы

_tty_tx_char_success:
    ANA  A                          ; Сброс флага ошибки: CY = 0 (УСПЕХ)
    POP  H
    POP  D
    POP  B                          ; Восстановили РОН супервизора
    RET

; =============================================================================
; НАЗВАНИЕ: tty_core.asm (Низкоуровневый посимвольный ввод из TTY)
; =============================================================================
k_tty_rx_char:
    ; Вход:  HL = Адрес 160-байтовой структуры TTY-слота
    ;        Прерывания гарантированно заперты вызывающим кодом (DI), стек в Ring 0.
    ; Выход: CY = 0 — Успех, регистр A = ASCII-символ из буфера.
    ;        CY = 1 — Буфер пуст, данных для чтения нет (EAGAIN / EWOULDBLOCK).
    ;        РОН супервизора (BC, DE, HL) полностью сохранены.
    PUSH B
    PUSH D
    PUSH H                          ; Сохранили контекст вызова

    ; === ШАГ 1: ПРОВЕРКА НАЛИЧИЯ ДАННЫХ ===
    PUSH H
    LXI  D, O_TTY_RX_COUNT
    DAD  D                          ; HL -> O_TTY_RX_COUNT
    MOV  A, M
    ORA  A                          ; RX_COUNT == 0?
    POP  H
    JZ   _tty_rx_char_empty         ; Буфер пуст — выходим с флагом переноса

    ; === ШАГ 2: ИЗВЛЕЧЕНИЕ СИМВОЛА ПО УКАЗАТЕЛЮ TAIL ===
    PUSH H                          ; Сохранили базу HL
    LXI  D, O_TTY_RX_TAIL
    DAD  D                          ; HL -> O_TTY_RX_TAIL
    MOV  E, M                       ; E = Текущий индекс TAIL (0..63)
    
    ; Инкремент и цикление TAIL
    INR  M                          ; TAIL++
    MOV  A, M
    CPI  TTY_BUF_SIZE
    JC   _tty_rx_char_no_wrap_tail
    MVI  M, 00H                     ; TAIL = 0

_tty_rx_char_no_wrap_tail:
    POP  H                          ; HL = База
    PUSH H                          ; Сохранили базу
    LXI  D, O_TTY_RX_BUFFER
    DAD  D                          ; HL -> O_TTY_RX_BUFFER
    MVI  D, 00H                     ; Зануление D
    DAD  D                          ; HL = O_TTY_RX_BUFFER + TAIL_old
    MOV  C, M                       ; C = Прочитанный из кольца символ

    ; === ШАГ 3: ДЕКРЕМЕНТ СЧЕТЧИКА RX_COUNT ===
    POP  H                          ; HL = База
    PUSH H                          ; Сохранили базу
    LXI  D, O_TTY_RX_COUNT
    DAD  D                          ; HL -> O_TTY_RX_COUNT
    DCR  M                          ; RX_COUNT--

    ; === ШАГ 4: ФОРМИРОВАНИЕ УСПЕШНОГО ВЫХОДА ===
    POP  H                          ; Полностью очистили локальный стек до оригинального кадра
    POP  H                          ; Достали сохраненный на входе HL супервизора
    POP  D                          ; Достали оригинальный DE
    POP  B                          ; Достали оригинальный BC
    
    MOV  A, C                       ; Результат помещен в регистр A
    ANA  A                          ; Сброс флага переноса: CY = 0 (УСПЕХ)
    RET

_tty_rx_char_empty:
    POP  H
    POP  D
    POP  B                          ; Восстановили исходный контекст супервизора
    STC                             ; Установка флага переноса: CY = 1 (НЕТ ДАННЫХ)
    RET

