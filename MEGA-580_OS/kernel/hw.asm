;
;[ОЗУ Ring 0 Ядра]
;0x0040 ┌──────────────────────────────────────┐
;       │ ВЕДУЩИЙ (Master) КР580ВН59           │
;       │ IRQ0 .. IRQ7                         │ 24 байта (8 слотов × 3 байта)
;0x0057 └──────────────────────────────────────┘
;0x0058 ┌──────────────────────────────────────┐
;       │ ВЕДОМЫЙ (Slave) КР580ВН59 на IRQ6    │
;       │ IRQ8 .. IRQ15                        │ 24 байта (8 слотов × 3 байта)
;0x006F └──────────────────────────────────────┘
;
; =============================================================================
; НАЗВАНИЕ: hw_init.asm (Инициализация КР580ВН59 с полной предмаскировкой линий)
; =============================================================================
; =============================================================================
; НАЗВАНИЕ: hw_init.asm (Инициализация КР580ВН59 с двухфазным автодетектом)
; ТОКЕН:    TOKEN::MEGA-580_OS_v1.5.68_HW_INT_INIT_PROBING_STABLE_20260708
; Вход:  Cold Boot, Ring 0, прерывания гарантированно заперты (DI).
; Задача: Проверить исправность ВН59, инициализировать без ICW4, выставить
;         базу векторов 0x0040, намертво замаскировать линии и забить заглушками.
; Выход: CY = 0 — Контроллер прерываний успешно обнаружен и инициализирован.
;        CY = 1 — Контроллер отсутствует или неисправен (Авария БИС).
;        Все РОН (BC, DE, HL) полностью сохранены согласно контракту HAL.
; =============================================================================
hw_int_init:
    PUSH B
    PUSH D
    PUSH H                          ; Сохранили контекст супервизора Ring 0

    ; === ЭТАП 1: ДВУХФАЗНЫЙ АППАРАТНЫЙ ТЕСТ РЕГИСТРА IMR ===
    ; --- Фаза 1.1: Проверка паттерна 0x5A ---
    MVI  A, 05AH                    ; Тестовая маска 1 (01011010b)
    OUT  MASTER_8259_DATA           ; Попытка записи в регистр масок ВН59
    NOP \ NOP                       ; Стабилизация шины данных eMMU РЕВ_2.0
    IN   MASTER_8259_DATA           ; Считали обратно
    CPI  05AH                       ; Совпало?
    JNZ  _hw_int_probe_fail         ; Нет — чип не отвечает!

    ; --- Фаза 1.2: Проверка инверсного паттерна 0xA5 (Защита от залипания шины) ---
    MVI  A, 0A5H                    ; Тестовая маска 2 (10100101b)
    OUT  MASTER_8259_DATA           ; Отправили в IMR
    NOP \ NOP
    IN   MASTER_8259_DATA           ; Считали обратно
    CPI  0A5H                       ; Совпало?
    JNZ  _hw_int_probe_fail         ; Нет — аппаратный сбой БИС!

    ; === ЭТАП 2: ПАСПОРТНАЯ ПРОШИВКА КР580ВН59 ===
    ; ICW1: Работа по фронту (Edge), одиночный режим (SNGL=1), без ICW4 (IC4=0) -> 012H
    MVI  A, 012H             
    OUT  MASTER_8259_CMD
    
    ; ICW2: Базовое смещение векторов в памяти = 0x0040
    MVI  A, SYS_ISR_TABLE_BASE      ; 040H
    OUT  MASTER_8259_DATA
    
    ; === ЭТАП 3: НАМЕРТВО МАСКИРУЕМ ВСЕ ЛИНИИ ПОСЛЕ УСПЕШНОГО ТЕСТА ===
    ; OCW1: Записываем 11111111B = 0FFH. Контроллер аппаратно закрыт до 
    ; вызова k_register_isr конкретными драйверами периферии.
    MVI  A, 0FFH
    OUT  MASTER_8259_DATA

    ; === ЭТАП 4: СТАТИЧЕСКАЯ ЗАБИВКА ТАБЛИЦЫ С ШАГОМ 3 БАЙТА ===
    LXI  H, SYS_ISR_TABLE_BASE      ; HL = 0x0040
    MVI  B, SYS_MAX_IRQS            ; Счетчик цикла = 8 аппаратных слотов

_hw_int_fill_loop:
    MVI  M, OPCODE_CALL             ; Прописали [0xCD] (CALL)
    INX  H
    
    LXI  D, hw_stub                 ; DE = Физический адрес безопасной заглушки ядра
    MOV  M, E                       ; Прописали младший байт адреса hw_stub
    INX  H
    MOV  M, D                       ; Прописали старший байт адреса hw_stub
    INX  H                          ; Фиксированный шаг: HL сместился на +3 байта!
    
    DCR  B
    JNZ  _hw_int_fill_loop

    POP  H
    POP  D
    POP  B                          ; Полное восстановление оригинального контекста РОН
    ANA  A                          ; СБРОС ФЛАГА ОШИБКИ: CY = 0 (Контроллер прерываний ОК)
    RET

_hw_int_probe_fail:
    POP  H
    POP  D
    POP  B                          ; Полное восстановление оригинального контекста РОН
    STC                             ; ВЗВОД ФЛАГА ОШИБКИ: CY = 1 (Аппаратный тупик: ВН59 неисправен!)
    RET

; =============================================================================
; ПРОЦЕДУРА: hw_sys_init
; Вход:  Cold Boot, Ring 0, прерывания гарантированно заперты (DI).
; Задача: Проверить наличие БИС ВИ53 №1. Если чип исправен — настроить Канал 0
;         в Режим 3 с делителем 20 000 для генерации тиков IRQ0 каждые 20 мс.
; Выход: CY = 0 — Системный таймер успешно обнаружен, протестирован и запущен.
;        CY = 1 — Авария: БИС ВИ53 №1 отсутствует или повреждена на шине.
;        Все РОН полностью сохранены согласно контракту HAL Варианта B.
; =============================================================================
hw_sys_init:
    PUSH B
    PUSH D
    PUSH H

    ; === ЭТАП 1: АВТОДЕТЕКТ ЧЕРЕЗ РЕЖИМ 0 ===
    ; Программируем Канал 0 в Режим 0
    MVI  A, PIT_CW_CH0_M0           ; 030H
    OUT  SYS_PIT_CMD

    ; Записываем максимальное значение 0xFFFF
    MVI  A, 0FFH
    OUT  SYS_PIT_CH0
    OUT  SYS_PIT_CH0                ; LSB = MSB = 0xFF

    ; Немедленно выдаем Latch Counter (минимум задержки!)
    MVI  A, PIT_CW_LATCH_CH0
    OUT  SYS_PIT_CMD

    ; Считываем значение
    IN   SYS_PIT_CH0
    MOV  B, A                       ; B = LSB
    IN   SYS_PIT_CH0
    MOV  C, A                       ; C = MSB

    ; Проверяем: значение должно быть близко к 0xFFFF
    ; (возможно, 0xFFFE или 0xFFFD из-за задержки)
    MOV  A, C
    CPI  0FFH
    JNZ  _hw_sys_pit_fail           ; MSB не 0xFF — чип отсутствует!

    MOV  A, B
    CPI  0FEH                       ; LSB может быть 0xFF, 0xFE или 0xFD
    JC   _hw_sys_pit_fail           ; Если LSB < 0xFE — слишком далеко от 0xFFFF

    ; === ЭТАП 2: РАБОЧЕЕ ПРОГРАММИРОВАНИЕ НА КВАНТ 20 МС (50 ГЦ) ===
    ; Канал 0, Чтение/Запись LSB/MSB, Режим 3 (Генератор меандра)
    MVI  A, PIT_CW_CH0_M3           ; 036H
    OUT  SYS_PIT_CMD
    
    ; Прошиваем канонический делитель 20 000 (4E20H) строго по ТУ: LSB -> MSB
    MVI  A, 020H                    ; Младший байт делителя (0x20)
    OUT  SYS_PIT_CH0
    MVI  A, 04EH                    ; Старший байт делителя (0x4E)
    OUT  SYS_PIT_CH0

    POP  H
    POP  D
    POP  B                          ; Полное восстановление оригинального контекста РОН
    ANA  A                          ; СБРОС ФЛАГА ОШИБКИ: CY = 0 (Таймер успешно запущен)
    RET

_hw_sys_pit_fail:
    POP  H
    POP  D
    POP  B
    STC                             ; CY = 1 (провал)
    RET

; =============================================================================
; УНИВЕРСАЛЬНАЯ АППАРАТНАЯ ЗАГЛУШКА ЯДРА (СЛОТЫ IRQ0 - IRQ7 И ЛОЖНЫЕ ТИКИ)
; =============================================================================
hw_stub:
    ; Вход: Сюда процессор прыгает при срабатывании пустых IRQ,
    ;       Либо при перенаправлении ложного (Spurious IRQ) тика IRQ7 из Violation.
    ; Внимание: Если мы пришли сюда по ложному тику с адреса 0x007C (после JZ),
    ; регистры и стек Ring 3 находятся в идеальной чистоте, PUSH-данных нет!

    PUSH PSW                        ; Сохраняем аккумулятор на время отправки EOI
    
    ; Проверим, как именно мы сюда попали: если ISR пуст — это ложный тик
    MVI  A, 00BH
    OUT  MASTER_8259_CMD
    IN   MASTER_8259_CMD            ; Читаем актуальное состояние регистра ISR
    CPI  00H
    JZ   hw_stub_exit               ; Если ISR полностью пуст — это ложный тик, EOI не шлем

    ; Это легитимное пустое прерывание от линий IRQ0-IRQ7 — шлем неспецифический EOI
    MVI  A, 020H
    OUT  MASTER_8259_CMD

hw_stub_exit:
    POP  PSW                        ; Восстановили аккумулятор
    EI                              ; Снова разрешаем аппаратную карусель прерываний в Ring 3
    RET                             ; Возврат к прерванной кодовой строке пользователя
