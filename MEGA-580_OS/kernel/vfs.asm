; =============================================================================
; /kernel/vfs.asm - ИСПОЛНЯЕМЫЙ КОД ПОДСИС ТЕМЫ ВИРТУАЛЬНОЙ ФАЙЛОВОЙ СИСТЕМЫ
; =============================================================================
; MEGA-580 OS - МОДУЛЬ VFS: ИСПОЛНЯЕМЫЙ КОД ЦЕНТРАЛЬНЫХ ДИСПЕТЧЕРОВ
; =============================================================================
; -----------------------------------------------------------------------------
; ПОДПРОГРАММА ИНИЦИАЛИЗАЦИИ ТАБЛИЦЫ ПЕРЕКЛЮЧАТЕЛЕЙ VFS
; -----------------------------------------------------------------------------
k_vfs_subsys_init:
    ; --- ЭТАП 1: ПОЛНОЕ ЗАНУЛЕНИЕ ГЛОБАЛЬНОЙ ТАБЛИЦЫ ФАЙЛОВ (1280 БАЙТ) ---
    LXI  H, SYS_FILE_TABLE
    LXI  D, MAX_SYS_FILES * SYS_FILE_SLOT_SIZE
    XRA  A

_vfs_clear_ft_loop:
    MOV  M, A
    INX  H
    DCX  D
    MOV  A, D
    ORA  E
    JZ   _vfs_clear_ft_done
    XRA  A
    JMP  _vfs_clear_ft_loop
_vfs_clear_ft_done:

    ; --- ЭТАП 2: ЗАНУЛЕНИЕ ТАБЛИЦЫ КОММУТАТОРА ДРАЙВЕРОВ (320 БАЙТ) ---
    LXI  H, VFS_SWITCH_TABLE
    LXI  D, VFS_MAX_TYPES * VFS_ROW_SIZE
    XRA  A

_vfs_clear_sw_loop:
    MOV  M, A
    INX  H
    DCX  D
    MOV  A, D
    ORA  E
    JZ   _vfs_clear_sw_done
    XRA  A
    JMP  _vfs_clear_sw_loop
_vfs_clear_sw_done:

    ; --- ЭТАП 3: ОЧИСТКА СЛУЖЕБНЫХ ЯЧЕЕК, ISR И K_TMP_* (30 БАЙТ) ---
    ; 9 байт скаляров + 16 байт ISR + 5 байт K_TMP_* = 30 байт
    LXI  H, VFS_DISPATCH_MAJOR  
    MVI  B, 9 + 16 + 5          ; ИСПРАВЛЕНО: 30 вместо 28
    XRA  A                      

_vfs_clear_sys_vars_loop:
    MOV  M, A                   
    INX  H
    DCR  B                      
    JNZ  _vfs_clear_sys_vars_loop 

    ; --- ЭТАП 4: ЗАНУЛЕНИЕ ТАБЛИЦЫ МОНТИРОВАНИЯ УЗЛОВ (96 БАЙТ) ---
    LXI  H, DEV_MOUNT_TABLE
    MVI  B, MAX_MOUNT_DEVS * MNT_ENTRY_SZ 
    XRA  A                      

_vfs_clear_mnt_loop:
    MOV  M, A                   
    INX  H
    DCR  B                      
    JNZ  _vfs_clear_mnt_loop    
    
    RET                         ; Возврат в глобальную точку инициализации

; -----------------------------------------------------------------------------
; СИСТЕМНЫЙ ШЛЮЗ МОДУЛЬНОЙ РЕГИСТРАЦИИ ДРАЙВЕРОВ (Ring 0 CALL)
; -----------------------------------------------------------------------------
; Вход:  A  = Утвержденный Major Number драйвера (1..31).
;        HL = Адрес структуры 5 методов драйвера (10 байт в памяти модуля).
; -----------------------------------------------------------------------------
k_vfs_register_driver:
    ; Вход:  A  = Major-номер регистрируемого драйвера (1..31)
    ;        HL = Физический 16-битный адрес структуры 5 векторов функций драйвера
    ;        Прерывания заперты (DI), стек в Ring 0.
    PUSH PSW
    PUSH B
    PUSH D
    PUSH H                          ; Сохранили полный контекст супервизора

    ; === ШАГ 1: АППАРАТНАЯ ВАЛИДАЦИЯ ГРАНИЦ MAJOR (1..31) ===
    CPI  1                          
    JC   _vfs_reg_err_inval         ; Если A == 0 — ошибка EINVAL
    
    CPI  VFS_MAX_TYPES              
    JNC  _vfs_reg_err_inval         ; Если A >= 32 — ошибка EINVAL

    ; === ШАГ 2: БЕЗОПАСНЫЙ РАСЧЕТ АДРЕСА СТРОКИ КОММУТАТОРА ===
    MOV  B, H
    MOV  C, L                       ; BC = Истинный адрес структуры методов драйвера

    ; Вычисляем смещение: HL = Major * 10
    MOV  L, A
    MVI  H, 0                       ; HL = Major
    DAD  H                          ; HL = Major * 2
    PUSH H                          ; Спрятали Major * 2 на вершину стека ядра
    DAD  H                          ; HL = Major * 4
    DAD  H                          ; HL = Major * 8
    POP  D                          ; DE = Major * 2
    DAD  D                          ; HL = (Major * 8) + (Major * 2) = Major * 10

    LXI  D, VFS_SWITCH_TABLE
    DAD  D                          ; HL = Физический адрес целевой строки в коммутаторе
    
    XCHG                            ; DE = Адрес строки коммутатора (Куда писать)
    MOV  H, B
    MOV  L, C                       ; HL = Адрес структуры методов драйвера (Откуда читать)

    ; === ШАГ 3: БЫСТРОЕ ТАБЛИЧНОЕ КОПИРОВАНИЕ 10 БАЙТ ВЕКТOРОВ ===
    MVI  B, VFS_ROW_SIZE            ; Счетчик копирования = 10 байт

_vfs_register_copy_loop:
    MOV  A, M                       
    STAX D                          
    INX  H                          
    INX  D                          
    DCR  B                          
    JNZ  _vfs_register_copy_loop    

    POP  H
    POP  D
    POP  B
    POP  PSW                        ; Восстановили все РОН в исходное состояние
    RET                             ; УСПЕХ: Флаги и РОН не затерты ошибками

_vfs_reg_err_inval:
    POP  H
    POP  D
    POP  B
    POP  PSW                        ; Сняли базовый сохраненный контекст
    MVI  A, EINVAL                  ; ЖЕСТКИЙ ПОСИКС-ФИКС: Возвращаем канонический код 22 (0x16)
    RET

; === Системная заглушка нереализованных методов VFS ===
k_vfs_enotsupp:
    MVI  A, ENODEV                  ; ЖЕСТКИЙ ПОСИКС-ФИКС: Возвращаем канонический код 19 (0x13)
    RET

; =============================================================================
; НАЗВАНИЕ: vfs.asm (Выделение свободного глобального дескриптора)
; =============================================================================
k_vfs_malloc_file:
    ; Вход:  A = Major-номер драйвера для установки в F_TYPE (1..31)
    ;        Прерывания гарантированно заперты (DI), стек в Ring 0.
    ; Задача: Найти первый свободный слот в глобальной SYS_FILE_TABLE (F_TYPE == 0).
    ;         Инициализировать счетчик ссылок F_REF = 1, сбросить остальные поля.
    ; Выход: В случае успеха:
    ;           Флаг переноса CY = 0 (Успех)
    ;           Пара HL = Точный 16-битный физический адрес выделенного слота в ядре.
    ;        В случае ошибки (Таблица файлов переполнена, лимит POSIX ENFILE):
    ;           Флаг переноса CY = 1 (Провал)
    ;           Пара HL = 0000H
    PUSH B
    PUSH D                          ; Сохраняем РОН супервизора
    
    PUSH PSW                        ; КРИТИЧЕСКИЙ ФИКС: Спрятали исходный Major-номер (регистр A) 
                                    ; на вершину стека ядра от затирания в цикле!

    LXI  H, SYS_FILE_TABLE          ; HL = Стартовый адрес глобальной таблицы файлов
    MVI  B, MAX_SYS_FILES           ; B = Счетчик цикла (128 глобальных файлов)

_vfs_malloc_scan_loop:
    MOV  A, M                       ; A = Значение поля F_TYPE (+0) текущего слота
    CPI  VFS_TYPE_FREE              ; Проверяем, свободен ли слот (00h)?
    JZ   _vfs_malloc_slot_found     ; Слот найден! Переходим к инициализации

    ; Слот занят. Сдвигаем HL на следующую строку таблицы (SYS_FILE_SLOT_SIZE = 10)
    LXI  D, SYS_FILE_SLOT_SIZE
    DAD  D                          ; HL переместился на начало следующего слота
    
    DCR  B                          ; Уменьшаем счетчик проходов
    JNZ  _vfs_malloc_scan_loop      ; Повторяем для всех 128 дескрипторов

    ; --- ОШИБКА: ТАБЛИЦА ФАЙЛОВ ПЕРЕПОЛНЕНА (ENFILE) ---
    POP  PSW                        ; Сняли со стека неиспользованный Major-номер (баланс стека)
    LXI  H, 0000H                   ; Обнуляем HL в знак ошибки
    STC                             ; Устанавливаем флаг провала CY = 1
    JMP  _vfs_malloc_exit           ; Уходим на выход

_vfs_malloc_slot_found:
    ; --- ИНИЦИАЛИЗАЦИЯ НАЙДЕННОГО СЛОТА ---
    ; В HL сейчас лежит точный базовый адрес найденной 10-байтовой строки.
    
    ; Защита: полностью зануляем внутренности слота перед возвратом
    PUSH H                          ; Сохранили базовый адрес для возврата
    MVI  C, SYS_FILE_SLOT_SIZE      ; Счетчик зануления = 10 байт
_vfs_malloc_clear_slot:
    MVI  M, 00H                     ; Пишем 00h по всей длине слота
    INX  H
    DCR  C
    JNZ  _vfs_malloc_clear_slot
    
    POP  H                          ; HL = Восстановили базовый адрес строки (+0)
    
    POP  PSW                        ; ИСПРАВЛЕНИЕ БАГА: Извлекли из стека наш оригинальный 
                                    ; Major-номер обратно в Аккумулятор A!

    ; Устанавливаем F_TYPE = A (Истинный Major-номер)
    MOV  M, A                       ; Теперь сюда пишется корректный номер драйвера (1..31)
    
    ; Устанавливаем канонический счетчик ссылок: F_REF (+2) = 1
    INX  H
    INX  H                          ; HL указывает строго на поле F_REF (+2)
    MVI  M, 001H                    ; Выставляем 1 активную ссылку дескриптора (v1.4.7)
    
    ; Возвращаем HL на базовый адрес строки (+0) для корректного ABI ядра
    DCX  H
    DCX  H                          ; HL -> Снова базовая точка выделенного слота
    
    ANA  A                          ; СБРОС ФЛАГА ПЕРЕНОСА: Аппаратно выставляем CY = 0 (Успех)

_vfs_malloc_exit:
    POP  D
    POP  B                          ; Восстановили РОН супервизора
    RET                             ; Сквозной возврат в вызывающий сисколл (sys_open)

; =============================================================================
; РАСЧЕТ АДРЕСА СТРУКТУРЫ ОТКРЫТОГО ФАЙЛА
; =============================================================================
; Вход:  D = Локальный дескриптор пользователя FD (0..15).
; Выход: HL = Абсолютный физический адрес 16-байтовой структуры файла в ядре.
; =============================================================================
k_vfs_get_file_struct_address:
    ; Вход:  D  = Локальный файловый дескриптор процесса Ring 3 (FD 0..15)
    ;        Прерывания заперты (DI), стек в Ring 0.
    ; Выход: В случае успеха:
    ;           Флаг переноса CY = 0
    ;           Пара HL = Точный 16-битный адрес 10-байтового слота в SYS_FILE_TABLE
    ;        В случае ошибки (EBADF - дескриптор закрыт или нелегален):
    ;           Флаг переноса CY = 1
    ;           Пара HL = 0000H
    ; ВНИМАНИЕ: Все РОН пользователя (включая входящий D) полностью сохраняются!
    PUSH B
    PUSH D                          ; КРИТИЧЕСКИЙ ФИКС: Спрятали DE пользователя в стек ядра

    LHLD CURRENT_PCB_PTR            ; HL = Физический базовый адрес активного PCB
    
    ; Вычисляем адрес ячейки локального дескриптора внутри структуры PCB
    MOV  A, D                       ; A = Входящий FD из регистра D
    ANI  00FH                       ; Жесткая маска защиты границ (FD строго 0..15)
    ADI  O_FILE_TABLE               ; A = Внутреннее смещение до нужного FD в PCB
    
    ; Переносим смещение в пару BC, чтобы не трогать сохраненный в стеке DE
    MOV  C, A
    MVI  B, 0                       ; BC = 16-битное смещение внутри PCB
    DAD  B                          ; HL -> Указывает точно на ячейку локального FD в PCB

    MOV  A, M                       ; A = Номер глобального системного дескриптора (0..127)
    CPI  RES_FREE_MARKER            ; Проверяем, не равен ли он 0xFF (Свободный слот)?
    JZ   _vfs_get_struct_err        ; Если 0xFF — дескриптор не открыт, уходим на ошибку

    ; === ШАГ 2: ВЫЧИСЛЕНИЕ АДРЕСА СТРОКИ В SYS_FILE_TABLE ===
    MOV  L, A
    MVI  H, 0                       ; HL = Чистый глобальный индекс файла (0..127)
    
    ; Математическое быстрое умножение индекса на каноничные 10 байт:
    ; Формула: HL * 10 = (HL * 8) + (HL * 2)
    DAD  H                          ; HL = Индекс * 2
    PUSH H                          ; Временно сохранили Индекс * 2 в стек ядра
    DAD  H                          ; HL = Индекс * 4
    DAD  H                          ; HL = Индекс * 8
    POP  B                          ; BC = Индекс * 2 (Извлекли без затирания DE!)
    DAD  B                          ; HL = (Индекс * 8) + (Индекс * 2) = Индекс * 10
    
    ; Прибавляем физическую базу глобальной таблицы файлов ядра
    LXI  B, SYS_FILE_TABLE
    DAD  B                          ; HL -> Указывает на точный адрес 10-байтового слота
    
    ANA  A                          ; АППАРАТНЫЙ СБРОС: Сбрасываем CY = 0 (Успех)
    JMP  _vfs_get_struct_exit

_vfs_get_struct_err:
    LXI  H, 0000H                   ; Возвращаем пустой указатель
    STC                             ; АППАРАТНЫЙ ФЛАГ ОШИБКИ: CY = 1 (POSIX EBADF)

_vfs_get_struct_exit:
    POP  D                          ; ПОЛНЫЙ ФИКС: Восстановили оригинальный DE пользователя!
    POP  B
    RET

; -----------------------------------------------------------------------------
; K_VFS_DISPATCH
; Центральный коммутатор операций виртуальной файловой системы ядра Ring 0
; Вход:  HL = Физический 16-битный адрес 10-байтового слота в SYS_FILE_TABLE
;        C  = Смещение запрашиваемого метода операции внутри строки драйвера
;        DE, BC_user = Входящие аргументы сисколла от пользователя (буфер, длина)
;        Прерывания заперты (DI), стек в Ring 0.
; -----------------------------------------------------------------------------
k_vfs_dispatch:
    ; === ШАГ 1: ТОТАЛЬНОЕ СПАСЕНИЕ ПОЛЬЗОВАТЕЛЬСКИХ АРГУМЕНТОВ ===
    PUSH B                          ; Спрятали BC пользователя (включая длину/аргумент)
    PUSH D                          ; Спрятали DE пользователя (включая адрес буфера/cmd)
    
    MOV  B, C                       ; B = Смещение метода драйвера (0, 2, 4, 6, 8)
    SHLD VFS_DRV_ROW_PTR            ; Кэшируем адрес структуры файла в ОЗУ ядра
    
    ; Извлекаем Major и Minor из структуры файла
    MOV  A, M                       ; A = Major-номер устройства (F_TYPE)
    STA  VFS_DISPATCH_MAJOR         ; Зафиксировали в кэш ядра
    
    INX  H                          ; HL -> Поле F_MINOR (+1)
    MOV  A, M                       ; A = Minor-номер (F_MINOR)
    STA  VFS_DISPATCH_MINOR         ; Зафиксировали в кэш ядра

    ; === ШАГ 2: ВЫЧИСЛЕНИЕ АДРЕСА СТРОКИ ДРАЙВЕРА С КОНТРОЛЕМ ГРАНИЦ ===
    LDA  VFS_DISPATCH_MAJOR         ; A = Major-номер (0..31)
    
    ; Аппаратный файрвол защиты от некорректных мажоров
    CPI  VFS_MAX_TYPES              ; Сравниваем с физическим пределом 32 (из v1.5.150)
    JNC  _vfs_dispatch_fatal_index  ; Если Major >= 32 — мгновенный уход в панику! [1.3]

    ; Вычисляем смещение в таблице коммутации: HL = Major * 10
    MOV  L, A
    MVI  H, 0                       ; HL = Major
    DAD  H                          ; HL = Major * 2
    PUSH H                          ; Спрятали Major * 2 на вершину стека ядра
    DAD  H                          ; HL = Major * 4
    DAD  H                          ; HL = Major * 8
    POP  D                          ; DE = Major * 2
    DAD  D                          ; HL = (Major * 8) + (Major * 2) = Major * 10
    
    LXI  D, VFS_SWITCH_TABLE
    DAD  D                          ; HL = Физический базовый адрес строки драйвера
    
    ; Прибавляем смещение запрашиваемого метода (из сохранённого регистра B)
    MOV  E, B
    MVI  D, 0                       ; DE = Смещение метода (0, 2, 4, 6, 8)
    DAD  D                          ; HL -> Точная ячейка DW с адресом функции в ОЗУ

    ; === ШАГ 3: ИЗВЛЕЧЕНИЕ АДРЕСА ОБРАБОТЧИКА И ТАБЛИЧНЫЙ БАРЬЕР ===
    MOV  E, M
    INX  H
    MOV  D, M                       ; DE = Физический 16-битный адрес обработчика драйвера
    
    ; Проверяем вектор на зануление (Адрес == 0000H)?
    MOV  A, D
    ORA  E                          ; Логическое ИЛИ над байтами адреса
    JNZ  _vfs_dispatch_execute      ; Если адрес != 0000H — обработчик готов к прыжку!

    ; --- КРИТИЧЕСКИЙ БАРЬЕР ПРОГРАММНОГО ФАЙРВОЛА ЯДРА ---
    LDA  VFS_DISPATCH_MAJOR         ; Извлекаем мажор сбойного устройства
    CPI  VFS_TYPE_PIPE              ; Сравниваем с границей критических узлов VFS [1.3]
    JNC  _vfs_dispatch_fatal_index  ; Если узел >= PIPE (TTY, DISK, RTC, NVRAM) -> ПАНИКА! [1.3]

    ; Мягкое парирование для некритичных пустых слотов (Файлы, пайпы Ring 3)
    LXI  D, k_vfs_enotsupp          ; DE = Адрес канонического стаба-заглушки (возврат ENODEV)
    JMP  _vfs_dispatch_execute

_vfs_dispatch_fatal_index:
    MVI  A, PANIC_VFS_INDEX_CORRUPT ; A = 0x46 (Из запечатанного panic.inc v1.5.171)
    JMP  k_panic                    ; ВЫЗОВ ГЛOБАЛЬНОГО ДЕСТРУКТОРА ЯДРА (Тотальный HLT) [1.3]

_vfs_dispatch_execute:
    ; Прячем целевой адрес функции (DE) в память ядра, чтобы освободить регистры 
    ; под извлечение оригинальных пользовательских параметров из стека
    XCHG                            ; HL = Адрес функции драйвера, DE = Мусор
    SHLD K_TEMP_PC                  ; Временно спрятали адрес функции в ячейку ядра

    ; === ШАГ 4: ВОССТАНОВЛЕНИЕ КОНТЕКСТА И АППАРАТНЫЙ ПРЫЖОК ===
    POP  D                          ; Восстановили чистый буфер/cmd пользователя Ring 3
    POP  B                          ; Восстановили чистую длину/arg пользователя Ring 3
    
    LHLD VFS_DRV_ROW_PTR            ; HL = Адрес структуры открытого файла для драйвера (ABI)
    
    PUSH H                          ; Временно сохранили HL на стек ядра
    LHLD K_TEMP_PC                  ; HL = Физический адрес функции драйвера
    XTHL                            ; ТРЮК 8080: адрес функции на вершине стека, HL восстановлен [1.3]
                                    
    RET                             ; Процессор снимает адрес функции со стека в PC 
                                    ; и прыгает в драйвер с абсолютно чистыми РОН! [1.3]

; =============================================================================
; НАЗВАНИЕ: vfs.asm (Связывание глобального файла с локальным дескриптором FD)
; =============================================================================
k_vfs_bind_fd:
    ; Вход:  A  = Номер глобального системного дескриптора из SYS_FILE_TABLE (0..127)
    ;        Прерывания заперты (DI), стек в Ring 0.
    ; Задача: Линейно просканировать байты O_FILE_TABLE (+34..+49) внутри PCB 
    ;         текущей активной задачи (CURRENT_PCB_PTR). Найти первый свободный 
    ;         слот (0xFF) и привязать к нему глобальный индекс файла.
    ; Выход: В случае успеха:
    ;           Флаг переноса CY = 0 (Успех)
    ;           Регистр A = Локальный номер файлового дескриптора процесса (FD 0..15)
    ;        В случае ошибки (Локальная таблица процесса переполнена, лимит EMFILE):
    ;           Флаг переноса CY = 1 (Провал)
    ;           Регистр A = EMFILE (Канонический POSIX-код ошибки 24)
    ; ВНИМАНИЕ: Все РОН супервизора (за исключением целевого A) сохраняются.
    PUSH B
    PUSH D
    PUSH H                          ; Сохранили контекст планировщика

    MOV  C, A                       ; C = Наш глобальный индекс файла (0..127)

    LHLD CURRENT_PCB_PTR            ; HL = Физический базовый адрес активного PCB задачи
    LXI  D, O_FILE_TABLE
    DAD  D                          ; HL указывает точно на старт O_FILE_TABLE (+34)

    MVI  B, 16                      ; Счетчик цикла = 16 локальных файловых дескрипторов
    MVI  E, 0                       ; E = Текущий проверяемый локальный FD (0..15)

_vfs_bind_scan_loop:
    MOV  A, M                       ; Читаем значение байта дескриптора из PCB процесса
    CPI  RES_FREE_MARKER            ; Проверяем, свободен ли локальный FD (Равен ли 0xFF)?
    JZ   _vfs_bind_slot_found       ; Свободный слот найден! Переходим к связыванию

    INX  H                          ; Сдвигаем HL на следующий дескриптор в PCB (+1 байт)
    INR  E                          ; Переходим к следующему индексу FD (E++)
    
    DCR  B                          ; Уменьшаем счетчик проходов
    JNZ  _vfs_bind_scan_loop        ; Повторяем для всей таблицы из 16 дескрипторов

    ; --- ОШИБКА: ЛОКАЛЬНАЯ ТАБЛИЦА ФАЙЛОВ ПРОЦЕССА ПЕРЕПОЛНЕНА (EMFILE) ---
    MVI  A, EMFILE                  ; A = Код ошибки POSIX EMFILE (Too many open files = 24)
    STC                             ; АППАРАТНЫЙ ФЛАГ ПРОВАЛА: CY = 1 (Провал)
    JMP  _vfs_bind_exit             ; Уходим на выход

_vfs_bind_slot_found:
    ; --- СВЯЗЫВАНИЕ И ФИКСАЦИЯ ДЕСКРИПТОРА ---
    MOV  M, C                       ; Прошиваем глобальный индекс файла (из C) в ОЗУ текущего PCB!
    
    MOV  A, E                       ; Аккумулятор A = Вычисленный локальный номер дескриптора (0..15)
    ANA  A                          ; АППАРАТНЫЙ СБРОС: Сбрасываем флаг переноса CY = 0 (УСПЕХ)

_vfs_bind_exit:
    POP  H
    POP  D
    POP  B                          ; Восстановили РОН супервизора
    RET                             ; Сквозной возврат в вызывающий сисколл (sys_open)

; =============================================================================
; НАЗВАНИЕ: vfs.asm (Диспетчер закрытия дескрипторов и освобождения ресурсов)
; =============================================================================
k_vfs_close_fd:
    ; Вход:  D = Локальный файловый дескриптор процесса Ring 3 (FD 0..15)
    ;        Прерывания гарантированно заперты (DI), стек в Ring 0.
    ; Задача: Найти глобальный индекс файла в PCB, очистить локальный FD.
    ;         Уменьшить F_REF в SYS_FILE_TABLE. Если F_REF == 0, вызвать метод 
    ;         V_OP_CLOSE соответствующего мажорного драйвера устройства.
    ; Выход: В случае успеха:
    ;           Флаг переноса CY = 0 (Успех), регистр A = 00H
    ;        В случае ошибки (EBADF - дескриптор не существует или уже свободен):
    ;           Флаг переноса CY = 1 (Провал), регистр A = EBADF (9)
    ; ВНИМАНИЕ: Все РОН супервизора полностью сохраняются (кроме A).
    PUSH B
    PUSH D
    PUSH H                          ; Сохранили контекст планировщика

    ; --- ШАГ 1: ИЗВЛЕЧЕНИЕ И ОЧИСТКА ЛОКАЛЬНОГО FD В PCB ---
    LHLD CURRENT_PCB_PTR            ; HL = Физическая база PCB активной задачи
    MOV  A, D                       ; A = Локальный FD из регистра D
    ANI  00FH                       ; Защитная маска границ (FD строго 0..15)
    ADI  O_FILE_TABLE               ; A = Смещение до нужного слота внутри PCB
    
    MOV  C, A
    MVI  B, 0                       ; BC = 16-битное смещение
    DAD  B                          ; HL -> Указывает точно на ячейку FD в PCB
    
    MOV  A, M                       ; A = Глобальный системный индекс файла (0..127)
    CPI  RES_FREE_MARKER            ; Проверяем, не свободен ли он уже (0xFF)?
    JZ   _vfs_close_err_ebadf       ; Если 0xFF — возвращаем ошибку EBADF (9)
    
    MOV  C, A                       ; C = Сохранили глобальный индекс файла (0..127)
    MVI  M, RES_FREE_MARKER         ; АТОМАРНАЯ ОЧИСТКА: Локальный FD в PCB сброшен в 0xFF!

    ; --- ШАГ 2: ВЫЧИСЛЕНИЕ АДРЕСА СТРОКИ В SYS_FILE_TABLE ---
    MOV  L, C
    MVI  H, 0                       ; HL = Чистый глобальный индекс файла (0..127)
    
    ; Математическое быстрое умножение индекса на каноничные 10 байт:
    DAD  H                          ; * 2
    PUSH H                          
    DAD  H                          ; * 4
    DAD  H                          ; * 8
    POP  B                          ; BC = Индекс * 2
    DAD  B                          ; HL = Индекс * 10
    
    LXI  B, SYS_FILE_TABLE
    DAD  B                          ; HL = Точный физический адрес 10-байтового слота файла
    SHLD VFS_CLOSE_STRUCT_PTR       ; Сохранили адрес структуры во временный кэш ядра

    ; --- ШАГ 3: ДЕКРЕМЕНТ POSIX СЧЕТЧИКА ССЫЛОК F_REF ---
    LXI  D, F_REF
    DAD  D                          ; HL -> Указывает на поле F_REF (+2) структуры файла
    DCR  M                          ; F_REF-- (Уменьшаем количество удержаний дескриптора)
    MOV  A, M                       ; A = Текущее значение F_REF после декремента
    JNZ  _vfs_close_success         ; Если F_REF > 0, файл ещё открыт в других процессах -> На выход

    ; === ШАГ 4: СЧЕТЧИК ССЫЛОК ОБНУЛИЛСЯ — ФИЗИЧЕСКОЕ ЗАКРЫТИЕ ДРАЙВЕРА ===
    ; Нам нужно вызвать метод V_OP_CLOSE драйвера. Наш HL сейчас на F_REF (+2).
    ; Извлекаем Major и Minor из структуры. Нам нельзя портить пользовательские РОН.
    LHLD VFS_CLOSE_STRUCT_PTR       ; HL = База структуры открытого файла
    
    MOV  A, M                       ; A = Major-номер устройства (F_TYPE)
    STA  VFS_CLOSE_MAJOR_TMP        ; Кэшируем во временную ячейку закрытия
    
    INX  H                          ; HL -> Поле F_MINOR (+1)
    MOV  A, M                       ; A = Minor-номер физического порта
    STA  VFS_CLOSE_MINOR_TMP        ; Кэшируем во временную ячейку закрытия

    ; Вызываем центральный атомарный коммутатор vfs_dispatch. 
    ; Параметры: HL = Физический адрес структуры открытого файла
    ;             C  = Смещение метода драйвера (V_OP_CLOSE = 2)
    LHLD VFS_CLOSE_STRUCT_PTR       ; HL = Структура файла для ABI драйвера
    MVI  C, V_OP_CLOSE              ; C = 2 (Константа метода закрытия из vfs.inc)
    
    ; ВНИМАНИЕ: k_vfs_dispatch полностью сохраняет РОН супервизора, 
    ; считывает Major/Minor и выполняет PUSH + RET в код драйвера.
    ; Чтобы после выполнения кода драйвера управление вернулось сюда, в k_vfs_close_fd,
    ; мы имитируем CALL: заталкиваем адрес возврата на стек ядра!
    LXI  B, _vfs_close_driver_return
    PUSH B                          ; Адрес возврата теперь на вершине стека ядра
    JMP  k_vfs_dispatch             ; Безусловный прыжок в коммутатор. Он сделает RET в драйвер.

_vfs_close_driver_return:
    ; Драйвер физического устройства отработал и вернул управление сюда через свой RET.
    ; Полностью затираем освобождённый глобальный слот файла в SYS_FILE_TABLE, выставляя F_TYPE = 0
    LHLD VFS_CLOSE_STRUCT_PTR       ; HL = База освобождённого слота
    MVI  M, VFS_TYPE_FREE           ; F_TYPE = 00h (Слот официально объявлен свободным в ядре)

_vfs_close_success:
    XRA  A                          ; A = 00H, сброс флага переноса: CY = 0 (УСПЕХ)
    JMP  _vfs_close_final

_vfs_close_err_ebadf:
    MVI  A, EBADF                   ; A = POSIX ошибка EBADF (Bad file number = 9)
    STC                             ; АППАРАТНЫЙ ФЛАГ ПРОВАЛА: CY = 1

_vfs_close_final:
    POP  H
    POP  D
    POP  B                          ; Восстановили РОН супервизора в исходное состояние
    RET                             ; Сквозной возврат в вызывающий сисколл (sys_close)

; -----------------------------------------------------------------------------
; K_VFS_RESOLVE_DEV
; Разрешение полных путей символьных устройств по таблице DEV_MOUNT_TABLE (0..15)
; Вход:  DE_user на стэке шлюза (указывает на ASCIIZ-строку полного пути в Ring 3)
;        Прерывания заперты (DI), стек в Ring 0.
; Выход: В случае успеха:
;           Флаг переноса CY = 0
;           H = Выходной Major-номер драйвера устройства (8 = RTC, 9 = NVRAM)
;           L = Выходной Minor-номер устройства / порта (0 или 1)
;        В случае ошибки (POSIX ENOENT - файл устройства не найден в /dev):
;           Флаг переноса CY = 1
;           HL = 0x0000, регистр A = ENOENT (Канонический код ошибки 2)
; -----------------------------------------------------------------------------
k_vfs_resolve_dev:
    PUSH B
    PUSH D                          ; Сохранили внутренний контекст супервизора Ring 0

    ; --- ШАГ 1: АКТИВАЦИЯ РЕЖИМА KERNEL_OVERRIDE ---
    MVI  A, KERNEL_OVERRIDE_ON
    OUT  EMMU_OVERRIDE              ; Открыли Сегмент 2 на User RAM для КР580ВМ80А [1.3]
    
    LXI  B, DEV_MOUNT_TABLE         ; BC = Начало таблицы монтирования активных узлов
    MVI  A, MAX_MOUNT_DEVS          ; A = Лимит сканирования (16 слотов из v1.5.150)
    STA  K_TMP_SLOTS_CNT

_vfs_dev_scan_loop:
    ; Сохраняем текущий указатель таблицы BC в кэш-ячейки ядра
    MOV  A, C \ STA K_TMP_CURR_LO              
    MOV  A, B \ STA K_TMP_CURR_HI

    ; Извлекаем физический адрес эталонной строки из таблицы монтирования
    MOV  L, C
    MOV  H, B                       ; HL = Текущая позиция в DEV_MOUNT_TABLE
    MOV  E, M                       ; E = Младший байт адреса строки (например, "/dev/rtc")
    INX  H
    MOV  D, M                       ; D = Старший байт адреса строки
    
    ; --- КРИТИЧЕСКИЙ АППАРАТНЫЙ БАРЬЕР ПУСТОГО СЛОТА БЕЗОПАСНОСТИ ---
    MOV  A, D
    ORA  E                          ; Проверяем, равен ли адрес эталона 0x0000?
    JZ   _vfs_dev_next_mount_slot   ; Если узел пуст — безопасно шагаем к следующему слоту! [1.3]

    ; --- ШАГ 2: БЕЗОПАСНОЕ ИЗВЛЕЧЕНИЕ USER_DE С УЧЕТОМ КАДРА СТЭКА ---
    PUSH D                          ; Временно спрятали адрес эталона ядра (SP уехал на -2)
    
    ; Математический расчёт кадра стэка Ring 0 от текущего SP:
    ; +0: DE эталона, +2: DE супервизора, +4: BC супервизора, 
    ; +6: Адрес возврата, +8: BC_user пользователя, +10: Истинный DE_user
    LXI  H, 10                      ; КАНOНИЧЕСКИЙ СДВИГ: Выход на оригинальный DE_user [1.3]
    DAD  SP                         ; HL указывает строго на ячейку входящего DE_user на стэке
    MOV  E, M                       ; E = Младший байт DE_user
    INX  H
    MOV  D, M                       ; D = Старший байт DE_user
    
    MOV  H, D
    MOV  L, E                       ; HL = Истинный адрес строки пользователя в Окне 2 Ring 3 [1.3]
    POP  D                          ; DE = Восстановили физический адрес эталонной строки ядра
    
    ; Инициализируем защитный 8-битный счётчик длины строки перед циклом сравнения
    MVI  A, 64                      ; Защитный лимит = 64 символа (0x40)
    STA  K_TMP_STR_LEN

_vfs_dev_string_cmp_loop:
    LDAX D                          ; A = Символ эталонной строки ядра (начинается с '/')
    CMP  M                          ; Сравниваем с символом из User RAM через Сегмент 2 [1.3]
    JNZ  _vfs_dev_next_mount_slot   ; Не совпало — сброс сравнения и переход на следующий слот [1.3]
    
    ORA  A                          ; Достигли конца строки (нуль-терминатор)?
    JZ   _vfs_dev_node_matched      ; Да — найдено полное сквозное совпадение путей! [1.3]
    
    INX  D                          ; Шаг вперед по строке ядра Ring 0
    INX  H                          ; Шаг вперед по строке пользователя в User RAM
    
    ; --- ЗАЩИТНЫЙ ФАЙРВОЛ ЗАЦИКЛИВАНИЯ И ВРЕДОНОСНЫХ СТРОК ---
    LDA  K_TMP_STR_LEN
    DCR  A                          ; Минус один легитимный символ
    STA  K_TMP_STR_LEN
    JNZ  _vfs_dev_string_cmp_loop   ; Баланс лимита не исчерпан, продолжаем сравнение [1.3]
    JMP  _vfs_dev_next_mount_slot   ; Оверран длины (>64 байт без нуля) — принудительный сброс слота

_vfs_dev_next_mount_slot:
    ; Восстанавливаем базовый указатель текущего слота таблицы из кэша
    LDA  K_TMP_CURR_LO \ MOV C, A
    LDA  K_TMP_CURR_HI \ MOV B, A   
    
    MOV  L, C
    MOV  H, B                       
    LXI  D, MNT_ENTRY_SZ            ; Шаг равен каноничным 6 байтам дескриптора монтирования
    DAD  D                          ; HL перешагнул на начало следующего слота
    MOV  B, H
    MOV  C, L                       ; Пара BC зафиксирована на новом дескрипторе [1.3]

    LDA  K_TMP_SLOTS_CNT
    DCR  A                          ; Уменьшаем счетчик слотов (16..0)
    STA  K_TMP_SLOTS_CNT
    JNZ  _vfs_dev_scan_loop         ; Таблица не исчерпана, сканируем дальше [1.3]

    ; --- ОШИБКА: УЗЕЛ НЕ НАЙДЕН В ДЕРЕВЕ /dev (POSIX ENOENT) ---
    MVI  A, KERNEL_OVERRIDE_OFF
    OUT  EMMU_OVERRIDE              ; Аппаратно запечатываем память ядра от Ring 3 [1.3]
    
    LXI  H, 0x0000                  ; Возвращаем пустой указатель HL
    MVI  A, ENOENT                  ; А = Канонический код ошибки 2 (No such file)
    STC                             ; Взводим аппаратный флаг сбоя сисколла: CY = 1 [1.3]
    JMP  _vfs_resolve_exit

_vfs_dev_node_matched:
    ; Полное совпадение путей подтверждено. Считываем ассоциированные Major/Minor.
    LDA  K_TMP_CURR_LO \ MOV C, A
    LDA  K_TMP_CURR_HI \ MOV B, A
    
    MOV  L, C
    MOV  H, B                       ; HL = Базовый адрес совпавшего слота в таблице
    LXI  D, O_MNT_MAJOR
    DAD  D                          ; HL сдвинут на поле MAJOR (+2)
    MOV  E, M                       ; E = Зафиксированный Major драйвера (8 или 9)
    
    INX  H                          ; HL указывает на поле MINOR (+3)
    MOV  D, M                       ; D = Зафиксированный Minor (0 или 1)
    
    MVI  A, KERNEL_OVERRIDE_OFF
    OUT  EMMU_OVERRIDE              ; Аппаратно изолируем ядро Ring 0 перед выходом [1.3]

    MOV  H, E                       ; H = Выходной Major драйвера (Параметр ABI)
    MOV  L, D                       ; L = Выходной Minor порта (Параметр ABI)
    ANA  A                          ; СБРОС ФЛАГА ОШИБКИ: Выставляем флаг успеха CY = 0 [1.3]

_vfs_resolve_exit:
    POP  D
    POP  B                          ; Полностью восстановили оригинальный контекст супервизора
    RET                             ; Возврат в вызывающий сисколл открытия файлов [1.3]

; =============================================================================
; ПРОЦЕДУРА: k_vfs_mknod (УНИВЕРСАЛЬНАЯ БЕЗСТЕКОВАЯ ВЕРСИЯ С ВАЛИДАЦИЕЙ ГРАНИЦ)
; Вход:  A  = Тип создаваемого узла (I_TYPE_REGULAR, I_TYPE_CHRDEV, I_TYPE_BLKDEV)
;        C  = Major-номер драйвера (Для терминалов = 4)
;        B  = Minor-номер устройства / порта (0..3)
;        DE = Физический адрес ASCIIZ-строки полного пути к файлу
; Задача: Найти свободный Inode, выделить чистое имя из пути методом реверсивного
;         поиска разделителя '/', прошить атрибуты и скопировать имя в RAM-диск.
; Выход: CY = 0 — Успех. Узел интегрирован в топологию VFS.
;        CY = 1 — Ошибка: Превышен лимит слотов или передано пустое имя (A = код).
;        Все РОН полностью сохранены согласно контракту HAL Варианта B.
; =============================================================================
; =============================================================================
; ПРОЦЕДУРА: k_vfs_mknod (СИНТАКСИЧЕСКИ ВЕРИФИЦИРОВАННАЯ КАНOНИЧЕСКАЯ ВЕРСИЯ)
; =============================================================================
k_vfs_mknod:
    PUSH B
    PUSH D
    PUSH H                          ; Спасли рантайм-контекст супервизора Ring 0

    ; --- ЭТАП 0: БЕЗОПАСНАЯ ИЗОЛЯЦИЯ ПАРАМЕТРОВ В ПАМЯТЬ ЯДРА ---
    STA  _vfs_mknod_tmp_type        ; Спрятали входной тип узла
    MOV  A, C
    STA  _vfs_mknod_tmp_major       ; Спрятали Major-номер
    MOV  A, B
    STA  _vfs_mknod_tmp_minor       ; Спрятали Minor-номер

    ; === ЭТАП 1: РЕВЕРСИВНЫЙ ТОКЕНИЗАТОР (ОБНАРУЖЕНИЕ КОНЦА СТРОКИ) ===
    MOV  H, D
    MOV  L, E                       ; HL = начало строки пути (из DE)
    
    ; Валидация на пустую строку
    MOV  A, M
    ORA  A
    JZ   _vfs_mknod_err_empty       ; Первым же байтом идет 00H — авария!

_vfs_mknod_seek_end:
    MOV  A, M
    ORA  A                          ; Нашли терминальный ноль?
    JZ   _vfs_mknod_end_found       ; Да — HL указывает на замыкающий ноль
    INX  H
    JMP  _vfs_mknod_seek_end

_vfs_mknod_end_found:
    ; === ЭТАП 2: ОБРАТНЫЙ ПОИСК РАЗДЕЛИТЕЛЯ '/' ===
_vfs_mknod_reverse_loop:
    DCX  H
    
    ; Валидация на выход за границы начала строки
    MOV  A, L
    CMP  E
    JNZ  _vfs_mknod_check_slash
    MOV  A, H
    CMP  D
    JZ   _vfs_mknod_name_isolated_direct  ; Откатились до самого начала пути (нет слешей)
    
_vfs_mknod_check_slash:
    MOV  A, M
    CPI  '/'                        ; Встретили косую черту?
    JZ   _vfs_mknod_name_isolated   ; Да — имя файла выделено!
    JMP  _vfs_mknod_reverse_loop

_vfs_mknod_name_isolated:
    INX  H                          ; Сдвиг вперед: HL указывает строго на первый символ имени

_vfs_mknod_name_isolated_direct:
    ; Валидация на пустое имя (например, если путь заканчивается на слеш "/dev/")
    MOV  A, M
    ORA  A
    JZ   _vfs_mknod_err_empty       ; Имя отсутствует — авария!
    
    XCHG                            ; DE = Вычисленный точный адрес чистого имени файла
                                    ; Теперь пара DE зафиксирована как источник для копирования.

    ; === ЭТАП 3: РЕЕНТЕРАБЕЛЬНЫЙ ПОИСК СВОБОДНОГО СЛОТА В ОЗУ ===
    LXI  H, VFS_INODE_TABLE         ; HL = Начало массива Inode
    MVI  B, VFS_MAX_INODES          ; Регистр B = Счетчик цикла (64 слота)

_vfs_mknod_scan_loop:
    MOV  A, M                       ; A = Считали I_TYPE текущего слота строго в аккумулятор
    CPI  I_TYPE_FREE                ; Слот свободен (00H)?
    JZ   _vfs_mknod_slot_found      ; Да — уходим на заполнение!
    
    ; Слот занят — шагаем вперед на размер структуры (+16 байт)
    PUSH D                          ; ← ИСПРАВЛЕНО СИНТАКСИЧЕСКИ (Вместо PUSH DE)
    LXI  D, INODE_STRUCT_SIZE       ; DE = 16
    DAD  D                          ; HL = HL + 16
    POP  D                          ; ← ИСПРАВЛЕНО СИНТАКСИЧЕСКИ (Вместо POP DE)
    
    DCR  B                          ; Декремент счетчика в изолированном регистре B
    JNZ  _vfs_mknod_scan_loop

    ; Таблица RAM-диска переполнена
    POP  H
	POP  D
	POP  B        ; Полностью восстановили оригинальные РОН
    STC                             ; CY = 1 (Провал: No space left on device)
    RET

_vfs_mknod_slot_found:
    ; === ЭТАП 4: АТОМАРНАЯ ПРОШИВКА АТРИБУТОВ УЗЛА ИЗ ПАМЯТИ ЯДРА ===
    LDA  _vfs_mknod_tmp_type
    MOV  M, A                       ; 1. Прошили ТИП УЗЛА (01H, 02H или 03H)
    
    INX  H                          ; HL -> O_I_MAJOR (+1)
    LDA  _vfs_mknod_tmp_major
    MOV  M, A                       ; 2. Прошили MAJOR-номер драйвера
    
    INX  H                          ; HL -> O_I_MINOR (+2)
    LDA  _vfs_mknod_tmp_minor
    MOV  M, A                       ; 3. Прошили MINOR-номер порта / устройства
    
    INX  H                          ; HL -> O_I_NAME (+3) — Буфер ASCIIZ-имени

    ; === ЭТАП 5: ПОБАЙТОВОЕ КУРЬЕРСКОЕ КОПИРОВАНИЕ ИМЕНИ ===
    MVI  C, 11                      ; Защитный лимит длины = 11 байт (10 символов + ноль)

_vfs_mknod_copy_name:
    LDAX D                          ; ← ИСПРАВЛЕНО СИНТАКСИЧЕСКИ (Вместо LDAX DE)
    MOV  M, A                       ; Записали в структуру Inode (HL)
    ORA  A                          ; Достигли конца строки (терминального нуля)?
    JZ   _vfs_mknod_copy_done       ; Да — копирование успешно завершено!
    
    INX  D                          ; ← ИСПРАВЛЕНО СИНТАКСИЧЕСКИ (Вместо INX DE)
    INX  H                          ; Инкремент указателя приемника
    DCR  C                          ; Уменьшаем защитный лимит длины
    JNZ  _vfs_mknod_copy_name
    
    MVI  M, 00H                     ; Оверран длины — принудительно глушим буфер нулем

_vfs_mknod_copy_done:
    POP  H
	POP  D
	POP  B        ; Идеальное каноническое восстановление РОН супервизора по LIFO
    ANA  A                          ; СБРОС ФЛАГА ОШИБКИ: CY = 0 (Полный рантайм-успех!)
    RET

_vfs_mknod_err_empty:
    POP  H
	POP  D
	POP  B        ; Симметрично очистили оригинальный РОН-кадр со стека
    MVI  A, EINVAL                  ; Аккумулятор А = 0x16 (Код ошибки недопустимого аргумента)
    STC                             ; ВЗВОД ФЛАГА ОШИБКИ: CY = 1 (Провал валдиации пути)
    RET
