; =============================================================================
; НАЗВАНИЕ: hw_lp.asm (Конфигуратор и инициализатор БИС КР580ВВ55А)
; ТОКЕН:    TOKEN::MEGA-580_OS_v1.5.104_HW_I8255_LP_INIT_STABLE_20260710
; СРЕДА:    Ring 0 супервизора | Архитектура КР580ВМ80А | Драйверы Печати
; =============================================================================
; =============================================================================
; hw_i8255_lp_init (ИСПРАВЛЕННАЯ ВЕРСИЯ)
; ТОКЕН: TOKEN::MEGA-580_OS_v1.5.105_HW_I8255_LP_INIT_FIXED_20260710
; =============================================================================
hw_i8255_lp_init:
    PUSH B
    PUSH D
    PUSH H                          ; Спасли рантайм-контекст супервизора Ring 0

    MOV  C, A                       ; C = Тестируемый BASE_PORT (например, 0x78)

    ; --- ЭТАП 1: ДИНАМИЧЕСКИЙ SMC-РАСЧЕТ И ПРОШИВКА ПОРТОВ ВВ55А ---
    ; В архитектуре ВВ55А: Регистр Управляющего Слова (РРУ) = BASE_PORT + 3
    ADI  3
    STA  _lp_55_init_cmd_out + 1
    STA  _lp_55_init_cmd_out2 + 1   ; Прошили оба порта вывода в РРУ
    
    MOV  A, C
    INR  A
    STA  _lp_55_init_status_in + 1  ; Порт статуса = BASE_PORT + 1
    
    ; --- ЭТАП 2: ПРОГРАММИРОВАНИЕ РЕЖИМА ВВ55А (HANDSHAKE IRQ) ---
    ; Конфигурируем ВВ55А: Порт А = Режим 1 вывода (стробируемый вывод),
    ; Порт B = Ввод (линии состояния принтера Centronics). Управляющее слово = 0xA8
    MVI  A, 0A8H
_lp_55_init_cmd_out:
    OUT  000H                       ; Стреляем в РРУ чипа. Патчится на BASE+3

    ; ИСПРАВЛЕНО: Разрешаем асинхронные прерывания INTE A через бит 3 порта C.
    ; Команда Set/Reset бита PC3: 00000111b = 0x07
    MVI  A, 007H                    ; ← ИСПРАВЛЕНО: было 0x0D (бит 6), стало 0x07 (бит 3)
_lp_55_init_cmd_out2:
    OUT  000H                       ; Взводим INTE A через РРУ. Патчится на BASE+3

    ; --- ЭТАП 3: ТЕСТ ФУНКЦИОНИРОВАНИЯ (ВАЛИДАЦИЯ КРЕМНИЯ) ---
_lp_55_init_status_in:
    IN   000H                       ; Считали статус Centronics. Патчится на BASE+1
    MOV  B, A                       ; B = Локальный кэш статуса

    ; Валидация 1: Проверяем, выбран ли принтер (LP_SELECT / Бит 4 == 1)
    ANI  LP_SELECT                  ; Маска 0x10 из lp.inc
    JZ   _hw_lp_55_init_fail        ; Если 0 — линия пуста или принтер выключен

    ; Валидация 2: Проверяем отсутствие конца бумаги (LP_PAPER_OUT / Бит 5 == 0)
    MOV  A, B
    ANI  LP_PAPER_OUT               ; Маска 0x20 из lp.inc
    JNZ  _hw_lp_55_init_fail        ; Если 1 — датчик бумаги активен, чип не готов

    ; --- ЭТАП 4: ПОСЛЕДОВАТЕЛЬНОЕ ЗАПОЛНЕНИЕ ДЕСКРИПТОРА СЛОТА ---
    MOV  H, D
    MOV  L, E                       ; HL = Истинный адрес слота LP_TABLE

    ; Прописываем текущий статус (O_LP_STATUS = 0)
    MVI  M, 00H                     
    
    ; Прописываем физический порт DATA (BASE + 0)
    INX  H                          ; HL -> O_LP_PORT_DATA (+1)
    MOV  M, C
    
    ; Прописываем физический порт STATUS (BASE + 1)
    MOV  A, C \ INR A
    INX  H                          ; HL -> O_LP_PORT_STATUS (+2)
    MOV  M, A
    
    ; Прописываем физический порт CTRL (BASE + 2)
    INR  A
    INX  H                          ; HL -> O_LP_PORT_CTRL (+3)
    MOV  M, A
    
    ; Прописываем флаги успешного открытия драйвера
    INX  H                          ; HL -> O_LP_FLAGS (+4)
    MVI  M, LP_OPEN                 ; Выставили флаг "Порт открыт" (0x01)
    
    ; Прописываем аппаратный тип чипа в дескриптор слота
    MOV  H, D
    MOV  L, E                       ; HL = Восстановили чистую базу слота
    LXI  B, O_LP_HW_TYPE            ; Смещение +19 согласно lp.inc
    DAD  B                          ; HL -> O_LP_HW_TYPE
    MVI  M, LP_TYPE_8255            ; Зафиксировали тип кремния (01H)

    POP  H
    POP  D
    POP  B                          ; Полное каноническое восстановление РОН по LIFO
    ANA  A                          ; СБРОС ФЛАГА ОШИБКИ: CY = 0 (Успех!)
    RET

_hw_lp_55_init_fail:
    POP  H
    POP  D
    POP  B                          ; Восстановили оригинальный контекст супервизора
    STC                             ; ВЗВОД ФЛАГА ОШИБКИ: CY = 1 (Аппаратный отказ)
    RET
